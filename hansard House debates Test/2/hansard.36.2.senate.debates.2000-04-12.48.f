L'arrestation de 13 juifs 
L'honorable Erminie J. Cohen :
Honorables sénateurs, dans une veine un peu plus sérieuse, rappelons qu'il y a plus d'un an, les autorités iraniennes ont appréhendé 13 juifs sous l'inculpation d'espionnage. 
Ce groupe comprenait le grand rabbin de Shiraz et d'autres dirigeants religieux. 
Selon les Iraniens, les 13 sont des espions à la solde d'Israël et des États-Unis, accusations qui ont immédiatement été réfutées avec véhémence par les deux gouvernements. 
Leur procès est prévu pour le jeudi 13 avril, soit demain. 
Trois d'entre eux ont été libérés sous caution récemment, mais les 10 autres croupissent en prison depuis un an. 
