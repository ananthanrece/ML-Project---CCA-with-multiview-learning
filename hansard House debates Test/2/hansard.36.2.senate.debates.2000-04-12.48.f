La justice 
L'utilisation des langues officielles par les tribunaux 
L'honorable Jean-Robert Gauthier :
Honorables sénateurs, le Parlement canadien doit modifier la Loi sur les contraventions pour protéger les droits linguistiques. 
La Loi sur les contraventions prévoit la substitution du régime du procès-verbal de contravention à la procédure sommaire prévue au Code criminel.  
Cette loi a été modifiée en 1996 afin de permettre que le régime pénal de chaque province et territoire s'applique aux poursuites relatives aux infractions fédérales. 
La loi modifiée autorise le ministre de la Justice à conclure des ententes avec les autorités provinciales, municipales ou locales pour préciser les modalités du traitement des contraventions.  
En décembre 1997, l'Association des juristes d'expression française de l'Ontario a attiré l'attention du commissaire aux langues officielles sur l'accord conclu le 9 juin 1997 entre le gouvernement du Canada et la ville de Mississauga. 
Cet accord visait le stationnement illégal à l'aéroport internationalLester B. Pearson, à Mississauga. 
Il déterminait le traitement des contraventions de stationnement et le paiement des amendes imposées dans un tel cadre, y compris la procédure applicable aux poursuites judiciaires relatives à ces contraventions et amendes.  
Dans l'accord, il n'était aucunement fait mention de droits ni d'obligations d'ordre linguistique. 
Par suite de cette entente, les personnes ayant reçu des contraventions de stationnement se trouvaient dépouillées des droits linguistiques que leur avait garantis le Code criminel.  
Après avoir analysé les dispositions pertinentes de la Loi sur les contraventions, de la Loi sur les langues officielles, ainsi que de l'accord et de la documentation fournie par le ministère de la Justice, la commissaire aux langues officielles conclut que la plainte de l'Association des juristes d'expression française de l'Ontario est fondée. 
La commissaire recommande que le ministère de la Justice du Canada, et je cite: 
prenne les mesures nécessaires pour que soient incorporés dans la Loi sur les contraventions, au minimum, les mêmes droits linguistiques que: 
a) ceux reconnus au Code Criminel (pouvant être exercés devant les tribunaux); 
b) ceux prévus à la Partie IV de la Loi sur les langues officielles (pouvant être exercés à l'extérieur des tribunaux); 
2. et, par conséquent, veille à ce que les accords conclus sur le fondement de la Loi sur les contraventions garantissent que les provinces, les territoires et les municipalités respecteront les droits énumérés à la recommandation numéro 1; 
3. consulte la communauté minoritaire de langue officielle et les associations de juristes avant d'entreprendre tout projet, de conclure tout accord ou de procéder à toutes modifications législatives du régime contraventionnel susceptibles d'influer sur l'emploi du français et de l'anglais; 
4. réexamine tout accord ratifié à ce jour pour s'assurer qu'il y a une protection uniforme des droits linguistiques mentionnés à la recommandation numéro 1.  
