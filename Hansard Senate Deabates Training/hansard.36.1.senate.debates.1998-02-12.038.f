Le cardinal Ambrozic 
La motion portant envoi des félicitations du Sénat à l'occasion de la nomination de Son Éminence 
L'honorable Stanley Haidasz , conformément à l'avis du10 février 1998, propose: 
- Honorables sénateurs, c'est avec beaucoup de joie et de fierté que les membres de l'archidiocèse catholique de Toronto ont reçu en janvier l'heureuse nouvelle que Sa Sainteté le pape Jean-Paul II avait nommé Son Éminence Aloysius Ambrozic au Sacré Collège. 
Cette grande distinction a été accordée à un éminent Canadien de Toronto, qui est arrivé dans notre pays à titre de réfugié de la Slovénie.  
Après de brillantes études supérieures, il a été nommé recteur du collège St. Michaels de l'Université de Toronto, qu'il a servi avec distinction. 
Ce vaillant berger et pragmatique oecuméniste, qui dispose d'une connaissance approfondie de toutes les couches de la société canadienne, s'est acquis la confiance et le respect de tous ceux qu'il a côtoyés.  
Qui plus est, le nouveau cardinal s'est toujours intéressé aux délibérations du Parlement, notamment à celles du Sénat qui ont porté sur les questions concernant la vie. 
Je me souviens vivement de nos conversations téléphoniques à ces occasions, alors qu'il tentait d'obtenir de l'information et des éclaircissements sur ces questions ainsi que sur les projets de loi. 
Ses nombreuses réalisations et ses services inestimables à l'Église ont été reconnus par Sa Sainteté le pape Jean-Paul II, qui l'a élevé au rang de cardinal et de prince de l'Église catholique.  
Par conséquent, honorables sénateurs, je vous engage à appuyer cette motion.  
Son Honneur le Président :
Vous plaît-il, honorables sénateurs, d'adopter la motion?  
(La motion est adoptée.)  
