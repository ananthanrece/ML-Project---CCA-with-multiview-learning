L'une des raisons pour lesquelles notre santé économique est si bonne est que les indicateurs liés au revenu sont à la hausse et qu'ils le sont depuis un bon bout de temps.  
Pêches et Océans 
La pêche au crabe des neiges dans le golfe du St-Laurent-Possibilité d'accorder des quotas aux pêcheurs côtiers du Nouveau-Brunswick-La position du gouvernement 
L'honorable Fernand Robichaud (Saint-Louis-de-Kent) :
Honorables sénateurs, ma question s'adresse au leader du gouvernement au Sénat. 
Les pêcheurs côtiers du Nouveau-Brunswick sont inquiets et vivent dans l'incertitude. 
La pêche au homard est à la baisse, et pour la plupart, cette pêche est la base de leurs activités. 
Comme le ministre le sait, ces dernières années, les pêcheurs côtiers ont eu accès à la pêche au crabe des neiges dans le golfe du St-Laurent. 
Étant donné que les pêcheurs côtiers ont déjà pris du crabe des neiges et qu'ils ont fait preuve de bonne gestion pour la petite part qui leur était attribuée, le leader du gouvernement au Sénat pourrait-il nous informer si les pêcheurs côtiers retourneront pêcher le crabe des neiges dans le golfe cette prochaine saison?  
L'honorable B. Alasdair Graham (leader du gouvernement) :
Honorables sénateurs, en tant qu'ancien ministre d'État aux Pêches et à titre de représentant qui a suivi ces questions de près, l'honorable sénateur Robichaud sait que, depuis quelques années, les ressources de crabes dans le golfe sont temporairement partagées avec l'industrie de la pêche côtière. 
Ce partage repose sur des principes de conservation établis, sur la viabilité des titulaires de permis et sur l'absence d'une augmentation permanente de la capacité de pêche.  
Si ma mémoire est fidèle, le ministère a approuvé, pour la période de 1997 à 2001, une entente de cogestion passée avec les pêcheurs de crabes du secteur sud du golfe. 
