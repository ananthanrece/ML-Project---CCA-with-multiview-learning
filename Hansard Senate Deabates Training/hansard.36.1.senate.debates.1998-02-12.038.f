Le décès de John G.H. Halstead 
Hommages 
L'honorable Jerahmiel S. Grafstein :
Honorables sénateurs, il arrive parfois un moment où nous devons faire une pause et méditer sur les origines de la politique étrangère du Canada, une politique active et respectée dans le monde entier.  
Il y a plus de 70 ans, un petit cercle de jeunes bureaucrates, brillants intellectuels, sous la tutelle de O.D. Skelton, avait été rassemblé par M. King pour créer un service des affaires étrangères indépendant qui serait une annexe du Bureau du Conseil privé, dans l'édifice de l'Est, à quelques pas du Sénat. 
Ces brillants et jeunes érudits et ces jeunes athlètes qui partageaient les mêmes valeurs en tant que fils de pasteurs et de missionnaires avaient été choisis pour leur intelligence, leur jeunesse, leur vitalité et leur connaissance du monde. 
À leurs yeux et aux yeux des autres, ils étaient tout simplement les plus brillants et les meilleurs. 
Tous étaient imbus du rôle social auquel les avait préparés leur foi. 
Tous voulaient mettre leurs valeurs morales au service de la politique et de la diplomatie, comme leurs parents l'avaient fait au sein de l'Église. 
La moralité était à la base de leurs principes et de leurs politiques. 
Les normes d'excellence et d'honnêteté qu'ils s'étaient fixées pour eux-mêmes et pour les autres en faisaient au sein de la fonction publique des modèles exemplaires et devaient élever l'un d'eux, Lester B. Pearson, au rang de fonctionnaire, de diplomate, de lauréat du prix Nobel et de premier ministre.  
Telle fut la vie de John Halstead, décédé à Ottawa lundi dernier à l'âge de 76 ans. 
Né à Vancouver, John avait fait ses études à l'Université de la Colombie-Britannique et à la London School of Economics. 
Pendant la guerre, il avait servi en tant qu'officier de marine dans les services de renseignements des forces navales dans l'Atlantique-Nord, en Grande-Bretagne et en Allemagne. 
Sa connaissance de l'allemand lui valut d'interroger les sous-mariniers allemands faits prisonniers et, après la guerre, d'interroger les éléments des forces navales du haut-commandement allemand et de faire la chasse aux présumés criminels de guerre.  
À son retour au Canada, il s'est joint au service extérieur. 
Au cours de ses 36 ans de carrière dans la fonction publique, essentiellement durant la guerre froide, il a rapidement grimpé les échelons au sein du ministère des Affaires extérieures, passant de sous-secrétaire adjoint à ambassadeur en Allemagne puis à l'OTAN.  
John écrivait remarquablement bien et avait un grand sens de la stratégie. 
La carrière de John a aussi comporté des périodes de travail à titre de professeur invité et de conférencier à Georgetown, à Carleton, à Queen's, à Dalhousie, à Windsor, dans des centres d'études et de recherches aux États-Unis et dans des universités d'Allemagne. 
Il était actif au sein de l'Institut d'études stratégiques.  
Les grands amours de John ont été l'Europe, l'OTAN et l'Allemagne. 
John et moi partagions un grand intérêt pour la littérature et l'histoire allemandes. 
C'est ainsi que je l'ai connu, en tant que membre de la Conférence Atlantik-Brucke. 
Pendant plus de dix ans, nous nous sommes rencontrés chaque année, au Canada ou en Allemagne, pour discuter des relations entre le Canada, l'Allemagne et l'Europe. 
Quand on lui a décerné l'Ordre du Canada, en 1996, il a déclaré ceci: 
Il n'est pas là pour faire de l'argent. 
Le gouvernement est là pour servir les gens qui l'ont élu.  
John savait envisager l'avenir dans un esprit clair et concis.  
J'offre mes condoléances à sa femme, Jean, et à son fils, Christopher. 
La mort de John est une grande perte pour le Canada et le service extérieur, comme pour nous tous. 
Il manquera grandement à ses nombreux amis en Amérique du Nord et aux quatre coins de l'Europe.  
Qu'il repose en paix. 
La crise dans le système de santé 
L'honorable Erminie J. Cohen :
Honorables sénateurs, le déclin de l'aide fédérale au Nouveau-Brunswick met notre système de soins de santé en danger. 
C'est une source de grandes préoccupations. 
La semaine dernière seulement, à quatre reprises, les journaux de Saint John lançaient en manchettes les cris d'appels suivants: 
Le système de santé en ruine; 
Épuisés, les médecins cherchent une solution; 
Les compressions font mal; 
Les médecins réclament de l'aide.  
En dehors du Nouveau-Brunswick, pendant cette même semaine, nous avons entendu les mêmes sentiments exprimés à Montréal, où la liste d'attente pour les opérations électives a atteint la cote d'alarme. 
L'Ontario vient de constituer un groupe de travail pour étudier le nombre croissant de problèmes dans son système de soins de santé.  
Il devient de plus en plus évident pour tous les Canadiens que le déclin continu de l'aide fédérale a presque entièrement sapé les fondements de notre précieux système de soins de santé. 
Dans un communiqué de presse publié le 8 décembre 1997, le ministre des Finances, Paul Martin, a déclaré: «Gouverner est une question de choix, de priorités et de valeurs. 
Ce que n'a pas dit le ministre des Finances, c'est que, depuis 1993, son gouvernement a diminué les transferts pécuniaires au titre de la santé, de l'éducation et de l'assistance sociale d'environ 6,3 milliards de dollars, les ramenant de 18,8 milliards à 12,5 milliards. 
Dans sept des dix provinces, les transferts pécuniaires vont continuer à diminuer au cours des cinq prochaines années. 
En fait, d'ici l'an 2000, les sept provinces les moins riches perdront 384 millions de plus par an. 
Là est le problème.  
Honorables sénateurs, il ne faut pas oublier que le fait de ne dépenser que le «strict minimum» dans le domaine des soins de santé a des conséquences humaines très réelles. 
Pour reprendre les mots du docteur Gregg MacLean, président de la Saint John Medical Society: 
Le problème est le sous-financement. 
Nous voyons les files d'attente s'allonger et nous sommes témoins de la frustration des chirurgiens et des médecins de famille qui, tous les jours, doivent dire à leurs malades que l'intervention a été reportée et essayer de leur expliquer pourquoi.  
Honorables sénateurs, pour conclure que notre précieux système de soins de santé est en ruine, nous n'avons pas besoin d'autres preuves que l'augmentation des souffrances des malades, le manque de personnel et de matériel dans les hôpitaux et la longueur croissante des listes d'attente.  
Feu l'honorable juge Mark McGuigan, c.p., c.r. 
