Les pêches et les océans 
Les provinces maritimes-Le jugement de la Cour suprême confirmant les droits de pêche autochtones 
L'honorable Lowell Murray :
Honorables sénateurs, je vous demanderais de vous montrer un peu indulgents, le temps que je touche un mot sur la situation qui a cours dans le secteur des pêches sur la côte est. 
Aux dernières nouvelles, et je dois avouer que je m'en remets presque exclusivement aux bulletins de presse de ce matin, les chefs semblent avoir renoncé au moratoire de 30 jours qu'ils avaient consenti à respecter.  
Toujours d'après les comptes rendus de presse du débat qui a eu lieu à l'autre endroit hier soir, il semble que le ministre des Pêches, M. Dhaliwal, a été pris par surprise par cette nouvelle, tout comme le gouvernement est resté bouche bée, il y a quatre semaines, quand la Cour suprême du Canada a rendu sa décision.  
Loin de moi l'idée, honorables sénateurs, de me poser en expert juriste et encore moins en interprète du jugement de la Cour suprême du Canada, mais la déclaration faite par un des chefs ce matin, selon laquelle lui et ses collègues se réservent le droit de réglementer leurs propres activités de pêche, est, à mon humble avis, contraire au jugement rendu par la Cour suprême du Canada. 
Dans la décision majoritaire, le juge Binnie fait état de: 
... la portée limitée du droit issu du traité.  
Il écrit ceci: 
Le droit issu du traité est un droit réglementé qui peut, par règlement, être circonscrit à ses limites appropriées. 
Des limites de prises, dont il serait raisonnable de s'attendre à ce qu'elles permettent aux familles mi'kmaq de s'assurer une subsistance convenable selon les normes d'aujourd'hui, peuvent être établies par règlement et appliquées sans porter atteinte au droit issu du traité. 
Un tel règlement respecterait ce droit et constituerait pas une atteinte qui devrait être justifiée suivant la norme établie dans l'arrêt Badger .  
Honorables sénateurs, il me semble que ces déclarations sont une invitation lancée par la Cour suprême du Canada au gouvernement afin qu'il propose un règlement. 
Quand le juge fait référence au règlement, aux limites de captures et à l'imposition d'un règlement, il y a lieu de supposer qu'il parle d'un règlement qui serait proposé par le gouvernement.  
Il y a environ deux semaines, le premier ministre aurait dit que le gouvernement envisageait de demander à la Cour suprême de surseoir à son jugement. 
Plus récemment, le ministre des Pêches s'en est pris aux députés de l'opposition qui demandaient la même chose. 
Je suppose que le gouvernement a étudié et rejeté l'option de demander de surseoir au jugement et que cette option a été éliminée. 
Il me semble donc que tout ce qu'il reste à faire au gouvernement, c'est de présenter une mesure législative ou un règlement, ou les deux.  
En outre, le gouvernement devrait envisager de renvoyer le règlement en question directement à la Cour suprême pour veiller à ce qu'il soit bien compatible avec le jugement rendu par cette dernière. 
Je doute fort que les autochtones se satisfassent de moins de cela. 
N'importe quel règlement qui sera déposé sera obligatoirement contesté et devra passer par l'ensemble du processus judiciaire. 
J'estime prudent que le gouvernement renvoie un tel règlement à la Cour suprême du Canada.  
Honorables sénateurs, si je dis cela, c'est simplement parce que la situation s'aggrave de jour en jour. 
Sans vouloir être alarmiste, je pense que nous ne sommes que trop conscients des incidents violents qui se sont produits dans la région et sont à la veille de se produire. 
Un vide juridique a été créé. 
D'autres ont rempli ce vide créé par la Cour suprême du Canada et il me semble que ça va être le chaos.  
Les prix de l'affaire «Personne» pour 1999 
L'honorable Catherine S. Callbeck :
Honorables sénateurs, j'aimerais souligner que nous célébrerons le lundi 18 octobre prochain la Journée de la personne. 
C'est le 18 octobre 1929 que le Conseil privé britannique a statué que les femmes étaient des «personnes» en droit canadien et qu'elles pouvaient donc être élues au Sénat.  
Cette décision a été rendue à la suite d'une longue bataille juridique et politique menée par Emily Murphy, Nellie McClung, Louise McKinny, Irene Parlby et Henrietta Muir Edwards, cinq femmes canadiennes mieux connues comme étant les «Famous Five».  
Depuis 1979, en commémoration de l'affaire «Personne», des prix du Gouverneur général sont décernés chaque année aux alentours du 18 octobre pour honorer des femmes qui ont activement contribué à la promotion de la femme.  
Je suis fière de faire savoir aux honorables sénateurs qu'une femme de Tignish, en Île-du-Prince-Édouard, Anne-Marie Perry, sera parmi les cinq lauréates de cette année. 
Anne-Marie est une personne remarquable qui continue de contribuer activement au bien-être de sa communauté, de sa province et de son pays. 
C'est la deuxième femme de l'île à recevoir ce prix prestigieux. 
Helen Yeo, de Charlottetown, en Île-du-Prince-Édouard, l'a déjà remporté en 1986.  
Au niveau provincial, elle a défini et appuyé des changements de programmes en vue d'accroître les chances des femmes durant son mandat de membre et de présidente du comité des appels des services sociaux de l'Île-du-Prince-Édouard.  
Au niveau national, Mme Perry a été un membre actif du Conseil consultatif canadien de la situation de la femme de 1981 à 1986.  
Quatre autres femmes recevront le même honneur qu'Anne-Marie cette année. 
Il s'agit de Mmes Bertha Allen, d'Inuvik, dans les Territoires du Nord-Ouest, Maris Eriksen, de Calgary, en Alberta, Enid Page, d'Aylmer, au Québec et Bette Stephenson, de Richmond Hill, en Ontario.  
Honorables sénateurs, les cinq lauréates des prix du Gouverneur général de cette année méritent des félicitations pour le remarquable travail qu'elles ont accompli dans leurs communautés en vue de faire avancer la cause des femmes.  
Des voix:
Bravo!  
Son Honneur le Président :
J'ajouterais, pour l'information des honorables sénateurs, que la présentation des prix du Gouverneur général se déroule normalement à Rideau Hall et qu'elle est toujours suivie d'un dîner offert au Sénat par le Président du Sénat. 
Cette année, il a été décidé de présenter les prix à Calgary parce que c'était en Alberta qu'habitaient les cinq femmes de l'époque. 
La présentation sera donc faite lundi prochain à Calgary. 
Je représenterai le Sénat à la cérémonie et tous les sénateurs qui désirent y participer sont invités. 
Si vous désirez de plus amples détails à ce sujet, veuillez entrer en communication avec mon bureau.  
