Que le comité soit autorisé, nonobstant les pratiques habituelles, à déposer son rapport auprès du greffier du Sénat, si le Sénat ne siège pas, et que ledit rapport soit réputé avoir été déposé au Sénat.  
(La motion est adoptée.)  
Les travaux du Sénat 
Autorisation aux comités de siégerpendant les ajournements 
L'honorable Dan Hays (leader adjoint du gouvernement) :
Honorables sénateurs, avec la permission du Sénat et nonobstant l'alinéa 58(1) f ) du Règlement, je propose: 
Que, pour la durée de la présente session, les comités particuliers puissent se réunir pendant les ajournements du Sénat.  
Son Honneur le Président suppléant :
La permission est-elle accordée, honorables sénateurs?  
Des voix:
D'accord.  
Son Honneur le Président suppléant :
Plaît-il aux honorables sénateurs d'adopter la motion?  
Des voix:
D'accord.  
(La motion est adoptée.)  
(Le Sénat s'ajourne au mardi 2 novembre 1999, à 14 heures.)  
