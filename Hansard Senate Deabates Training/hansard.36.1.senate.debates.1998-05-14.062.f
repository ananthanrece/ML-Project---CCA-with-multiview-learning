La dette des pays en développement 
L'honorable Nicholas W. Taylor :
En 1996, la dette des pays en développement était de 1,5 billion de dollars américains. 
Plusieurs pays consacrent plus de 20 p. 100 de leur PIB rien qu'au service de leur part de cette dette. 
Le Fonds monétaire international a répertorié 41 pays «fortement endettés». 
Sur ces 41 pays, 32 sont en Afrique subsaharienne.  
(1410) 
Dans certains pays du monde, on ne peut pas, de façon réaliste, espérer que les habitants se serrent la ceinture pour résoudre le problème de la dette. 
Au contraire, la croissance requise pour commencer à rembourser ces dettes ne peut venir que d'investissements massifs dans le capital humain, physique et social.  
Il serait possible de trouver une solution profitable à tous, le prêteur et le créancier, en se souvenant que le Canada est à la fois un exportateur de matériel et aussi de capitaux. 
Les pays industrialisés exportent un pourcentage toujours plus grand de leurs produits vers les marchés du tiers monde. 
Il s'ensuit que des économies prospères dans ces parties du monde profiteraient aux entreprises canadiennes. 
Comme pour beaucoup d'autres choses dans la vie, nous pouvons bien faire en faisant de petits sacrifices maintenant, ou nous pouvons attendre les conséquences de l'inaction et en payer le prix plus tard.  
Deuxièmement, nous devons nous assurer que les pays en développement empruntent dans les meilleures conditions possibles. 
Les gouvernements de ces pays en développement devraient pouvoir appeler des experts qui soient indépendants du processus politique et des organismes internationaux de prêt. 
Autrement dit, honorables sénateurs, ils ont besoin d'experts financiers qui leur diront quels trucs employer, quelle devise rembourser en premier et, cela va sans dire, quel système utiliser pour contrôler la dette.  
