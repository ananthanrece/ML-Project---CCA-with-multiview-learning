DÉCLARATIONS DE SÉNATEURS 
La santé 
L'engagement d'un ancien candidat du Parti québécois au poste de président-directeur général de l'hôpital d'Ottawa 
L'honorable Philippe Deane Gigantès :
Honorables sénateurs, une chose scandaleuse se produit à Ottawa et on doit y mettre un terme.  
Je parle de la nomination de M. David Levine au poste de président-directeur général du nouveau centre hospitalier d'Ottawa. 
Des gens à Ottawa s'opposent ouvertement à cette nomination parce qu'il a un passé péquiste. 
Les péquistes font la même chose, mais nous ne sommes pas comme les péquistes. 
Nous ne devons pas imiter les mauvaises pratiques d'un gouvernement séparatiste qui demande quelles sont les convictions politiques d'un candidat avant de l'embaucher.  
J'ai déjà dit à une personne qu'elle avait remporté un concours. 
Elle m'a répondu: «Mais, sénateur, saviez-vous que je suis séparatiste?» 
Je lui ai dit: «Je vous embauche.» 
Elle a été loyale envers moi. 
C'est cela, la réalité canadienne, l'idéal canadien. 
S'attaquer à quelqu'un à cause de ses convictions politiques est indigne.  
L'ÉCONOMIE 
La croissance de l'activité économique dans l'Île-du-Prince-Édouard 
L'honorable Catherine S. Callbeck :
Dans leur rapport, ils soulignent qu'avec l'Alberta et la Colombie-Britannique, l'Île-du-Prince-Édouard a eu le plus fort taux net de démarrage de nouvelles entreprises dans lesannées 90. 
Une bonne partie de la croissance positive qu'on prévoit est reliée au pont de la Confédération. 
Cependant, dans le contexte de ce rapport, le pont n'est qu'un symbole des liens mondiaux qui sont établis et doivent être maintenus, étant donné ce qui se passe sur la scène économique, dans le monde entier. 
Les auteurs du rapport offrent quatre thèmes, auxquels je souscris pour bâtir la prospérité de l'île: la préparation au commerce, les compétences, les capacités commerciales et la responsabilité financière.  
En 1995 et 1996, lorsque je faisais partie d'Équipe Canada, j'ai eu l'occasion de voir l'importance des liens que nous tissons en tant que pays pour notre développement. 
L'accent mis sur les industries du savoir était particulièrement frappant. 
Comme le rapport le signale, nous sommes liés de plus en plus étroitement à d'autres peuples, cultures et économies et nous devrons être prêts à relever les défis qui nous attendent.  
Maintenant que nous améliorons nos liens avec le monde et le reste du Canada, j'invite tous les gens à venir voir par eux-mêmes cette évolution. 
L'été est une période particulièrement propice pour visiter l'île.  
Honorables sénateurs, il y a beaucoup d'autres choses à faire et à voir sur l'Île-du-Prince-Édouard que cette nouvelle croissance économique remarquable. 
L'année dernière, un million de personnes ont découvert la beauté de l'île en tant que destination touristique. 
Cette année, nous nous attendons à recevoir 1,4 million de personnes. 
J'invite tous mes honorables collègues au Sénat et à l'autre endroit, ainsi que les Canadiens, à venir profiter de notre hospitalité bien connue, cet été.  
