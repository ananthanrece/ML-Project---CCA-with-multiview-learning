La convention prévoit essentiellement que chaque État signataire criminalise la subornation de hauts fonctionnaires étrangers et prenne des mesures pour assurer la responsabilité des personnes légales, dont les sociétés, dans la subornation de hauts fonctionnaires étrangers. 
Lisez l'article 3, et vous verrez que c'est le clou du projet de loi dont vous êtes saisis.  
Cette disposition interdit la corruption d'un agent public étranger dans le cadre d'opérations commerciales. 
Quiconque commet pareille infraction est coupable d'un acte criminel passible d'un emprisonnement maximal de cinq ans.  
Le projet de loi décrit les paiements de facilitation qui ne constitueraient pas une infraction. 
Ces paiements sont des droits versés normalement pour la délivrance de permis et licences qui sont utilisés dans le contexte du commerce international, par exemple, des droits de transport.  
