La violence familiale 
Interpellation-Suite du débat 
L'ordre du jour appelle: 
Reprise du débat sur l'interpellation de l'honorable sénateur Carstairs, attirant l'attention du Sénat sur la violence familiale dans notre société et, en particulier, sur la nécessité de prendre des mesures coopératives pour trouver des solutions aux divers aspects de cette forme de violence.-( 
L'honorable sénateur Robertson) .  
L'honorable A. Raynell Andreychuk :
Honorables sénateurs, je félicite le sénateur Carstairs pour avoir fait cette interpellation et pour avoir parlé avec autant d'éloquence de la définition de la violence qui se fait sentir un peu partout dans notre société et d'avoir proposé des moyens possibles de régler ce problème.  
L'anniversaire, le 6 décembre, du massacre de l'École polytechnique à Montréal, et la propre tragédie qu'elle a vécue montrent que ce problème intéresse tous les Canadiens et que la clef de la solution est entre nos mains à tous. 
Je voudrais souligner certains points qu'a mentionnés le sénateur Carstairs.  
Pendant toutes les années au cours desquelles j'ai siégé dans des tribunaux de la famille, ou même dans des cours criminelles, la violence familiale a toujours été le domaine le moins compris et le plus horrible dans notre société.  
Après les dix années que j'ai passées dans les tribunaux de la famille, des dossiers horribles continuent de me hanter parce que je ne comprends toujours pas comment des crimes d'une telle cruauté peuvent se produire. 
Combien de fois ai-je vu des photos d'enfants et même des enfants en personne qui avaient été brûlés par des cigarettes, dont les bras avaient été tordus, qui avaient été battus ou qui avaient été privés de nourriture?  
Lorsque l'enfant est de nouveau entré et que la mère a tenté de lui retirer les mitaines qui étaient restées collées dans la pommade, la peau s'est littéralement arrachée des mains gelées du petit, le mutilant à jamais.  
J'avais l'habitude de rentrer chez moi en espérant que ce ne soit que des incidents isolés, en priant pour que ce soit le cas. 
Cependant, tous les jours et toutes les semaines, il y avait un autre cas, une autre situation, et une autre vie d'enfant détruite.  
J'ai vu des femmes qui étaient mariées, qui vivaient en union de fait ou qui étaient sans attache être dégradées, battues et violées par leur conjoint ou ami, et, comme le sénateur Carstairs l'a signalé, j'ai vu des hommes avilis et, dans quelques cas isolés, battus par de prétendus êtres chers.  
Pour ceux qui penseraient qu'il s'agit d'un phénomène canadien ou nord-américain, je tiens à dire que mes années à l'étranger en Afrique et en Europe m'ont prouvé que nous ne sommes pas les seuls à vivre la violence. 
Dans beaucoup de pays, ces questions sont cachées, et les gouvernements et les dirigeants communautaires n'en parlent pas et ne reconnaissent pas le phénomène.  
Je suis heureuse qu'on cherche, au Canada, à relever ces questions, car les racines de la violence sont nombreuses et elles se retrouvent souvent dans l'histoire, la culture et les attitudes des gens. 
Il n'y a pas une seule raison qui explique la violence et même lorsqu'on peut identifier la cause de la violence, il est extrêmement complexe, coûteux et difficile de proposer des solutions.  
Ainsi, une approche multidisciplinaire est nécessaire. 
Je suis heureuse de soutenir la campagne de la Prairie Action Foundation pour appuyer le Prairie Research Network qui est établi en Saskatchewan, au Manitoba et en Alberta et met l'accent sur une recherche pratique et très large, marquée au coin de la collaboration, sur la prévention de la violence et des mauvais traitements, sur l'évaluation des solutions traditionnelles et l'élaboration de nouvelles méthodes et sur la sensibilisation de la population pour mettre un terme à la conspiration du silence dans nos foyers, nos écoles et nos milieux de travail.  
Je félicite le sénateur Carstairs pour son dévouement et sa persévérance et d'autres qui ont persisté malgré tout pour former une nouvelle alliance dans les prairies relativement à cette initiative.  
Il y a deux questions sur lesquelles on doit se pencher si nous voulons pouvoir réaliser des progrès dans la prévention de la violence familiale. 
Il est important de briser le cycle de la violence. 
Très souvent, la violence sert d'arme parce que c'est une arme qu'on a utilisée précédemment contre l'agresseur. 
Dans un très grand nombre de cas dont je me suis occupé et où il y avait eu un excès de discipline, une enquête sur les parents a permis de déterminer que la violence faisait partie de l'éducation des enfants. 
Une intervention est nécessaire pour mettre un terme à cette acceptation répétée de la violence comme mode de comportement. 
Même si c'est plus facile à dire qu'à faire, des stratégies pour intervenir, pour venir en aide aux intéressés et pour modifier les comportements sont nécessaires. 
Il faut donner aux victimes le soutien et l'aide nécessaires pour qu'elles aient la force d'éviter ou de surmonter les gestes violents à leur endroit ou de quitter leurs agresseurs.  
La mise en oeuvre doit passer par les lois, l'éducation et des ressources communautaires. 
Les comportements violents ne doivent pas être tolérés, cachés ou excusés.  
Ce qui m'étonne, c'est que bien que nous rejetions la violence par la voix de nos dirigeants politiques et de nos éducateurs, nous contribuons néanmoins à une culture de violence et nous l'encourageons.  
Grâce aux organisations comme International Alert, qui s'intéresse aux enfants plongés dans des situations de conflit, notamment les guerres et les insurrections civiles, et grâce à l'éloquent rapport de Mme Machel, des Nations Unies, qui traite des enfants utilisés comme soldats, lequel rapport a été diffusé dans le monde entier, nous sommes amenés à condamner aisément ces actions au nom des enfants et la dévastation qu'elles leur causent puisqu'elles les privent de tout espoir de voir de véritables et précieux efforts visant à promouvoir la co-existence pacifique dans leurs sociétés.  
Dans notre propre société, la culture dominante actuelle encourage la violence. 
Il suffit de suivre un match de hockey pour voir le comportement des parents ou observer les altercations physiques qui l'emportent sur les prouesses sportives dans notre sport national.  
Il suffit de consulter Internet, d'écouter le rap et de regarder les vidéos et les films qui passent actuellement sur le grand ou le petit écran pour constater que la violence est omniprésente dans notre société. 
Trop souvent, on associe la force brutale et le pouvoir dans notre société.  
Ce qui m'inquiète, c'est le nombre grandissant de très jeunes enfants qui usent de la violence à toutes les sauces et commettent des actes d'agression et d'intimidation dans les cours de récréation.  
Au fur et mesure que ces nouvelles technologies s'implantent, nous finirons peut-être par en observer les effets secondaires. 
Si, en tant que membres de la société canadienne, nous disons non à la violence familiale, nous allons devoir joindre le geste à la parole.  
À l'occasion de l'anniversaire du massacre de l'École polytechnique, à Montréal, nous devrions méditer toutes affaires cessantes sur notre comportement et notre dévouement envers la cause.  
J'ose espérer que, dans une faible mesure, cette interpellation, initiée par le sénateur Carstairs, saura nous inciter à briser le cercle du silence et à rejeter la culture de la violence.  
(Sur la motion du sénateur Kinsella, au nom du sénateur Robertson, le débat est ajourné.)  
Affaires juridiques et constitutionnelles 
Autorisation au comité de siéger en même temps que le Sénat 
L'honorable Lorna Milne , conformément à l'avis qu'elle a donné le 2 décembre 1998, propose: 
Que le comité sénatorial permanent des affaires juridiques et constitutionnelles soit autorisé à siéger le lundi 7 décembre 1998, même si le Sénat siège à ce moment-là, et que l'application du paragraphe 95(4) du Règlement soit suspendue à cet égard.  
L'honorable Noël A. Kinsella (chef adjoint suppléant de l'opposition) :
Honorables sénateurs, j'aimerais qu'on m'explique la motion.  
Le sénateur Milne :
Honorables sénateurs, le comité des affaires juridiques et constitutionnelles a planifié depuis longtemps de siéger toute la journée lundi. 
Quand il a été annoncé que le Sénat siégerait ce jour-là, je ne me suis pas inquiétée étant donné que quand nous siégeons le lundi, nous siégeons normalement à 20 heures. 
Toutefois, quand on nous a annoncé que le Sénat siègerait à 16 heures pour tenir compte des projets de Noël du Parti conservateur, que j'appuie totalement, la comparution du nouveau solliciteur général devant le comité, qui avait été convenue antérieurement, a posé un problème.  
En général, je n'approuve pas que les comités siègent en même temps que le Sénat mais, comme cette comparution était prévue d'avance et que le ministre ne peut pas venir à un autre moment, je demande que le comité soit autorisé à siéger même si le Sénat siège à ce moment-là.  
Le sénateur Kinsella :
Je remercie le sénateur Milne de cette explication. 
Je suis d'accord pour que le comité ait l'autorisation de siéger même si le Sénat siège à ce moment-là. 
Toutefois, je ne donnerai mon accord que si c'est pour entendre le ministre et rien d'autre.  
Si nécessaire, honorables sénateurs, je présenterai un amendement à cette motion. 
Ce ne sera pas nécessaire si nous sommes d'accord.  
Son Honneur le Président suppléant :
Vous plaît-il, honorables sénateurs, d'adopter la motion?  
Des voix:
D'accord.  
(La motion est adoptée.)  
