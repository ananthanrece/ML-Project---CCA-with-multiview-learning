Dans aucune société, ni au nord, ni au sud, ni à l'est, il n'est acceptable que les dirigeants garnissent leurs goussets au détriment du public. 
Une fois cette simple vérité acceptée, il y aura un développement social et économique plus réel.»  
Le général Obasanjo, vous vous en souviendrez, a été ensuite escorté dans les prisons nigérianes, où il a passé un bon bout de temps. 
Le Canada a joué un rôle important au sein du Commonwealth pour réclamer des changements en profondeur au Nigeria, et il a joué beaucoup plus récemment un rôle dans la libération du général. 
Celui-ci reste actif au sein de Transparence internationale à titre de président du comité consultatif et fait figure pour nous de symbole des opinions du tiers monde sur la corruption.  
Il n'est pas sans importance que le monde en développement observe de près le problème de la corruption et les solutions qu'on y apporte dans le monde industrialisé. 
Je puis vous dire que des observateurs du monde entier s'intéressent vivement à ce qui se passe au Sénat aujourd'hui et s'intéresseront à ce que les Communes, je l'espère, feront la semaine prochaine.  
Transparence internationale s'inquiète de la corruption. 
Elle a résolu ces cinq dernières années d'imposer ce problème à l'opinion et d'en saisir les institutions internationales ainsi que des gouvernements comme celui du Canada, afin de susciter une action pour combattre ce fléau.  
Il vaut la peine d'expliquer pourquoi nous nous inquiétons de la corruption. 
De nombreux dirigeants de Transparence internationale, au départ, provenaient de la Banque mondiale. 
Certains ont pris une retraite précoce de cette institution afin de faire de la corruption une problème public à un moment où la Banque mondiale ne s'en souciait pas. 
Cela les préoccupait pour trois raisons. 
D'abord, surtout dans les pays en voie de développement, la corruption fausse la politique gouvernementale. 
Elle porte les gouvernements à prendre des décisions qui ne sont pas dans l'intérêt public, mais plutôt dans celui des gens qui toucheront les pots-de-vin lorsque la politique entrera en vigueur et que des acquisitions seront effectuées. 
Nous savons maintenant que c'est bien le cas. 
La recherche a établi très clairement que cela a un tel effet de distorsion.  
Un des effets importants qu'a la corruption, c'est de faire passer l'argent de ce que nous appelons le côté doux de l'économie, c'est-à-dire la santé, l'éducation et le bien-être social, vers des projets comme les routes et les dépenses militaires comme les ponts et ainsi de suite. 
Il est beaucoup plus facile de distribuer des pots-de-vin quand on s'occupe de ce genre de projets que lorsqu'on s'occupe de santé ou d'éducation. 
Voilà un des effets de distorsion importants qui s'observent dans les pays plus pauvres.  
Le second effet, qui est également important, et nous le perdons souvent de vue dans notre partie du monde, c'est que la corruption fait baisser la qualité des biens et des services fournis par les entreprises du secteur privé pour honorer leurs contrats. 
On trouve l'argent soit en roulant les pays avec lesquels on a un contrat, soit en réduisant la qualité des biens et des services que l'on offre en retour pour les contrats. 
Cela est un fait établi et a une incidence importante sur la qualité des services que les habitants des pays en voie de développement obtiennent pour leurs impôts et aussi pour l'aide internationale que leurs gouvernements reçoivent.  
Enfin, et c'est une chose très importante, la corruption sape le gouvernement. 
Elle sape le développement d'institutions politiques et démocratiques compétentes. 
Quand ces institutions sont en cours de développement, la corruption en bloque le développement. 
C'est un problème qui a été reconnu par l'Agence canadienne de développement international, pour citer un exemple. 
Le bon gouvernement est un des outils dont nous faisons maintenant la promotion au Canada en y voyant un moyen de commencer à remédier au problème de la corruption. 
La convention de l'OCDE représente le deuxième outil en importance à cet égard.  
Transparence internationale est une coalition internationale de lutte contre la corruption qui compte ou comptera bientôt des sections dans 70 pays du monde entier. 
Notre organisation est authentiquement internationale. 
Notre secrétariat international s'attache à monter l'opinion publique internationale contre la corruption et y arrive parfaitement. 
Nous collaborons dans le monde entier avec diverses institutions internationales comme la Banque mondiale, le FMI et les Nations Unies à mettre sur pied des programmes de lutte contre la corruption. 
Nous organisons aussi des missions vers des pays dont les gouvernements nous demandent de les aider à lutter contre la corruption au plan national. 
C'est une tâche dont nous nous acquittons très bien.  
Nous organisons des congrès internationaux de lutte contre la corruption afin de donner l'occasion aux divers pays de se réunir pour discuter des problèmes que pose la corruption.  
