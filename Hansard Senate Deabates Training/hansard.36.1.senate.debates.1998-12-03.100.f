La Journée nationale de commémoration 
Neuvième anniversaire de la tragédie à l'École Polytechnique 
L'honorable Joyce Fairbairn :
Honorables sénateurs, nous commémorons le neuvième anniversaire d'un acte de violence contre des femmes tellement choquant et tragique qu'il est devenu depuis un symbole d'action dans le pays tout entier.  
Le Massacre de Montréal, comme on l'appelle, s'est produitle 6 décembre 1989, un jour où comme à l'ordinaire les étudiants de l'École polytechnique assistaient à leurs cours ou se retrouvaient à la cafétéria.  
Leur vie a été bouleversée par un homme tellement perturbé par ses propres problèmes qu'il est entré dans une classe avec une arme semi-automatique, a écarté les hommes et, en proclamant sa haine des féministes, a tué 14 jeunes femmes. 
Il s'est tué lui-même après avoir mis fin à leurs espoirs et à leurs aspirations.  
Nommons-les encore une fois:
Des femmes et des hommes se joindront à eux en fin de semaine pour assister à des cérémonies qui se tiendront partout au pays afin de donner plus de poids à notre engagement de mettre un terme à la violence dans notre société.  
Le triste héritage que nous laissent ces jeunes femmes, c'est l'adoption d'une Journée nationale de commémoration et d'action contre la violence faite aux femmes. 
Comme toujours, nous examinons les statistiques, espérant qu'elles se seront améliorées, mais sachant qu'elles demeurent désespérément élevées - qu'il s'agisse de la violence sexuelle, psychologique ou physique, des agressions, du harcèlement ou du meurtre.  
Des femmes de tout âge, enfants ou personnes âgées, sont constamment la cible de violences. 
Nous avons appliqué un contrôle des armes à feu; nous avons rendu plus sévères beaucoup de peines prévues par la loi; nous avons créé des refuges; nous avons sensibilisé la population par tous les moyens possibles, mais c'est clair que nous sommes loin d'avoir changé les attitudes ainsi que les conditions sociales et économiques qui favorisent la crainte, l'insécurité et le désespoir qui, à leur tour, engendrent la colère et la violence.  
Honorables sénateurs, il ne s'agit pas seulement des femmes. 
Il importe que nous tous, en cette enceinte, femmes et hommes, profitions de toutes les occasions que nous offre notre situation privilégiée pour appuyer les solutions et inciter au changement.  
La seule tolérance que nous devrions avoir est la tolérance zéro contre toutes les formes de violence dans un pays que nous voulons prévenant et attentif. 
Ces 14 jeunes femmes, honorables sénateurs, nous diraient de laisser faire les excuses et de nous mettre au travail.  
Des voix:
Bravo!  
