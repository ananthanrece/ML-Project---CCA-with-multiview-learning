Elle mine le principe de bonne gouvernance, menace la stabilité des institutions démocratiques et sape les fondements moraux de la société. 
La corruption fausse le commerce international et la libre concurrence, et elle entrave le développement économique, en particulier dans les pays en voie de développement.  
Le Canada tente activement, avec ses partenaires de l'OCDE, d'encourager les systèmes globaux de sécurité et d'amélioration de la condition humaine et de resserrer les liens du commerce, afin de pouvoir aider les peuples à s'épanouir et à prospérer. 
On est de plus en plus conscient que la meilleure manière de défendre nos intérêts nationaux consiste à les défendre au sein des institutions et tribunes internationales et à bâtir les règles et les institutions qui permettront aux Canadiens et aux Canadiennes d'obtenir le genre de protection dont ils ont besoin.  
La corruption des agents publics est l'un des principaux problèmes du commerce et de l'investissement international. 
Au sein de l'OCDE, le Canada et d'autres organisations internationales y attachent beaucoup d'importance. 
L'organisation, qui regroupe 29 membres dont le Canada, les États-Unis, la plupart des pays européens, le Japon et la Corée du Sud, est la principale tribune de politique économique des démocraties industrialisées les plus avancées.  
En vertu de la convention, chaque partie, chaque pays est tenu de criminaliser la corruption d'agents publics étrangers dans la conduite des transactions internationales et de prendre les mesures nécessaires, conformément à ses principes juridiques, pour établir la responsabilité des personnes morales en cas de corruption d'un agent public étranger.  
Chaque partie, donc chaque pays, doit veiller à ce que les sanctions soient «efficaces, proportionnées et dissuasives», et à ce que la corruption et le produit de la corruption puissent être saisis. 
La gamme des sanctions doit être comparable à celle établie pour la corruption interne. 
Ainsi, la nouvelle infraction de corruption d'agents publics étrangers est un acte criminel punissable d'une peine d'emprisonnement maximale de cinq ans. 
L'article 67.5 de la Loi de l'impôt sur le revenu a été modifié également pour ajouter cette nouvelle infraction à la liste des infractions du Code criminel auxquelles renvoie cette disposition, empêchant ainsi que des pots-de-vin payés aux agents publics étrangers soient déductibles d'impôt.  
La convention exige par ailleurs que les parties veillent à ce que les pots-de-vin et les produits de la corruption d'un agent public étranger puissent être saisis et confisqués, et que les parties envisagent d'imposer d'autres sanctions d'ordre civil ou administratif. 
La nouvelle infraction de corruption d'agents publics étrangers est une infraction de criminalité organisée autorisant la perquisition, la saisie et la détention de produits du crime, et elle est aussi considérée comme l'infraction principale sur une accusation de blanchiment de produits du crime.  
On trouve par ailleurs dans la convention des dispositions d'exécution, d'entraide juridique et d'extradition auxquelles le Canada peut déjà satisfaire. 
Si leur système juridique le permet, les parties doivent également fournir de l'aide judiciaire en matière pénale et civile. 
Lors des négociations, le Canada a déclaré qu'il pouvait assister d'autres États en matière pénale mais qu'il n'était pas en mesure de le faire en matière civile.  
Au Sommet du G-8, à Birmingham, le Canada et les États membres du G-8 ont promis de faire tous les efforts possibles pour ratifier la convention de l'OCDE d'ici la fin del'année 1998. 
La communauté canadienne des affaires considère la convention de l'OCDE comme étant l'un des plus importants accomplissements réalisés jusqu'à maintenant dans la campagne internationale de lutte contre la corruption. 
Cette convention est considérée comme une opportunité de créer un environnement dans lequel les compagnies canadiennes pourront être en compétition sur la base de la qualité, des prix et des services puisqu'elle limitera la capacité des concurrents étrangers à faire usage de la corruption pour décrocher des contrats.  
Jusqu'à maintenant, le Japon et l'Allemagne ont déposé leurs instruments de ratification auprès du secrétaire général de l'OCDE. 
Présentement, les États-Unis et le Royaume-Uni ont complété leurs procédures internes et devraient déposer les leurs dans les prochaines semaines.  
Cinq des dix plus grands pays de l'OCDE doivent avoir ratifié la convention avant la fin de l'année 1998, afin que celle-ci puisse entrer en vigueur. 
On espère que le Canada, en tant que chef de file de la lutte contre la corruption, sera le cinquième pays à ratifier la convention.  
