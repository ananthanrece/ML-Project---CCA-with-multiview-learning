Projet de loi modificatif-Deuxième lecture 
L'ordre du jour appelle: 
Reprise du débat sur la motion de l'honorable sénateur Moore, appuyée par l'honorable sénateur Lucier, tendant à la deuxième lecture du projet de loi C-51, Loi modifiant le Code criminel, la Loi réglementant certaines drogues et autres substances et la Loi sur le système correctionnel et la mise en liberté sous condition.  
L'honorable Ron Ghitter :
Honorables sénateurs, je prends la parole pour la deuxième lecture de ce projet de loi omnibus. 
Cette mesure comporte des modifications importantes à trois lois. 
Comme c'est normalement le cas des mesures omnibus, il n'y a pas grand-chose qui soit controversé. 
Les modifications ont l'appui des milieux juridiques, politiques et policiers. 
Seul le Parti réformiste semble trouver ces modifications inacceptables, parce qu'il estime qu'il aurait dû y avoir des peines beaucoup plus sévères.  
La raison de cela c'est qu'avec les progrès de la médecine, une personne blessée lors d'un crime peut vivre bien plus d'un an et un jour, même si l'on peut prouver que sa mort éventuelle est le résultat de l'acte criminel.  
Il serait intéressant de savoir combien de cas semblables ont permis à des personnes d'éviter des poursuites. 
Il n'y en a sans doute pas beaucoup, mais le comité qui étudie le projet de loi pourrait peut-être s'informer. 
Quoi qu'il en soit, cette modification est valable et je l'appuie.  
Le projet de loi modernise les dispositions sur le vol et la possession de minéraux précieux; il prévoit des règles sur l'exécution de l'ordonnance de sursis en cas de manquement à l'une de ses conditions; il élargit le champ d'application de l'infraction relative à l'obtention de services sexuels de personnes âgées de moins de 18 ans et il ne permet l'exécution de mandat de perquisition que par les personnes chargées de l'exécution de la loi. 
Ce sont là certaines des mesures contenues dans le projet de loi.  
Cette disposition vise seulement les personnes qui ont les moyens de voyager à bord de ces bateaux coûteux et luxueux. 
Aussi, je ne suis guère préoccupé par la perspective de voir ces personnes perdre leur revenu disponible aux tables de jeux de ces navires.  
J'ai cependant des réserves au sujet des dispositions qui autorisent le jeu de dés dans les casinos exploités par les provinces. 
J'ai une certaine connaissance du jeu de dés, qui a été immortalisé Guys and Dolls . 
Le jeu de dés est un jeu rapide. 
On peut y gagner et y perdre de l'argent très rapidement, mais on y perd surtout. 
Ce jeu exige une certaine habilité et une connaissance que la plupart des joueurs n'ont pas.  
Je n'ai pas l'intention de moraliser en parlant des méfaits des jeux de hasard. 
L'Alberta vient tout juste de mettre un terme, en octobre dernier, à un pénible débat à l'issue duquel une majorité d'Albertains ont décidé, à la suite de mois de pétitions, de débats et de réflexions, de conserver les appareils de loterie vidéo. 
Cette question met cependant en évidence des vérités indiscutables. 
Les gouvernements sont tout aussi dépendants du jeu de hasard que ceux qui jouent au blackjack dans les casinos ou qui utilisent les machines de loterie vidéo dans les restaurants et les bars. 
En Alberta, les appareils de loterie vidéo rapportent entre 700 et 800 millions de dollars par année et les autres formes de jeux de hasard rapportent plus de 2 milliards de dollars.  
Alors que les pauvres s'appauvrissent davantage et que leurs familles en souffrent, en fin de compte, l'argent reçu par le gouvernement va d'une poche à l'autre pour tenter d'aider ceux qui dépendent de l'assistance sociale à cause de leurs pertes au jeu. 
Je dois dire que je crois fondamentalement que la responsabilité individuelle doit toujours s'appliquer et qu'il n'incombe pas au gouvernement d'établir ses politiques en fonction d'une minorité qui est incapable d'assumer ses responsabilités. 
Lorsque les gouvernements commencent à dire aux gens ce qui convient le mieux pour eux, et à assumer des responsabilités qui incombent aux gens à titre individuel, un problème se pose alors, car où cela s'arrête-t-il et qui dit que le gouvernement fait ce qui convient le mieux?  
Je tiens à assurer la Chambre que ces modifications ne visent pas à accroître l'ampleur du jeu au Canada, pas plus que nous prévoyons qu'elles auront cet effet.  
Avec tout le respect que je dois à la ministre, permettez-moi de dire que non seulement il y aura un accroissement du jeu au Canada du fait qu'on permettra les jeux de dés dans les casinos, mais qu'il y aura également un accroissement des pertes des gens qui vont jouer dans nos casinos. 
Affirmer le contraire reflète un manque de compréhension des qualités mystiques et de l'attrait qu'ont ces dés. 
Vous savez tout comme moi que l'expression «coup de dés», au sens politique, a certes causé une certaine fureur dans certains cercles politiques au Canada dans le passé.  
Je soulève cette question car on peut se demander: où nous arrêtons-nous, si nous le faisons? 
Jusqu'à quel point le gouvernement devrait-il permettre les jeux au Canada? 
C'est une question morale difficile et importante sur laquelle le comité chargé d'examiner ce projet de loi devra se pencher.  
