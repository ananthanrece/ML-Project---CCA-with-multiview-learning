Il y a quelques années à peine, le mot «corruption» n'était tout simplement pas prononcé en bonne compagnie et certainement pas en public. 
Personne n'osait le faire sur la scène internationale ou sur la scène nationale.  
La position de la Banque mondiale et d'autres institutions du genre, comme le Fonds monétaire international, consistait à dire que la corruption était essentiellement un problème culturel ou politique. 
En général, on estimait que ce problème touchait les pays du tiers monde et que ceux-ci devaient le régler eux-mêmes. 
On jugeait également que la corruption n'entraînait aucune difficulté économique, surtout pas dans le monde industrialisé. 
Elle servait tout au plus à lubrifier la machine économique, lorsqu'il existait des obstacles bureaucratiques.  
Transparence internationale est un organisme qui a été créé il y a cinq ans essentiellement pour changer cette perception. 
Ces opinions, à notre avis et de l'avis des fondateurs de Transparence internationale, sont extrêmement préjudiciables et inexactes. 
La position de Transparence internationale est fort bien résumée par le premier président de l'organisme et l'actuel président du conseil consultatif de Transparence internationale, le général Obasanjo, dont certains d'entre vous reconnaissent peut-être le nom. 
