Le développement des ressources humaines 
Le programme de prêts d'études-La proposition de hausser les primes versées aux banques 
L'honorable Erminie J. Cohen :
Honorables sénateurs, je m'interroge sur la proposition du gouvernement de hausser les primes versées aux banques dans le cadre du Programme national de prêts aux étudiants.  
On a dit que cette proposition aurait pour effet d'accroître de 100 millions de dollars la contribution du gouvernement fédéral. 
Cette mesure ne fait que traiter un symptôme d'un problème beaucoup plus grand. 
Je ne comprends pas pourquoi le gouvernement est si prompt à répondre aux préoccupations de nos riches banquiers alors qu'il fait si peu pour remédier à l'incapacité des étudiants de rembourser leurs emprunts.  
Depuis 1993, le gouvernement a amputé de 6 milliards de dollars les fonds destinés à l'éducation. 
Malgré la modeste augmentation prévue à cet égard dans le budget de l'année dernière, les universités et les collèges ont toujours du mal à répondre aux besoins de leurs étudiants en essayant de faire plus avec moins de ressources. 
Pour compenser les moins-perçus en revenus, les frais de scolarité ont grimpé en flèche et les étudiants sont forcés d'emprunter encore davantage. 
Les étudiants canadiens, après avoir complété un cycle d'études de quatre ans, figurent parmi les plus endettés au monde avec une dette moyenne de 25 000 $. 
Il est clair, honorables sénateurs, que ce n'est pas un bon départ pour nos jeunes qui entrent dans la vie active. 
Cet endettement inévitable dissuade les étudiants, surtout ceux de milieux à faible revenu, d'entreprendre des études.  
Vous en conviendrez sûrement, honorables sénateurs, ce montant de 100 millions de dollars serait dépensé à bien meilleur escient s'il servait à accroître les programmes de bourses et à augmenter les paiements de transfert aux provinces. 
Cela permettrait d'accroître l'accessibilité aux études et de faire en sorte que les étudiants n'accumulent pas des dettes aussi élevées et puissent entreprendre leur carrière en partant du bon pied.  
Uniquement au cours du mois dernier, le gouvernement a pris plusieurs décisions contestables en ce qui concerne l'argent des contribuables. 
En parcourant le pays avec le groupe de travail du Parti progressiste-conservateur sur la pauvreté, je suis devenue plus consciente du problème et je ne saurais trop insister sur la nécessité de décisions de principe capables de briser les cycles contribuant à la pauvreté. 
Il est encore temps pour le gouvernement de repenser cette proposition et d'investir l'argent là où cela s'impose, c'est-à-dire pour former nos jeunes et les préparer à relever les défis qu'ils auront à affronter dans le siècle qui commence.  
Martin Luther King junior 
L'honorable Donald H. Oliver :
Je suis l'un de ces Canadiens. 
Ce fut une expérience marquante. 
Je fus à la fois motivé et inspiré par ces propos. 
Ils renforcèrent ma détermination de réaliser mes rêves en dépit des obstacles qui se dressaient sur mon chemin.  
L'intensité de son action en sa qualité de leader du mouvement pour la défense des droit civiques aux États-Unis témoigne du dévouement de M. King à la cause de l'égalité et de la justice pour tous les êtres humains, sans égard à leur race. 
Pendant les années 50 et 60, une année typique de manifestations et de protestations le voit sillonner les États-Unis et faire plus de200 discours, exploit épuisant que Martin Luther King junior ne semble jamais chercher à esquiver.  
Pendant cette période, il est attaqué, poignardé et lapidé alors qu'il fait la promotion des droits civiques auprès de la population américaine. 
En 1963, il est emprisonné pendant 11 jours à Birmingham, en Alabama, pour avoir manifesté, défiant une injonction du tribunal, contre la ségrégation dans les grands magasins et contre leur politique d'embauche injuste. 
En 1965, il est de nouveau emprisonné, cette fois à Selma, toujours en Alabama, pour avoir protesté contre les pratiques discriminatoires d'inscription des électeurs. 
Malgré tout cela, il trouve le temps de publier plusieurs livres et, en 1964, il devient le plus jeune lauréat du prix Nobel de la paix. 
Aujourd'hui, presque 32 ans après son assassinat,le 4 avril 1968, l'héritage de Martin Luther King est toujours d'actualité. 
Dans nos coeurs, nous n'en sommes pas encore au point où il aurait voulu que nous soyons, le point où tous les êtres humains sont jugés d'après leur personnalité et non la couleur de leur peau.  
En conclusion, cette année, en l'honneur de M. King, j'encourage tous les honorables sénateurs à se joindre à moi pour faire la promotion de ces principes ici, au Sénat, et parmi les gens que nous représentons. 
Notre rôle à la recherche de cette paix véritable exige que nous nous servions de notre charge de sénateur pour promouvoir une justice véritable.  
