L'honorable Shirley Maheu :
Honorables sénateurs, je désire apporter aujourd'hui ma contribution au débat sur le projetde loi S-2, présenté en cette Chambre par le sénateur Carstairs.  
Mes collègues ont exprimé leurs sentiments sur cette question délicate et ils ont manifesté leur appui au projet de loi. 
Pour la majorité d'entre eux, la compassion surtout justifiait leurs interventions en faveur de ce projet de loi. 
Je suis d'accord avec eux et je crois aussi que des mesures devraient être mises en place pour alléger la douleur au moment de la mort. 
Un professionnel de la santé ne devrait pas être coupable d'une infraction criminelle quand il donne à un malade des doses de médicament qui risquent d'abréger la vie de ce dernier si, et seulement à cette condition, son objectif est de soulager la douleur, et non de causer la mort. 
Je suis d'avis que cela fait partie de bons soins palliatifs et que cette méthode devrait être encouragée.  
Je crois aussi, conformément à la décision rendue dans l'affaire Nancy B , que les professionnels de la santé ont l'obligation de respecter le droit du malade de refuser ou de retirer son consentement à un traitement médical essentiel au maintien de la vie. 
Il serait alors normal qu'ils n'aient pas à faire face à des accusations d'infractions criminelles, quand ils agissent selon la volonté du malade.  
Ceci étant dit, je crois, honorables sénateurs, que nous devons être prudents dans la rédaction d'une législation portant sur un sujet aussi délicat. 
C'est pourquoi j'ai lu avec grande attention le projet de loi S-2. 
Il m'apparaît toutefois évident que ce projet de loi est incomplet. 
Il est aussi clair dans mon esprit que plusieurs des dispositions qui y sont énoncées risquent de poser des problèmes d'interprétation et d'application.  
J'aimerais premièrement porter mon attention sur l'article 2 du projet de loi. 
Cet article énonçant le principe de la non-culpabilité du soignant dans les cas de traitements de la douleur peut être difficile à appliquer de façon concrète.  
En effet, il peut paraître très facile, de façon théorique et juridique, de déterminer l'intention du soignant. 
Cependant, dans la pratique et dans les faits, la situation est toute autre. 
Je crains qu'une telle mesure ne serve de camouflage à des actes d'euthanasie qui resteront impunis, faute de pouvoir prouver les intentions du soignant.  
