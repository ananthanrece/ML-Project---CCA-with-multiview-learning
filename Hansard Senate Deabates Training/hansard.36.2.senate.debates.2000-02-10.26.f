Les peuples autochtones 
Demande de réponse au rapport du comité sur les anciens combattants autochtones 
( Réponse à la question posée par l'honorable sénateur Andreychuk le 16 décembre 1999 ) 
À la suite du rapport déposé en 1995 par le comité sénatorial permanent des peuples autochtones sur les anciens combattants autochtones, le gouvernement du Canada a examiné les réclamations de tous ceux, venus de partout au pays, qui ont témoigné devant le comité. 
Il a examiné en détail tous ses dossiers concernant l'application, dans leur cas, des dispositions de la Loi sur les terres destinées aux anciens combattants . 
À la lumière de cet examen, nous avons conclu qu'ils ont bénéficié des avantages auxquels ils avaient droit en vertu de cette loi.  
Le gouvernement a communiqué les résultats de cet examen au greffier du comité sénatorial permanent. 
Des représentants du ministère des Affaires indiennes et duNord canadien et du ministère des Anciens combattantsse sont également présentés devant le comité sénatorialle 17 mars 1998 pour répondre aux questions de ses membres.  
Le gouvernement de Canada continue de souligner l'importante contribution des anciens combattants autochtones et poursuit les discussions déjà amorcées sur les questions qui les concernent. 
En outre, depuis le dépôt du rapport, le gouvernement a mis sur pied plusieurs projets à cet effet.  
Le dernier de ces projets en date, le Projet pour le nouveau millénaire, vise à rendre un hommage tout spécial aux anciens combattants autochtones en l'an 2000.  
