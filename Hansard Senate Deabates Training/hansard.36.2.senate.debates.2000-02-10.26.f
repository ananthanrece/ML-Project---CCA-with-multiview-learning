Le professeur Eike-Henner Kluge a proposé ceci: 
Au cas où la vie d'une personne sera ou est susceptible d'être abrégée par le recours à des soins palliatifs faisant appel à des médicaments ou à d'autres moyens semblables et où la vie est abrégée d'une durée dépassant ce que l'on peut normalement espérer lorsque des soins palliatifs appropriés et reconnus sont prodigués, le dossier devrait être revu par un organisme indépendant composé d'un médecin (n'ayant aucun lien avec l'une ou l'autre des parties en cause), d'un membre du bureau du procureur général de la province ou du territoire où la mort est survenue et d'un membre indépendant provenant de la population et ayant reçu une formation en éthique.  
Honorables sénateurs, l'article 3 du projet de loi me préoccupe beaucoup. 
Comme je l'ai déjà mentionné, je suis d'accord avec le principe général de cet article, qui vient confirmer le droit d'un patient de refuser l'administration d'un traitement de survie. 
Toutefois, le libellé du paragraphe 2 b ) de cet article, où l'on édicte de quelle façon la demande d'abstention ou d'interruption du traitement doit être formulée, me laisse songeuse. 
Je suis d'avis que cet article est incomplet et qu'il ouvre la porte à de nombreux abus.  
Il est prévu que la demande du patient doit être «libre et éclairée». 
L'article 4 définit en ces termes une «demande libre et éclairée»: 
Le projet de loi ne prévoit toutefois aucun mécanisme pour garantir que la décision du patient est conforme à cette définition. 
Ne devrait-on pas mettre en place une forme de contrôle pour garantir que le patient a pris sa décision volontairement et qu'il n'a pas fait l'objet de quelque contrainte que ce soit, imposée par lui-même ou par quelqu'un d'autre?  
L'histoire relatée par Mme Rodney est un exemple flagrant de ce genre de situation. 
Cette infirmière a comparu devant le comité spécial du Sénat sur l'euthanasie et l'aide au suicide et a raconté l'histoire d'un diabétique de 76 ans qui était limité dans sa mobilité physique et qui avait besoin de dialyse à long terme.  
Un jour, il a dit à l'équipe qui lui dispensait des soins qu'il ne voulait plus subir de dialyse. 
L'équipe a appris qu'il voulait mettre un terme à ses traitements parce qu'il avait l'impression de devenir un fardeau de plus en plus lourd pour sa femme. 
Lorsqu'on a mis en place un nouveau programme de soins prévoyant davantage de soutien à domicile pour les aider, lui et sa femme, il est revenu sur sa décision de cesser ses traitements. 
Il a vécu quatre autres années.  
Honorables sénateurs, cette histoire nous montre que les demandes d'interruption de traitements de survie ne doivent pas être prises à la légère. 
La situation n'est pas toujours évidente et la décision n'est pas toujours aussi volontaire qu'elle peut le paraître. 
Le projet de loi devrait inclure une série de modalités encadrant les demandes relatives aux traitements de survie. 
J'espère que le comité qui étudiera le projet de loi se penchera sur cette question.  
La demande de cessation de traitement devrait être répétée au moins une deuxième fois et un délai minimum de 48 heures devrait être respecté entre les deux demandes. 
Un processus d'enquête auprès du patient devrait être prévu dans le projet de loi afin de s'assurer que sa volonté de cesser les traitements est réellement libre et éclairée, et qu'elle ne résulte pas plutôt d'une dépression passagère.  
Honorables sénateurs, toujours à l'article 3, paragraphe 2 b ), il est mentionné que la volonté du patient, quant à l'interruption des traitements de survie, pourrait se faire, entre autres, par signes, et en présence d'au moins un témoin qui n'est pas un soignant. 
Cette façon de faire risque de créer, à mon avis, plusieurs problèmes d'interprétation.  
En effet, il pourrait être hasardeux d'interpréter les signes de certains patients. 
Un risque d'erreur pourrait causer la mort de patients qui ne sont pas prêts à mourir. 
Cette situation pourrait, par la suite, inciter des patients ayant perdu l'usage de la parole à ne pas exprimer leurs besoins de peur que leur geste soit mal interprété. 
Je crois aussi que l'alinéa c ) du même article devra être clarifié lorsqu'il sera étudié en comité.  
On y énonce que le conjoint, le compagnon ou le parent qui est le plus intimement lié au patient pourrait demander l'interruption de soins de survie si celui-ci n'a plus la capacité de formuler une telle demande et qu'il n'y a pas de représentant légal désigné pour prendre en son nom les décisions en matière de soins de santé.  
La situation risque d'être particulièrement problématique si les enfants d'une même famille ont des opinions divergentes sur les soins qui doivent être dispensés à leurs parents. 
Cette situation a été vécue par une famille d'amis. 
L'un des enfants voulait que les traitements de survie soient interrompus et l'autre voulait que ces traitements soient maintenus. 
Les deux enfants étaient très près de leur mère et j'ai été obligée de prendre la décision finale. 
Ce fut une situation très difficile.  
Enfin, j'espère que, au comité, on étudiera de près la définition de «traitement de survie». 
Je ne suis pas d'accord pour que cette définition dise que l'hydratation et l'alimentation artificielles sont des traitements de survie. 
Selon moi, l'alimentation et l'hydratation sont des besoins élémentaires, et non des traitements. 
Nous n'avons pas le droit de laisser une personne mourir de faim. 
Considérer l'hydratation et l'alimentation artificielles comme des traitements médicaux est peut-être une étape vers la légalisation de l'euthanasie et du suicide assisté. 
Si cette définition était adoptée dans un texte de loi, elle permettrait aux services de santé de faire indirectement ce qu'ils ne peuvent faire directement. 
Il ne faut pas ouvrir la porte à ces pratiques.  
On pourrait me répondre que certaines de mes préoccupations seront traitées lors de l'établissement de lignes directrices nationales, comme le suggère l'alinéa a ) de l'article 6. Je crois toutefois qu'il serait préférable que nous adoptions un projet de loi complet afin de laisser le moins de trous noirs et ainsi, éviter tout risque d'abus.  
Voici donc quelles sont mes inquiétudes quant au projetde loi S-2. 
J'ai soulevé plusieurs questions et j'espère que mes réflexions seront utiles au comité qui sera chargé d'étudier ce projet de loi.  
Le projet de loi S-2, qui devra faire preuve de compassion à l'égard des gens qui souffrent, ne devra pas ouvrir la porte à une législation encourageant l'euthanasie et l'aide au suicide. 
J'ose espèrer que cet équilibre sera atteint.  
