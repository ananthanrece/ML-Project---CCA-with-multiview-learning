Les affaires étrangères 
La Russie-Le conflit en Tchétchénie 
L'honorable Francis William Mahovlich :
Honorables sénateurs, je désire vous faire part de ma visite récente à l'Assemblée parlementaire du Conseil de l'Europe. 
Pendant ce voyage, le Canada était représenté par des députés et des sénateurs. 
Au cours de nos rencontres, je n'ai pas eu l'occasion de participer au débat visant à déterminer si les Russes devraient être exclus du conseil ou autorisés à continuer d'en faire partie. 
Je traiterai aujourd'hui de cette question.  
En décembre dernier, j'ai passé dix jours en Russie. 
J'ai constaté que de nombreux changements s'étaient produits depuis mon premier séjour au début des années 70. 
Des signes positifs de l'instauration d'une nouvelle démocratie commencent à se manifester. 
Notre délégation a été accueillie de façon fort obligeante, et le traitement dont nous avons fait l'objet a de loin dépassé nos attentes. 
De fait, pendant nos déplacements autour de Moscou, on pouvait se demander si une guerre était en cours en Tchétchénie et si la population était adéquatement informée.  
Je partage les vues exprimées par les députés au cours de cette semaine, qui ont dit que l'on ne peut mettre fin au terrorisme en adoptant un comportement de terroriste. 
Le Canada condamne les tactiques militaires russes adoptées dans ce tragique conflit. 
Nous condamnons le fait que l'on mette en danger la vie de soldats conscrits dans une guerre qui ne peut être gagnée et dans le cadre de laquelle tout le monde est perdant. 
Le Canada exige donc un cessez-le-feu immédiat et l'amorce d'un dialogue dans les meilleurs délais avec les représentants élus du peuple tchétchène.  
Le Canada prie instamment la Fédération russe de respecter les engagements qu'elle a pris au sommet d'Istanbul de l'Organisation pour la sécurité et la coopération en Europe, afin de permettre à l'OSCE de contribuer au règlement politique de la crise en Tchétchénie dans le cadre de négociations pacifiques. 
Nous exhortons toutes les parties intéressées à respecter les droits de la personne. 
Le Canada insiste expressément pour que les civils, les journalistes, les préposés à l'aide humanitaire et les fournitures puissent circuler librement.  
Lorsque le président de l'assemblée, le lord Russell Johnston, exige que le Conseil de l'Europe intervienne rapidement, les mesures préconisées par le Canada comptent parmi celles qui orientent les décisions.  
Dans un esprit d'amitié avec les Russes et de coopération démocratique, nous espérons sincèrement que le gouvernement russe accueillera favorablement les mesures préconisée par cette assemblée.  
