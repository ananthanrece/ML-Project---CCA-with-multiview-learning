L'Ontario 
Wiarton-L'influence de Wiarton Willie sur la communauté 
L'honorable Lorna Milne :
Honorables sénateurs, j'ai entendu avec grand plaisir prononcer le nom de «Wiarton Willie» de l'autre côté de la Chambre, hier. 
Il semble que Mike Harris ait lu le hansard du Sénat et qu'il s'en prenne lui aussi à Wiarton Willie, tout en donnant au passage une taloche au Sénat.  
Permettez-moi de vous parler de Wiarton, en Ontario. 
Cette magnifique localité de 2 300 habitants est située à l'extrémité de la péninsule Bruce en Ontario, sur les rives de la baie Georgienne et à proximité de l'Escarpement du Niagara. 
On y trouve un microclimat unique puisque, à la différence des communautés environnantes dont le climat est tempéré par les eaux des Grands Lacs, Wiarton connaît un climat extrêmement froid en hiver. 
La plus grande partie des emplois étant saisonniers, lorsque le service de traversier de Chi-Cheemaun ferme pour l'hiver, de nombreux emplois disparaissent aussi. 
Cette situation et les grands froids forcent de nombreuses personnes à quitter la ville chaque hiver pour aller chercher de l'emploi ailleurs.  
Que signifie pour la ville l'aide qu'a apportée DRHC à «Wiarton Willie», cette gentille petite marmotte albinos? 
En plus d'aider à égayer les esprits au milieu de l'hiver, une subvention de 50 000 $ par année permet de donner de l'emploi à deux jeunes gens. 
Le site Web que ces jeunes ont mis sur pied reçoit des dizaines de milliers de visites chaque année. 
Des millions de dollars sont versés à l'économie locale, ce qui permet de créer des emplois. 
Cette année, 10 000 personnes ont visité Wiarton le 2 février pour voir Willie prédire six semaines supplémentaires de climat canadien.  
Le défilé de mode de Wiarton Willie a permis d'amasser 860 $ qui ont servi à l'achat d'un numériseur pour l'hôpital de Wiarton. 
Les magasins locaux ont rapporté que leurs ventes avaient atteint un niveau inégalé au cours de cette fin de semaine du 2 au 6 février.  
Il semble donc que chaque dollar de la subvention de DRHC a été utilisé à bon escient pour aider une ville dont la main-d'9uvre est principalement saisonnière à maintenir sa population et qu'il a permis à au moins deux jeunes de l'endroit de s'assurer un bel avenir. 
La subvention de DRHC accordée à Wiarton Willie à titre de capital de lancement a remporté un franc succès.  
