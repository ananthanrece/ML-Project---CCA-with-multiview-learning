Tel est le cas du projet de loi C-9, Loi portant mise en vigueur de l'Accord définitif nisga'a.  
Le traitement ou, faudrait-il dire, le mauvais traitement réservé aux autochtones remonte avant la Confédération et a débuté au moment de la prétendue première découverte pour se poursuivre avec l'occupation, par des États européens, de terres désormais connues sous le nom de Canada, qui vient du mot autochtone «Kanata», qui signifie «lieu de rencontre».  
Qui, dans cette Chambre, ou qui, au Canada, peut nier qu'un des chapitres les plus sombres et les plus affligeants de l'histoire de l'Amérique du Nord et de l'Amérique du Sud est le traitement que nous avons réservé aux autochtones? 
Cette loi s'est révélée à la fois raciste et limitative.Sir 
John A. Macdonald, père de la Confédération, espérait que les «Indiens», surnommés «Peaux-Rouges», seraient assimilés grâce à ces politiques d'isolement, puis d'assimilation.  
Les Églises, leur zèle missionnaire et leurs écoles faisaient partie du problème. 
Le ministère était soutenu par le pouvoir et le prestige du gouvernement fédéral et de ses homologues provinciaux, toutes ces mesures ayant été lancées par des colons et des entrepreneurs avaricieux.  
Pendant des décennies, le traitement des autochtones est allé de mal en pis. 
Les autochtones ont même été privés des droits à la citoyenneté. 
Dans les années 60, le gouvernement fédéral, par l'entremise de la commission Hawthorn-Tremblay, a défini le problème essentiellement en termes économiques et a recommandé l'autonomie économique des autochtones, aussi rapidement que possible, pour assurer un traitement égal à tous les autochtones en tant que citoyens.  
En 1969, le livre blanc du gouvernement, présenté au cours du mandat de l'actuel premier ministre, alors ministre des Affaires indiennes et du Nord, a ouvert un nouveau chapitre, demandant à la fois un traitement égal et une action positive. 
La recherche active d'une solution moderne était commencée. 
Elle est devenue un élément actif du discours public.  
En 1982, la Charte des droits a propulsé le débat public encore plus loin. 
Les articles 25 et 35 reconnaissaient, sans les définir, les droits et les traités des peuples autochtones. 
Ce n'était que juste; ce n'était que convenable.  
Trop peu de Canadiens se rappellent que le Canada a échappé à l'intégration aux États-Unis au cours de la guerre de 1812. 
C'est le grand chef indien Tecumseh et sa confédération qui, se joignant aux soldats britanniques et canadiens, ont repoussé l'invasion américaine dans le Haut-Canada. 
Tecumseh est mort durant la bataille contre les envahisseurs américains, près de la rivière Thames, non loin de London, Ontario, ma ville natale. 
Il a quitté le territoire américain et grimpé vers le nord et les terres canadiennes pour se joindre à la bataille contre les Américains, parce qu'on lui avait promis un traitement équitable des revendications autochtones et de meilleures perspectives que celles qu'offraient ou pratiquaient les Américains.  
Le Canada a une énorme dette sociale et historique envers les autochtones, d'où le désir de justice économique et politique. 
La création du nouveau Territoire du Nunavut, l'an dernier, était un pas dans cette direction.  
Comme le sénateur Austin l'a si éloquemment illustré durant son discours complet et détaillé en faveur du projet de loi, à l'étape de la deuxième lecture, après des années de négociation, le gouvernement de la Colombie-Britannique et les Nisga'as de la vallée de la Nass sont parvenus à une entente qui réglait les revendications territoriales et reconnaissait une forme d'autonomie gouvernementale très différente et distincte de tout ce que l'on avait connu jusqu'à maintenant au Canada. 
Depuis longtemps, cette petite bande de quelque 6 000 personnes pratique sa propre forme d'autonomie gouvernementale axée sur la communauté.  
Personne ne peut nier qu'un renouveau s'impose dans le dossier des autochtones. 
Il y a maintenant 80 tables de négociation au pays et les revendications étudiées visent plus de 10 p. 100 du territoire canadien. 
Le ministre à l'autre endroit a déclaré que ce règlement ne constituait pas un précédent. 
Cependant, hier, lors d'un discours touchant, éloquent, passionné et convaincant, le sénateur Gill a dit que d'autres autochtones se serviraient de ce règlement.  
Examinons un peu le mode de gouvernement nisga'a. 
En y regardant de près, nous avons découvert certains éléments absolument uniques et différents. 
Ils sont à ce point différents que la plupart des Canadiens ne les ont ni compris ni intégré. 
J'ai trouvé que le virage dans la dialectique sur les solutions aux questions autochtones s'est produit lors de la présentation du rapport Hawthorn-Tremblay.  
Les idées égalitaires à la base du livre blanc de 1969 et de la Charte de 1982 ont commencé à changer de façon spectaculaire durant les débats sur les accords ratés du lac Meech et de Charlottetown. 
La Cour suprême du Canada s'est jointe au débat public en rendant certaines décisions, comme dans l'affaire Calder , dans l'espoir qu'elles permettraient d'apporter des éclaircissements et d'accélérer les solutions.  
Dans le traité nisga'a, les Nisga'as ont substantiellement réduit au cours des négociations la portée de leurs revendications territoriales et autres en échange de la reconnaissance d'une nouvelle forme, d'une forme différente de gouvernement doté de pouvoirs légaux.  
Dans le traité nisga'a, il est fait une distinction entre un citoyen nisga'a et un non-Nisga'a résidant sur des terres nisga'as. 
En vertu de la Constitution nisga'a, seuls les citoyens nisga'as jouissent du plein droit de vote et du droit de jouir économiquement des fruits d'un règlement. 
Seuls les Nisga'as peuvent définir la citoyenneté nisga'a. 
Il y a eu ici une délégation de pouvoirs hors de la portée des futurs gouvernements fédéraux. 
En fait, les articles 25 et 35 de la Charte de 1982 reconnaissent les droits ancestraux ou issus de traités des peuples autochtones et affirment que rien ne portera atteinte aux droits non encore définis à l'époque. 
La question n'est pas seulement de savoir si le gouvernement fédéral a le pouvoir de créer, sans modification constitutionnelle, un troisième ordre de gouvernement auquel les futurs gouvernements fédéraux ne pourraient pas toucher. 
Ces questions ont fait et continuent de faire l'objet de débats animés en regard des articles 25 et 35. 
Ces questions sont source de division sur le plan constitutionnel. 
Même si elles résistaient à un examen judiciaire, est-ce ainsi que nous concevons l'unité canadienne en ce XXIe siècle où les distances entre les pays disparaissent graduellement? 
Nous n'avons pas encore tiré les dures leçons du XXe siècle en ce qui a trait au choc entre l'ethnicité et la citoyenneté à part entière.  
Il s'agit d'un partage, d'un partenariat. 
Plus nous serons ce que nous sommes et plus il y aura d'ouverture entre nous. 
L'identité distincte n'oblige pas à séparer les cultures: ce serait plutôt le contraire. 
Son «ethnicité» fait partie de la réalité positive.  
Qui peut le contester? 
Or, si on lit attentivement, comme je l'ai fait, l'accord nisga'a, la loi et la Constitution nisga'as, on se rend compte que la question de la citoyenneté nisga'a est laissée entièrement à la discrétion des Nisga'as eux-mêmes et qu'elle échappe aux principes de la Charte. 
À l'instar du sénateur Gill, je souhaite que la définition de «citoyenneté» ne soit pas «ethnique». 
Je crains cependant qu'en voulant enfin rendre justice aux autochtones, ce qui constitue un noble objectif, et relever leur situation qui attendait depuis longtemps de l'être, nous n'ayons involontairement créé des enclaves «ethniques féodales» à statut spécial et constituées de deux catégories de citoyens, ce qui va à l'encontre de la notion supérieure d'égalité de tous les citoyens canadiens.  
Honorables sénateurs, cette mesure législative pose un défi exigeant et un problème délicat et difficile à résoudre. 
Après avoir examiné en profondeur le traité, la Constitution nisga'a, la loi et les cinq volumes du rapport de la Commission royale d'enquête sur les peuples autochtones, je ne suis pas sûr si mes principales préoccupations au sujet du projet de loi portent sur les principes ou sur les clarifications. 
J'ai l'intention de m'abstenir à l'étape de la deuxième lecture et d'examiner attentivement les témoignages recueillis par le comité des affaires autochtones qui, je le sais, examinera en profondeur ces préoccupations et les autres questions que je me pose.  
