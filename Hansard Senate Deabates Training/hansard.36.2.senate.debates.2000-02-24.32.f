Présentation du quatrième rapport du comité 
L'honorable Bill Rompkey , président du comité permanent de la régie, des budgets et de l'administration, présente le rapport suivant: 
Le mardi 22 février 2000 
Le Comité permanent de la régie interne, des budgets et de l'administration a l'honneur de déposer son 
QUATRIÈME RAPPORT 
Votre Comité recommande l'adoption d'un budget supplémentaire de 1 200 000 $ pour l'exercice 1999-2000. 
Ce montant est un report de crédits de fonctionnement. 
Tout comme les ministères fédéraux, le Sénat est autorisé à reporter jusqu'à 5 p. 100 des fonds approuvés qui n'ont pas été dépensés les années précédentes.  
Ces crédits supplémentaires sont demandés pour absorber des dépenses imprévues. 
Comme le budget principal est préparé de 12 à 18 mois d'avance, ces crédits serviront à payer les études spéciales nécessitant des déplacements qui seront entreprises par les comités du Sénat.  
Ils serviront également à payer des frais de personnel liés aux services de recherche dans les bureaux de sénateurs et au service de publication des Débats du Sénat.  
