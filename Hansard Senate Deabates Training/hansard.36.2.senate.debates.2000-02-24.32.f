La santé 
Les nominations au conseil d'administration de l'Initiative sur la santé de la population canadienne 
( Réponse à la question posée par l'honorableErminie J. Cohen le 10 février 2000) 
L'Initiative sur la santé de la population canadienne (ISPC) est un projet de recherche national sur les politiques, orienté sur la santé de la population, qui fait partie de l'initiative appelée Carnet de route de l'information sur la santé, annoncée dans le budget fédéral de février 1999. 
L'initiative est gérée par l'Institut canadien d'information sur la santé (ICIS); le conseil d'administration de l'ISPC a été mis sur pied en tant que comité du Conseil de l'ICIS. 
Le conseil d'administration offre leadership et coordination à l'ISPC afin de l'aider à réaliser sa vision: créer les connaissances, favoriser la compréhension des Canadiens en ce qui concerne les questions de santé et les déterminants influant sur celle-ci et favoriser la compréhension de la recherche sur les politiques menant à l'amélioration de la santé et du bien-être des Canadiens. 
Le premier conseil d'administration compte onze membres, dont un président, choisis parmi des Canadiens chevronnés représentant divers intérêts dans le domaine de la santé de la population et les régions du Canada.  
Les membres, érudits et dignes de foi, ayant des connaissances et des compétences particulières ont été choisis en fonction de leurs contributions dans des domaines reliés à la santé de la population et aux principales fonctions de l'ISPC. 
Ces membres viennent de divers milieux, notamment des universités et des instituts de recherche; des secteurs public, privé et bénévole; du monde de l'éducation et des organismes non gouvernementaux. 
En choisissant le groupe, on a voulu représenter les diverses régions du Canada et assurer l'équilibre entre les groupes travaillant en recherche et les décideurs. 
On a tenu à ce que les femmes, les autochtones et les francophones soient représentés.  
Le modèle du conseil d'administration de l'ISPC exige une représentation régionale plutôt qu'une représentation de chaque province ou territoire. 
Toutefois, il permet de relier divers intervenants des provinces et des territoires à un réseau pancanadien de compétences en matière de recherche et d'analyse sur la santé de la population. 
Parmi les projets en cours, le Atlantic Center for Policy Research de l'université du Nouveau-Brunswick et la Population Health Research Unit de l'Université Dalhousie sont des centres de recherche très actifs dans le réseau de l'ISPC. 
Le but de l'ISPC est de faire participer au réseau, au cours des prochains mois, d'autres centres de recherche de l'ensemble des territoires et des provinces, dont l'Alberta.  
