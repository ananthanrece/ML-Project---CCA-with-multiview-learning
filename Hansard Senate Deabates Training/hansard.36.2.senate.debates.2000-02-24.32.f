Dans notre cas, cela s'élève approximativement à 1,2 million de dollars de dépenses imprévues en plus de notre Budget principal des dépenses. 
Cette demande figurera donc dans le Budget supplémentaire du gouvernement, le livre bleu, comme un report du budget de fonctionnement. 
Il s'agit de fonds non utilisés au cours des années précédentes, dont nous demandons maintenant le report.  
Le Budget supplémentaire sert à financer deux dépenses fondamentales imprévues en 1999-2000: premièrement, les dépenses, y compris les frais de déplacement, encourues par les comités sénatoriaux pour un certain nombre d'études spéciales et, deuxièmement, les coûts de personnel du service de recherche pour les sénateurs et de la Direction des débats du Sénat. 
Comme les honorables sénateurs le savent, ces deux choses sont liées.  
Les comités sénatoriaux sont vitaux pour l'examen de questions d'importance primordiale et pour l'étude et l'élaboration des options politiques du gouvernement fédéral. 
C'est en dehors d'Ottawa que nos comités font souvent le travail le plus efficace. 
Honorables sénateurs, permettez-moi de vous donner quelques exemples. 
Le comité des banques et du commerce a tenu des audiences à Toronto en avril dernier en vue d'étudier le système financier canadien et, en particulier, le financement par actions des petites entreprises. 
Le comité mixte d'examen de la réglementation s'est rendu à l'étranger en juillet. 
Le comité des pêches tiendra des réunions au Nouveau-Brunswick et aux Îles-de-la-Madeleine en février et en mars, ainsi qu'en Colombie-Britannique, du 27 au 31 mars. 
Je crois également comprendre qu'ils se rendra plus tard à Terre-Neuve et, ensuite, encore beaucoup plus tard, au Labrador qui, bien sûr, comme vous le savez, est une masse terrestre fort différente. 
Terre-Neuve, c'est cette petite île située au sud du Labrador, ce qui fait que le comité des pêches doit se rendre aux deux endroits.  
Le comité de l'énergie et des ressources naturelles et celui des banques et du commerce se sont déplacés. 
Ces déplacements n'étant pas prévus, nous n'avons pu en tenir compte dans notre Budget principal des dépenses. 
C'est pourquoi des fonds supplémentaires sont nécessaires.  
Lorsque j'ai parlé du Budget des dépenses devant le Sénat en ma qualité de président du comité de la régie interne en février dernier, j'ai insisté sur le fait que notre budget comprenait un certain nombre de postes dont les fonds pourraient être insuffisants en cas d'accroissement de l'activité législative. 
Ce sont donc là deux postes où nous devons combler un déficit.  
La Direction des débats non plus n'a pas assez de fonds. 
Par conséquent, des crédits supplémentaires d'environ 127 000 $ s'imposent en vue de combler ce déficit. 
Je suis sûr que les gens du hansard seront heureux de l'apprendre.  
Dans le but d'accroître la justification de l'emploi des fonds, la transparence, l'économie et l'utilisation judicieuse de l'argent des contribuables, nous fonctionnons avec des budgets prévoyant des dépenses minimales et de très modestes hausses, alors que les coûts continuent de croître. 
Chacun veut que l'on en fasse plus avec moins, et c'est ainsi qu'il devrait en être. 
Nous prévoyons des dépenses réduites au strict minimum nécessaire pour fonctionner avec efficacité et servir au mieux les Canadiens. Toutefois, nous devons avoir un fonctionnement efficace et servir les Canadiens, et cela entraîne des frais.  
C'est donc dire que nous devons recourir au budget supplémentaire des dépenses pour affecter des fonds aux postes qui n'avaient pas été prévus ou qui n'avaient pu être planifiés avec certitude. 
Parallèlement, nous devons continuer à mieux planifier nos activités. 
L'an prochain, nous avons l'intention d'établir un budget encore plus réaliste afin que, dans l'avenir, il ne soit plus nécessaire de demander des crédits supplémentaires.  
Honorables sénateurs, le comité de la régie interne recommande l'adoption d'un Budget supplémentaire des dépenses de 1,2 million de dollars pour l'exercice 1999-2000.  
