Adoption du cinquième rapport du comité 
Le Sénat passe à l'étude du cinquième rapport du comité permanent de la régie interne, des budgets et de l'administration (prévisions budgétaires du Sénat pour 2000-2001) présenté aujourd'hui au Sénat.  
Au cours des cinq dernières années, des crédits supplémentaires ont été nécessaires pour couvrir les dépenses non prévues et les budgets insuffisants des comités et de la recherche. 
Des crédits législatifs ont également été nécessaires pour combler le manque pour les frais estimés de déplacement, et cela a été la même chose en 1999-2000. 
En raison du coût du combustible et d'autres facteurs, les frais de déplacement ont augmenté de 80 p. 100 au cours des deux dernières années; il faut donc prévoir cela.  
L'an dernier, quand je me suis levé au Sénat pour déposer le budget de 1999-2000, j'ai déclaré sincèrement que je craignais que des fonds insuffisants ne soient alloués à certains postes, ceux des comités et de la recherche, entre autres, et qu'il faudrait des Budgets supplémentaires des dépenses pour y remédier.  
À moins de circonstances vraiment inhabituelles, il n'y aura pas lieu de recourir à des Budgets supplémentaires des dépenses avec le budget de 2000-2001. 
Ce budget prévoit une hausse des dépenses de seulement 1,3 p. 100 par rapport aux dépenses réelles de 1998-1999.  
À l'instar de la population que nous servons, nous essayons de plus en plus de tirer le maximum de nos dollars et d'utiliser au mieux les ressources affectées. 
Mais ce budget est important à d'autres égards. 
Il traduit un changement d'approche, de sorte que notre processus budgétaire est plus rationnel et plus transparent, même si l'obligation de rendre compte est plus grande.  
Depuis plusieurs années maintenant, le Sénat participe spontanément et de plein gré aux programmes de restriction des dépenses publiques. 
Nous avons restreint nos dépenses tout en assumant des activités additionnelles se traduisant surtout par un niveau sans précédent de travaux fort utiles et visibles des comités sénatoriaux.  
À l'heure actuelle, les membres du comité sénatorial permanent de la régie interne, des budgets et de l'administration se penchent sur l'idée fondamentale voulant que le Sénat ait des exigences opérationnelles précises pour lesquelles il faut affecter des fonds appropriés. 
On pourrait croire que ce principe va de soi, mais on n'en a pas toujours tenu compte dans le monde complexe et souvent obscur de la comptabilité publique. 
Nous sommes d'avis que le Budget principal des dépenses proposé pour cette année fait exactement cela - prévoir un niveau de financement minimal, mais nécessaire pour le travail que nous accomplissons. 
Nous croyons que c'est logique, transparent et responsable.  
Honorables sénateurs, la valeur va au-delà des chiffres. 
Elle est le reflet de la substance. 
Le Sénat contribue beaucoup au processus public au Canada. 
De nombreux sénateurs sont aujourd'hui des spécialistes des questions sociales, économiques et culturelles. 
Les sénateurs promeuvent activement la sensibilisation à des questions qui sont chères à leurs concitoyens. 
Ils le font par leur travail individuel, par leurs délibérations au Sénat et, de plus en plus, par les travaux des comités.  
En dépit de la prorogation de la première session de la présente législature, nos comités ont été très actifs. 
Les comités sénatoriaux ont tenu 265 audiences, ont entendu près de800 témoins en 473 heures et ont publié 80 rapports au cours de la dernière année seulement. 
Les comités ont apporté13 amendements à cinq projets de loi ministériels. 
Les Canadiens qui connaissent le Sénat savent aussi qu'il offre un excellent rapport qualité/prix. 
Le budget proposé sera sagement dépensé.  
Honorables sénateurs, je vous demande d'appuyer l'adoption de ce rapport.  
Son Honneur le Président :
Plaît-il aux honorables sénateurs d'adopter la motion?  
Des voix:
D'accord.  
(La motion est adoptée, et le rapport est adopté.)  
