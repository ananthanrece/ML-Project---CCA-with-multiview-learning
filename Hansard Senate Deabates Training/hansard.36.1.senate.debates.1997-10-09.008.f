L'Association médicale canadienne 
Félicitations à l'occasion de son 130e anniversaire 
L'honorable Stanley Haidasz :
Honorables sénateurs,le 9 octobre 1867, trois mois après la Proclamation royale de la Confédération du Canada, 167 médecins des quatre provinces qui constituaient alors le Canada se sont réunis dans la ville historique de Québec pour fonder l'Association médicale canadienne. 
L'inspirateur de cette rencontre et le «père de l'association» était le docteur William Marsden. 
Le premier président de l'Association médicale canadienne fut Sir Charles Tupper, un éminent médecin et politicien de la Nouvelle-Écosse qui allait devenir, en 1896, premier ministre du Canada.  
Aujourd'hui, l'Association médicale canadienne célèbre ses 130 ans, ce qui en fait l'une des plus anciennes associations au Canada. 
L'AMC est le porte-parole de la médecine organisée au Canada. 
Elle joue un rôle de leadership auprès des médecins et fait la promotion de l'excellence dans le domaine des soins de santé aux Canadiens. 
Honorables sénateurs, c'est pour moi un honneur, en tant que médecin, d'annoncer cet anniversaire au Sénat. 
J'espère que tous les honorables sénateurs se joindront à moi pour offrir notre profonde reconnaissance, nos félicitations et nos meilleurs voeux aux médecins du Canada et à leur association.  
La Coalition contre la violence à l'égard des femmes du Nouveau-Brunswick 
L'honorable Erminie J. Cohen :
Honorables sénateurs, j'ai le plaisir de vous faire part d'un important projet innovateur lancé dans ma ville, Saint John, au Nouveau-Brunswick.  
Le projet «Making Waves» a été conçu et mis en oeuvre par un comité spécial, qui regroupait plusieurs organisations bien connues du Nouveau-Brunswick dont les activités consistent à lutter contre la violence à l'égard des femmes. 
Il s'agit du Sexual Assault Centre de Fredericton, du Saint John Women's Centre et de la New Brunswick Coalition of Transition House/Emergency Shelters for Women.  
Ce projet fait appel à une approche innovatrice pour une plus grande sensibilisation et pour la recherche de solutions au grave problème de la violence dans les fréquentations, un problème répandu. 
Il consiste, à l'aide du manuel «Making Waves», spécialement conçu à cet effet, à entraîner les élèves du secondaire à parler à leurs pairs de la violence dans les fréquentations, des relations saines et du cycle de la violence. 
Il apporte aux jeunes le soutien et l'aide dont ils ont besoin pour mettre fin aux relations de violence. 
C'est un projet qui est aussi efficace. 
En l'espace de seulement an, honorables sénateurs, les séminaires, les ateliers et les exposés organisés dans le cadre de ce projet ont permis d'atteindre plus de 5 000 étudiants, et la demande augmente.  
J'ai été très heureuse d'appuyer la demande du comité spécial afin d'obtenir des fonds du fédéral pour la poursuite de cet important programme et j'ai appris récemment avec joie que le comité s'était vu octroyer une subvention de 50 000 $ de la part de Condition féminine Canada. 
Ces fonds vont permettre au comité spécial d'étendre le programme «Making Waves» aux écoles secondaires francophones, en partenariat avec les Dames de l'Acadie.  
Honorables sénateurs, je félicite le gouvernement - en particulier Condition féminine Canada et le ministère du Solliciteur général - du soutien qu'ils manifestent à l'égard d'un problème longtemps négligé. 
Je crois que des projets de ce genre méritent l'appui des sénateurs.  
La pauvreté chez les enfants au Nouveau-Brunswick 
L'honorable Brenda M. Robertson :
Honorables sénateurs, le moment est bien choisi, au cours de la Semaine nationale de la famille, pour attirer votre attention sur le drame des enfants qui vivent dans des familles pauvres au Nouveau-Brunswick. 
Je ne perds pas espoir puisque le gouvernement a reconnu l'utilité d'investir dans les premières années de vie des enfants pour assurer la prospérité future de notre pays. 
J'appuie pleinement les mesures prises par les gouvernements fédéral et provinciaux pour collaborer plus efficacement sur le chapitre de la pauvreté chez les enfants. 
Je n'ai que des éloges à faire au ministre du Développement des ressources humaines et à ses homologues provinciaux quant au travail qu'ils ont entrepris pour la conception d'un programme national concernant les enfants, afin d'améliorer les conditions de vie des petits Canadiens. 
L'argent ne représente qu'une partie de la solution. 
Les enfants ont besoin qu'on leur consacre beaucoup de temps et d'attention pour se développer sainement. 
Ils grandissent non seulement dans un foyer, mais également en société. 
Ils ont besoin de familles fortes et de collectivités sans danger, de bonnes écoles, de bons soins de santé et de possibilités de s'épanouir.  
Honorables sénateurs, le ministre a cité des éléments importants dans la vie des enfants, mais il y en a un autre, peut-être encore plus critique pour l'amélioration de leur sort, c'est l'emploi. 
Leurs parents doivent avoir un bon emploi bien rémunéré et stable.  
Plus de 49 000 personnes reçoivent des prestations d'assurance-emploi et, chaque année, environ 100 000 présentent une demande. 
Un taux de chômage élevé, une dépendance prononcée à l'égard de l'assurance-emploi et de l'aide sociale, un revenu hebdomadaire moyen qui a augmenté de 4,08 $ seulement au cours des trois dernières années, comparativement à une augmentation de 26,62 $ à l'échelle nationale, et le taux d'aide sociale le plus bas au pays; voilà les véritables obstacles qui nous empêchent d'assurer aux enfants du Nouveau-Brunswick un bon départ dans la vie.  
Honorables sénateurs, j'appuie entièrement les efforts visant à établir un programme national pour les enfants, mais je crains que les efforts n'échouent dans les régions du Canada où la croissance est plus faible. 
Je me demande s'il est pertinent, au Nouveau-Brunswick, de faire des efforts pour améliorer la vie des enfants si l'on ne fait pas, en parallèle, des efforts pour améliorer la vie de leurs parents en leur assurant la dignité par le travail.  
Permettez-moi de citer l'éditorial du Daily Gleaner de Frederiction qui disait, le 24 octobre, au sujet du discours du Trône: 
Si le gouvernement veut vraiment aider les enfants qui vivent dans la pauvreté, la meilleure chose à faire est de créer le climat qui permettra à leurs pères et mères de trouver des emplois stables et satisfaisants.  
Honorables sénateurs, je reprendrai un thème que j'ai souvent abordé en cette Chambre. 
L'élaboration des politiques se fait trop souvent en vase clos au gouvernement. 
Les politiques sont conçues isolément et ne reflètent pas les pressions multiples que subit la société. 
Étant donné l'interdépendance entre la politique sociale et la politique économique, en notre qualité de sénateurs, nous devons demander aux gouvernements des deux paliers de comprendre que, pour réussir à combattre la pauvreté chez les enfants, il faut à la fois réduire le chômage et améliorer les programmes sociaux. 
Adopter une approche fragmentaire pour améliorer la vie des enfants, et surtout ceux des régions à plus faible croissance, mènera tout droit à l'échec.  
