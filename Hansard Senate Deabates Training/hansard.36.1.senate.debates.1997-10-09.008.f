Avis de motion visant la création d'un comité mixte spécial-Ajournement du débat 
L'honorable Landon Pearson (au nom du sénateur Carstairs) , conformément à l'avis du 7 octobre 1997, propose: 
Que le comité mixte soit formé de sept sénateurs et seize députés avec deux coprésidents; 
Que tout changement dans la députation influant sur la composition des membres du comité prenne effet immédiatement après que l'avis signé par la personne agissant à titre de whip en chef d'un parti reconnu a été déposé auprès du secrétaire du comité; 
Qu'il soit ordonné au comité de mener de vastes consultations et d'examiner les démarches adoptées à l'égard de ces questions au Canada et dans les régimes gouvernementaux comparables; 
Que le comité ait le pouvoir de siéger pendant les séances et les périodes d'ajournement du Sénat; 
Que le comité ait le pouvoir de faire rapport de temps à autre, de convoquer des témoins, de demander le dépôt de documents et de dossiers, et de faire imprimer des documents et des témoignages dont le comité peut ordonner l'impression; 
Que le comité ait le pouvoir de recourir aux services d'experts, notamment de conseillers juridiques, de professionnels, de techniciens et d'employés de bureau; 
Que le quorum soit de douze membres chaque fois que l'on passe une question aux voix, que l'on adopte une résolution ou que l'on veut prendre quelque autre décision, dans la mesure où les deux Chambres sont représentées, et que les coprésidents soient habilités à convoquer une réunion, à recevoir des éléments de preuve et à en autoriser l'impression, dans la mesure où six membres sont présents et que les deux Chambres sont représentées; 
Que le comité soit habilité à désigner certains de ses membres pour constituer autant de sous-comités qu'il jugera utile, et à déléguer auxdits comités la totalité ou une partie de ses pouvoirs au Sénat et à la Chambre des communes; 
Que le comité soit habilité à autoriser la télédiffusion ou la radiodiffusion d'une partie ou de la totalité de ses délibérations; 
Que le comité soumette son rapport final d'icile 7 novembre 1997; 
Qu'un message soit transmis à la Chambre des communes pour l'en informer de ce qui précède.  
Au cours de la dernière session, le comité sénatorial permanent des affaires sociales, des sciences et de la technologie a étudié le projet de loi C-41, qui traitait des responsabilités des parents divorcés par rapport au soutien financier de leurs enfants. 
À l'époque, le comité a entendu les témoignages de divers groupes et particuliers qui se préoccupaient non seulement de la pension alimentaire pour les enfants, mais aussi des questions de garde et de visite. 
Bon nombre se sont dits préoccupés par des questions comme les définitions de garde et de visite, les coûts élevés et les délais associés aux procédures juridiques, la nécessité de mieux faire respecter les droits de visite et de mieux s'attaquer aux problèmes de violence familiale et de mauvais traitements.  
Nous avons tous entendu des histoires d'enfants qui sont des pions dans la guerre de pouvoir que se livrent leurs parents et nous avons tous entendu parler des expériences traumatisantes des enfants qui vivent l'échec d'un mariage. 
Il faut absolument améliorer le sort des enfants. 
De plus, les Canadiens portent un grand intérêt à ces questions, ils s'en préoccupent beaucoup et ils veulent s'exprimer sur ces questions. 
Voilà pourquoi un comité mixte peut être très utile.  
Comme le prévoit la motion, en vertu de son mandat et des pouvoirs qui lui seront conférés, le comité pourra faire un examen sérieux et exhaustif de toutes les questions pertinentes. 
Il s'emploiera à déterminer les règles, les principes et les mécanismes qui garantissent ce qu'il y a de mieux pour les enfants.  
La motion propose de former un comité qui adoptera une approche centrée sur les enfants plutôt que sur les droits et les besoins des parents. 
À mon avis, c'est très important. 
Cette approche centrée sur les enfants permettra au comité de déterminer les besoins fondamentaux des enfants dont les parents divorcent, à la suite de la ratification par le Canada de la Convention des Nations Unies relative aux droits de l'enfant. 
Elle permettra au comité de rencontrer et d'écouter attentivement des individus et des groupes qui se soucient de la qualité de vie de nos enfants et qui ont des idées sur la façon d'atténuer le climat de confrontation entourant la prise de décisions en matière de garde et de visite.  
Elle permettra aussi au comité d'examiner d'importants documents sur les besoins inhérents au développement de l'enfant et d'examiner des modèles que d'autres autorités appliquent pour tenter de limiter les effets négatifs du divorce sur les enfants. 
Rien n'est plus important pour l'avenir de notre pays que de trouver des moyens d'aider les parents à mieux résoudre leurs différends et à faire ce qui est le mieux pour leurs enfants.  
Honorables sénateurs, je vous demande d'appuyer la motion.  
