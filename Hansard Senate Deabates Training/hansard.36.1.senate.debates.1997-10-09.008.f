L'économie 
La hausse récente des taux d'intérêt-Le risque d'inflation-Un taux de chômage acceptable-La position du gouvernement 
L'honorable Mira Spivak :
Honorables sénateurs, la semaine dernière, la Banque du Canada a porté les taux d'intérêtà 3,75 p. 100 et le gouverneur de la Banque nous menace d'autres hausses. 
Des critiques de la politique de la banque centrale estiment que le taux de chômage est trop élevé au Canada pour justifier de telles hausses. 
Jeff Rubin, économiste principal chez Wood Gundy, qualifie la hausse des taux de prématurée et de dangereuse car, comme l'a fait remarquer aujourd'hui le Conference Board du Canada, même une hausse de 50 points de base seulement, soit un demi-point de pourcentage, pourrait déclencher cette année un repli sérieux des consommateurs.  
(1440) 
M. Rubin craint que ne se répète ce qui s'est passé dans les années 80, tandis que le taux d'inflation est de 9 p. 100 au Canada et lorsque la banque a causé une dépression canadienne en haussant les taux d'intérêt.  
M. Paul Martin, qui a déclaré en février dernier qu'un taux de chômage de 5 p. 100, soit le taux américain, pourrait être atteint sans provoquer d'inflation, défend maintenant la Banque du Canada.  
Le gouvernement fédéral estime-t-il maintenant qu'il vaut la peine de risquer de freiner sérieusement la croissance et d'aggraver le chômage pour lutter contre un risque perçu d'un taux d'inflation de 1,8 p. 100? 
L'honorable B. Alasdair Graham (leader du gouvernement) :
Honorables sénateurs, les économistes sont ce qu'ils sont, et ils auront toujours leurs débats. 
