L'accroissement des pouvoirs du Président durant la période des questions 
L'honorable William M. Kelly :
Honorables sénateurs, ce matin, à 7 heures, j'ai regardé au réseau CPAC une reprise de la période des questions à l'autre Chambre. 
Quand nous étions de l'autre côté et les gens de l'actuel gouvernement de ce côté-ci, on commettait la même faute. 
Je fais allusion au paragraphe 24(4) du Règlement qui traite de la période des questions. 
Il dit ceci: 
Il n'y a pas de débat à la suite d'une question orale; mais le sénateur qui la pose et celui qui y répond, peuvent fournir de brèves explications.  
Honorables sénateurs, nous avons presque abandonné la pratique de fournir de brèves explications. 
Je pense qu'il n'y a pas un seul sénateur qui n'ait pas commis cette faute.  
D'autres options s'offrent à nous. 
Si nous n'aimons pas la réponse à une question et que nous n'avons pas une question complémentaire dans son vrai sens à poser pour obtenir des précisions, nous pouvons recourir aux interpellations, aux motions et que sais-je encore.  
En regardant la période des questions de ce matin, j'ai été frappé par le nombre de questions qu'il est possible de poser quand les explications sont brèves. 
On pose des questions, on y répond, et bien d'autres ont la possibilité de soulever des questions qu'ils se posent, ce qui est la raison d'être de la période des questions.  
Nous avons tous été heureux, pour une raison ou pour une autre - ça m'a fait du bien mais j'ignorais pourquoi - d'entendre cette déclaration touchant l'égalité des sénateurs. 
Dans cette enceinte, nous sommes tous égaux. 
Il n'existe pas 
d'autres pouvoirs que ceux que nous voulons bien nous conférer les uns aux autres et à nous-mêmes. 
Nous devrions peut-être songer à accorder à notre Président les mêmes pouvoirs, à tout le moins pour la période des questions, que possède le Président de l'autre Chambre. 
Le Président de l'autre Chambre semblait effectivement jouer un rôle en ce qui concerne le déroulement harmonieux des procédures et le fait qu'un plus grand nombre de personnes aient la possibilité de poser des questions.  
Là-dessus, j'inviterais tous les honorables sénateurs à y réfléchir et à prendre les dispositions nécessaires la prochaine fois que nous nous pencherons sur nos règles et procédures.  
