Que le Sénat se joigne à la Chambre des communes pour constituer un comité mixte spécial du Sénat et de la Chambre des communes... 
Le sénateur Carstairs :
Suffit!  
Son Honneur le Président :
Que tous ceux qui sont en faveur de la motion principale veuillent bien dire oui.  
Des voix:
Oui.  
Son Honneur le Président :
Que tous ceux qui sont contre veuillent bien dire non. 
Le sénateur Prud'homme :
Non.  
Son Honneur le Président :
Je déclare la motion adoptée.  
(La motion est adoptée.)  
Adoption de la motion visant à nommer les membres du comité mixte spécial 
Permission ayant été accordée de revenir aux avis de motion du gouvernement: 
L'honorable Sharon Carstairs (leader adjoint du gouvernement) :
Honorables sénateurs, avec la permission du Sénat et nonobstant l'alinéa 85(1) b ) du Règlement, je propose, avec l'appui de l'honorable sénateur Kinsella: 
Que les sénateurs suivants soient désignés pour représenter le Sénat au sein du comité mixte spécial chargé d'étudier le projet de modification constitutionnelle du Québec: les honorables sénateurs Beaudoin, Grafstein, Lavoie-Roux, Lynch-Staunton, Pépin, Robichaud (Saint-Louis-de-Kent) et Wood; et 
Qu'un message soit transmis à la Chambre des communes pour l'en informer.  
Son Honneur le Président :
Vous plaît-il, honorables sénateurs, d'adopter la motion?  
L'honorable Marcel Prud'homme :
Honorables sénateurs, j'ai bien appris des leçons de mon père: lorsque l'on a les apparences de la colère, il ne faut pas transposer un débat sur un autre.  
Parmi les nouveaux sénateurs, certains m'ont demandé quel était le règlement qui s'appliquait, alors je vais en parler un peu. 
Je vous rappelle que je pouvais dire non et que je peux encore dire non; cela est clair. 
Je ne le ferai cependant pas, pour vous démontrer que le débat passionné auquel j'ai participé tantôt peut se dissocier lorsqu'il s'agit d'une autre question. 
Je ne tiens pas rancune à un point tel que, ayant exprimé ma colère sur un sujet, j'en vienne à retirer mon appui sur un autre. 
Ce ne serait pas digne d'un sénateur.  
Je laisserai plutôt à la Chambre des communes ou au sénateur le soin de participer à cette ruse.  
J'ai effectivement tenu des propos très fermes plus tôt. 
Je veux que les sénateurs sachent que c'est exactement ce que m'a enseigné la tradition parlementaire britannique. 
On discute vigoureusement, puis on se rallie à la majorité. 
Bien que je ne sois pas en accord avec cette entente «royale», elle a eu lieu.  
Le sénateur Cools :
Longue vie à l'Angleterre!  
Le sénateur Prud'homme :
Je donnerai mon consentement, et je ne dirai même pas de mauvaise grâce.  
Son Honneur le Président :
Vous plaît-il, honorables sénateurs, d'adopter la motion?  
Des voix:
D'accord.  
(La motion est adoptée.)  
