La sûreté du système d'approvisionnement sanguin 
La motion d'ajournement visée à l'article 60 pour l'étude d'une question urgente d'intérêt public 
L'honorable Richard J. Doyle :
Honorables sénateurs, je prends la parole au sujet d'une question urgente d'intérêt public. 
Plus d'une semaine s'est écoulée depuis que trois sénateurs ont posé des questions au leader du gouvernement au Sénat sur la réponse que le gouvernement devrait donner au rapport final du juge Horace Krever sur la Commission d'enquête sur l'approvisionnement en sang du Canada.  
Maintenant, grâce aux médias, les Canadiens sont conscients de l'ampleur des problèmes qui doivent être réglés. 
Dès le tout début de son rapport, le juge Krever souligne l'étendue des difficultés. 
Il dit: 
Une catastrophe nationale en santé publique a frappé l'ensemble du Canada à la fin des années 1970 et pendant les années 1980. 
Au début des années 1990, plusieurs centaines de Canadiens infectés par le VIH avaient contracté le sida. 
Bon nombre d'entre eux étaient déjà décédés. 
D'autres, qui avaient reçu une transfusion au début des années 1980, apprenaient qu'ils étaient infectés par le VIH. 
On reconnaissait de plus en plus l'importance et la gravité de la contamination des réserves de sang...  
Honorables sénateurs, faites bien attention au langage très direct qu'utilise le juge. 
Le cinquième mot du rapport est «catastrophe». 
Puis arrivent en cascade les mots «infectieux», «contaminé» et «décès». 
L'histoire de la progression de cette calamité est racontée sur un ton sombre, dans trois volumes, révélant la lenteur avec laquelle ce pays a réagi à la propagation de l'infection et l'insouciance des gardiens de notre santé face aux avertissements. 
Tout indique que nous avons négligé les précautions prises aux États-Unis. 
Le juge Krever a fourni aux gardiens de la santé publique le plus solide des arguments en faveur d'une transformation globale et immédiate du système de protection de l'approvisionnement en sang. 
J'ai constaté, dans les réponses données au Sénat la semaine dernière, une sympathie authentique à l'égard des victimes d'une négligence nationale. 
Le ministre a dit que «le gouvernement présenterait une réponse très exhaustive». 
On peut seulement espérer que les communiqués de presse décrivant ce qu'il a fait jusqu'à maintenant ne font pas partie de cette «réponse exhaustive».  
Il ne faudrait pas qu'il reporte à plus tard cette étape des correctifs.  
La population, et surtout les organismes représentant les personnes à risque, devraient pouvoir présenter publiquement leurs arguments en faveur du changement. 
On ne doit pas les faire attendre jusqu'en février, car les gouvernements auront alors établi des programmes qui ne pourront plus être modifiés.  
Seul un débat global et permanent, lancé en cette Chambre, incitera les gens à s'intéresser à un domaine où l'échec a fait loi pendant trop longtemps.  
L'honorable B. Alasdair Graham (leader du gouvernement) :
Honorables sénateurs, j'aurai quelque chose à dire dans un moment au sujet de la question de savoir si la motion est recevable ou non. 
Je prends les inquiétudes du sénateur très au sérieux, comme toujours, particulièrement lorsqu'elles concernent l'enquête Krever. 
Je pense que nous sommes tous d'accord pour dire que le juge Krever a fait un travail remarquable. 
Il a énormément contribué à la santé et à la sûreté des Canadiens. 
Il a soulevé des craintes légitimes au sujet de la sûreté de notre service national de transfusion. 
Personne ne pouvait s'empêcher d'être ému par le sort des victimes. 
Comme je l'ai dit plus tôt, lorsque le sénateur Doyle a soulevé cette importante question, les conclusions du juge Krever sur le rôle du fédéral ont été entièrement acceptées par le gouvernement du Canada. 
Nous acceptons ces conclusions sans réserves. 
Nous les acceptons dans leur intégralité. 
Le gouvernement fédéral accepte également sa part de responsabilité pour les lacunes passées du système. 
Le gouvernement a exprimé sa tristesse devant les effets dévastateurs des événements qui se sont produits et il fournira une réponse aux Canadiens dès que possible.  
Maintenant, je voudrais renvoyer les sénateurs au Règlement du Sénat du Canada , plus précisément à la partie VII, page 64, qui traite des «Motions». 
Le paragraphe 60(1) dit: 
Le sénateur qui désire proposer «que le Sénat ajourne maintenant», afin de soulever une question urgente d'intérêt public, en demande la permission par préavis écrit au Greffier du Sénat au moins trois heures avant l'heure prévue de convocation du Sénat...  
Cela n'a pas été fait. 
Cela étant dit, l'alinéa 60(1) a ) précise que la question: 
...doit avoir trait à une urgence véritable et nécessiter l'attention immédiate du Sénat; 
À l'alinéa 60(6) b ), à la page 66, on dit que le sénateur doit expliquer: 
...pourquoi il est peu probable que le Sénat ait une autre occasion d'étudier la question dans un délai raisonnable.  
J'ai dit que le gouvernement acceptait sa part de responsabilité et qu'il répondra aux préoccupations légitimes énoncées dans le rapport Krever. 
Cela dit, honorables sénateurs, je ne vois pas la motion comme étant une motion recevable à ce moment.  
L'honorable Consiglio Di Nino :
Honorables sénateurs, j'aimerais exprimer mon appui à la motion de mon collègue, le sénateur Doyle, et faire quelques remarques à ce sujet.  
Pour ce qui est des problèmes que la commission Krever a décelés, l'Assemblée nationale du Québec exhorte Ottawa et les autres provinces à suivre son exemple. 
La semaine dernière, elle a présenté une résolution prévoyant un programme d'indemnisation à l'intention de toutes les victimes de l'hépatite C et leurs familles. 
Le Québec donne ainsi suite à une recommandation du rapport de la commission Krever selon laquelle les victimes de l'hépatite C devraient être indemnisées. 
Les personnes dont le sang a été contaminé par le VIH ont déjà été indemnisées dans le cadre d'un programme que le gouvernement a créé en 1993. 
J'ajouterai, à titre personnel, honorables sénateurs, que la première personne à mourir d'une infection causée par le VIH était le mari d'une amie à moi. 
Je sais quel événement tragique ce fut.  
Le juge Krever a qualifié d'injustice flagrante le fait de verser de l'argent à une catégorie de personnes ainsi infectées sans en faire autant pour les autres. 
À l'heure actuelle, certains ministres fédéraux s'opposent encore au plan proposé en raison de son coût élevé. 
Encore une fois, c'est une question d'argent, l'argent, toujours l'argent. 
Les ministres fédéraux n'acceptent pas la réalité. 
Ils se demandent comment ils peuvent justifier que l'argent des contribuables serve à des fins d'indemnisation. 
Honorables sénateurs, la sympathie du public va aux victimes du scandale du sang contaminé - d'innocentes personnes qui ont été infectées sans que ce ne soit de leur faute. 
Les contribuables sont prêts à payer parce que d'autres contribuables, qui ont été amenés à croire que notre système d'approvisionnement sanguin était sûr, sont aujourd'hui malades, souffrants et angoissés. 
Cela aurait pu arriver à n'importe qui, et les contribuables se font un devoir d'aider ces personnes.  
Le test avait été utilisé depuis quatre ans aux États-Unis avant d'être introduit au Canada. 
Au lieu de cela, des Canadiens ont servi de cobayes dans une étude où la moitié ont reçu du sang ayant fait l'objet d'un test de dépistage indirect, et l'autre moitié, du sang non dépisté, même si la Croix-Rouge savait que la maladie était répandue.  
Je suis sûr que les sénateurs sont intéressés d'entendre l'explication impardonnable qu'on a donnée pour justifier cette étude. 
Je citerai des extraits de documents officiels soumis à la commission d'enquête Krever lors de la conférence à laquelle le comité canadien du sang a été convoqué le 22 août 1989.  
Le retard de deux ans dans l'achèvement du projet de recherche a retardé l'application du test de dépistage indirect, ce qui peut représenter des économies de 20 millions de dollars.  
Si le projet de recherche n'était pas mis à exécution, la Croix-Rouge se sentirait dans l'obligation de recommander l'introduction du test de dépistage indirect sur la foi des preuves scientifiques actuelles et le fait que tout le sang aux États-Unis (et la majorité du sang en Europe) fait l'objet d'un test de dépistage du virus de l'hépatite autre que A et B. 
C'est le nom qu'on donnait autrefois à l'hépatite C. 
De toute évidence, honorables sénateurs, lorsque des innocents ont dû souffrir à cause des fautes d'autres personnes, une indemnisation s'impose. 
Le gouvernement fédéral doit créer un programme national d'indemnisation comme celui qui est énoncé dans la résolution du Québec. 
C'est le moins que nous puissions faire pour les milliers de personnes qui ont été infectées et dont la vie et celle de leurs enfants et de leurs proches ont été ruinées sans qu'elles y aient été pour quelque chose. 
Il s'agit-là d'une question d'une grande importance et je crois que la motion du sénateur Doyle mérite notre appui.  
