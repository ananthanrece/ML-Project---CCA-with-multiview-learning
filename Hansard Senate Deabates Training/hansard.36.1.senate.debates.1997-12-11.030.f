Lettres de dépôt et billets de dépôt 
Deuxième lecture-Ajournement du débat 
- Honorables sénateurs, je suis heureuse de proposer la deuxième lecture du projet de loi sur les lettres de dépôt et les billets de dépôt et d'en appuyer l'adoption rapide.  
J'appuie l'adoption rapide parce que, comme bon nombre d'entre vous le savent, ce projet de loi est de nature essentiellement technique. 
Il jouit du plein appui du secteur bancaire ainsi que de l'industrie des services financiers.  
Comme vous êtes sans doute au courant, le secteur des services financiers canadiens remanie en profondeur son mode de fonctionnement.  
Ce changement procède évidemment d'une concurrence internationale accrue, qui résulte elle-même d'énormes progrès technologiques.  
En fait, le secteur des services financiers est sans doute celui qui a été le plus touché par l'évolution de la technologie de l'information.  
C'est pourquoi des secteurs de l'industrie qui n'avaient pas évolué depuis des années sont maintenant obligés de se moderniser et d'accroître leur efficience.  
La Loi sur les lettres de dépôt et les billets de dépôt constitue une autre mesure prise par le gouvernement pour faciliter la modernisation de notre secteur des services financiers.  
Ce projet de loi porte sur le transfert de propriété de certains instruments négociables du marché monétaire, comme les acceptations bancaires et les effets commerciaux.  
À une certaine époque, les institutions détenaient et échangeaient directement ces instruments. 
Des documents changeaient effectivement de mains.  
Toutefois de nos jours, les banques et d'autres intervenants détiennent de plus en plus couramment ces instruments, c'est-à-dire des obligations, des bons du Trésor et d'autres instruments négociables par le biais de dépositaires.  
Lorsqu'il y a cession de propriété de ces instruments, le transfert s'effectue au moyen d'une inscription dans les registres du dépositaire. 
On assiste donc à l'inscription du nouveau propriétaire dans le registre de la chambre de compensation, et non à la remise de l'effet proprement dit.  
Or, la législation régissant les institutions financières ne reflète pas cette pratique établie.  
Le problème tient au fait que la loi fédérale pertinente, la Loi sur les lettres de change, continue de faire allusion à la possession effective d'un instrument négociable lorsqu'il est question des droits des parties à une opération.  
Ainsi, le terme «porteur» désigne la personne en possession d'une lettre ou d'un billet payable au porteur.  
Par conséquent, lorsqu'un instrument négociable est détenu par un dépositaire et que le transfert s'effectue au moyen d'une inscription dans un registre, les droits mentionnés dans la Loi sur les lettres de change sont impossibles à interpréter puisque l'instrument lui-même demeure auprès du dépositaire.  
Jusqu'ici, ces exigences empêchaient de faire appel à un dépositaire ou à un système de transfert de propriété par inscription.  
Le projet de loi que nous étudions aujourd'hui corrige le problème en créant deux nouvelles catégories d'instruments: les lettres et les billets de dépôt.  
Le projet de loi assure que l'acquéreur d'une lettre ou d'un billet de dépôt jouit des mêmes droits légaux que l'acquéreur d'une lettre ou d'un billet en vertu de la Loi sur les lettres de change, sans qu'il y ait livraison de l'instrument proprement dit.  
Toutefois, puisqu'ils sont conçus pour être diffusés et négociés relativement à grande échelle, les droits et les responsabilités qui en découlent seront définis en faisant clairement référence à la fonction de la chambre de compensation et au transfert par inscription.  
Pour les distinguer d'autres titres semblables, ces nouveaux instruments arboreront au recto une mention attestant qu'il s'agit de lettres et de billets de dépôt assujettis à la Loi sur les lettres de dépôt et les billets de dépôt.  
La Caisse canadienne de dépôt de valeurs limitée, honorables sénateurs, souhaite pouvoir détenir des instruments négociables du marché monétaire, comme des acceptations bancaires et des effets commerciaux, et consigner le transfert de ces instruments dans ses registres dès que possible. 
L'adoption de ce projet de loi l'y autorisera.  
Cette nouvelle pratique sera beaucoup plus efficiente que le régime actuel, qui requiert la prise de possession effective de l'instrument en cause.  
J'ajoute, honorables sénateurs, que ce projet de loi n'empêche nullement des particuliers ou des institutions d'acquérir et de détenir d'autres lettres et billets négociables, lesquels demeurent assujettis à la Loi sur les lettres de change.  
Honorables sénateurs, le dépôt de ce projet de loi donne suite aux recommandations du groupe du secteur privé qui examine le fonctionnement du système financier international, communément appelé le G30.  
Ce groupe réclame la mise en place à grande échelle de systèmes de dépôt de valeurs et de consignation des opérations par voie de registre, au motif que cela améliorera l'efficience des marchés monétaires.  
Cette initiative jouit également de l'appui de tous les intervenants des milieux financiers.  
Je vous invite à adopter sans tarder ce projet de loi technique constructif. 
Le projet de loi S-9 n'est pas controversé et, comme je le disais plus tôt, il mérite d'être adopté rapidement.  
J'invite les honorables sénateurs à donner leur approbation pour que nous puissions passer à d'autres projets de loi.  
