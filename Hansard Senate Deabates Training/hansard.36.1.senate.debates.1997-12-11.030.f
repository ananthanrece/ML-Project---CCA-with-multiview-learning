Les ressources humaines 
L'âge limite pour la conversion des régimes enregistrés d'épargne-retraite par les personnes âgées-La possibilité de concessions-La position du gouvernement 
(Réponse à une question posée par l'honorable sénateur Donald H. Oliver le 18 novembre 1997) 
L'âge auquel les REER doivent être convertis en FERR a été ramené de 71 ans à 69 ans dans le budget de 1996. 
La nouvelle limite d'âge entre en vigueur cette année, ce qui veut dire que le REER des personnes qui auront 69, 70 ou 71 ans en 1997 doit venir à échéance d'ici le 31 décembre 1997.  
On a exprimé des inquiétudes à l'effet que certaines personnes de 69 à 70 ans ne soient pas au courant de la date limite et pourraient, par inadvertance, ne pas accomplir les formalités voulues pour que leur REER vienne à échéance d'ici le 31 décembre, les conséquences de cela étant que les régimes seraient désenregistrés, et les intéressés seraient alors assujettis à l'impôt sur le plein montant des fonds investis.  
Toutefois, cette préoccupation n'est pas justifiée. 
En effet, les émetteurs de REER ont averti, en juin, tous leurs clients qui sont visés par la date limite de la fin de l'année. 
Le budget de 1996 exigeait qu'ils le fassent. 
De plus, les banques, les sociétés de fiducie et d'autres émetteurs de REER ont communiqué la date limite dans des documents promotionnels et des documents d'information publics. 
Et surtout, les contrats relatifs à un REER que la plupart des émetteurs utilisent comprennent une disposition qui stipule que le régime doit parvenir à échéance selon les instructions de l'émetteur afin de respecter la date limite, lorsque l'émetteur ne parvient pas à communiquer avec le détenteur du régime.  
Cela signifie que dans la quasi-totalité des cas, les REER des clients qui pourraient ne pas être au courant du changement de la date limite seront automatiquement convertis en FERR d'ici le 31 décembre - évitant ainsi qu'ils soient désenregistrés et assujettis à l'impôt.  
Lorsque cette clause d'inexécution est appliquée, le REER est converti en un FERR par l'émetteur. 
Le client reste libre de convertir le FERR en une rente ou de la transférer dans une autre institution financière, à une date ultérieure.  
De plus, au cours des dernières années, Revenu Canada a constaté très peu de cas de non-respect de l'échéance à 71 ans, et le ministère a pour principe de faire preuve de souplesse à l'égard de ce genre de situation. 
En fait, les cas qui se sont produits se rattachaient à des situations inhabituelles, par exemple au fait que l'émetteur du REER avait mal pris en note l'âge du client. 
En pareil cas d'erreur commise par inadvertance, Revenu Canada a pu lever les pénalités qui dans d'autres cas se seraient appliquées.  
Étant donné les mécanismes de protection en place, changer la date limite maintenant ne ferait qu'invalider tous les avis et tous les documents publics qui ont été diffusés à des fins d'information, ce qui sèmerait la confusion et l'incertitude chez les personnes âgées qui sont touchées par le changement.  
Les affaires intergouvernementales 
Le statut du partenariat avec l'Ontario pour les programmes sociaux 
(Réponse à la question posée par l'honorable Norman K. Atkins le 8 octobre 1997) 
Le gouvernement du Canada entend bien collaborer avec toutes les provinces pour répondre aux besoins des Canadiens en matière d'emploi et de programmes sociaux. 
Le gouvernement est conscient du fait que la meilleure façon de s'attaquer à de nombreux problèmes sociaux comme la pauvreté chez les enfants, le chômage chez les jeunes et l'évolution du marché du travail en général consiste à s'assurer que tous les gouvernements collaborent ensemble.  
En ce qui concerne l'Ontario en particulier, le ministre du Développement des ressources humaines travaille en étroite collaboration avec son homologue ontarien pour mettre en oeuvre une prestation nationale pour enfants et le programme de la prestation fiscale pour enfants. 
Ce travail avance rapidement grâce au processus multilatéral sur le renouvellement de l'union sociale. 
L'Ontario joue un rôle de chef de file en la matière.  
Le ministre du Développement des ressources humaines et le ministre ontarien des Services sociaux et communautaires coprésident à l'heure actuelle le comité des ministres fédéral, provinciaux et territoriaux responsables des services sociaux, qui est une tribune clé pour des discussions intergouvernementales sur la mise en oeuvre de la prestation nationale pour enfants.  
En tant que partenaire dans le projet de prestation nationale pour enfants, l'Ontario s'est engagée à réinvestir 150 millions de dollars des économies réalisées au niveau provincial pour élargir les programmes afin d'aider les familles avec des enfants à trouver et à garder des emplois. 
À la suite de cet engagement, le gouvernement ontarien a annoncé un programme de 100 millions de dollars de crédits d'impôt pour garde d'enfants offert aux familles à faible revenu en tant qu'initiative de réinvestissement dans le cadre de la prestation nationale pour enfants.  
Le ministre du Développement des ressources humaines a également écrit à ses homologues provinciaux pour les inviter à entamer des négociations sur le nouveau programme d'aide à l'employabilité des personnes handicapées qui va remplacer le programme de réadaptation professionnelle des personnes handicapées. 
Le gouvernement fédéral débloque d'autres crédits pour régler une question de financement en souffrance. 
De nouvelles ententes avec les provinces seront en place le 1er avril 1998. 
Le gouvernement fédéral collabore également avec l'Ontario et d'autres provinces pour s'attaquer au chômage chez les jeunes par l'entremise du Forum des ministres du Travail.  
Même si les négociations n'ont pas encore commencé, je suis persuadé que lorsqu'on sera arrivé à conclure un partenariat avec le gouvernement ontarien, il profitera aux chômeurs de la province.  
