Honorables sénateurs, le 15 avril 1997, l'Assemblée nationale a voté à l'unanimité une modification qui aurait essentiellement pour effet de mettre un terme aux droits et privilèges dont jouissent les catholiques et les protestants de cette province en matière scolaire. 
