DÉCLARATION SOUS SERMENT D'YVAN ROY 
Je, soussigné, Yvan Roy, membre du Barreau du Québec et avocat, de la ville d'Ottawa, dans la province d'Ontario, déclare sous serment ce qui suit: 
Je suis avocat auprès du ministère de Justice et je suis actuellement avocat général principal à la section de la politique - droit pénal, poste que j'occupe depuis 1990.  
2. Que la section de la politique - droit pénal est responsable de l'élaboration des initiatives de politique de même que de la préparation des mesures législatives en matière de droit criminel. 
En conséquence, les fonctionnaires de la section de la politique - droit pénal - identifient les questions juridiques d'actualité en matière de droit criminel et préparent les modifications au Code criminel et aux autres lois pénales. 
La section de droit pénal est aussi chargée de fournir des avis juridiques à d'autres ministères fédéraux, aux provinces et aux territoires de même qu'aux autorités de police sur des questions de droit criminel.  
4. Le 22 mai 1997, cette honorable Cour a rendu son jugement dans l'affaire R. c. Feeney. 
À première vue, le Code criminel ne comporte de mécanisme législatif qui permettrait d'obtenir une telle autorisation. 
5. Le 10 juin 1997, M. Daniel Bellemare, sous-procureur général adjoint (droit pénal) au ministère de la Justice a réuni un conférence téléphonique à laquelle j'ai participé, avec ses homologues provinciaux et territoriaux relativement à la question d'interprétation et a donné des avis à la police relativement aux pouvoirs d'arrestation. 
Puisque qu'aucun consensus ne se dégageait de cette conférence sur les termes qu'il faudrait présumer que le Code criminel comporte et à quel article ils seraient insérés, il a été évident que chaque ressort adopterait une attitude différente de celle des autres quant à l'obligation établie par l'arrêt Feeney d'avoir un mandat. 
De fait, il y a eu des différences importantes quant à l'attitude que les différents ressorts canadiens ont adoptée immédiatement après l'arrêt Feeney, et vu les différentes considérations pratiques auxquelles les forces de police et les substituts de chaque ressort devaient faire face.  
6. À mon avis les questions graves soulevées par l'arrêt Feeney créaient de l'incertitude en droit pénal et soulevaient la possibilité que le Code criminel ne soit pas appliqué de façon uniforme d'un bout à l'autre du pays. 
J'ai été informé par mes homologues provinciaux qu'ils étaient assaillis de demandes de renseignements et d'avis juridiques relativement à l'effet de l'arrêt Feeney et aux procédures qu'il fallait suivre pour s'assurer que les arrestation soient menées conformément à la Charte. 
Ces demandes venaient principalement des forces de police et des substituts de différents ressorts. 
L'arrêt Feeney a eu pour conséquence d'exiger que les pratiques de routine de la police subissent une modification importante et immédiate.  
7. Par avis de requête en date du 17 juin 1997, le procureur général de la Colombie-Britannique, avec intervention subséquente des procureurs généraux de l'Alberta, du Canada, de l'Ontario et du Québec, a déposé devant cette honorable Cour, une motion demandant soit une ordonnance établissant une période de transition soit une ordonnance accordant une nouvelle audition afin de clarifier la nature de la disposition relative au mandat présumément présente au Code criminel. 
Le 27 juin 1997, cette honorable Cour a accordé une suspension de six mois de l'application de l'arrêt R.. c .Feeney 
quant à l'obligation d'avoir un mandat pour procéder à une arrestation dans une maison d'habitation. 
Cette suspension expire le 22 novembre 1997.  
8. Immédiatement après la décision de cette honorable Cour d'accorder la suspension de l'application de l'arrêt Feeney, le sous-procureur général adjoint (droit pénal) et moi avons commencé de recevoir des réponses à une lettre que nous avions fait parvenir à nos homologues provinciaux, le 20 juin 1997, au sujet des procédures intérimaires appliquées dans chaque ressort avant la suspension de l'arrêt Feeney, lettre par laquelle nous demandions l'avis des provinces quant au régime législatif en vertu duquel les agents de la paix pourraient obtenir le mandat exigé par la Constitution pour entrer dans une maison d'habitation pour y procéder à une arrestation.  
9. Le 4 juillet 1997, j'ai transmis une lettre aux services juridiques des autres ministères fédéraux afin de déterminer les dispositions législatives touchées par l'arrêt Feeney et d'obtenir leurs opinions sur les réformes législatives qu'il serait possible d'adopter en réponse à cet arrêt. 
Nous avons reçu des réponses pendant tout l'été.  
10. Pendant le mois de juillet 1997, des collègues chargés de cette question et moi-même avons consulté des collègues du ministère de la Justice afin de préparer différentes options et recommandations de réforme législative. 
Des recherches ont été menées au sujet des lois adoptées aux États-Unis et ans d'autres ressorts de common law. 
J'ai alors transmis un document de discussion dans ce sens à mes collègues provinciaux afin de discuter des possibilités raisonnables pendant la Conférence sur l'uniformisation des lois tenue du 17 au 22 août 1997. 
J'ai demandé à mes collègues de réagir à ces propositions concrètes dans un délai de deux semaines.  
11. Vers la fin d'août 1997, j'ai eu l'occasion de soulever la question d'une possible solution législative à l'arrêt de cette honorable Cour dans l'affaire Feeney avec la nouvelle ministre de la Justice l'honorable Anne McLellan. 
Avec l'assentiment de la nouvelle ministre, j'ai immédiatement commencé à prendre les mesures nécessaires pour présenter un projet de loi du gouvernement, pour pourvoir à un mécanisme en vertu duquel un agent de la paix pourrait obtenir l'autorisation judiciaire d'entrer dans une maison d'habitation pour y procéder à une arrestation.  
12. Le 9 septembre 1997, j'ai rencontré, avec des collègues, des représentants de l'Association canadienne de la police, de l'Association canadienne des chefs de police, de la GRC et du Solliciteur général afin de discuter les options retenues par la ministre en vue d'obtenir leurs vues sur les mesures à prendre selon le point de vue des autorités de police.  
13. Le 12 septembre 1997, j'ai rencontré, avec des collègues, des représentants des ministères fédéraux touchés par l'arrêt Feeney afin de discuter des conséquences des options retenues par la ministre sur l'exécution des arrestations ou les dispositions relatives aux appréhensions des lois fédérales autres que le Code criminel et de déterminer s'il fallait prendre des mesures dans le texte législatif pour tenir compte de ces dispositions. 
La rédaction préliminaire a commencé par la suite.  
Le 26 septembre 1997, j'ai tenu une conférence téléphonique avec des représentants de l'Association du Barreau canadien afin de discuter les options retenues par la ministre et de demander leurs points de vue sur une solution législative qui respecterait un équilibre entre les intérêts du public à une application efficace des lois et les intérêts des citoyens canadiens au caractère privé de leur maison d'habitation.  
15. Le 26 septembre 1997, mes collègues du ministère de la Justice ont tenu une conférence téléphonique avec des fonctionnaires des procureurs généraux des provinces afin de discuter les propositions à la lumière du document de discussion qui leur avait été remis à la Conférence sur l'uniformisation des lois.  
Le 29 septembre 1997, le premier avant-projet de loi a été distribué à différentes sections du ministère de la Justice pour fins de consultations internes.  
Le 9 octobre, une version préliminaire du projet de loi a été remise aux fonctionnaires des procureurs généraux provinciaux et aux ministères fédéraux intéressés.  
Le 14 octobre, mes collègues du ministère de la Justice ont tenu une téléconférence avec des fonctionnaires des procureurs généraux provinciaux pour discuter du texte et de toute question non résolue dans leur champ de compétence.  
De même, le 14 octobre, mes collègues du ministère de la Justice se sont réunis avec des responsables chargés de l'application de la loi pour discuter du texte et de toute question non résolue qu'ils pourraient souhaiter soulever.  
Les 15 et 17 octobre, deux autres téléconférences ont été tenues entre mes collègues du ministère de la Justice et des fonctionnaires des procureurs généraux provinciaux.  
Les 21 et 22 octobre, mes collègues du ministère de la Justice ont de nouveau rencontré des responsables chargés de l'application de la loi pour discuter d'une version révisée de l'avant-projet de loi.  
22. Le 23 octobre, mes collègues du ministère de la Justice se sont de nouveau réunis avec des fonctionnaires d'Immigration Canada et du Service correctionnel pour discuter plus à fond de certains aspects du texte qui concernaient leurs ministères respectifs.  
Le 24 octobre, j'ai rencontré des représentants de l'Association du Barreau canadien et les ai mis au courant du contenu de la version préliminaire du texte. 
Ils m'ont fait part de leurs suggestions et commentaires.  
Le 27 octobre, j'ai rencontré le leader du gouvernement à la Chambre, l'honorable Don Boudria, pour obtenir son appui et établir un calendrier qui nous permettrait de faire en sorte que la Chambre ait le temps voulu pour bien examiner le projet de loi, compte tenu du délai serré que nous devions respecter.  
Le projet de loi C-16 est annexé comme Pièce A. 
Le 31 octobre, le projet de loi C-16 a reçu la deuxième lecture à la Chambre des communes et a été renvoyé au Comité permanent de la justice et des droits de la personne.  
Le 4 novembre, le Comité de la justice et des droits de la personne a entendu deux témoins, soit la ministre de la Justice, l'honorable Ann McLellan, que j'accompagnais, et l'Association canadienne des policiers.  
Le 5 novembre, le Comité de la justice et des droits de la personne a entendu trois témoins, soit le Centre canadien de ressources pour les victimes de crimes, l'Association canadienne des criminalistes et l'Association canadienne des chefs de police. 
Le Barreau du Québec a soumis un mémoire au Comité, mais aucun représentant n'est venu témoigner.  
J'ai assisté aux audiences du Comité de la justice et des droits de la personne. 
À ces audiences, j'ai entendu des membres du Comité de tous les partis indiquer qu'ils craignaient que sans nouveau délai pour examiner le projet de loi, les parlementaires ne seraient pas en mesure de jouer leur rôle de représentants bien informés chargés par le gouvernement du Canada d'adopter cette mesure législative.  
30. Pour préparer cette mesure, les fonctionnaires du ministère de la Justice ont tenu un grand nombre de consultations, jusqu'au dépôt du projet de loi, avec de nombreux groupes que la question intéresse, ainsi qu'avec des fonctionnaires des ministères fédéraux intéressés et des ministères provinciaux des procureurs généraux. 
Ces consultations étaient essentielles à l'élaboration d'un projet de loi qui tente de trouver la meilleure solution de compromis sur les nombreuses questions découlant de l'arrêt Feeney. 
Toutefois, à cause de ces nécessaires consultations, le Parlement s'est retrouvé à court de temps pour étudier ces questions importantes et les témoins ont manqué de temps pour bien préparer leurs exposés ou mémoires.  
Néanmoins, la Chambre a procédé à la troisième lecture du projet de loi le 7 novembre, compte tenu du délai. 
Des motions d'amendement ont été débattues ce jour-là, et des députés ont de nouveau exprimé leur crainte de voir le projet de loi adopté sans examen en profondeur des enjeux.  
Le Sénat doit maintenant examiner le projet de loi. 
Si le projet de loi était adopté sans amendement par le Sénat, il pourrait alors recevoir la sanction royale du gouverneur général ou de son suppléant. 
Par contre, si le Sénat l'adopte avec des amendements, alors il faudra en informer la Chambre des communes, qui devra discuter les amendements et voter le projet de loi amendé avant qu'il puisse recevoir la sanction royale.  
34. 
Ainsi, il y a plusieurs étapes du processus parlementaire qui doivent être franchies avant que le projet de loi C-16 ne devienne loi. 
Il n'est pas possible de prévoir la route que suivra cette mesure, ni combien de temps il lui faudra pour franchir toutes les étapes. 
Par conséquent, en ce moment, il n'est pas possible de dire si le projet de loi deviendra loi d'ici le délai du 22 novembre 1997.  
