L'agriculture 
La crise agricole dans les provinces des Prairies- La possibilité d'une aide gouvernementale 
L'honorable Leonard J. Gustafson :
Honorables sénateurs, ma question s'adresse au leader du gouvernement au Sénat et concerne la crise dans le secteur agricole. 
Si les sénateurs s'étaient trouvés à Regina samedi, ils y auraient vu de la colère, de la désillusion et du désespoir. 
La situation y est très grave.  
Ma question ne concerne pas directement le Programme d'aide en cas de catastrophe liée au revenu agricole, ou ACRA, parce que nous avons déjà dit qu'il ne fonctionne pas. 
Le ministre de l'Agriculture de la Saskatchewan a dit qu'il faudrait remplacer ce programme parce qu'il ne permet pas de résoudre efficacement les problèmes des agriculteurs, en particulier en Saskatchewan, mais également dans certaines parties de l'Alberta et du Manitoba. 
Compte tenu du nombre très peu élevé de contribuables en Saskatchewan et du fait que cette province compte pour 65 p. 100 de l'industrie canadienne du grain, la province ne peut supporter la situation actuelle. 
Il appartient au gouvernement fédéral de trouver une solution.  
Lorsque le comité sénatorial permanent de l'agriculture et des forêts s'est rendu en Europe, nous avons appris, au cours des25 séances que nous avons tenues dans quatre pays, que les Européens sont déterminés à épauler solidement leurs agriculteurs. 
