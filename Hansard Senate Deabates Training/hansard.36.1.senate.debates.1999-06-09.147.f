L'agriculture et l'agroalimentaire 
L'honorable Eugene Whelan :
Honorables sénateurs, j'aimerais soulever de nouveau la question de la décision prise par le gouvernement du Canada de faire disparaître les stations de recherche d'Agriculture Canada et de laisser au secteur privé le soin de diriger et de financer la recherche. 
Il semble que j'aie eu raison de craindre que les multinationales s'intéressent au plus haut point au financement de la recherche pour ce qui est de certains produits qu'ils pourraient ensuite contrôler grâce à leurs brevets ou en effectuant des manipulations génétiques dans le but d'empêcher les fermiers de reproduire eux-mêmes leurs semences, les forçant ainsi à en acheter de nouvelles chaque année.  
Barry Wilson a publié récemment dans le Western Producer un article qui prouve bien que mes craintes et mes avertissements répétés relativement aux dangers de la perte de notre potentiel de recherche sont maintenant partagés par bon nombre de personnes. 
J'espère toutefois qu'il n'est pas trop tard.  
L'auteur souligne qu'au cours des dix dernières années, la situation s'est empirée au fur et à mesure que les multinationales ont commencé à s'occuper du financement de la recherche. 
Il semble maintenant que les scientifiques passent plus du tiers de leur temps à ramasser des fonds plutôt qu'à s'occuper de leurs travaux de recherche. 
Les fonds sont plus accessibles pour les projets à court terme qui permettront de réaliser rapidement des profits que pour les travaux de recherche fondamentale qui visent le bien public en général. 
L'équipement des laboratoires gouvernementaux sert davantage aux projets à court terme et on ne le remplace pas lorsqu'il devient désuet. 
Les sociétés privées ne sont pas prêtes à financer des laboratoires qui ne leur appartiennent pas.  
Nous avons été témoins, ici, dans la capitale nationale, de certaines décisions coûteuses et très peu prévoyantes. 
La station de recherche agricole de Greenbank était une des plus modernes au monde. 
Elle était dotée des terrains et des installations nécessaires pour mener de la recherche sur les grains, les produits laitiers, le boeuf, le porc et la volaille. 
Avec la fermeture de nos installations à Ottawa, nous avons perdu plus de 300 scientifiques de la région de la capitale.  
Honorables sénateurs, je souligne que nous avions le plus vaste programme de production laitière au monde. 
Nous comptions plus de 1 200 vaches laitières, dont 500 à Ottawa seulement, qui servaient à la recherche dans deux stations différentes. 
Nous disposions d'un matériel remarquable.  
Nous avions également la possibilité de montrer aux visiteurs de l'étranger nos compétences en matière de recherche et nos pratiques de production modernes. 
Lorsque j'étais ministre, rien ne me faisait autant plaisir que de visiter ces installations de recherche en compagnie de dignitaires de l'étranger qui venaient au Canada pour observer les raisons de notre grande productivité au Canada, ce pays de grands froids appelé «pays de glace et de neige».  
Honorables sénateurs, nous ne saurions tolérer ces pertes. 
J'exhorte fortement le gouvernement fédéral à agir immédiatement pour rétablir le financement de la station de recherche agricole et, surtout, pour rouvrir la station agricole de Greenbank. 
Bientôt, il sera trop tard, et nos compétences scientifiques et nos installations de recherche de niveau mondial seront perdues à jamais.  
