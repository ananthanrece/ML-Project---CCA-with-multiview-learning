Le commerce international 
L'accord entre le Canada et les États-Unis sur les périodiques-L'interprétation du libellé-La position du gouvernement 
L'honorable John Lynch-Staunton (chef de l'opposition) :
Le leader du gouvernement a précisé que, selon lui, la définition canadienne du mot «substantiel» signifiait «majoritaire» et avait été acceptée par les Américains. 
Cependant, je n'ai rien trouvé jusqu'à maintenant dans la documentation présentée par les États-Unis concernant cet accord qui indique que la définition canadienne ait été acceptée par le gouvernement des États-Unis. 
Depuis hier, le ministre a-t-il pu trouver un peu de documentation ou quelque chose d'écrit à l'appui de l'idée que les Américains acceptent la définition canadienne du mot «substantiel» comme voulant dire «majoritaire»?  
L'honorable B. Alasdair Graham (leader du gouvernement) :
Honorables sénateurs, je n'ai rien par écrit qui confirme ce que le chef de l'opposition suggère.  
Je me suis renseigné, comme d'habitude, et on m'a dit que les Américains savaient comment nous l'interpréterions avant même que les lettres ne soient signées. 
Les États-Unis n'ont pas utilisé le mot «majoritaire» dans les lettres qui ont été échangées, comme le Canada l'a fait. 
Pour le Canada, «majoritaire» est le terme performatif.  
Le sénateur Lynch-Staunton :
Honorables sénateurs, je suis désolé de contredire le leader du gouvernement, mais le Canada n'a pas utilisé le mot «majoritaire» dans les lettres, il a utilisé le mot «substantiel».  
Le Canada a écrit une lettre à la déléguée commerciale générale des États-Unis qui, à son tour, a inclus le terme dans sa lettre disant: «Je souscris à ce qui suit...». 
Autrement dit, le Canada a rédigé la lettre et l'a fait confirmer par les Américains, et c'est le mot «substantiel» qui apparaît dans la lettre.  
Le sénateur Graham :
Honorables sénateurs, peut-être devrais-je préciser mes propos. 
L'honorable sénateur Lynch-Staunton a absolument raison. 
Le mot «majoritaire» n'apparaît pas dans les lettres qui ont été échangées. 
Ce que je voulais dire, c'est que le mot «substantiel» serait interprété par les autorités canadiennes comme signifiant «majoritaire».  
Le sénateur Lynch-Staunton :
Honorables sénateurs, la question n'est pas de savoir comment les autorités canadiennes vont interpréter le mot «substantiel», mais bien comment les Américains vont l'interpréter.  
J'attire l'attention du ministre sur la transcription de la séance d'information donnée par les Américains le 26 mai. 
Je vais en citer deux ou trois extraits, puis je demanderai au leader du gouvernement si, après les avoir écoutés, il ne trouve pas qu'ils contredisent son interprétation par trop optimiste.  
La question suivante a été posée au haut fonctionnaire américain au commerce: 
N'est-il pas vrai que si au moins 50 p. 100 du contenu d'une publication est canadien, celle-ci sera traitée comme un périodique canadien?  
À cela, il a répondu: 
Je pense que vous parlez de l'examen de l'avantage net, auquel cas le terme performatif est «substantiel» comme dans «niveau substantiel de contenu rédactionnel original».  
Un peu plus loin, on peut lire: 
Q. Le principe du contenu majoritaire est-il inclus dans les critères permettant d'établir l'avantage net?  
Le haut fonctionnaire: 
Le terme utilisé dans l'accord est «substantiel».  
Q.«Substantiel» ne veut pas dire «majoritaire»?  
Le haut fonctionnaire: 
«Substantiel» signifie «substantiel». 
C'est ce qui est dans l'accord. 
Quand les Canadiens rédigeront le règlement, nous verrons quel terme ils utilisent.  
Il est tout à fait clair que pour les Américains, «substantiel» veut dire «substantiel». 
Les Canadiens prétendent que les Américains ont accepté le terme «substantiel» comme signifiant «majoritaire». 
Le sénateur Graham :
Honorables sénateurs, je vous concède que ce n'est pas précisé par écrit, mais les autorités canadiennes ont bien précisé que «substantiel» signifiait «majoritaire».  
Le sénateur Lynch-Staunton :
Les autorités canadiennes ont bien précisé que «substantiel» signifie «majoritaire». 
Parfait.  
Si vous vous reportez au communiqué du 4 juin, dans lequel le ministère du Patrimoine canadien donne sa version de l'accord, vous pouvez lire à la page 3: 
Les États-Unis acceptent les termes de l'entente selon lesquels le Canada effectuera un examen de l'avantage net que représentent les nouveaux investissements dans le secteur de l'édition des périodiques de façon à inclure «les engagements de la part d'investisseurs étrangers qui apporteront un niveau substantiel de contenu rédactionnel original au marché canadien dans chaque périodique visé».  
C'est l'interprétation américaine de «substantiel».  
Le communiqué de presse poursuit: 
Le Canada suivra des principes directeurs, dont celui «d'inclure un contenu original majoritaire dans chaque numéro de chacun des périodiques destiné au marché canadien», dans le processus d'examen de tout nouvel investissement dans l'industrie des périodiques.  
Dans ce communiqué, Patrimoine Canada reconnaît l'incapacité de s'entendre sur l'interprétation du terme «substantiel». 
Les Américains disent «substantiel». 
Selon les Canadiens, «substantiel» veut dire «majoritaire», ce que nient les Américains, d'après le communiqué de Patrimoine Canada.  
Comment le leader du gouvernement au Sénat peut-il déclarer que les deux pays conviennent que le terme «substantiel» veut vraiment dire plus que cela?  
Le sénateur Graham :
Honorables sénateurs, l'important, c'est la compréhension qu'ont les Canadiens quand ils négocient avec leurs homologues américains. 
J'admets que le terme «majoritaire» n'apparaît pas dans la correspondance, mais je saisis clairement qu'au cours des négociations, les Canadiens ont fait comprendre aux Américains que «substantiel» voudrait dire «majoritaire».  
Le sénateur Lynch-Staunton :
Honorables sénateurs, je ne vais pas m'éterniser là-dessus. 
Oui, le Canada est d'accord, il insiste et lutte avec acharnement - mieux que nous l'avions fait avant de céder devant les sanctions commerciales illégales, j'espère - pour que «substantiel» veuille dire «majoritaire», mais où est-il écrit que les Américains acceptaient l'interprétation que les Canadiens donnaient à «substantiel»? 
Où est le document, où est l'engagement? 
Où?  
