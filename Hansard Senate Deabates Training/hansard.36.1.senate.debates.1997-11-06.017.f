Les coupes dans le financement de la recherche-La nouvelle de l'augmentation des dépenses au titre de la technologie-La position du gouvernement 
( Réponse à la question posée par l'honorable Mira Spivak le 21 octobre 1997 ) 
La décision de la CGC d'éliminer le groupe de recherche sur les sites d'impact météoritique découle des compressions budgétaires imposées à la Commission et à d'autres organisations du Ministère. 
On ne met en doute ni l'excellence ni la pertinence scientifique de ces activités, mais elles ont été jugées comme étant moins prioritaires que d'autres par rapport au mandat fondamental de cartographie de la CGC.  
La CGC fait tout en son pouvoir pour maintenir un équilibre pratique entre les fonds salariaux et les fonds de fonctionnement. 
Garder des employés sans pouvoir leur fournir l'équipement ou les budgets dont ils ont besoin pour mettre à profit leurs compétences ne constitue pas une manière judicieuse d'utiliser l'argent des contribuables.  
Le projet du Milieu de bureautique commun a été mis sur pied pour uniformiser les outils de bureautique utilisés à Ressources naturelles Canada (RNCan). 
Il fera en sorte que tous les employés travaillent avec de l'équipement et des logiciels compatibles et capables de communiquer les uns avec les autres. 
Ce projet donnera à RNCan l'infrastructure et les outils dont il a besoin, sur le plan de la technologie de l'information, pour accroître son efficacité.  
La CGC étudie actuellement des façons de poursuivre ces activités dans les fonds documentaires et les bases de données pour le groupe de recherche sur les sites d'impact météoritique par l'intermédiaire de partenariats et d'ententes de collaboration.  
De récentes études montrent que l'exode des cerveaux n'est pas un problème majeur pour le Canada. 
Le nombre de scientifiques qui arrivent ou qui reviennent au Canada est suffisant pour contrebalancer ceux partent.  
RNCan est fier de mettre à profit son expertise scientifique pour appuyer le développement d'une industrie de haute technologie dans le secteur des ressources naturelles. 
Nous avons établi notre propre Cadre de gestion de la S-T en réponse à l'examen des programmes fédéraux de science-technologie, et nous donnons actuellement suite à plusieurs recommandations qui nous permettront de gérer la science avec une plus grande efficacité. 
Le perfectionnement professionnel, la reconnaissance et la formation sont au nombre des questions visées.  
Les coupures aux programmes de science ont été faites en fonction de l'importance que revêt chaque activité par rapport au mandat et aux objectifs globaux de la CGC.  
Cependant, dans un ministère où les sciences représentent 75 p. 100 des activités, on doit s'attendre à ce que certains programmes scientifiques soient touchés.  
Depuis 1994-1995, les coupures de personnel dans les catégories scientifique, professionnelle et technique ont été moins importantes, toutes proportions gardées, que les coupures à l'échelle du Ministère. 
On n'envisage pas pour l'instant que la CGC obtienne un statut spécial.  
