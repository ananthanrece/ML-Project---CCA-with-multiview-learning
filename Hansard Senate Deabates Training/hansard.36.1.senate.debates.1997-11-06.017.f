Plus sérieusement, je voulais attirer l'attention aujourd'hui sur l'excellent travail que le sénateur Andreychuk a effectué il y a quelques années lorsqu'elle présidait le sous-comité des anciens combattants, qui était chargé d'examiner la question des anciens combattants autochtones. 
Beaucoup d'autochtones se sont engagés pendant les deux guerres mondiales et la guerre de Corée. 
À cette époque, les autochtones n'avaient pas le droit de boire, ni de voter, ce qui fait que, souvent, ils cachaient leurs origines lorsqu'ils s'engageaient dans l'armée. 
Pourtant, ils furent parmi nos meilleurs soldats. 
Ils furent parmi les plus décorés.  
