L'honorable Richard J. Doyle :
Honorables sénateurs, contrairement à ce que le sénateur Orville Phillips, ancien du 426e escadron de Thunderbird, vous le dira peut-être, le ministère des Anciens combattants essaie de garder trace de tout. 
Par exemple, j'ai appris hier que je suis le survivantnuméro 439 798 sur plus d'un million de femmes et d'hommes qui ont servi dans la marine, l'armée et l'aviation canadiennes durant la Deuxième Guerre mondiale.  
Qui plus est, je suis l'un des six sénateurs présents qui, comme R. John Pratt, avait l'habitude de nous dire dans son spectacle à l'intention de la marine, a constaté: 
Que la première année est la pire.  
Puis, que l'on s'y fait.  
«Que l'on s'y fait?» 
La plupart de nous avions à peine eu le temps de nous faire à la vie civile que nous devions décider de l'arme dans laquelle nous voulions servir avant qu'on ne nous l'impose.  
Le plus près que je me sois approché de la vraie guerre, au début de ma carrière de journaliste au Chatham Daily News , fut lors de la publication des listes des morts et des blessés de l'Essex Scottish Regiment au lendemain du raid sur Dieppe. 
Je me suis rendu dans des quartiers familiers à la recherche de photos et de biographies de soldats dont les frères, les soeurs et les femmes avaient été mes camarades de classe.  
Peut-être est-ce cette expérience qui m'a amené, durant le reste de la guerre, à voir les événements autour de moi d'un point de vue journalistique. 
Quatre cents gardes ont péri à une distance d'un parc londonien de l'abbaye de Westminster, où je m'étais rendu avec un copain afin d'assister à une messe marquant la libération de Paris.  
Ce lien qui a toujours existé entre la presse et la guerre m'est revenu à l'esprit cette semaine pendant que je réfléchissais à ce que j'allais dire cet après-midi à propos du jour du Souvenir. 
La dernière se termine par ces phrases: 
La Guerre mondiale prendra fin ce matin à 6 h, heure de Washington, et à 11 h, heure de Paris. 
L'armistice a été signé à minuit par les représentants allemands. 
Le Département d'État en a fait l'annonce à 2 h 50 ce matin.  
Deux petites images viennent interrompre les colonnes de texte: l'une du kaiser Wilhelm, qui «tente de conserver sa couronne», l'autre de David Lloyd George, qui dit: 
Les conditions de paix doivent être justes, mais doivent empêcher qu'une telle absurdité ne se reproduise.  
On nous dit que «la grosse sirène d'Eaton a réveillé la ville tout entière».  
Dominant la fureur de la guerre 
Font retentir les cieux de leur chant: 
Paix sur terre aux combattants.  
Des blagues et un autre poème sur l'achat d'obligations constituent l'éditorial. 
L'article principal, intitulé «La fin de l'autocratie», contient cette prophétie: 
L'empire allemand n'est plus, mais le peuple allemand demeure. 
C'est avec lui que les alliés doivent conclure un règlement permanent. 
Les conditions de ce dernier feront que jamais plus de notre vivant l'Allemagne ne mettra la paix mondiale en danger.  
Et voici une citation de Lloyd George: 
L'insouciance qui a plongé le monde dans cette agonie atroce doit être sévèrement jugée.  
Le 8 mai 1945, la guerre qui, à en croire les journaux, ne devait jamais avoir lieu, prenait fin, après cinq ans et neuf mois. 
La manchette disait «This is Victory», en caractère Jesus, comme disent les imprimeurs. 
C'était la manchette. 
Les reportages abondent toujours, portant sur tous les fronts et citant le nom des héros de la victoire. 
Les éditoriaux saluent plus spécialement le général H.D.G. Crerar, ainsi que Churchill, Roosevelt et Staline. 
Le lendemain, un hommage à Harris, sans aide de Radio-Canada, et puis la chronique décapante de J.V. McAree: 
Nous nous rappelons le dangereux maire Houde qui sévit de nouveau, se fait acclamer et continue de répandre son venin contre nos alliés, et nous nous rappelons le drame spirituel du général McNaughton et l'entêtante révélation des divisions du Canada.  
Nous nous rappelons, non sans honte, les diverses attaques menées contre la présumée léthargie de la population, chez nous, et le passage d'étoiles de Hollywood venues nous rappeler notre devoir. 
Nous nous rappelons encore Dublin, la seule grande ville d'Europe de l'Ouest à être restée illuminée comme un arbre de Noël, aidant peut-être les bombardiers allemands à repérer leur cible dans la nuit.  
Dans la rubrique du très révérend R.J. Renison, on pouvait lire l'inévitable poème, cette fois sous la signature d'Arthur Noyes: 
Les cymbales retentissent et les danseuses s'avancent, 
Avec de longs bas de soie et des bras d'albâtre, 
Et les ombres d'hommes disparus les regardent.  
Une ombre dit: «Que pensiez-vous trouver, 
Quand le dernier coup fut tiré et que la paix fut faite?»  
Son ami répond, de son sourire absent: «Diable, 
Je pensais qu'ils prieraient pour la réconciliation.»  
Au moins, la poésie s'améliorait un peu, et cela veut peut-être dire que, après 52 ans, le Musée canadien de la guerre à Ottawa a encore quelques leçons à donner avant de se tourner vers de nouveaux horizons. 
Souvenons-nous.  
