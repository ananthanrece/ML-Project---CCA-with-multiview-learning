Le jour du Souvenir 
L'honorable B. Alasdair Graham (leader du gouvernement) :
Honorables sénateurs, on le sait, sir Max Aitken, devenu plus tard lord Beaverbrook, était un expatrié imaginatif et dynamique qui avait fait sa marque comme magnat britannique de la presse avant la Première Guerre mondiale. 
Pendant la guerre, il a joué un rôle essentiel en tant qu'envoyé spécial du Dominion du Canada sur le front occidental et il a, à ce titre, informé les Canadiens et le reste du monde sur cette guerre qui devait être la dernière.  
On doit à Aitken d'avoir commandé l'un des plus remarquables programmes d'art de tous les temps, le Canadian War Memorials Fund. 
Huit des immenses et magnifiques huiles créées dans le cadre de ce programme embellisent cette Chambre. 
Nos délibérations se tiennent sous le regard attentif des messagers du passé. 
Nos murs font écho à leurs sacrifices.  
D'autres ont décrit les événements de la Grande Guerre dans des lettres et journaux personnels. 
L'un des plus célèbres auteurs de ce type de récit est Talbot Papineau, qui a servi dans le Princess Patricia's Canadian Light Infantry, du mois d'août 1914 jusqu'à sa mort tragique survenue durant le carnage de Passchendaele, à l'automne de 1917.  
Petit-fils de Louis-Joseph Papineau, Talbot Papineau a incarné avec passion et intelligence l'esprit même d'un Canada bilingue et biculturel. 
Il est devenu un héros canadien qui a captivé le coeur et l'esprit de son époque.  
Le compte rendu qu'il nous fait de l'épreuve du feu sur le front occidental, pour en avoir été témoin, décrit l'horreur des gaz toxiques et des tranchées à l'odeur fétide, l'agonie des hommes rendus fous par l'artillerie lourde et décrit le courage, la valeur et les souffrances d'un monde qui avait perdu la raison.  
Le pays a pleuré sa mort. 
Des témoignages sont venus de partout dans le Dominion. 
Le Daily Mail de Grande-Bretagne a sans doute trouvé la meilleure formule, saluant en cet homme un «leader perdu». 
L'auteur de l'éditorial écrivait ce qui suit: «Puisse le Canada tirer de sa mort les leçons qu'il lui aurait enseignées s'il avait vécu».  
Un an avant sa mort, Talbot Papineau a illustré ces leçons dans une lettre ouverte à Henri Bourassa, l'éloquent directeur du journal Le Devoir .  
Papineau écrivait: 
Alors que je vous écris cette lettre, les Canadiens anglais et français se battent et meurent côte à côte. 
Leur sacrifice sera-t-il vain ou servira-t-il à cimenter les assises d'une véritable nation canadienne, une nation indépendante en pensée et en action?  
Honorables sénateurs, durant les prochaines heures et les prochains jours, nous rendrons hommage au sacrifice de ces générations. 
Nous rendrons hommage aux jeunes hommes et aux jeunes femmes qui ont combattu côte à côte pour cimenter les assises d'une véritable nation canadienne. 
Nous rendrons hommage à ceux qui, dans les airs, sur la mer et sur la terre, ont contribué à la défense du Canada. 
Nous nous souviendrons. 
Nous nous souviendrons des membres des divisions canadiennes qui ont libéré l'Europe de l'occupation nazie et dont le sacrifice a jeté un rayon de lumière sur tout un continent. 
Ils ont tant à nous dire au sujet de l'espoir, du sacrifice et de la puissance du coeur humain.  
Ils ont beaucoup à nous apprendre, surtout à ceux qui ont perdu le sens de l'engagement et l'esprit de tolérance que représente, et que représentera toujours le Canada, et à ceux parmi nous qui sont arrogants, indifférents ou négligents à l'égard de notre pays. 
Ils nous diraient que les citoyens victimes de telles propensions payent le prix ultime de l'échec, ils payent de leur propre liberté.  
Ils nous racontaient ces choses pour que leur souvenir ne soit pas perdu, pour que le souvenir de tant de leaders ne meure pas.  
En cette enceinte particulière, nous nous souvenons d'eux. 
Nous entendons leurs voix et leurs paroles. 
En ce lieu particulier où les murs nous rappellent à leur souvenir, nous rendons hommage à tous ceux qui ont donné leur vie et leur avenir pour que nous puissions vivre en paix. 
