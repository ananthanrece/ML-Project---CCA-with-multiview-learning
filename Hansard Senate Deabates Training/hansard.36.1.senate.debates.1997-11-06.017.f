Il sera haut de trois étages environ. 
Un compte a été ouvert à la banque. 
Les honorables sénateurs recevront peut-être sous peu une lettre, et vous pourriez très bien faire don d'une partie des frais de voyages qui vous sont remboursés. 
Je vais donner l'exemple.  
La deuxième moitié de la traduction concrète des recommandations du sénateur Andreychuk est sur le point de prendre forme. 
Nous érigerons un monument. 
Au prorata, les anciens combattants autochtones ont été tout aussi sinon plus nombreux à se porter volontaires que les autres citoyens.  
En cette période du Souvenir, il ne faudrait pas oublier les centaines d'autochtones qui ont sacrifié leur vie. 
Ils étaient courageux et ils ont su se montrer de valeureux guerriers doués d'une grande capacité d'adaptation. 
La longue réputation dont jouit notre armée et même certains de nos corps de la marine, nous la devons en partie du moins au courage et aux multiples talents de nos autochtones. 
Ils sont partis là-bas et ils se sont fort bien acquittés de leur mission.  
