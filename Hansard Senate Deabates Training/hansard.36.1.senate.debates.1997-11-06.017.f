L'honorable Orville H. Phillips :
Honorables sénateurs, je prends la parole aujourd'hui pour rendre hommage à ceux qui ont fait le sacrifice suprême, ainsi qu'à ceux qui ont fait la guerre et survécu. 
Je crois qu'il convient que nous honorions leur mémoire en cette dernière séance du Sénat avant le jour du Souvenir.  
Le sénateur Graham a dit que les images de la guerre mondiale qui nous entouraient nous rappelaient les sacrifices de nos soldats et l'horreur de la guerre. 
En tant que parlementaires, nous ne devons jamais oublier l'histoire de nos guerres. 
Tous les Canadiens qui ont connu l'une des deux guerres mondiales savent ce que cela représente en fait de sacrifices, de pertes, de démonstrations de courage et, oui, de regrets.  
J'ai parfois l'impression que, en honorant la mémoire de ceux qui ont fait le sacrifice suprême, nous avons tendance à oublier leur famille et le choc que doit ressentir une mère, une épouse, un frère ou une soeur en recevant un télégramme qui nous annonce qu'un être cher est porté disparu, a été blessé ou a été tué au combat. 
Nous ne devons pas oublier qu'un grand nombre de ces combattants morts au champ d'honneur laissaient derrière eux de jeunes enfants.  
Nous ne devons pas oublier les centaines de milliers de Canadiens qui ont quitté nos rives. 
Bon nombre d'entre eux ne sont jamais revenus. 
D'autres sont revenus blessés, physiquement ou mentalement, parce que beaucoup d'entre eux avaient de la difficulté à oublier les horreurs de la guerre et des combats. 
Ils en ont gardé les cicatrices toute leur vie.  
Comme les membres des nouvelles générations n'ont participé ni à la Première Guerre mondiale, ni à la Deuxième, nous devons veiller attentivement à promouvoir et à diffuser l'histoire de nos conflits armés. 
Il faut également prendre garde à ne pas minimiser l'importance de ces guerres mondiales. 
Beaucoup d'anciens combattants acceptent mal, par exemple, qu'on songe à changer le nom ou l'orientation du Musée canadien de la guerre. 
Quel épisode de l'histoire canadienne peut se comparer aux batailles de Vimy, de Dieppe, de Normandie et de l'Escaut, aux 1 000 raids aériens sur l'Europe ou à la Bataille de l'Atlantique? 
Honorables sénateurs, ces efforts ne peuvent être dilués en étant mêlés à d'autres segments de notre histoire.  
Aujourd'hui, le jour du Souvenir revêt de moins en moins d'importance aux yeux des générations qui n'ont pas connu la guerre. 
Dans le cadre de leurs négociations collectives, les syndicats demandent à ce que le jour du Souvenir soit désigné jour férié et probablement combiné au jour de Noël.  
Que nous oublions vite. 
De nombreuses universités ne célèbrent pas le jour du Souvenir. 
Le 11 novembre est un jour sacré et doit toujours le demeurer. 
Ce jour-là, les Canadiens doivent rendre hommage à ceux qui ont fait le sacrifice ultime et honorer ceux qui ont connu les horreurs de la guerre, qui ont façonné la nouvelle souveraineté de notre nation et défendu notre liberté. 
J'implore tous les parlementaires canadiens et tous les Canadiens de veiller à ce que l'histoire de ces conflits ne s'estompe pas comme les souvenirs des anciens combattants ont tendance à le faire.  
Je vous rappelle à tous l'engagement que nous prenons dans la Prière du souvenir. 
Comme il nous est impossible de garder une minute de silence, je terminerai en récitant la Prière du souvenir: 
Ils ne vieilliront pas comme nous qui leur avons survécu; 
Ils ne connaîtront plus l'outrage ni le poids des années.  
Quand viendra l'heure du crépuscule et celle de l'aurore, 
Nous nous souviendrons d'eux.  
