La Loi sur le tabac 
Projet de loi modificatif-Deuxième lecture-Ajournement du débat 
- Honorables sénateurs, je vous remercie de me permettre de participer au débat sur cet important projet de loi.  
Lorsqu'il est question de la lutte contre le tabagisme, je pense que le Sénat a amplement montré sa volonté collective. 
Nous avons certainement damé le pion aux fabricants de tabac qui vendent leur marchandise aux adultes et aux adolescents de notre pays.  
C'est dans cette optique que nous examinons aujourd'hui le projet de loi C-42, qui vise à modifier la Loi sur le tabac. 
Parce que nous avons à coeur la cause d'un société sans fumée, j'encourage mes honorables collègues à appuyer le projet de loi à l'étude.  
Honorables sénateurs, permettez-moi de prendre quelques minutes pour expliquer le but du projet de loi C-42. 
De toute évidence, ce projet de loi ne permettra pas d'enrayer immédiatement le tabagisme, objectif que je considère certainement louable, mais qui n'est guère réaliste pour l'instant. 
La triste réalité, c'est que près du tiers des Canadiens de plus de 15 ans - environ 7 millions de personnes - sont des fumeurs; et après quelques années, ils sont accrochés à cette substance qui crée une forte dépendance, la nicotine contenue dans les cigarettes.  
Même si la plupart des fumeurs connaissent les dangers inhérents au tabagisme et s'ils ont souvent essayé d'arrêter de fumer, ils en sont tout simplement incapables.  
Si l'obstacle est si terrible, pourquoi se donner toute cette peine? 
Quelle différence le projet de loi C-42 peut-il bien faire? 
La réponse, honorables sénateurs, c'est que l'habitude de fumer est une menace pour la santé publique qu'il faut combattre sur tous les fronts. 
La nécessité est aussi grande pour les restaurants de restreindre leurs sections pour fumeurs que pour les enseignants de renseigner les jeunes sur les risques du tabagisme. 
Les provinces doivent explorer toutes les solutions pour décourager la consommation de tabac.  
Dans le cadre de cet important effort de la société, le gouvernement fédéral a un rôle déterminant à jouer. 
Le Parlement peut exercer une grande variété de pouvoirs, allant de la possibilité d'influer sur le prix des cigarettes, au moyen de politiques fiscales, jusqu'au fait d'exiger la divulgation des additifs dans le tabac et des émissions produites par ce dernier.  
Le projet de loi C-42 ajouterait un autre pouvoir, celui d'interdire les commandites, par des fabricants de produits du tabac, de toute manifestation ou activité culturelle ou sportive une fois les cinq prochaines années écoulées. 
En 2003, les Canadiens, et surtout nos jeunes qui sont influençables, ne seront plus forcés de voir des noms de compagnies de tabac ou des marques de cigarettes quand ils iront assister à un match de tennis, des régates ou un festival de musique.  
Pourquoi est-ce si important? 
Parce qu'il est prouvé que la commercialisation de la cigarette est efficace. 
En associant la cigarette avec des manifestations populaires, qu'il s'agisse de courses automobiles ou de concerts, les fabricants font tout pour que leurs produits soient attirants. 
Ils y ont réussi. 
Aujourd'hui, près d'un jeune sur quatre âgé de 15 à 19 ans prend une habitude qui risque vraiment de ruiner sa vie un jour. 
Nous savons tous que plus de 40 000 Canadiens meurent chaque année de causes liées à la cigarette, et d'autres, innombrables, souffrent d'emphysème et d'autres maladies débilitantes.  
Par conséquent, si nous tenons vraiment à protéger la santé des Canadiens, nous devons faire notre possible pour dissuader les jeunes de commencer à fumer. 
Nous devons les aider à résister à la forte pression des camarades qui pèse si lourd sur eux - une pression qui est nourrie et exploitée par les compagnies de tabac qui font la publicité de leurs produits lors d'événements populaires.  
J'aimerais, parvenu à ce point, résumer les principaux éléments du projet de loi.  
Brièvement, les modifications proposées dans le projetde loi C-42 visent à renforcer la Loi sur le tabac adoptée au cours de la dernière législature. 
Cette mesure nous placerait carrément à l'avant-garde internationale de la lutte contre le tabagisme. 
D'autres pays élaborent des interdictions similaires, mais nous aurons les nôtres en place les premiers, trois ans avant l'Union européenne.  
Le projet de loi C-42 crée un processus transitoire qui donnerait aux événements existants une période de deux ans sans restrictions sur le parrainage. 
Pendant les trois années suivantes, les promotions extérieures au site de l'événement devraient se limiter aux 10 p. 100 inférieurs de tous les panneaux, affiches et autres placards associés à l'événement, et encore dans des endroits donnés. 
Cette période de transition donnera aux organisateurs le temps de trouver d'autres parrains.  
Dans cinq ans, il n'y aura plus du tout de parrainage par les compagnies de tabac. 
Même les noms et les installations ne pourront plus servir à rappeler le tabac de manière plus ou moins subtile, et la loi s'appliquera à toutes les formes d'événements, festivals d'artistes des rues, championnats de deltaplane, compétitions de danse ou salons de l'aviation.  
Honorables sénateurs, je voudrais attirer votre attention sur quelques autres changements apportés au projet de loi original durant l'étude en comité à l'autre endroit. 
Ces amendements, si je comprends bien, visent à répondre aux préoccupations de la communauté médicale, qui cherche activement à combattre la menace du tabagisme au Canada.  
Plus précisément, le premier de ces amendements précise que la période de cinq ans a commencé à courir le 1er octobre dernier. 
Autrement dit, le compte à rebours est déjà commencé.  
Les deux autres amendements limitent les dispositions de protection aux événements tenus récemment et déjà annoncés au Canada. 
C'est pour éviter que des événements devant avoir lieu aux États-Unis ou ailleurs soient transférés au Canada et traités comme des événements déjà tenus ici pendant la période de transition. 
De même, il ne serait pas permis de ressusciter des événements abandonnés depuis longtemps uniquement parce qu'ils constitueraient de bons véhicules promotionnels pour les produits du tabac.  
J'ai dit tout à l'heure que le projet de loi C-42 renforcerait la Loi sur le tabac. 
C'est vrai parce que la loi actuelle n'interdit pas complètement ce genre de promotion. 
Tout ce qu'elle exige, c'est que les compagnies de tabac respectent la règledes 90-10, c'est-à-dire que leur publicité doit être limitée à la partie inférieure des panneaux de publicité, dans un espace ne représentant pas plus de 10 p. 100 de la surface totale des panneaux.  
Le monde de la santé n'était pas satisfait de cette situation, et nous ne devrions pas l'être non plus. 
Si nous croyons qu'il faut briser l'association néfaste entre les cigarettes et le divertissement, nous devons le faire comme il faut. 
Se contenter de réduire l'espace prévu pour inscrire les marques ou déplacer ces marques sur les panneaux d'affichage ne suffira pas. 
Nous devons être prêts à débarrasser notre pays des signes et des symboles qui renvoient au tabac, parce qu'ils sont autant d'invitations malvenues lancées à nos jeunes. 
Ils sont comme l'appel des sirènes qui attirent les Canadiens vers un danger manifeste.  
Honorables sénateurs, nous sénateurs, comprenons bien l'importance de lutter avec tous les outils qui sont à notre disposition contre le plus grand fléau qui menace la santé publique. 
Le projet de loi C-42 à lui seul ne débarrassera pas le pays de tous les produits du tabac, mais il nous permettra sans nul doute de franchir une autre étape importante dans ce but.  
J'exhorte donc tous mes collègues à appuyer cette mesure législative.  
