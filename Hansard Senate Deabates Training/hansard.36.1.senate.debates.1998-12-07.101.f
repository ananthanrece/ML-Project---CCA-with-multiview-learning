L'honorable Serge Joyal :
Honorables sénateurs, je voudrais ajouter ma voix à celle de mes collègues pour rendre hommage au sénateur Forest. 
Je le fais après avoir entendu les paroles du sénateur Lavoie-Roux. 
Je veux rappeler le rôle que sa famille a joué dans la reconnaissance des droits linguistiques des Franco-Manitobains.  
En effet, j'étais secrétaire d'État du Canada en 1982, lorsque le beau-frère du sénateur Forest est venu me voir avec une contravention entre les mains en me demandant ce qu'il devait en faire.  
À l'époque, nous avions un programme de contestation judiciaire qui aidait les minorités de langues officielles à se présenter devant les cours pour tenter d'obtenir réparation lorsque les dispositions de la Charte devaient s'appliquer.  
Après avoir pris connaissance des arguments de M. Forest, j'ai demandé aux services juridiques du secrétariat d'État un avis sur l'opportunité d'offrir le financement requis par M. Forest. 
Je dois dire, pour l'histoire de notre pays, que l'avis des services juridiques du secrétariat d'État ne recommandait pas d'appuyer l'action de M. Forest. 
Les dispositions de la Charte ne permettaient pas de rendre illégale la loi de 1898 du Manitoba qui prohibait totalement l'usage du français dans les documents publics au Manitoba.  
J'ai revu les arguments mis de l'avant par les légistes du ministère. 
Ayant un minimum de compréhension juridique de la question, j'en ai conclu que dans le doute, il valait mieux prendre le risque. 
Nous prîmes donc le risque, et ce risque amena la Cour suprême du Canada, dans un jugement demeuré célèbre, à élaborer la doctrine de la nécessité. 
La cour arriva à la conclusion que toutes les lois du Manitoba, depuis 1898, étaient inconstitutionnelles et ne respectaient pas les dispositions qui régissaient le statut du Manitoba lors de son entrée dans la Confédération en 1870. 
Cependant, dans sa sagesse, la Cour suprême était d'avis qu'il fallait suspendre l'application du jugement jusqu'à ce qu'on en arrive à une méthode pour traduire la montagne des statuts, des lois et des règlements qui avaient été adoptés au Manitoba depuis 1898.  
Vous connaissez la suite des événements. 
Grâce à l'appui du gouvernement canadien et, je dois le dire, honorables sénateurs, grâce à la bonne volonté du gouvernement du Manitoba, à l'époque dirigé par le premier ministre Pawley, nous avons pu satisfaire aux obligations du jugement de la Cour suprême.  
Cependant, lorsque pour la première fois j'ai eu l'occasion de pénétrer dans cette Chambre et de faire la connaissance de Mme Forest, je lui ai dit qu'elle était tributaire d'un héritage important, celui qui a consisté à changer l'histoire de notre pays. 
Si aujourd'hui, madame, nous avons la possibilité de vous rendre hommage et de reconnaître votre mérite et celui de votre famille, ce n'est qu'un juste retour des choses.  
