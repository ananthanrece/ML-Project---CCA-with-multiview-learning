La violence dirigée contre les femmes 
L'honorable Lucie Pépin :
Honorables sénateurs, aujourd'hui, à nouveau, nous nous joignons aux Canadiens et aux Canadiennes qui, d'un océan à l'autre, ont souligné hier la Journée nationale de commémoration et d'activités concernant la violence dirigée contre les femmes.  
