Le 6 décembre est devenu plus qu'une simple commémoration de la mort de ces femmes fauchées dans leur prime jeunesse. 
C'est un appel à l'action qui nous rappelle que nous devons travailler ensemble pour mettre un terme à tous les actes de violence commis contre toutes les femmes et tous les enfants.  
Les travaux de recherche ont confirmé ce que les femmes et leurs organisations ont toujours su: la violence faite aux femmes marque profondément et a des effets pernicieux. 
Selon Statistique Canada, 51 p. 100 des Canadiennes ont vécu au moins un incident de violence physique ou sexuelle après l'âge de 16 ans.  
Depuis de nombreuses années, les organisations féminines oeuvrent inlassablement pour sensibiliser la population au problème de la violence contre les femmes et les enfants. 
Nous retrouvons, entre autres, le «Clothesline Project», qui est une exposition de T-shirts dessinés par des artistes qui ont survécu à une agression, à un viol ou à l'inceste.  
Le projet envoie un message clair, un message selon lequel la violence touche toutes les collectivités et a des répercussions socioéconomiques énormes pour le Canada.  
Le projet Clothesline contribue à faire en sorte que la violence ne soit plus un secret honteux que les femmes partagent alors qu'elles se racontent leurs histoires en étendant leurs vêtements sur la corde à linge, dans la cour. 
Le linge sale que l'on n'étalait pas dans une conversation polie est maintenant montré publiquement.  
Tout comme les chemises qui sont suspendues les unes contre les autres, les Canadiennes et les Canadiens doivent aussi travailler coude à coude pour tenter de changer les mentalités et les comportements. 
Tous les secteurs de la société, aussi bien les gouvernements que les entreprises, les organisations bénévoles ainsi que les hommes et les femmes, devront déployer des efforts pour éliminer la violence faite aux femmes et pour provoquer des changements importants et durables.  
J'exhorte tous les sénateurs à contribuer à faire de notre société un endroit plus sûr, en appuyant les mesures de lutte contre la violence dans nos collectivités.  
Les femmes et les enfants du Canada comptent sur nous.  
Les relations anglo-américaines 
L'anniversaire de la signature de la Charte de l'Atlantique à Terre-Neuve 
L'honorable Ethel Cochrane :
Honorables sénateurs, comme vous venez de l'entendre de la bouche du sénateur DeWare, la Déclaration universelle des droits de l'homme a été adoptée par l'Assemblée générale des Nations Unies le 10 décembre 1948. 
Avant leur rencontre, les observateurs avaient craint que les deux hommes ne parviennent jamais à trouver un terrain d'entente philosophique commun, étant donné leurs caractères énormément différents et leur indépendance d'esprit.  
Le 14 août 1941, les deux leaders se sont rencontrés, le croiriez-vous, au large des côtes de Terre-Neuve. 
Ils ont convenu que tous les peuples devraient avoir le droit de choisir leur propre gouvernement, que les frontières ne devraient pas être imposées par la force, que la coopération économique internationale était nécessaire et que l'usage de la force dans les relations internationales devrait cesser.  
Réunis à Londres le 24 septembre 1941, les représentants de dix autres pays en guerre contre les puissances de l'Axe ont déclaré leur appui à l'égard de la charte. 
En 1942, 26 pays avaient déclaré leur appui. 
Leur coopération devait mener plus tard à la création de l'Organisation des Nations Unies.  
Nous devrions remarquer, honorables sénateurs, que les grands principes de l'autonomie gouvernementale et de la liberté à l'égard de la persécution ont cimenté la détermination des forces alliées à combattre un adversaire commun. 
Cette coopération, fondée sur des principes, a clairement démontré qu'on peut faire abstraction des différences individuelles quand on travaille à la réalisation d'un objectif commun. 
C'est dans un esprit de coopération et de fraternité qu'on a gagné la Seconde Guerre mondiale.  
La Charte de l'Atlantique, cependant, parle un langage beaucoup plus important que la simple coopération entre les chefs d'État. 
La Charte de l'Atlantique revêt une importance historique parce qu'elle représente une des premières initiatives de coopération transfrontalière fondée sur la moralité et les principes, et parce qu'elle a établi les principes fondamentaux sur lesquels l'Organisation des Nations Unies a été édifiée.  
