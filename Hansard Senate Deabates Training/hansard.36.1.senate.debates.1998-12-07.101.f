Les Nations Unies 
Le cinquantième anniversaire de la Déclaration des droits de l'homme 
L'honorable Mabel M. DeWare :
Honorables sénateurs, je suis heureuse de souligner le 50e anniversaire de la Déclaration universelle des droits de l'homme, que le Canada et le monde entier célébreront le jeudi 10 décembre.  
Cet anniversaire sera une célébration glorieuse de la vie et de la liberté. 
La déclaration reconnaît que tous les humains, où qu'ils vivent, ont certains droits du seul fait qu'ils sont humains. 
Mais surtout, la déclaration protège ces droits.  
J'attirerai aujourd'hui l'attention de cette Chambre sur les droits politiques des femmes. 
Les droits politiques des femmes sont les mêmes que ceux des hommes. 
Les droits des femmes font partie des droits de la personne. 
Toutefois, les femmes ayant toujours fait l'objet d'une plus grande discrimination, nous devons accorder une attention plus soutenue à la protection de leurs droits.  
Honorables sénateurs, il est facile pour les Canadiens de tenir ces droits pour acquis. 
Après tout, les Canadiennes ont le droit de vote, elles peuvent être candidates aux élections et exercer des fonctions politiques. 
Je suis moi-même, à titre de femme sénateur, un exemple des droits politiques dont jouissent les femmes.  
Mes nombreuses collègues féminines au Sénat et ailleurs en sont également la preuve. 
Nous devons nous rappeler, cependant, que durant la majeure partie de l'histoire du Canada, les femmes n'avaient aucun droit politique. 
Aujourd'hui encore, ces mêmes droits ne sont encore qu'un rêve pour les femmes dans de trop nombreux pays.  
Les droits politiques des femmes et des hommes sont définis dans la Déclaration universelle des droits de l'homme. 
Les Nations Unies ont reconnu la nécessité d'un accord distinct pour protéger les droits politiques des femmes. 
C'est pourquoi l'Assemblée générale des Nations Unies a adopté, en décembre 1952, la Convention sur les droits politiques de la femme, entrée en vigueur en juillet 1954.  
Cette convention était le premier texte de droit international qui reconnaissait et protégeait les droits politiques des femmes, où qu'elles vivent. 
La convention reconnaît aux femmes le même droit qu'aux hommes de voter aux élections, de briguer des charges électives, d'occuper des fonctions politiques et d'exercer toutes les fonctions politiques en vertu du droit national.  
Il faut passer en revue l'historique de cet accord pour bien comprendre pourquoi il était nécessaire et comment il a vu le jour. 
Il faut, pour cela, remonter au tout début de l'existence des Nations Unies.  
Quand la charte des Nations Unies a été signée, le monde se relevait des horreurs de la Seconde Guerre mondiale. 
Dans de nombreux pays, les hommes et les femmes avaient travaillé, lutté et souffert ensemble, sur un pied d'égalité. 
Au cours de l'effort de guerre, de nombreux obstacles à la participation des femmes à la société sont tombés et les femmes ont entrevu un monde dans lequel elles bénéficieraient de libertés et d'un statut qui seraient égaux à ceux des hommes.  
Les auteurs du document fondateur de l'ONU ont donc tenu compte d'une vision d'égalité des sexes. 
Honorables sénateurs, la Convention de l'ONU sur les droits politiques de la femme a contribué à améliorer considérablement le rôle politique des femmes. 
Mais il reste beaucoup à faire. 
Certains pays continuent de nier aux femmes leurs droits politiques. 
Même dans les pays qui ne nient pas ces droits, les femmes ne les exercent pas uniformément.  
Aujourd'hui, les Nations Unies continuent d'élaborer des programmes d'éducation civile et politique ainsi que d'autres mesures pour encourager les femmes à participer activement.  
Mes collègues se joindront certainement à moi pour applaudir ces efforts au moment où nous célébrons le 50e anniversaire de la Déclaration universelle des droits de l'homme.  
Des voix:
Bravo!  
