L'honorable B. Alasdair Graham (leader du gouvernement) :
Honorables sénateurs, le célèbre chef-d'oeuvre de Lewis Carroll, «À travers le miroir», est riche en précieuses réflexions sur la vie et la condition humaine. 
Un de mes passages préférés, que j'aime bien réciter à mes petits-enfants, c'est celui où il est question de rêver le rêve impossible. 
Alice et la Reine conversent ensemble et la fillette dit en riant: «Il ne sert à rien de pleurer. 
On ne peut pas croire des choses impossibles». 
Ce à quoi la Reine répond: «C'est sans doute faute de ne pas avoir encore beaucoup pratiqué. 
Quand j'avais ton âge, je croyais jusqu'à six choses impossibles avant le petit-déjeuner!»  
Rêver le rêve impossible. 
Comment résumer mieux la magnifique existence de madame le sénateur Jean Forest, notre distinguée collègue et amie, qui a représenté un énorme apport pour cette Chambre et nous a quittés beaucoup trop tôt. 
Cependant, elle est de retour parmi nous aujourd'hui et se trouve à la tribune.  
Des voix:
Bravo!  
Le sénateur Graham :
Jean Forest a commencé à travailler à l'âge de 17 ans en enseignant à des élèves de 12e année qui ne devaient pas être beaucoup plus vieux qu'elle ni beaucoup plus grands d'ailleurs. 
Elle devint plus tard présidente du conseil scolaire catholique d'Edmonton, puis chancelière de l'Université de l'Alberta. 
Elle fut un des premiers membres de la Commission des droits de la personne de l'Alberta, puis membre de la fondation Dignité.  
Membre influent du conseil d'administration du Canadien national, elle avait, avant sa nomination au Sénat du Canada, participé activement à la privatisation de cette immense société d'État. 
Jean a siégé à divers comités consultatifs du gouvernement et a reçu de nombreux diplômes honorifiques. 
Elle est également officier de l'Ordre du Canada.  
En tant que politicienne, éducatrice et femme d'affaires, en tant que mère de sept enfants et grand-mère de 13 petits-enfants, chose qu'elle et moi avons en commun, elle n'a jamais décroché de la vraie vie. 
Quelqu'un a dit qu'il faut apprendre jour après jour, année après année, pour élargir nos horizons. 
Plus il y a de choses qu'on aime, plus on s'intéresse à beaucoup de choses, plus on apprécie ce qui nous entoure, plus on s'indigne, plus il nous en reste quand quelque chose se produit.  
Malgré toute l'énergie qu'elle a dépensée au fil des ans, malgré son engagement au service des autres, il en reste toujours beaucoup à Jean Forest: il lui reste du courage; il lui reste de la volonté pour aider ceux qui se battent pour faire régner la justice et pour réaliser la promesse d'un monde meilleur.  
Elle a été une source d'inspiration pour nous tous. 
Au cours de cette décennie où tant de Canadiennes tentent de mener de front leur carrière et un autre mode de vie, son grand bon sens au sujet de la vie et des relations humaines ont pesé beaucoup dans notre interminable quête d'une vie équilibrée. 
Ses croyances, sa foi et ses convictions ont toujours été à la base de sa vie active. 
Saint Mathieu parlait du rêve impossible de faire du monde un meilleur endroit où vivre, et de la foi qu'il faut pour y arriver. 
Pour Jean Forest, qui a toujours cru à au moins six choses impossibles avant le déjeuner, transporter des montagnes faisait partie d'une journée normale de travail.  
Jean, vous nous manquez. 
Nous sommes heureux de vous ravoir parmi nous aujourd'hui et nous vous invitons à revenir quand vous voudrez. 
Des voix:
Bravo!  
