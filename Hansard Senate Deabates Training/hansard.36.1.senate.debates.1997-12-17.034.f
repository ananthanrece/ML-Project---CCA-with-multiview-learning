Dans cette affaire, il convient de garder à l'esprit ce qui constitue un privilège au Sénat. 
Le privilège parlementaire est la somme des droits particuliers à chaque Chambre, collectivement, parlant en tant que parties constitutives de la Haute Cour qu'est le Parlement et aux membres de chaque Chambre individuellement, faute desquels il leur serait impossible de s'acquitter de leurs fonctions. 
Certains droits et immunités, comme l'immunité d'arrestation et la liberté de parole, appartiennent principalement à chaque parlementaire et existent parce que la Chambre ne pourrait s'acquitter de ses fonctions sans l'utilisation sans entraves des services de ses membres. 
D'autres droits et immunités, comme le pouvoir de sanctionner l'outrage et celui de réglementer sa propre constitution appartiennent principalement à chaque Chambre collectivement, aux fins de la protection de ses membres et de la défense de son autorité et de sa dignité propres. 
