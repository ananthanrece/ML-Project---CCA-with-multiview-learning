Les pêches 
La baie de Fundy-La destruction du saumon infecté dans des piscicultures-Le progrès de la mise en oeuvre du programme d'aide financière aux éleveurs-La position du gouvernement 
L'honorable Brenda M. Robertson :
Honorables sénateurs, ma question s'adresse au leader du gouvernement au Sénat et porte sur le programme d'aide financière à l'intention des salmoniculteurs de la baie de Fundy, qui sont aux prises avec l'anémie infectieuse du saumon.  
Récemment, le ministre des Pêches et de l'Aquaculture du Nouveau-Brunswick a ordonné le nettoyage de tous les bassins d'élevage du saumon infecté, qui avaient été utilisés en 1997. 
Cette mesure vise 21 piscicultures à Lime Kiln Bay, Bliss Harbour et Seal Cove. 
L'éradication du saumon de ces piscicultures est nécessaire pour réduire le risque de propagation de la maladie.  
Cette mesure portera un dur coup aux salmoniculteurs de la région, se traduisant par une baisse de 15 millions de dollars de leur chiffre d'affaires qui s'ajoute à la perte de 11 millions de dollars qu'ils ont déjà ont subie cette année. 
Le gouvernement provincial a mis sur pied un programme d'aide financière prévoyant des prêts sans intérêt et des garanties de prêt, et il collabore avec le gouvernement fédéral en vue d'élaborer un programme d'aide financière additionnel.  
Le leader du gouvernement au Sénat pourrait-il vérifier auprès du ministre des Pêches si des progrès ont été réalisés en vue d'élaborer un programme d'aide financière pour les éleveurs? 
Plus précisément, pourriez-vous obtenir l'échéancier que le gouvernement fédéral observe à cet égard? 
Les salmoniculteurs subissent cette perte depuis quelque temps déjà et ils ont besoin de connaître les détails de la participation et du soutien du gouvernement fédéral, ainsi que le moment où ils pourraient compter sur cette aide.  
L'honorable B. Alasdair Graham (leader du gouvernement) :
Encore une fois, je m'engage à fournir une réponse dans les plus brefs délais.  
