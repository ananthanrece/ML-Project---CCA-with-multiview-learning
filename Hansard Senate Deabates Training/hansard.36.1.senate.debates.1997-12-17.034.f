Attendu: que l'article 43 de la Loi constitutionnelle de 1982 prévoit que la Constitution du Canada peut être modifiée par proclamation du Gouverneur général, sous le grand sceau du Canada, autorisée par des résolutions du Sénat, de la Chambre des communes et de l'assemblée législative de chaque province concernée, 
Le Sénat a résolu d'autoriser la modification de la Constitution du Canada par la proclamation de Son Excellence le Gouverneur général sous le grand sceau du Canada, en conformité avec l'annexe ci-jointe.  
ANNEXE 
MODIFICATION DE LA CONSTITUTION DU CANADA 
La clause 17 des Conditions de l'union de Terre-Neuve avec le Canada figurant à l'annexe de la Loi sur Terre-Neuve est abrogée et remplacée par ce qui suit: 
«17.(1) 
En ce qui concerne la province de Terre-Neuve, la présente clause s'applique au lieu de l'article quatre-vingt-treize de la Loi constitutionnelle de 1867.  
(2) Dans la province de Terre-Neuve et pour cette province, la Législature a compétence exclusive pour légiférer en matière d'éducation, mais elle doit prévoir un enseignement religieux qui ne vise pas une religion en particulier.  
(3) L'observance d'une religion doit être permise dans une école si les parents le demandent.»  
TITRE 
L'honorable Landon Pearson :
Honorables sénateurs, je prends la parole pour appuyer la résolution visant à modifier la Constitution du Canada en ce qui a trait à la clause 17 des Conditions de l'union de Terre-Neuve avec le Canada. 
C'est la deuxième fois en deux ans que la province de Terre-Neuve soumet une résolution au Parlement en vertu de l'article 43 de la Loi constitutionnelle de 1982, afin de modifier les conditions de son union en ce qui a trait à l'éducation. 
J'ai participé au processus à chaque fois. 
J'étais membre du comité sénatorial permanent des affaires juridiques et constitutionnelles lorsque celui-ci a étudié la première demande de modification, et je siégeais au comité mixte spécial du Sénat et de la Chambre des communes qui a étudié la deuxième résolution, dont nous sommes maintenant saisis. 
J'ai écouté attentivement tous les témoins qui ont comparu, dans certains cas deux et même trois fois.  
Honorables sénateurs, j'ai appuyé la modification de 1996 parce que, comme je l'ai dit dans mon discours à l'époque, j'étais convaincue, d'après les témoignages que j'ai entendus à St. John's, que: 
Du même souffle, j'ai dit que si le Sénat n'adoptait pas la résolution, «la tâche de réconcilier les parties et de rétablir les relations serait encore plus difficile lorsque notre veto suspensif serait levé» et que lorsque la Constitution serait modifiée sans nous, de nouveaux problèmes surgiraient par suite du mécontentement.  
Honorables sénateurs, c'est précisément ce qui s'est produit.  
Nous avons maintenant une autre résolution à examiner. 
Comme je n'ai guère besoin de le rappeler à mes collègues, la nouvelle clause 17 que renferme cette résolution jouit de bien plus d'appuis que la précédente. 
Soixante-treize pour cent des Terre-Neuviens qui se sont rendus aux urnes pour le référendum de septembre ont voté en faveur de la résolution. 
L'Assemblée législative de Terre-Neuve et du Labrador l'a adoptée à l'unanimité. 
Les appuis à la Chambre des communes se sont accrus. 
La dernière étape nous revient à nous. 
J'espère que cette étape sera positive et constructive.  
Depuis que j'ai commencé à étudier la clause 17, j'ai été préoccupée par deux séries de questions. 
La première concerne les droits des élèves et de leurs familles, à l'aube du XXIe siècle. 
La seconde a trait aux questions liées à l'étude et à la pratique de la religion.  
En ce qui concerne les élèves et leurs familles, j'ai été très heureuse d'écouter leurs représentants pendant les récentes audiences du comité spécial mixte. 
C'est avec un plaisir particulier que j'ai écouté les jeunes de St. John's et de Corner Brook qui nous ont parlé par vidéoconférence. 
Ces élèves ont exprimé leur satisfaction d'avoir été consultés. 
