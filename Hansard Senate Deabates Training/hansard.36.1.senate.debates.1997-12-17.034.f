Nous sommes dans les écoles actuellement et cela va nous toucher.  
Stephanie Short, une étudiante du Heardman Collegiate, a dit: 
Je vous remercie d'avoir pris le temps de nous écouter. 
Je crois que nous avons tous trouvé très intéressant ce que les élèves avaient à dire. 
Une chose est sûre, ils ont montré, par leurs commentaires, leur réponses à nos questions et les discussions qu'ils ont eues entre eux - et ils l'ont fait un peu mieux que les témoins adultes - la réalité des tensions qui existent entre certains, nombreux, qui aspirent au cadre familier marqué par la présence de l'Église qu'offre le système confessionnel et d'autres qui aspirent au défi intéressant qu'offre un système public plus ouvert mais moins prévisible. 
Néanmoins, ils ont convenu que des changements étaient nécessaires.  
Jennifer Thornhill, représentante des élèves catholiques de la Regina High School, a décrit les problèmes dans son école: 
L'an dernier, nous avons perdu trois unités et demie d'enseignement. 
