Les résultats du référendum semblent montrer que la majorité des parents de la province sont d'accord avec cette affirmation.  
Pour ce qui est de la deuxième série d'enjeux, je rappelle aux sénateurs que la liberté d'information et la liberté de religion sont des droits fondamentaux garantis aux enfants en vertu des articles 13 et 14 de la Convention des Nations Unies relative aux droits de l'enfant, convention que le Canada a ratifiée en 1991. 
Ces articles se lisent ainsi: 
Il s'agit du paragraphe 13(1).  
Les États parties respectent le droit de l'enfant à la liberté de pensée, de conscience et de religion.  
La liberté de manifester sa religion ou ses convictions ne peut être soumise qu'aux seules restrictions qui sont prescrites par la loi et qui sont nécessaires pour préserver la sûreté publique, l'ordre public, la santé et la moralité publiques, ou les libertés et droits fondamentaux d'autrui.  
C'est ce disent les paragraphes (1) et (3) de l'article 14.  
Honorables sénateurs, je suis très convaincue que ces droits, qui sont selon moi plus fondamentaux que les droits de toute Église à contrôler ses écoles aux frais du gouvernement, sont tout à fait respectés dans la nouvelle clause 17. 
J'ai examiné attentivement le programme des études religieuses qu'on suit actuellement dans les écoles intégrées et j'ai écouté ce que les jeunes en disent. 
C'est un programme sensible aux besoins, large d'esprit, qui traite la question en profondeur et avec générosité.  
Au cours de nos audiences, on nous a dit que le ministère de l'Éducation de Terre-Neuve et du Labrador voulait vraiment travailler avec toutes les parties touchées - les représentants des Églises, les parents, d'autres membres de la collectivité, des spécialistes des programmes d'études - pour faire en sorte que le programme d'études offert à tous les niveaux soit enrichissant et adéquat et qu'il offre aux élèves des choix qui leur conviennent. 
Je n'ai pas de raison d'en douter.  
Des réserves ont été exprimées concernant le fait que la nouvelle clause 17 stipule que «la Législature [...] doit prévoir un enseignement religieux qui ne vise pas une religion en particulier» et que «l'observance d'une religion doit être permise dans une école si les parents le demandent». 
Toutefois, étant donné le climat créé à l'égard de ces questions dans le reste du Canada par la Charte des droits et libertés, je ne peux qu'envier les enfants de Terre-Neuve et leurs garanties constitutionnelles d'apprendre la religion et de participer aux cérémonies religieuses dans leur école, ou de les observer. 
J'aimerais que mes petits-enfants puissent jouir de ce même privilège dans le système public ontarien.  
Si nous choisissons la première option, selon le témoignage du ministre de l'Éducation, Robert Grimes, nous ferions recommencer le processus à zéro et il ne fait aucun doute que, ce faisant, nous nous attirerions la colère d'un grand nombre de Terre-Neuviens. 
Si nous choisissons la deuxième option, tout ce que nous réussirions à faire c'est retarder les préparatifs pour l'année scolaire prochaine et laisser les enfants, leurs parents et leurs enseignants dans l'incertitude. 
Toutefois, si nous choisissons la dernière option, nous serons gagnants sur toute la ligne.  
Je suis convaincue que c'est ce que veut la majorité des habitants de Terre-Neuve et du Labrador, y compris les élèves qui sont actuellement dans le système. 
Ils espèrent un avenir meilleur. 
Nous devrions faire tout ce qui est en notre pouvoir pour ne pas les décevoir.  
