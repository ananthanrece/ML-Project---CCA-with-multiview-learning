La défense nationale 
(Réponse à la question posée par l'honorable J. Michael Forrestall le 26 novembre 1997) 
Le 2e Bataillon du Royal Canadian Regiment dispose d'un effectif égal à celui de tout autre bataillon d'infanterie de la Force régulière. 
En prévision de la prochaine rotation en Bosnie, on a décidé d'envoyer une autre unité d'infanterie, à partir de la BFC Petawawa, afin de profiter de son bassin plus important de militaires qui ne font pas partie de l'infanterie et qui sont nécessaires pour former un groupement tactique. 
L'effectif des forces régulières n'a pas chuté en-dessous du plafond de 60 000 membres. 
L'effectif des forces régulières est de 61 205 membres depuis le 30 novembre 1997.  
En ce qui concerne la solde de la Réserve, des améliorations ont été apportées aux mécanismes de distribution de la solde aux membres de la Force de réserve. 
Le nouveau système, qu'on appelle le Système de solde révisé pour la Réserve (SSRR), a été livré par le contracteur au début de 1997 et a été mis en place à travers le Canada, en novembre dernier.  
Comme c'est le cas dans toute mise en oeuvre de système informatique majeur, soit au gouvernement ou dans le secteur privé, il y a eu un certain nombre de difficultés lors de la mise en place de la nouvelle technologie. 
Dans de tels cas, les unités locales transmettent au centre le montant précis de la solde que devrait recevoir un militaire pour chaque journée où il a travaillé. 
Les unités locales sont parfaitement au courant de ce processus; elles sont d'ailleurs constamment en communication avec le personnel qui administre le système central de solde. 
Quelle que soit la méthode de paiement, à part quelques cas d'exception, tous les membres sont payés à temps et reçoivent le montant exact de leur solde. 
S'il y a des cas précis d'individus qui ne sont pas payés, ces cas devraient être référés au gouvernement afin que l'on puisse intervenir et trouver la cause du problème.  
Les membres de la Réserve devraient avoir reçu le montant exact qui leur est dû, le 15 décembre 1997, journée de la paye.  
