L'honorable Paul Martin, ministre des Finances:
Honorables sénateurs, je vous remercie beaucoup de m'avoir invité aujourd'hui. 
J'ignore si c'était le même à son époque, honorables sénateurs, mais je ne le trouve pas très confortable.  
Honorables sénateurs, je tiens tout d'abord à vous remercier de m'offrir cette occasion de vous parler du projet de loi C-2. 
Grâce à cette loi, le Régime de pensions du Canada continuera de garantir une assise solide de revenu de retraite sûre aux travailleurs canadiens et à leur famille, à l'aube du nouveau millénaire. 
Il est le fruit d'un accord négocié par le gouvernement fédéral, huit provinces et les Territoires du Nord-Ouest. 
Cet accord lui-même est l'aboutissement du processus de consultations publiques le plus important exécuté conjointement par les deux niveaux de gouvernement dans un passé récent.  
Il est le symbole des grandes valeurs nourries par une très vaste majorité de Canadiens et de Canadiennes. 
C'est un fait que l'établissement du Régime de pensions du Canada, en 1966, se classe parmi les plus importantes initiatives de politique publique que notre pays ait connues. 
Il est inspiré d'une conviction qui dicte que le temps de la retraite ne devrait jamais être une période de pénurie pour les travailleuses et les travailleurs canadiens. 
Notre réussite en tant que pays peut se mesurer en grande partie par la protection que nous accordons à nos aînés.  
Un fait indéniable demeure: le Régime de pensions du Canada, qui a tant fait pour tant de gens, est soumis à des pressions de plus en plus fortes, et nous devons prendre des mesures dès maintenant, avant qu'il ne soit trop tard. 
L'accord fédéral-provincial conclu en février dernier nous ouvre la voie en ce sens. 
Le gouvernement fédéral et huit provinces ont montré qu'ils étaient prêts à prendre leurs responsabilités et à agir dès aujourd'hui, pendant qu'il est encore possible de gérer les problèmes qui touchent le RPC. 
Nous vivons en une époque d'évolution démographique - et historique - accélérée. 
Le Canada compte actuellement environ 3,7 millions d'aînés. 
Toutefois, ce nombre devrait augmenter considérablement à partir de 2011 environ, lorsque les «baby boomers» atteindront l'âge de la retraite. 
En 2030, il y aura quelque 8,8 millions d'aînés au Canada. 
Or, comme l'explosion des naissances survenue à la fin de la guerre a été suivie par un effondrement de la natalité, ces aînés représenteront une fraction plus importante que jamais de la population du pays.  
En 1966, lors de l'instauration du RPC, on comptait environ huit Canadiens en âge de travailler par aîné. 
En 1997, on compte cinq personnes en âge de travailler par aîné. 
En 2030, il n'y aura plus que trois Canadiens en âge de travailler par aîné.  
Un autre changement d'importance vient s'ajouter à ce dilemme démographique. 
Grâce aux progrès réalisés dans le domaine de la médecine et à un meilleur niveau de vie, l'espérance de vie est de plus en plus élevée et cette tendance devrait se poursuivre. 
L'espérance de vie des Canadiens et des Canadiennes d'aujourd'hui est de trois ans plus longue qu'en 1966. 
En l'an 2030, l'espérance de vie sera en moyenne de 4,5 ans plus longue. 
Ce phénomène a des répercussions importantes sur le Régime de pensions du Canada. 
Lorsque les «baby boomers» prendront leur retraite, ils recevront des prestations pendant une période moyenne de 20 ans contre 15 à l'époque où le RPC a été instauré.  
Compte tenu de ces réalités nouvelles, force est de constater que le RPC n'est plus viable sous sa forme actuelle. 
Il ne pourra relever les défis qui se poseront bientôt. 
L'actuaire en chef du RPC a énoncé ce défi en termes non équivoques. 
Monsieur le président, nous n'avons pas le droit de léguer un tel fardeau à nos enfants et à nos petits-enfants.  
Le projet de loi C-2 contient la solution à ce problème. 
Il renouvelle un engagement commun qui est de veiller à ce que le RPC demeure viable et abordable pour les travailleurs canadiens d'aujourd'hui et de demain. 
De plus, le projet de loi est le reflet d'un large consensus sur les changements requis.  
Lors des audiences fédérales-provinciales menées d'un bout à l'autre du pays, nous avons pris connaissance des avis d'actuaires et de spécialistes des pensions, de groupes de planification sociale et de chambres de commerce, de regroupements d'aînés et d'organisations représentant les jeunes, ainsi que de nombreux Canadiens qui se sentent concernés et qui ont réfléchi de manière consciencieuse. 
Le message que nous retenons de ces audiences est clair: les Canadiens veulent bénéficier du Régime de pensions du Canada, ont besoin de ce régime et comptent sur lui.  
Nous avons par la même occasion appris ce que les Canadiens ne veulent pas. 
Ils nous ont dit que nous ne devons pas laisser le RPC aller à la dérive, que le RPC ne doit pas être privatisé et qu'il ne doit pas non plus être éliminé. 
Par ailleurs, les Canadiens souhaitent que les problèmes du RPC soient réglés sans pour autant que l'on transmette aux jeunes générations un fardeau écrasant. 
Ils veulent que les problèmes du RPC soient réglés dès maintenant et qu'ils soient réglés de la bonne façon.  
Ils nous ont dit de préserver le RPC en renforçant son financement, en améliorant les pratiques de placement, et en freinant la hausse du coût des prestations. 
Le coût des pensions sera réparti de façon égale et équitable entre les générations. 
Grâce à cette capitalisation supérieure, les fonds du RPC augmenteront de façon importante.  
Une nouvelle politique de placement est donc proposée afin que les fonds du RPC soient investis de manière à offrir le meilleur rendement possible, ce qui est dans l'intérêt de tous les Canadiens qui cotisent au régime.  
Chaque dollar gagné sur les placements est un dollar de moins à payer par les travailleurs canadiens et leurs employeurs. 
Les Canadiens ont le droit de profiter du meilleur taux de rendement possible, tout en sachant qu'ils peuvent compter sur leurs prestations.  
Le projet de loi C-2 propose que le fonds du RPC soit investi de façon prudente par des professionnels dans un portefeuille diversifié de valeurs mobilières. 
Toutes les précautions seront prises pour préserver les décisions de placement de toute influence d'ordre politique. 
L'Office d'investissement n'aura aucun lien de dépendance avec les gouvernements.  
Monsieur le président, en conjonction avec ces changements importants au financement du régime et à la politique de placement, nous apportons des modifications au mode d'administration et de calcul des prestations. 
Je précise immédiatement que ces modifications n'auront aucune incidence sur les prestations de pension reçues à l'heure actuelle par les aînés. 
J'ajouterai que 75 p. 100 des changements sont d'ordre financier et que 25 p. 100 seulement ont trait aux prestations proprement dites.  
Quiconque prétend que le RPC est en parfaite santé doit vivre dans un autre monde. 
La solution de facilité, pour un gouvernement, consiste à adopter la politique de l'autruche et à prétendre que tout va bien.  
Pourtant, il suffit de quelques notions mathématiques et d'un peu de bon sens pour comprendre que plus nous tardons à agir, plus il faudra hausser les cotisations dans l'avenir. 
Si on considère objectivement et rationnellement la situation, on comprendra que la solution la plus équitable et la seule qui soit responsable est de s'assurer que les gens comme vous et moi commencent à payer plus pendant que nous sommes encore sur le marché du travail de manière à ne pas refiler à nos enfants une facture absolument écrasante.  
D'autres considèrent que le projet de loi C-2 constitue une ponction fiscale, sous forme de cotisations plus élevées au RPC, et que ces mesures devraient être contrebalancées par une réduction des cotisations d'assurance-emploi. 
Ce raisonnement est non seulement boiteux, mais il est carrément absurde. 
Elles constituent une épargne, qui prend la forme de prestations de pension, et elles contribuent à la protection financière de la famille. 
C'est d'ailleurs la raison pour laquelle nous venons de ramener les cotisations d'assurance-emploi de 2,90 $ à 2,70 $. De plus, nous nous sommes engagés à utiliser une partie du dividende budgétaire, lorsque le budget fédéral sera équilibré, pour réduire le fardeau fiscal. 
Toutefois, nous ne permettrons pas que la question de la nécessité de préserver le Régime de pensions du Canada donne lieu à des marchandages insensés ou à des affirmations fausses. 
Nous devons aux Canadiens qui s'attendent de bénéficier d'une retraite sûre de choisir des solutions judicieuses. 
C'est ce que nous faisons avec le projet de loi C-2.  
À l'autre extrême, honorables sénateurs, il y a des personnes qui considèrent que le RPC est en trop mauvais état pour pouvoir être renfloué, et qu'il vaudrait mieux recourir à des REER obligatoires, qui coûteraient moins cher et fourniraient de meilleures pensions que le RPC. 
Tout d'abord, monsieur le président, je tiens à préciser que les REER sont un élément essentiel et très important de notre régime de retraite, de notre revenu de retraite. 
Les Canadiens et les Canadiennes qui planifient en vue de leur retraite comptent à la fois sur le RPC et sur leurs économies détenues dans les REER. 
Comme je l'ai dit dans la mise à jour économique et financière, l'aide fiscale dont bénéficient les Canadiens et les Canadiennes qui économisent en vue de leur retraite dans le cadre de RPA ou de REER sera accrue aussi rapidement que possible.  
Toutefois, monsieur le président, les Canadiens d'un océan à l'autre nous ont clairement affirmé qu'ils ne veulent pas que la totalité de leurs pensions de base soit à la merci de décisions sur des placements privés. 
Ils ont tout à fait raison.  
Arrêtons-nous un instant à ce qui se passerait si nous décidions d'adopter la solution de l'épargne-retraite exclusivement privée, sous forme de REER. 
Pour tous les Canadiens, la planification de la retraite deviendrait en bout de ligne une question de chance ou de clairvoyance hors du commun dans les placements.  
En deuxième lieu, le choix de se retirer du marché du travail pour élever des enfants se traduirait par un revenu de retraite moins élevé.  
En troisième lieu, des familles entières n'auraient plus aucune protection financière en cas de décès ou d'invalidité d'un cotisant au RPC. 
