La Loi sur l'impôt sur le revenu 
Motion d'amendement-Ajournement du débat 
L'honorable Michael A. Meighen , conformément à l'avis du 15 décembre 1997, propose: 
Que le Sénat presse le gouvernement de proposer, dans le budget de février 1998, une modification à la Loi de l'impôt sur le revenu, tendant à porter à 30 p. 100, par augmentations de 2 p. 100 par année sur cinq ans, le plafond de la proportion de biens étrangers des régimes de revenu différé (régimes de pension, régimes enregistrés d'épargne retraite, régimes de pension agréés) comme cela a été fait entre 1990 et 1995, alors que le plafond de biens étrangers des régimes de revenu différé a été porté de 10 p. 100 à 20 p. 100: 
a ) parce que les Canadiens devraient avoir la possibilité de tirer avantage de meilleurs rendements sur leurs investissements dans d'autres marchés, ce qui aurait pour effet d'augmenter la valeur des avoirs financiers qu'ils détiennent en prévision de la retraite, de réduire le montant de supplément du revenu de sources gouvernementales dontles Canadiens pourraient avoir besoin et d'augmenter les recettes fiscales que le gouvernement tire des revenus de retraite; 
b ) parce que les Canadiens devraient avoir plus de flexibilité au moment d'investir les épargnes qu'ils accumulent en prévision de leur retraite tout en réduisant les risques que comportent ces placements grâce à la diversification; 
c ) parce qu'une amélioration de l'accès aux marchés boursiers mondiaux permettrait aux Canadiens de participer tant aux économies qu'aux secteurs industriels à plus forte croissance; 
d ) parce que le plafond actuel de 20 p. 100 est devenu artificiel depuis que les particuliers et les régimes de pension disposant de grandes ressources peuvent le contourner en ayant recours, par exemple, à des décisions stratégiques en matière d'investissement et à des produits dérivés; 
Cette motion va dans le sens de ce qui a été fait entre 1990 et 1995, alors que le gouvernement a porté le plafond de biens étrangers des régimes de revenu différé de 10 p. 100 à 20 p. 100. 
Puisque la motion dont nous sommes saisis concerne la grande majorité des Canadiens, elle énonce, en termes simples, les cinq raisons qui motivent l'augmentation proposée. 
Je vais épargner aux honorables sénateurs la répétition de ces raisons, qui figurent en bonne et due forme dans le hansard.  
J'expliquerai brièvement les origines de cette motion et son objectif.  
En 1990, le parti que je représente a entrepris de porter à 10 p. 100, par augmentations de 2 p. 100 par année sur cinq ans, le plafond de la proportion de biens étrangers des régimes de revenu différé, tout comme nous proposons de le faire aujourd'hui. 
En 1995, le parti de mes collègues d'en face a poursuivi le processus, si bien que le plafond de la proportion de biens étrangers est aujourd'hui fixé à 20 p. 100. 
Je dis ceci pour souligner que ce n'est pas une question d'idéologie politique, mais une question qui touche des gens en chair et en os. 
Je ne spéculerai pas sur les raisons qui justifient la règle sur les biens étrangers. 
Plutôt, je vais simplement dire que la décision d'assouplir la limite est justifiée du fait que c'est le marché mondial des valeurs mobilières qui donne aux Canadiens la possibilité d'accroître la valeur de leur épargne-retraite. 
Je crois que c'est aussi vrai aujourd'hui qu'en 1990, peut-être même plus.  
En août 1996, le comité sénatorial permanent des banques et du commerce a publié son rapport intitulé «Régie des sociétés» à la suite d'une longue étude des modifications proposées à la Loi sur les sociétés par actions. 
Le comité, sous la présidence du sénateur Kirby, a formulé 27 recommandations, la dernière demandant que le gouvernement étudie l'impact sur les marchés financiers canadiens de la règle sur les biens étrangers en vue de sa suppression graduelle à court terme.  
Les témoignages qui ont conduit à cette recommandation étaient impressionnants et portaient sur le rôle des grands investisseurs sur les marchés financiers canadiens que le comité des banques et du commerce étudie à l'heure actuelle. 
M. Tom Allen, un administrateur de plusieurs entreprises, qui a témoigné au nom de la Bourse de Toronto a résumé la situation ainsi: 
[...] nous avons des règles sur les biens étrangers au Canada qui nous obligent à conserver un certain pourcentage de nos fonds dans les valeurs mobilières canadiennes. 
L'éventail des valeurs mobilières qui sont admissibles pour ces investissements est si petit par rapport à nos fonds qui sont si importants...  
D'autres témoins, comme le président de la Caisse de retraite des employés municipaux de l'Ontario, M. Dale Richmond, se sont également élevés contre des restrictions si serrées. 
Il a déclaré: 
[...] nous sommes tenus d'investir 80 p. 100 de nos fonds sur le marché canadien et seulement 20 p. 100 sur les marchés étrangers. 
[...]La règle applicable aux placements en biens étrangers nous empêche vraiment de faire des investissements nous permettant de répondre aux objectifs qui nous sont fixés aux termes du droit fiduciaire et des lois qui nous régissent, et cela pour des raisons qui n'ont rien à voir avec la prudence dont il faut faire preuve dans la gestion d'une caisse de retraite.  
M. Edward Waitzer, qui était alors président de la Commission des valeurs mobilières de l'Ontario, a invoqué des arguments similaires. 
Il a déclaré, et je cite: 
Le Canada n'est pas un marché de grande envergure. 
Vous n'avez qu'à consulter l'indice Morgan Stanley pour vous rendre compte que les titres canadiens représentent moins de 2 p. 100 de la valeur du capital-actions à l'échelle mondiale. 
[...] La règle sur les biens étrangers est de nature à faire en sorte que les investisseurs de l'extérieur n'accordent pas au marché canadien toute l'importance qu'il mérite. 
À l'échelle mondiale, elle est tout à fait hors norme...  
En fin de compte, le comité des banques s'est rallié à l'unanimité à l'idée que le gouvernement devrait procéder rapidement à l'élimination progressive de la règle sur les biens étrangers.  
En octobre dernier, on a rendu publique une étude d'impact réalisée par Ernst & Young au sujet de la règle sur les biens étrangers. 
Il ressort de celle-ci que, si la limite avait été fixée à 30 p. 100 au cours des 25 dernières années, le rendement annuel des portefeuilles d'épargne-retraite des investisseurs canadiens aurait été de 1,6 p. 100 plus élevé.  
Cela a, bien sûr, eu un effet cumulatif énorme; pour un investisseur moyen, cela se traduit par une perte en capital de 32 000 $ à la retraite. 
Il y a quelques semaines, le comité des banques a entrepris son étude des investisseurs institutionnels et il a été inondé de demandes visant soit l'augmentation de la limite, soit l'élimination pure et simple de la règle sur les biens étrangers. 
Compte tenu du caractère extrêmement convaincant des représentations faites, j'avais l'intention à l'origine de rédiger un projet de loi du Sénat, de demander aux membres du comité sénatorial des banques d'accorder leur appui unanime, puis de solliciter l'appui de tous les sénateurs. 
Toutefois, j'ai décidé que même si un tel projet de loi jouirait probablement d'un appui généralisé ici et dans l'autre endroit - et je rappelle aux honorables sénateurs que le comité des finances a appuyé le contenu de la motion dans son étude prébudgétaire - ce n'était peut-être pas la meilleure façon de susciter des changements à court terme. 
Cela dit, je me réserve la possibilité de déposer le projet de loi si le ministre décide de ne pas prendre de mesures en février.  
Dans son exposé de principes, M. Keith Ambachtsheer - le nom de ce consultant et spécialiste des régimes de pensions vous deviendra de plus en plus familier - prône un élargissement de la règle de 20 p. 100 pour les raisons suivantes: 
Au cours de la période de 1985 à 1995, la règle relative à la propriété étrangère a entraîné un manque à gagner en revenus de placement de quelque 20 milliards de dollars pour les régimes de pensions en fiducie au Canada. 
Même avec la limite actuelle de 20 p. 100, on estime que la règle relative à la propriété étrangère prive les régimes de pensions des Canadiens de 700 millions de dollars par année. 
Une autre façon d'évaluer les coûts consiste à calculer les prestations de pension perdues et les cotisations supplémentaires qui doivent être payées par les employeurs pour compenser cette perte, ce qui désavantage les employeurs canadiens face à la concurrence. 
La règle relative à la propriété étrangère fait augmenter le coût du capital pour les émetteurs canadiens d'actions et d'obligations, au lieu de le faire diminuer. 
L'élimination de cette règle serait à l'avantage des émetteurs et des investisseurs canadiens.  
La mondialisation des capitaux rend de plus en plus vague la distinction entre les placements faits au Canada et à l'étranger. 
En outre, la règle relative à la propriété étrangère n'est pas de nature à inciter les fournisseurs canadiens de services de placement à devenir des participants à part entière aux marchés financiers internationaux, bien au contraire.  
Honorables sénateurs, l'actuelle crise asiatique justifie d'autant plus la nécessité d'assouplir la règle relative à la propriété étrangère, puisque seule la diversification permettra aux Canadiens de se protéger véritablement contre les secousses du marché et les cycles économiques, tant au niveau national qu'international. 
À ceux qui soutiennent que le moment est mal choisi, je dirai que le moment n'est jamais idéal, mais qu'une faible hausse de 2 p. 100 par année sur une période de cinq ans risque fort peu de miner notre dollar. 
D'ailleurs, ce n'est qu'une question de point de vue, puisque de nombreux investisseurs voient des occasions là où d'autres n'en voient pas.  
Non seulement la règle applicable aux biens étrangers va à l'encontre des obligations que doivent assumer les gestionnaires du Régime de pensions du Canada, mais les investisseurs canadiens ont, eux-mêmes, affirmé qu'ils étaient en faveur de l'accroissement du pourcentage de titres étrangers, lorsque la règle applicable aux biens étrangers est passée de 10 p. 100 en 1990 à 20 p. 100 en 1995. 
En moyenne, le pourcentage de titres étrangers que renferme les régimes de retraite canadiens est passé de 9 p. 100 à 18 p. 100 au cours de la même période.  
Honorables sénateurs, je n'ai entendu personne défendre le maintien de la règle applicable aux biens étrangers à son niveau actuel ou même aborder cette question. 
Tous les témoins que j'ai entendus parlaient plutôt de réviser la règle à la hausse ou de l'abolir. 
Néanmoins, je voudrais, en terminant, faire valoir des arguments en faveur du maintien de la règle à son niveau actuel.  
Certains affirment que, en forçant les administrateurs des caisses d'épargne-retraite à investir dans des valeurs mobilières d'émetteurs canadiens, on offre au Canada des avantages considérables, tant au niveau de l'investissement de capitaux que de la création d'emplois, dont il ne pourrait pas jouir autrement. 
Honorables sénateurs, rien ne prouve que cela soit vrai. 
En fait, l'existence de la règle applicable aux biens étrangers fait augmenter le coût des capitaux au Canada ainsi que les frais que l'employeur doit assumer pour fournir des prestations de retraite. 
De toute façon, il est inadmissible d'agir ainsi aux dépens des millions de Canadiens dont le revenu de retraite repose sur la capacité des gestionnaires de leur régime de pension de maximiser leurs rendements à un niveau acceptable de risque.  
D'autres pourraient prétendre que l'utilisation, par exemple, d'instruments dérivés peut permettre de contourner facilement la règle applicable aux biens étrangers. 
Toutefois, les honorables sénateurs ne sont pas sans savoir que les contribuables et les gestionnaires de régimes de retraite n'ont pas tous les ressources et les connaissances nécessaires pour avoir recours à des instruments d'investissement comme les instruments dérivés. 
De plus, ce ne sont pas tous les marchés étrangers qui négocient des contrats à terme. 
Certains investisseurs peuvent ne pas vouloir utiliser ce genre de contrats dans le cadre de leurs programmes d'investissement. 
De plus, comme de nombreux fiduciaires considèrent que la participation étrangère au-delà de la limite actuelle va à l'encontre de la lettre de la loi, ils refusent d'utiliser des instruments dérivés.  
Au cours des cinq dernières années, le plafond des placements en biens étrangers est passé de 10 à 20 p. 100 et l'entrée de capitaux financiers a été deux à trois fois plus grande que la sortie de capitaux d'investissement qui en a découlé, les investisseurs étrangers continuant d'acheter des titres de créance et des titres de participation canadiens. 
Je dirai à tous les sénateurs que les marchés de capitaux du Canada sont stables, mûrs et capables d'attirer les capitaux.  
Honorables sénateurs, je veux signaler deux points. 
D'abord, ce n'est pas en haussant le plafond de deux points de pourcentage par année qu'on va nuire aux marchés canadiens de capitaux. 
Les sénateurs seront peut-être intéressés d'apprendre que si le Canada limite à 20 p. 100 les placements en biens étrangers, nos principaux partenaires commerciaux que sont les États-Unis et le Royaume-Uni n'imposent aucun plafond aux placements en biens étrangers par les caisses de retraite et que le Japon les limite actuellement à 30 p. 100. 
Honorables sénateurs, la répartition de l'épargne-retraite des Canadiens est manifestement une activité du marché. 
Les Canadiens devraient être aussi libres que possible d'investir leur épargne-retraite où bon leur semble. 
Les régimes de retraite visés par la règle sur les biens étrangers constituent pour les Canadiens le principal moyen de se préparer à la retraite.  
Il faut hausser le plafond des placements en biens étrangers pour permettre aux Canadiens de diversifier davantage leurs effets en portefeuille et de maximiser le rendement de leurs placements afin de s'assurer une bonne retraite. 
