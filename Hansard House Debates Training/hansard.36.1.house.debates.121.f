Monsieur le Président, la Loi sur le Bureau canadien de la sécurité des transports a été adoptée pour la première fois en 1989, sous le gouvernement Mulroney. 
Il réunissait sous le même toit un certain nombre d'organismes fédéraux responsables de la sécurité des transports.  
Le NPD s'était opposé à la loi originale pour trois raisons. 
Premièrement, un Bureau de la sécurité des transports nouvellement créé était sous-financé et insuffisamment équipé pour s'acquitter de son vaste mandat, qui comprend les enquêtes relatives à tous les accidents aériens, maritimes et ferroviaires ainsi que ceux concernant les pipelines.  
Deuxièmement, la loi permet aux ministres et aux sociétés privées intéressées d'examiner les rapports provisoires du bureau et de les commenter. 
Cette façon de faire exerce une influence indue sur la rédaction des rapports finals et compromet l'indépendance du bureau.  
Troisièmement, la loi originale ne protège pas adéquatement la confidentialité des témoignages des travailleurs devant le bureau. 
Sans cette confidentialité, les travailleurs risquent des représailles s'ils témoignent contre leur employeur.  
Le projet de loi S-2 modifie la loi originale sur le Bureau d'enquête sur la sécurité des transports. 
Nous appuyons ces facettes du projet de loi. 
On y trouve aussi quelques nouvelles dispositions visant à étendre la protection des renseignements personnels aux gens qui témoignent devant le Bureau d'enquête sur la sécurité. 
Nous appuyons ces propositions parce qu'elles font tomber l'une des trois objections que le NPD opposait à la Loi sur le bureau de la sécurité des transports, en 1989. 
Toutefois, malgré ces changements favorables, nous nous opposons encore au projet de loi S-2, parce qu'il ne fait qu'empirer une mauvaise loi.  
Le Bureau de la sécurité des transports est toujours sous-financé. 
D'un bout à l'autre du pays, on a dénombré l'an dernier plus de 2 000 accidents aériens, maritimes, ferroviaires ou de pipeline. 
Pourtant le bureau n'emploie que 135 enquêteurs. 
Un bon nombre de ces accidents ne nécessitaient pas d'enquête approfondie, mais le bureau a néanmoins accumulé des arriérés importants. 
L'arriéré de travail est tellement important que le bureau a dû mener ses enquêtes avec précipitation pour faire du rattrapage. 
Cela constitue une menace pour la sécurité publique. 
Le bureau a besoin d'un financement accru pour pouvoir embaucher davantage d'enquêteurs qui assurent adéquatement la santé et la sécurité des Canadiens. 
Le projet de loi S-2 permettrait au gouvernement de transformer certains postes de membres du bureau qui sont à plein temps en postes à temps partiel. 
Le bureau disposerait donc d'encore moins d'heures de travail. 
Les fonctionnaires dévoués du Bureau de la sécurité des transports font de leur mieux. 
Les libéraux ont, envers les Canadiens, l'obligation de donner à ces fonctionnaires les ressources dont ils ont besoin pour s'acquitter de leur important travail.  
Le pire aspect du projet de loi S-2, c'est qu'il enveloppe de mystère le processus qui permet aux entreprises privées d'influencer le bureau de la sécurité. 
C'est déjà bien assez qu'elles puissent examiner les ébauches de rapports du bureau et faire des observations.  
Le projet de loi S-2 rendrait ces observations secrètes. 
Les Canadiens s'en remettent au bureau de la sécurité pour assurer leur santé et leur sécurité lorsqu'ils voyagent. 
Ils ont le droit de savoir ce que les entreprises privées disent au bureau de la sécurité.  
Quant au projet de loi proprement dit, il rend une mauvaise loi encore plus mauvaise en enveloppant de secret les travaux du bureau et en compromettant ainsi son indépendance.  
Les dispositions du projet de loi qui visent à dissimuler les observations que le secteur privé fait au bureau sont scandaleuses. 
Le simple fait que ce secteur puisse influencer le bureau de la sorte tient de la farce. 
Les libéraux auraient dû saisir cette occasion pour retirer de la loi ces dispositions ridicules. 
Au lieu de cela, ils essaient de faire des cachotteries aux Canadiens. 
Les Canadiens comptent sur le Bureau de la sécurité des transports pour garantir la sécurité du transport ferroviaire, et aérien, de la navigation et du transport par pipeline. 
La sécurité des transports est extrêmement importante pour le déplacement des marchandises et des passagers dans notre pays. 
Les gens doivent avoir l'assurance que le bureau fait son travail efficacement si l'on veut qu'ils se déplacent et qu'ils fassent des affaires en toute confiance au Canada. 
Les libéraux ont donc, envers les Canadiens, l'obligation de faire en sorte que le bureau de la sécurité soit transparent et qu'il rende des comptes.  
M. Charlie Power (St. John's-Ouest, PC):
La Loi sur le Bureau canadien d'enquête sur les accidents de transport et la sécurité des transports a été adoptée par le Parlement en juin 1989. 
Cette dernière prévoyait la création d'un organisme fédéral indépendant baptisé Bureau canadien d'enquête sur les accidents de transport et la sécurité des transports, ou BST.  
Le BST a pour mandat de faire enquête sur les accidents concernant les pipelines et les moyens de transport maritime, ferroviaire et aérien. 
La Loi originale sur le Bureau canadien d'enquête sur les accidents de transport et la sécurité des transports prévoyait, à l'article 63, un examen obligatoire du fonctionnement de la loi.  
Cet examen a été entrepris en 1993 par le Comité de révision du Bureau canadien d'enquête sur les accidents de transport et la sécurité des transports, qui a présenté son rapport au Parlement en 1994. Entre-temps, la commission Moshansky ,chargée d'enquêter sur l'accident survenu à l'aéroport de Dryden en mars 1989, avait terminé son travail et formulé des recommandations concernant le Bureau canadien d'enquête sur les accidents de transport et la sécurité des transports.  
Le projet de loi S-2 est issu des recommandations faites par la commission Moshansky et par le Comité de révision du Bureau canadien d'enquête sur les accidents de transport et la sécurité des transports. 
Le projet de loi S-2 propose de mettre de l'ordre dans ce qui est déjà une loi très solide.  
Le projet de loi S-2 a été présenté au Sénat le 30 septembre 1997. 
Il a été étudié par le Comité sénatorial des transports et des communications, qui a proposé trois amendements. 
L'un de ces amendements vient des sénateurs conservateurs, que j'aimerais d'ailleurs remercier pour le travail intensif qu'ils ont fait sur cet important projet de loi concernant la sécurité des transports.  
L'amendement proposé par les conservateurs au Sénat permet au BST d'avoir deux membres à temps partiel. 
Actuellement, le bureau ne compte que quatre membres et il y a une vacance car il est difficile de trouver un cinquième membre à plein temps. 
La nouvelle disposition concernant la nomination de membres à temps partiel est une mesure positive qui permettra de maintenir un effectif sain au BST.  
Les sénateurs libéraux ont proposé deux amendements au projet de loi S-2. 
L'un est très bon et l'autre très mauvais. 
Le premier amendement proposé par les libéraux était une disposition transitoire en vertu de laquelle les poursuites en cours ou en instance pourraient continuer une fois que le projet de loi sera adopté. 
C'est une mesure utile qui nous protégera des lacunes en attendant que le projet de loi S-2 entre en vigueur.  
Le deuxième amendement proposé par les libéraux n'était pas vraiment une bonne idée. 
En fait, il était si mauvais que le gouvernement libéral fait maintenant marche arrière et demande qu'il soit supprimé. 
Il a trait à la protection des enregistrements faits à l'aide de câbles terrestres dans les systèmes de contrôle du trafic aérien.  
Cet amendement a été conçu sans avis juridiques, sans consultation auprès du Bureau d'enquête sur les accidents de transport et, à ma connaissance, sans consultation auprès de quiconque au sujet des effets néfastes possibles de cet amendement. 
Celui-ci aurait pour effet non intentionnel d'empêcher l'employeur et l'organisme de réglementation d'accéder à l'information qui, de l'aveu même du gouvernement, est «nécessaire pour garantir la qualité et la sécurité de certains éléments du service de trafic aérien».  
Dans cette affaire, le gouvernement a manqué de vigilance. 
Il aurait dû être mieux préparé face au projet de loi. 
Celui-ci s'est fait beaucoup attendre. 
Le fait que le gouvernement ait été si mal préparé témoigne du peu de vigueur du gouvernement à cet égard. 
Il a été promis que le gouvernement supprimerait cet amendement aujourd'hui. 
J'espère qu'il va le faire parce qu'il est vraiment important qu'il le fasse.  
Nous sommes à l'avant-garde des questions de sécurité des transports au Canada et nous veillerons à ce que cette mesure législative en matière de sécurité et celles qui seront adoptées dans l'avenir représentent un progrès pour le Canada.  
Même s'il est quelque peu inhabituel qu'un projet de loi qui a été renvoyé à un comité permanent soit retourné à la Chambre sans qu'un comité puisse faire son travail, entendre des témoins ou effectuer le travail utile que font les comités parlementaires, nous allons appuyer cette façon de procéder aujourd'hui en ce qui concerne le projet de loi S-2.  
La raison en est qu'il faut accorder au bureau chargé de la sécurité des transports les modifications dont il a besoin pour mieux fonctionner et apporter des modifications mineures, mais importantes, à la loi qui régit le bureau. 
