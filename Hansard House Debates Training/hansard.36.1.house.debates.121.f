LA LOI SUR LES PARCS NATIONAUX 
L'hon. Andy Mitchell (secrétaire d'État (Parcs), Lib.):
Je voudrais pour commencer remercier, comme je l'ai déjà fait à la Chambre hier, tous les députés de tous les partis qui ont travaillé avec diligence au comité et ailleurs pour faciliter l'adoption du projet de loi et atteindre un objectif important que tous les Canadiens partagent, soit d'établir, de continuer à établir le réseau de nos parcs nationaux de façon à protéger des lieux d'un intérêt particulier dans notre pays.  
Le nouveau parc vient s'ajouter aux 38 qui existent déjà et contribuera à compléter, comme je l'ai dit, notre réseau de parcs nationaux qui doit compter au moins un parc représentant chacune des 39 régions naturelles qui ont été désignées dans l'ensemble du Canada.  
Ce sont effectivement des endroits d'un intérêt tout spécial. 
Dans le cas de Tuktut Nogait, la création du parc permettra de protéger le troupeau de caribous Bluenose, la principale zone de mise bas de ce troupeau et un paysage de la toundra du nord de l'Arctique. 
Il s'agit de l'un des endroits les plus beaux et les plus spectaculaires au Canada, plus particulièrement dans le nord du pays.  
La démarche qu'il a fallu suivre pour en arriver aujourd'hui à créer ce parc a été très longue et coûteuse. 
Ses porte-parole s'adressèrent à Parcs Canada et au gouvernement fédéral en faisant valoir que l'établissement d'un parc national contribuerait à protéger l'environnement de leur région.  
Suivirent alors sept années d'audiences publiques, de négociations et de discussions et, en 1996, toutes les parties furent en mesure de conclure une entente sur l'établissement d'un parc national. 
La mesure à l'étude met cette entente en oeuvre en l'intégrant à la Loi sur les parcs nationaux.  
Depuis la conclusion de l'entente, un comité de gestion a été constitué pour superviser l'établissement du parc. 
Il se compose de représentants de la collectivité locale et de représentants de Parc Canada. 
Il s'agit d'un véritable organisme de gestion grâce auquel nous allons administrer le parc. 
Je me réjouis de voir que le comité fonctionne bien et nous fournit des conseils opportuns sur l'aménagement du parc.  
Je n'ai pas l'intention de parler très longtemps de ce sujet, mais il y a quatre points que je voudrais aborder brièvement.  
Tout d'abord, il est important d'établir ce parc afin de protéger les terres de mise bas de la harde de caribous de Bluenose. 
C'est une des raisons d'être du parc. 
Les données scientifiques que nous possédons démontrent clairement que le parc est utilisé par les caribous pour la mise bas.  
Il est bon pour nous en tant que gouvernement et en tant que Canadiens de remplir une obligation internationale dont nous avons parlé. 
Notre gouvernement et les Canadiens ont beaucoup insisté auprès de nos homologues américains pour qu'ils protègent les caribous de leur côté de la frontière en Alaska. 
Nous avons travaillé avec diligence au Canada pour protéger les terres de mise bas. 
Nous avons demandé aux Américains de le faire. 
Jusqu'à présent, le territoire correspondant en Alaska, appelé section 1002, ne bénéficie toujours pas d'une entière protection. 
Nous faisons très clairement savoir au monde entier que nous sommes prêts à honorer nos engagements internationaux en protégeant le caribou ici au Canada.  
J'ai fait brièvement allusion à mon deuxième point dans mon introduction. 
L'établissement de ce parc découle d'une entente à laquelle toutes les parties sont arrivées en 1996. 
Cette entente a été conclue en sachant qu'une anomalie dans la région révélait un certain potentiel minier, un aspect qui avait suscité un certain débat.  
En fait, la société exploitante de ressources naturelles avait elle-même pris part à cette décision en acceptant en 1994 d'abandonner, à la demande des Inuvialuit, ses concessions minières dans la région. 
Il est entendu que ce n'était pas parce la région n'avait pas de potentiel minier, ce n'était pas parce qu'elle n'avait pas de valeur à nos yeux, mais une décision collective a été prise pour protéger cette région en dépit du fait qu'elle avait peut-être un potentiel minier. 
On a sciemment décidé de protéger un endroit très spécial au Canada et cela, en sachant pertinemment ce que cela entraînerait.  
Je tiens aussi à dire quelques mots seulement sur le statut actuel du parc Tuktut Nogait. 
On a laissé entendre que ce n'est pas vraiment un parc national et qu'on n'a pas vraiment à se préoccuper de respecter les ententes dans leur intégralité.  
Beaucoup d'endroits qu'on appelle habituellement des parcs nationaux ont exactement le même statut qu'a Tuktut Nogait avant l'adoption de ce projet de loi. 
Il importe que nous montrions clairement que nous allons protéger l'intégrité du territoire de nos parcs nationaux. 
Si nous renoncions à cela en autorisant l'exploitation forestière à un endroit et l'exploitation pétrolière et gazière ou minière à d'autres, nous aurions de gros problèmes.  
Nous, Canadiens, décidons collectivement de protéger certains endroits spéciaux de notre pays. 
En tant que secrétaire d'État chargé des Parcs, j'ai l'honneur et le devoir de m'assurer que ces décisions soient respectées.  
Nous nous sommes engagés à consacrer 10 millions de dollars au cours des prochaines années à la création du parc.  
En outre, nous avons dit clairement que nous allons travailler à assurer que la population locale ait la priorité sur les emplois qui seront créés dans le secteur. 
Nous collaborons aussi avec le gouvernement des Territoires du Nord-Ouest de façon à nous assurer que nous pouvons aller de l'avant avec le développement économique. 
Le fait est que 80 p. 100 du potentiel minier dont j'ai parlé tout à l'heure est à l'extérieur du parc et pourra éventuellement être exploité selon la tournure des événements.  
Bref, je suis heureux que nous allons adopter ce projet de loi sous peu et veiller à ce qu'une autre partie importante de notre réseau de parcs nationaux soit réalisée.  
