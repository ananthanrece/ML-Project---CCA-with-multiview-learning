M. Lee Morrison (Cypress Hills-Grasslands, Réf.): 
Madame la Présidente, le 4 novembre 1997, le vice-président du Comité sénatorial des transports et des communications a fait une déclaration plutôt remarquable au sujet du projet de loi S-2: «Nous franchissons un pas important cet après-midi en ce sens que ce projet de loi, qui vient du Sénat, est le premier que nous examinons ab initio. 
Nous comptons ensuite vous le soumettre sous une forme ou une autre.»  
Je reconnais que le projet de loi S-2 n'est pas très différent du projet de loi C-86 qui l'avait précédé durant la trente-cinquième législature. 
Mais en théorie, les sénateurs auraient pu faire de ce projet de loi ce qu'ils voulaient et nous le renvoyer. 
C'est inacceptable. 
J'espère sincèrement que nous ne recevrons plus de projets de loi précédés de la lettre S. 
Le Sénat devrait se borner à son rôle, c'est-à-dire celui de second examen objectif, et même là, il agirait de façon illégitime à cause de la façon dont les sénateurs sont choisis. 
Hier encore, le premier ministre a eu le front de nommer cinq nouveaux sénateurs, au mépris de l'opinion publique. 
Quoi qu'il en soit, le projet de loi S-2 est une mesure d'ordre administratif, foncièrement valable. 
Il comporte toutefois quelques lacunes, que j'aimerais porter à l'attention de la Chambre.  
L'article 4, qui fixe les modalités d'emploi des membres du bureau, dont il limite le nombre à cinq, dont trois à temps plein, ne prévoit aucun système de nomination transparent fondé sur le mérite. 
Les membres du bureau continueront d'être choisis, à la discrétion du ministre, comme c'est déjà le cas pour de nombreux autres organismes gouvernementaux.  
La seconde lacune tient au fait que le bureau conservera le pouvoir discrétionnaire de ne pas enquêter au sujet d'accidents mortels s'il estime qu'une enquête ne résulterait probablement pas en une réduction des risques pour les personnes, les biens ou l'environnement. 
Si aucune enquête n'a lieu, comment peut-on aboutir à de telles conclusions? 
L'organisme dispose actuellement de seulement 135 enquêteurs et d'un budget de seulement 22 millions de dollars pour 1998-1998.  
Il convient de s'interroger au sujet des priorités d'un gouvernement qui accorde des centaines de millions de dollars en subventions et prêts-subventions à des entreprises qui construisent des aéronefs, mais seulement une pitance pour tirer au clair pourquoi un avion s'écrase. 
Je rappelle que bien que les écrasements d'avion attirent beaucoup l'attention, le bureau doit aussi s'occuper des trains, navires et pipelines réduits en morceaux à la suite d'accidents.  
Les 135 enquêteurs sont trop éparpillés. 
J'exhorte le gouvernement à remédier à la situation.  
Le projet de loi S-2 ne remédie pas aux problèmes intrinsèques du bureau, mais les lacunes de l'organisme, que j'ai porté à l'attention de la Chambre, sont dues à des omissions beaucoup plus qu'à des actions. 
Aussi, les députés réformistes appuient le projet de loi, à titre de mesure administrative.  
