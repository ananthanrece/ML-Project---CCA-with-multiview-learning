M. Mark Muise (West Nova, PC):
Monsieur le Président, c'est avec plaisir que je prends la parole au sujet du Projet de loi C-5, Loi régissant les coopératives.  
Au Canada, l'histoire des coopératives débute au milieu des années 1880 avec la création de la Farmer's Mutual Fire Insurance Company. 
Cette innovation est née de la prise de conscience des agriculteurs qui s'étaient rendu compte qu'eux aussi pouvaient aspirer à la même sécurité que les grandes entreprises dans le cadre de la production et de la commercialisation de leurs produits. 
Les agriculteurs ont alors décidé de s'unir pour essayer de mieux contrôler le commercialisation de leur produits et leurs achats. 
C'est ainsi qu'est né le mouvement coopératif au Canada.  
Le Parti conservateur est heureux d'appuyer cette mesure législative. 
Permettez-moi maintenant de souligner certaines des principales caractéristiques du projet de loi C-5.  
Le projet de loi C-5 redéfinit et élargit les principes coopératifs. 
Les principes coopératifs remontent à 1844, année où ils ont été définis en Angleterre par la Rochdale Society of Equitable Pioneers. 
Pendant plus de 50 ans, ces principes ont formé la base de la plupart des lois provinciales avant d'être adoptés par le gouvernement fédéral qui les a officialisés dans la Loi sur les associations coopératives du Canada.  
La nouvelle définition qui figure dans le projet de loi C-5 est conforme à la déclaration sur l'identité coopérative faite en 1995 par l'Alliance coopérative internationale et donne le ton à toutes les modifications à la loi qui sont proposées. 
La nouvelle définition inclut le principe de l'adhésion ouverte. 
Elle assouplit le principe selon lequel chaque membre ou délégué a une seule voix.  
En outre, le principe des investissements financés par les membres est élargi pour permettre aux coopératives d'émettre des actions de placement. 
Le principe selon lequel l'excédent peut servir à l'expansion des activités commerciales a été ajouté. 
Enfin, le projet de loi C-5 reconnaît le principe de l'éducation.  
Les règles qui régissent la constitution sont modifiées. 
Les coopératives peuvent se constituer à condition qu'elles fonctionnent selon le principe coopératif.  
À l'heure actuelle, la loi exige un acte d'association pour que la coopérative soit constituée. 
Cet acte doit être soumis au ministre. 
Ce dernier décide alors, à condition que la demande satisfasse à toutes les conditions et exigences techniques, soit d'accorder le droit de constituer la coopérative soit de le refuser. 
Cette procédure paternaliste n'est pas conforme à l'esprit coopératif, qui veut que les décisions se prennent en groupe.  
Avec le projet de loi C-5, les mêmes principes s'appliqueront, ceux de la Loi canadienne sur les sociétés par actions, la constitution d'une coopérative étant un droit et non un privilège laissé à la discrétion de l'État. 
Le système proposé est plus simple et permettrait de rationaliser les pratiques administratives. 
Il réduirait aussi les coûts que doivent assumer toutes les parties lors de la constitution d'une coopérative.  
Le projet de loi C-5 introduit la notion de personne physique dans la description des coopératives. 
Ainsi, les coopératives se voient accorder les mêmes droits et privilèges qu'une personne physique. 
Il n'est donc pas nécessaire de décrire en détail les droits, pouvoirs et privilèges des coopératives. 
Ce changement est logique, compte tenu que ces mêmes droits sont maintenant accordés aux sociétés par actions et que cela correspond aux pouvoirs que certaines provinces confèrent déjà aux coopératives.  
Le projet de loi C-5 simplifie et rationalise également la tenue de registres. 
Ce changement se veut aussi le reflet de règles semblables établies dans la Loi sur les sociétés par actions.  
Le projet de loi C-5 simplifie les règles régissant les fusions avec d'autres coopératives ou sociétés. 
Par exemple, le projet de loi C-5 permettra au coopératives provinciales de fusionner avec des coopératives fédérales, à condition que la société devienne une coopérative fédérale après la fusion.  
En outre, les coopératives auront des droits comparables à ceux conférés aux sociétés par actions en vertu de la Loi canadienne sur les sociétés par actions. 
Quand les coopératives demanderont un arrangement quelconque, le pouvoir d'ordonner cet arrangement ou toute réorganisation relèvera des tribunaux, et non de l'État. 
C'est différent par rapport à la situation actuelle, qui exige que les membres de coopératives adressent leurs demandes directement au ministre.  
Le projet de loi C-5 comporte aussi des dispositions spécifiques pour les coopératives de travailleurs et d'habitation. 
Actuellement, ces coopératives sont régies suivant les mêmes règles générales établies par la loi. 
L'industrie a constaté plusieurs lacunes dans la législation actuelle par rapport aux besoins de ces deux types de coopératives. 
Le projet de loi C-5 tente de régler ce problème.  
En conclusion, je suis heureux de dire que j'appuie le projet de loi C-5. 
Je crois que cette mesure est avantageuse pour les coopératives et, par conséquent, pour des millions de Canadiens. 
