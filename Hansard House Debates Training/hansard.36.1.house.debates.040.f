Je sais fort bien que les ministériels de l'arrière-ban ont emboîté le pas aux membres du Cabinet. 
Cependant, lorsqu'il est question d'accroître les impôts des personnes âgées au Canada, cela devrait amener les députés à changer de caucus ou à défier leur caucus.  
Les aînés canadiens ont besoin d'une voix unifiée à la Chambre, une voix qui parlera résolument en leur faveur chaque fois que le gouvernement viendra prendre de l'argent dans leurs poches. 
Je crains qu'ils n'aient pas cette voix avec le gouvernement. 
Il s'agit de personnes âgées qui avaient des rêves pour leur retraite et qui vont vivre dans la pauvreté à cause du gouvernement.  
