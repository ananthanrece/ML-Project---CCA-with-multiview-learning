M. Rahim Jaffer:
Par ailleurs, comme ma collègue de Calgary-Sud-Est l'a fait remarquer lors de précédents débats sur ce projet de loi, la partie VII de cette loi, qui est consacrée au traitement fiscal des paiements de sécurité sociale versés par le fonds de sécurité sociale du gouvernement américain à des résidents canadiens se traduit par une autre ponction fiscale à ajouter au passif des libéraux.  
On fait valoir à la Chambre et au comité que ce projet de loi modifierait le troisième protocole, celui de 1995, de façon qu'il soit plus équitable sur le plan fiscal et qu'il atténue le fardeau des impôts des âgées à faible revenu.  
Une plus grande équité fiscale et un fardeau des impôts moindre pour les personnes âgées à faible revenu, voilà un but noble et beau, mais ce n'est pas avec cette mesure législative qu'on va le réaliser.  
En effet, j'ai du mal à croire qu'un gouvernement libéral soit vraiment disposé à accorder un allégement fiscal, car, ne l'oublions pas, cette même convention fiscale qui impose une retenue fiscale uniforme de 25 p. 100 sur les paiements de sécurité sociale du gouvernement américain versés aux résidents canadiens a été négociée et conclue par le gouvernement au pouvoir, ce même gouvernement qui compte dans ses rangs des députés qui reconnaissent que cet accord n'est pas équitable.  
Pour bien saisir l'enjeu, il m'a fallu passer en revue l'histoire de la politique gouvernemental en matière de paiements de sécurité sociale et de fiscalité. 
Cela m'a permis de mesurer la piètre qualité de la mesure législative à l'étude.  
En vertu de la Loi de 1984 sur la Convention Canada-États-Unis en matière d'impôts, les résidents canadiens devaient ajouter 50 p. 100 des paiements de sécurité sociale reçus à leurs revenus aux fins du calcul de l'impôt à payer. 
Cela allait de soi puisque les prestataires de sécurité sociale américains avaient droit au même traitement. 
Ainsi donc, les résidents canadiens devaient ajouter 50 p. 100 des paiements de sécurité sociale à leurs revenus aux fins du calcul de l'impôt à payer. 
Étant donné que 50 p. 100 seulement des prestations de sécurité sociale étaient inclus dans les sommes imposables, bien des aînés à faible et moyen revenu n'ont payé aucun impôt sur leur revenu de sécurité sociale.  
Cette politique a été modifiée en 1995 lorsque le gouvernement a amorcé des négociations avec les Américains en vue d'élaborer le troisième protocole qui imposait une retenue d'impôt uniforme de 25 p. 100 sur les prestations versées aux retraités américains s'établissant au Canada.  
Les aînés à faible revenu voyaient disparaître 25 p. 100 de leurs revenus. 
Lorsque les aînés canadiens ont fait savoir au gouvernement que le troisième protocole les traitait de manière totalement inacceptable, le gouvernement est retourné à la table.  
Le quatrième protocole, dont les résultats sont inscrits dans le projet de loi C-10, ferait grimper à 85 p. 100 le taux d'inclusion des prestations de sécurité sociale des États-Unis qui s'élevait à 50 p. 100 avant 1985. 
Cela représente une augmentation de 70 p. 100 du taux d'inclusion.  
Chaque personne âgée qui paie des impôts paiera davantage en vertu du projet de loi. 
Cette ponction fiscale ne touchera pas uniquement des aînés bien nantis, mais tous les aînés qui paient des impôts. 
On l'a reconnu au comité; je n'ai pas participé aux discussions, mais c'est un fait bien connu. 
L'article 7 de la partie VII du projet de loi propose nettement une autre ponction fiscale libérale.  
Lorsque les aînés subissant les effets du traité de 1985 ont demandé la justice fiscale, lorsqu'ils ont demandé au gouvernement de prendre des mesures à l'égard de la retenue d'impôt de 25 p. 100, ils ne s'attendaient pas à subir une augmentation de 70 p. 100 de leurs impôts.  
Cette politique pénalisera tous les aînés dont les épargnes personnelles s'ajoutent à leurs revenus et les placent au moins dans la première tranche d'imposition.  
Les aînés à revenu moyen qui ont fait des sacrifices et de judicieux choix financiers en prévision de leur retraite sont punis. 
Il ne s'agit pas uniquement de gens très riches. 
On parle de tous les aînés qui payent des impôts et reçoivent la prestation aux aînés des États-Unis, que leur revenu annuel soit de 10 000 $ ou de un million de dollars.  
Je suis convaincu qu'on refusera de voir cet aspect et que mes collègues d'en face diront que le Parti réformiste protège l'intérêt des riches. 
Ce n'est pas le cas et je prie mes collègues de réfléchir aux faits avant de perpétuer les malentendus et la désinformation.  
Pour en revenir aux paiements de sécurité sociale versés par les États-Unis à des résidents canadiens, il faut savoir que, contrairement aux paiements du RPC, qui ne sont imposés en tant que revenu que lorsqu'ils sont retirés, les paiements de sécurité sociale américains sont imposés au moment où ils sont faits, et non quand ils sont retirés. 
Donc, les Canadiens qui reçoivent des prestations de sécurité sociale des États-Unis ont déjà payé de l'impôt sur ces prestations.  
Un Canadien travaillant aux États-Unis paierait de l'impôt sur ses cotisations, mais très peu ou pas quand il percevrait ses prestations. 
C'est plein de bon sens, parce que les personnes âgées ne sont pas autant en mesure de payer leurs impôts quand ils sont à la retraite et qu'ils doivent se débrouiller avec un revenu fixe.  
Si ce travailleur qui a contribué au régime américain de sécurité sociale revenait au Canada, il aurait à payer de l'impôt sur 85 p. 100 de son revenu de sécurité sociale. 
Ces gens paient deux fois leurs impôts. 
C'est non seulement injuste, mais cela va à l'encontre des objectifs de notre convention fiscale, qui vise à éliminer tout dédoublement d'impôts.  
Je veux insister sur certains éléments clés avant de conclure. 
Premièrement, cette mesure a été étudiée en vitesse au Comité de l'industrie. 
Le porte-parole responsable du projet de loi, le député de Calgary-Sud-Est, n'a appris que par hasard, à la dernière minute, que le projet de loi C-10 serait étudié au comité. 
Cela me donne à penser que cette mesure ne comporte que des questions administratives mineures.  
Ensuite, d'après ce que j'ai compris, le président du Comité de l'industrie n'a pas accédé à la demande du député de Calgary-Sud-Est pour que les personnes âgées touchées par le projet de loi C-10 soient autorisées à venir témoigner. 
