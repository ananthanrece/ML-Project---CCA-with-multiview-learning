Dans un tableau qui illustre la répartition des capacités de lecture chez les adultes de 16 à 65 ans, le total du Canada atteint 101 p. 100 pour la catégorie «compréhension des textes suivis». 
Peut-on en tirer la conclusion qu'au pays, toutes les personnes peuvent et savent lire et écrire? 
La réponse est bien évidemment non. 
En effet, sur ce total de 101 p. 100, 17 p. 100 de la population n'atteint que le premier niveau de lecture. 
À ce niveau, les individus sont capables de lire les directives sur une bouteille d'aspirine. 
Vingt-six pour cent des gens atteignent le deuxième niveau de lecture. 
À ce niveau, les individus sont capables de lire, dans un article, une courte séquence de renseignements à propos des caractéristiques d'une plante de jardin. 
Trente-cinq pour cent des gens atteignent le troisième niveau de lecture. 
À ce niveau, les individus sont capables de lire des critiques de films et de choisir la moins favorable.  
La répartition des niveaux est similaire pour les deux autres catégories d'évaluation, qui ont fait partie du test, des capacités de lecture, soit la catégorie de compréhension des textes schématiques et la catégorie de compréhension des textes au contenu quantitatif.  
L'Enquête internationale de l'alphabétisation des adultes définit l'alphabétisme comme étant la «capacité d'utiliser les imprimés et l'information écrite pour fonctionner dans la société, atteindre ses objectifs, parfaire ses connaissances et accroître son potentiel». 
Cette étude constate qu'un nombre impressionnant d'adultes canadiens possèdent un niveau de lecture très faible, ce qui entrave leur participation à la vie économique et sociale.  
Cette étude démontre que la disponibilité du matériel de lecture est le facteur primordial pour assurer le maintien des capacités de lecture et de compréhension. 
Un pays où la population lit moins qu'un autre est un pays qui sera moins productif et moins compétitif. 
Ce qui nous ramène à l'objectif de cette motion, soit l'abolition de la TPS sur tous les livres.  
Selon l'Association internationale des éditeurs, le Canada est le pays du G7 qui impose les taxes les plus hautes sur le matériel de lecture. 
Au Canada, cette taxe n'est imposée que par le gouvernement fédéral. 
Les États-Unis, le Japon, l'Angleterre, l'Australie et l'Irlande font partie des pays qui ont compris l'importance de ne pas taxer les livres.  
Cette taxe a eu un impact fort négatif. 
Toutes ces baisses simultanées ont touché les éditeurs et les écrivains qui ont vu décroître leurs revenus.  
Il était prévisible que l'imposition de la TPS sur les livres aurait un tel impact. 
Chaque personne au pays qui veut acheter un livre ou lire doit payer une taxe. 
Les étudiantes et les étudiants qui veulent acheter des livres paient des taxes. 
Les analphabètes désirant apprendre à lire doivent payer une taxe. 
Les parents qui veulent acheter des livres à leurs enfants doivent payer la taxe. 
Les citoyens et les citoyennes qui veulent améliorer leur culture et s'instruire doivent payer des taxes.  
Je veux m'adresser aux gens d'en face. 
Je pense dans ma tête à un mot que je ne peux prononcer dans cette Chambre, car cela vous ferait bondir de votre siège. 
Mais je le pense néanmoins pour qualifier leur attitude et leur comportement en ce qui concerne la TPS sur les livres. 
Je m'explique.  
Lors de leur congrès de 1992, les libéraux avaient adopté une résolution où ils s'engageaient à abolir la TPS sur les livres et le matériel de lecture. 
En 1993, dans une lettre à la Coalition contre la taxe sur les livres, le premier ministre s'était engagé à mettre en oeuvre cette résolution de son parti.  
Je voudrais rappeler à cette Chambre les propos tenus par certains de nos amis d'en face, à l'époque où ils étaient dans l'opposition et que le gouvernement conservateur voulait faire adopter la Loi sur la TPS. 
En novembre 1990, le vice-premier ministre prétendait qu'imposer la TPS sur les livres, c'était imposer l'ignorance. 
La ministre du Patrimoine disait, en 1991, que ça nuisait à l'unité et à l'identité canadienne. 
En 1990, le ministre de l'Industrie avait prononcé un discours de 20 minutes pour donner une explication détaillée et méthodique de son opposition à la TPS. 
La ministre de la Coopération internationale et de la Francophonie déposait une pétition contre la taxation des livres et des magazines. 
Le député de Carleton-Gloucester déposait une pétition lui aussi, en disant qu'il était aberrant de taxer les journaux, toute lecture, que ce soit pour des gens cultivés, éduqués, ou pire encore, pour des gens qui essaient de lire.  
Il faut reconnaître que le gouvernement offre des réductions de la taxe sur les livres pour certaines bibliothèques, institutions d'enseignement et organismes d'alphabétisation. 
Mais ce n'est pas suffisant. 
Le gouvernement empoche encore 120 millions de dollars en revenu de la taxe sur le matériel de lecture.  
Le ministre du Développement des ressources humaines veut multiplier les programmes de formation et de perfectionnement. 
Il doit savoir que les études internationales concluent qu'un nombre considérable de citoyens et de citoyennes peuvent en être exclus en raison de leurs faibles capacités de lecture qui limitent leur aptitude à participer à l'éducation permanente et à la formation continue.  
