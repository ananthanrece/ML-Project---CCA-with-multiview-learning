Ce projet de loi découle d'une initiative des utilisateurs, soit le secteur des coopératives. 
Il émane des gens qui savent mieux que quiconque ce qu'il faut pour préserver la vitalité et le dynamisme du mouvement coopératif.  
Le secteur des coopératives du Canada a consulté ses membres pour déterminer par voie de consensus ce dont ils ont besoin pour survivre et prospérer dans un marché concurrentiel. 
Ce projet de loi est le fruit du travail acharné et du dévouement des membres du secteur des coopératives en vue de moderniser la législation.  
Le projet de loi a été bien accueilli par tous les partis lors de la deuxième lecture. 
En outre, il a recueilli l'adhésion générale du Comité, non seulement de tous les témoins qui se sont présentés devant lui, mais aussi de tous les membres qui le composaient.  
Le projet de loi vise trois grands objectifs. 
Le premier consiste à revitaliser les règles de gestion des coopératives. 
À cette fin, le projet de loi permet aux coopératives d'avoir accès aux outils dont disposent déjà les autres entreprises. 
De plus, il simplifie la procédure de constitution en personne morale et réduit au minimum l'intervention du gouvernement.  
Le deuxième objectif est de permettre aux coopératives d'avoir accès à de nouvelles possibilités de financement. 
Par exemple, il les autorise à émettre des actions participatives sur les marchés boursiers, ce qui leur permettra d'emprunter sur les marchés financiers si elles le désirent.  
Le troisième objectif consiste à renforcer le caractère distinct des coopératives en veillant à ce que seules les organisations fonctionnant selon le principe coopératif puissent se constituer en coopératives. 
Le projet de loi renforce également le contrôle exercé par les membres et donne à ces derniers le pouvoir de décider de la structure de la coopérative.  
La nouvelle législation permettra aux coopératives canadiennes de fonctionner de façon efficace. 
Elle contribuera à stimuler l'investissement, la croissance économique et la création d'emplois dans de nombreuses collectivités de tout le Canada.  
Les coopératives attendent cette réforme avec impatience. 
Elles ont exprimé clairement ce qu'elles voulaient dans ce projet de loi et ont travaillé d'arrache-pied pour atteindre leur but.  
Le travail des coopératives a considérablement facilité le processus d'étude de la législation. 
