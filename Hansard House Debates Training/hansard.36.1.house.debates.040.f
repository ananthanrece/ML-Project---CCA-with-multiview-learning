M. Tony Valeri (secrétaire parlementaire du ministre des Finances, Lib.):
Monsieur le Président, le gouvernement est tout à fait déterminé à appuyer l'alphabétisation. 
La question a toujours été de savoir comment nous pouvons y parvenir sans compromettre les progrès que nous avons accomplis au prix de durs efforts en remettant de l'ordre dans les finances publiques.  
Il est bien évident que toutes les mesures prises doivent être efficaces et responsables sur le plan budgétaire. 
C'est pour cette raison que le gouvernement a présenté une disposition qui prévoit une remise de 100 p. 100 de la TPS payée sur les livres aux bibliothèques publiques, aux écoles, aux universités, aux collèges et aux autres organismes qui font la promotion de l'alphabétisation.  
Cela signifie qu'il n'y a pas de TPS sur les livres distribués pour libre consultation dans les écoles primaires et secondaires et dans les autres établissements d'enseignement. 
L'allégement fiscal relatif aux livres vise non seulement l'apprentissage structuré dans nos écoles et collèges, mais aussi l'acquisition continue du savoir par l'entremise des bibliothèques publiques et des groupes d'alphabétisation de première ligne.  
Le remise de la TPS sur les livres reconnaît le rôle important que jouent les établissements d'enseignement et les groupes communautaires lorsqu'il s'agit d'aider des individus à obtenir les outils dont ils ont besoin pour apprendre à lire. 
De plus, c'est un investissement efficace et responsable. 
L'aide ciblée à l'intention des groupes d'alphabétisation de première ligne sera certainement beaucoup plus rentable.  
La remise spéciale s'ajoute aux initiatives gouvernementales annoncées dans le budget de 1997, à l'appui de l'acquisition du savoir et de l'éducation au Canada. 
Qui plus est, le financement accordé cette année au Secrétariat national à l'alphabétisation augmentera de 30 p. 100 et passera à 30,3 millions de dollars, offrant ainsi plus de possibilités aux individus de s'alphabétiser et d'améliorer leurs compétences à communiquer.  
Le gouvernement a par ailleurs augmenté le crédit pour études des étudiants, de manière à tenir compte de ce qu'il en coûte pour étudier, outre les frais des scolarité, et il a élargi le crédit pour frais de scolarité de manière à ce qu'il couvre non seulement les frais de scolarité, mais aussi les frais obligatoires exigés par les établissements d'enseignement postsecondaire, aux fins de l'éducation. 
De plus, les étudiants peuvent maintenant reporter toute partie non utilisée de leurs crédits pour études et pour frais de scolarité pour l'appliquer à un revenu ultérieur.  
De plus, la cotisation annuelle maximale aux régimes enregistrés d'épargne-études a été doublée et les parents peuvent désormais transférer la portion inutilisée du REEE dans leur régime enregistré d'épargne-retraite. 
Par ailleurs, le Conseil des arts du Canada offre du soutien aux auteurs canadiens et les éditeurs canadiens peuvent, quant à eux, obtenir de l'aide par l'entremise du ministère du Patrimoine canadien.  
Le gouvernement a pris un certain nombre de mesures pour renforcer l'alphabétisme et veiller à ce que les dépenses servent à appuyer les groupes d'alphabétisation de première ligne, afin que les personnes qui en ont le plus besoin reçoivent de l'aide dans ce domaine, grâce au remboursement de la TPS. 
De toute évidence, le gouvernement ne prétend pas que l'allégement fiscal permettra de relever tous les défis qui se présentent en matière d'alphabétisation et d'éducation. 
Toutefois, il est certainement persuadé que ces mesures contribueront grandement à appuyer les initiatives visant à améliorer le niveau d'alphabétisation au Canada. 
Le premier ministre, le ministre des Finances et tous les députés se sont engagés à faire de l'alphabétisation une des priorités du gouvernement. 
C'est d'ailleurs l'une des priorités que nous mentionnent très souvent les Canadiens. 
Les députés doivent s'entendre sur la façon de renforcer l'alphabétisme au Canada. 
Nous avons trouvé une approche équilibrée en utilisant les dépenses fiscales et nous poursuivrons dans cette voie pour veiller à ce que les dépenses qu'engage le gouvernement servent bien les intérêts des Canadiens. 
