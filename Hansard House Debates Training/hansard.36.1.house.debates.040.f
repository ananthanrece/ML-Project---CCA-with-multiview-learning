L'hon. Jim Peterson (au nom du ministre des Finances) propose que le projet de loi soit maintenant lu une troisième fois et adopté.  
Ce changement a trait aux prestations de sécurité sociales que le gouvernement américain verse à des personnes résidant au Canada.  
Essentiellement, environ 60 000 Canadiens sont touchés par cet impôt. 
Un tiers d'entre eux sont des personnes à faible revenu résidant au Canada qui touchent encore des prestations de sécurité sociale des États-Unis. 
Aux termes de la loi actuelle, les Américains ont le droit de retenir 25,5 p. 100 des prestations de sécurité sociale qu'ils versent à des personnes résidant au Canada. 
Environ 20 000 des prestataires y perdent en ce sens que, s'ils étaient imposés aux taux canadiens ordinaires d'imposition sur le revenu pour ces prestations, le taux d'imposition serait beaucoup moins élevé étant donné qu'ils se situent dans des tranches inférieures de revenu. 
Par conséquent, cette retenue générale de 25,5 p. 100 par les Américains nuit à cette classe de résidents du Canada.  
C'est pourquoi nous avons entrepris en leur nom de renégocier ce traité afin qu'ils ne soient pas imposés à la source par le pays d'origine, soit les États-Unis, mais au Canada où ils seraient imposés sur leur revenu net. 
En fin de compte, beaucoup de ces résidents du Canada à faible revenu ne paieront aucun impôt.  
Le projet de loi a franchi l'étape de la deuxième lecture à la Chambre où il a été appuyé chaleureusement par les deux côtés. 
Il a été étudié par les deux comités qui l'ont renvoyé sans amendement. 
Il est évidemment important que nous concluions de nouveaux traités de ce genre avec nos nouveaux partenaires commerciaux de façon à ce que le Canada continue à être à l'avant-garde pour ce qui est d'éviter la double imposition et d'encourager les investissements étrangers, la circulation des devises à l'échelle internationale et le travail à l'étranger, et ce, encore plus particulièrement dans le cas des dispositions concernant les États-Unis. 
Il est très important que nous adoptions ces dispositions d'allégement afin que nos puissions nous assurer que ces gens sont traités avec équité, tant par les États-Unis que par le Canada, en ce qui concerne leurs prestations de sécurité sociale.  
J'aimerais réitérer ce qu'a dit le député de Kamouraska-Rivière-du-Loup-Témiscouata-Les Basques, et je cite: «J'espère que nous aurons la collaboration de tous les partis, parce que la base de ce projet de loi est de corriger une iniquité fondamentale pour les gens à faible revenu.»  
Nous nous attendons à ce que, dès que ce projet de loi sera adopté-et nous espérons avoir le consentement unanime de tous les députés de la Chambre-il reçoive très rapidement la sanction royale. 
Nous croyons savoir qu'aux États-Unis le projet de loi a franchi toutes les étapes nécessaires et qu'il ne manque plus que la signature présidentielle. 
Dès que cela sera fait, nous nous attendons à ce que les chèques de remboursement soient émis dans les semaines qui suivront, ou du moins le plus rapidement possible. 
Revenu Canada coopérera avec les autorités fiscales américaines pour retrouver toutes le personnes qui, depuis deux ans, auront pu être traitées injustement aux termes de ces dispositions.  
Grâce à l'appui de tous les députés, nous nous attendons à pouvoir corriger cette iniquité et à faire en sorte que les contribuables résidant au Canada qui ont été traités injustement reçoivent leur remboursement.  
Je remercie tous les députés de la Chambre pour leur splendide coopération autour de cet excellent projet de loi.  
