Ce n'est pas vrai. 
En fait, toutes les personnes âgées qui touchent des prestations de sécurité sociale des États-Unis et qui paient des impôts verseront davantage aux termes de ce projet de loi. 
Le taux d'inclusion pour le calcul du revenu imposable passe de 50 p. 100 en vertu de la convention d'avant 1996 à 85 p. 100 aux termes de ce projet de loi, qui renferme le quatrième protocole.  
Lorsqu'il laisse entendre que les réformistes veulent d'une façon ou d'une autre protéger leurs riches partisans retraités en appuyant ce projet de loi, le gouvernement semble penser que toute personne âgée qui paie des impôts est un aîné à revenu élevé et un riche partisan du Parti réformiste.  
