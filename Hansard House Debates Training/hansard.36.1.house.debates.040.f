Il vise à corriger l'incongruité relative à la période pendant laquelle les administrateurs peuvent combler une vacance au sein du conseil.  
L'alinéa 85(1)b) prévoit qu'un administrateur nommé peut combler une vacance jusqu'à la prochaine assemblée annuelle. 
Le paragraphe 85(9) prévoit toutefois que l'administrateur peut remplir le mandat non expiré de son prédécesseur.  
L'amendement règle cette incongruité en supprimant la période mentionnée à l'alinéa 85(1)b). 
Cela assure la cohérence du projet de loi par rapport à des dispositions semblables d'autres lois fédérales sur les sociétés.  
