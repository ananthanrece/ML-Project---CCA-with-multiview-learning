LE PÉNITENCIER DE KINGSTON 
M. Myron Thompson (Wild Rose, Réf.): 
Monsieur le Président, en ce moment même, les gardiens du pénitencier de Kingston font du piquetage. 
Ils affirment que l'établissement est hors de contrôle et à la veille d'éclater.  
Auparavant, le pénitencier de Kingston était désigné établissement à sécurité multiniveau et recevait principalement des détenus en isolement protecteur. 
On y trouve maintenant un pourcentage beaucoup plus élevé de délinquants qui ne sont pas en isolement protecteur, mais sont plutôt rebelles, agressifs et bien organisés. 
Les incidents survenus récemment reflètent cette agressivité, car de nombreux employés ont été attaqués et il s'est produit deux prises d'otages et deux émeutes depuis le 27 août.  
Malheureusement, l'organisation matérielle des lieux et le fonctionnement quotidien du pénitencier se prêtent mal au contrôle de délinquants de la sorte qui requièrent la sécurité maximale. 
Le personnel travaille donc maintenant dans des conditions extrêmement dangereuses.  
Les gardiens veulent que la direction prenne des mesures et ils ont proposé leurs propres solutions économiques. 
Encore une fois, ce sont les travailleurs de première ligne qui peuvent le mieux prédire ce qui se produira si ces changements ne sont pas mis en oeuvre.  
Tenez-vous-le pour dit. 
Si la direction n'agit pas maintenant, nous assisterons à une destruction physique et humaine d'une envergure telle que l'émeute de Headingley, qui a coûté 3,1 millions de dollars, semblera banale en comparaison.  
