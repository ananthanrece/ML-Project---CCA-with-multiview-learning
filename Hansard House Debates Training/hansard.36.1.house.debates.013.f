Qu'a-t-il fait? 
Deux choses. 
Il a commencé par consulter les Canadiens pour qu'ils soient conscients du problème. 
La population de Thornhill est très consciente des problèmes du Régime de pensions du Canada. 
Elle sait qu'à défaut d'être modifié, le régime disparaîtra. 
Il n'existera pas pour les générations à venir.  
Il est important que l'on informe les gens. 
Le gouvernement n'a pas seulement consulté largement la population, il a également discuté avec les provinces. 
