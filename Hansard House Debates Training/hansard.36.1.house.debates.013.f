Était-ce une décision unanime? 
Absolument pas. 
Le gouvernement a-t-il cherché à obtenir un consensus national? 
Je crois que oui. 
La majorité des provinces, y compris l'Ontario, représentant la vaste majorité de la population du Canada, ont signé une entente pour apporter au Régime de pensions du Canada des changements qui régleront le problème. 
Nous renforcerons le régime, et c'est pourquoi je pourrai dire à la prochaine génération, aux jeunes de Thornhill et à tous les jeunes Canadiens que le Régime de pensions du Canada sera là pour eux et pour les générations futures.  
C'est l'engagement qu'a pris le gouvernement, et le projet de loi C-2 nous permettra de respecter cet engagement. 
Il règle le problème qu'on a décelé et assure la viabilité du plan pour l'avenir.  
La prochaine question que je pose au nom des habitants de Thornhill est: «Cela a-t-il été fait de façon juste? 
