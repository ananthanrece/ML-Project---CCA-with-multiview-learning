Nous croyons qu'il faut assurer un avenir durable au Régime de pensions du Canada. 
Certains députés pensent peut-être qu'il n'est pas important de protéger 225 000 Canadiens handicapés au moyen du Régime de pensions du Canada. 
Nous estimons toutefois que le Régime de pensions du Canada est un outil indispensable pour donner aux membres de notre société qui en ont besoin le soutien qu'ils ne peuvent s'offrir eux-mêmes.  
