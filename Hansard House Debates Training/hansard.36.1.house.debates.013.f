LE CANCER DU SEIN 
M. David Pratt (Nepean-Carleton, Lib.):
Monsieur le Président, plusieurs députés ont parlé récemment de la question du cancer du sein, un mal dévastateur qui est devenu la principale cause de décès chez les femmes de 35 à 55 ans.  
Le cancer du sein a touché très durement ma propre famille. 
Ma grand-mère a eu le cancer du sein il y a 35 ans et a survécu. 
Quoique ma mère soit morte d'un cancer du sein il y a 5 ans, je suis très fier de dire que ma soeur fait maintenant de la recherche de pointe dans ce domaine, à l'Université d'Ottawa.  
Toutes les 30 minutes, une Canadienne apprend qu'elle a le cancer du sein, et plus de 5 400 femmes en meurent chaque année. 
Ces statistiques sont alarmantes, mais nous avons fait de grands progrès dans la lutte contre cette maladie. 
D'importants programmes de sensibilisation et de dépistage précoce commencent à faire leur effet, car le taux de mortalité due au cancer du sein a commencé à diminuer au Canada.  
Octobre est le mois de la sensibilisation au cancer du sein. 
Arborons tous le ruban rose pour manifester notre appui aux femmes atteintes de cette terrible maladie.  
