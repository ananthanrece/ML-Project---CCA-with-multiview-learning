Quand l'État prend l'argent des gens sous forme de taxes et d'impôts, il se trouve à dire qu'il est mieux en mesure que les citoyens de décider quoi faire de cet argent. 
Le gouvernement a très clairement fait comprendre qu'il n'ont pas le droit de prendre ces décisions ou ne l'ont pas mérité.  
À notre époque, nous avons l'occasion grâce à l'histoire d'apprendre les leçons de la science économique, surtout celle des 30 dernières années. 
Le gouvernement fait la preuve, sans hésitation ni équivoque, que les obstacles fiscaux et autres qu'il dresse sur le chemin des petites et moyennes entreprises tuent les emplois.  
Le gouvernement ne peut prétendre ignorer ces faits. 
Il est inacceptable qu'il continue d'aller dans cette direction sans le reconnaître. 
Mes électeurs, et surtout le monde de la petite entreprise, me l'ont dit. 
Dans la région de l'Atlantique, dont je viens, beaucoup de petites entreprises luttent pour survivre. 
