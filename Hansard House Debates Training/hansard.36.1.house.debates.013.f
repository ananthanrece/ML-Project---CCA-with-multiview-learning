Il s'agit d'une toute nouvelle circonscription qui a été créée par suite d'un remaniement électoral. 
Pour la première fois depuis les années 70, Thornhill est unifié. 
Le gouvernement provincial avait scindé Thornhill en deux.  
Si vous venez dans Thornhill, vous verrez que le vieux Thornhill chevauche la rue Yonge et fait partie à la fois de la ville de Vaughan et de Markham. 
Voici qu'avec la création de la nouvelle circonscription fédérale de Thornhill, Markham, Vaughan, Concord et toutes leurs merveilleuses collectivités sont groupées pour la toute première fois dans la circonscription de Thornhill.  
Thornhill est une circonscription prospère qui compte beaucoup d'entreprises. 
Je dois dire que les entrepreneurs sont bien au fait de la nécessité de modifier le Régime de pensions du Canada et de régler les problèmes qui l'assaillent depuis de nombreuses années. 
Tout employé ou propriétaire d'entreprise comprend l'importance du Régime de pensions du Canada pour les Canadiens et les valeurs canadiennes.  
Thornhill regroupe une collectivité très jeune. 
Lorsque j'y ai fait du porte à porte, j'y ai rencontré des gens de toutes les ethnies qui s'inquiètent de savoir s'il y aura à l'avenir des programmes ou des régimes comme le Régime de pensions du Canada pour les protéger en cas d'invalidité. 
Beaucoup connaissent les problèmes qui assaillent depuis quelque temps le Régime de pensions du Canada. 
Beaucoup comprennent que le régime doit être modifié.  
J'ai abordé le projet de loi C-2 du point de vue suivant. 
Je me suis d'abord demandé s'il y avait un problème. 
Et j'ai répondu oui, il y a un problème. 
Ce problème n'est pas nouveau. 
Il existe depuis un bon bout de temps. 
Nous pourrions accuser le gouvernement conservateur de n'avoir rien fait pour remédier au problème. 
Mais cela ne donnerait rien, même si c'est vrai.  
