LOI DE MISE EN OEUVRE DE L'ACCORD SUR LA STATION SPATIALE INTERNATIONALE CIVILE 
M. John Cannis (secrétaire parlementaire du ministre de l'Industrie, Lib.):
Madame la Présidente, j'ai aujourd'hui l'honneur de parler en deuxième lecture du projet de loi C-4, mesure législative régissant la participation du Canada au plus important projet scientifique et technologique dans l'histoire de l'humanité, c'est-à-dire la Station spatiale internationale.  
Je cite tout d'abord l'un des grands pionniers du Canada, John H. Chapman, qui a déjà dit: «Au cours du second siècle de la Confédération, ce sont les liens tissés dans l'espace qui maintiendront en place le tissu de la société canadienne tout comme le chemin de fer et le télégraphe ont soudé les provinces éparpillées au cours du siècle dernier.»  
Ces paroles inspirantes ont été prononcées au départ par John H. Chapman à la fin des années 50, aux premiers stades de l'exploration spatiale. 
Avec sa vision, M. Chapman est maintenant considéré comme le père du programme spatial canadien. 
M. Chapman a ajouté: «Les progrès technologiques découlant de l'exploration spatiale se répercutent dans les activités normales de notre monde.»  
C'est ce sur quoi porte le projet de loi C-4. 
Il est question de l'exploration spatiale dans l'intérêt de tous les Canadiens et de l'humanité. 
Le projet de loi C-4 permet au Canada de devenir un partenaire à part entière dans la plus grande entreprise, la construction et l'exploitation de la Station spatiale internationale. 
Il officialise notre participation en tant que nation. 
Sans cela le Canada ne pourrait plus être considéré comme un partenaire. 
Notre nation perdrait d'énormes possibilités pour ce qui est du développement de la recherche scientifique canadienne dans l'espace. 
Le Canada mettrait également en péril des centaines voire des milliers d'emplois déjà créés dans ce domaine de haute technologie.  
Nos investissements dans des projets de science et de technologie spatiales tels que la station spatiale internationale, font que les scientifiques, ingénieurs et entrepreneurs canadiens occupent une position importante sur le marché international. 
Cela permet à notre pays de continuer à contribuer à la découverte scientifique et à notre compréhension de l'univers tout entier. 
Voilà ce dont il est question dans le projet de loi C-4.  
La station spatiale internationale est un symbole de coopération internationale et de l'effort concerté des grandes nations industrielles du monde parmi lesquelles figure le Canada en tant que partenaire-clé.  
Dans sa version définitive, la station couvrira une superficie égale à un terrain de football et logera une équipe permanente internationale de sept membres dont des astronautes canadiens spécialisés dans les domaines de la biotechnologie, du génie, de l'observation de la Terre et des télécommunications.  
La mesure législative dont nous sommes saisis porte sur l'exécution des obligations du Canada au titre de l'Accord sur la coopération relative à la Station spatiale internationale civile et sur l'application des dispositions du Code criminel canadien aux Canadiens à bord de la station.  
Les parties à l'accord s'engagent à établir un cadre de coopération internationale entre elles en vue de la conception détaillée, du développement, de l'exploitation et de l'utilisation, à des fins pacifiques, d'une station internationale civile.  
Le projet de loi à l'étude met la législation canadienne en harmonie avec les obligations internationales négociées dans le cadre de l'accord et, ce faisant, réaffirme la ferme résolution du Canada de participer à ce projet on ne peut plus historique.  
L'histoire de la participation du Canada au projet de station spatiale internationale date de l'année où le premier astronaute canadien, Marc Garneau, a voyagé dans l'espace. 
En 1984, le président Reagan avait invité des amis et des alliés à se joindre aux États-Unis en vue de la construction, de l'exploitation et de l'utilisation d'une station spatiale en orbite autour de la Terre, et ce, à des fins pacifiques.  
En mars 1985, le Canada accepta l'invitation et avisa la NASA que la contribution du Canada serait axée sur le concept d'un système d'entretien mobile. 
Ce système n'est rien de moins que la génération suivante du bras spatial canadien, qui aidera à monter la station spatiale et à l'entretenir une fois qu'elle sera en orbite.  
En 1988, le Canada, les États-Unis, l'Europe et le Japon établirent officiellement un partenariat en signant un accord intergouvernemental international concernant la station spatiale. 
Ce document énonce les grands principes et le fondement juridique de la coopération relative au programme d'une station spatiale.  
Après la refonte de la station spatiale en 1993, les partenaires ont finalement invité la Russie à s'associer à ce qui allait devenir une station spatiale vraiment internationale.  
Le 8 avril 1997, lors d'une conférence de presse avec le président Clinton, le premier ministre du Canada a réaffirmé la participation du Canada au programme international de station spatiale et annoncé que le Canada fournirait le manipulateur agile spécialisé. 
Ce télérobot, connu sous le nom de main robotique, est conçu pour fonctionner avec un nouveau bras manipulateur et sert aux opérations d'assemblage et aux réparations délicates.  
Après trois années de négociations, toutes les parties on signé le nouvel accord international sur la station spatiale le 29 janvier 1998, faisant officiellement un associé de la Russie. 
Cet accord prévoit une période de deux ans pour sa ratification qui doit donc se faire le 29 janvier de l'an 2000.  
Le projet C-4 permet au Canada de ratifier cet accord international et d'approuver officiellement notre engagement à l'égard de ce partenariat.  
Dans les discussions internationales, il est toujours question des nombreuses réalisations du Canada dans le domaine spatial, notamment du Canadarm. 
Ce qui est plus important, le succès du Canadarm a valu au Canada la crédibilité nécessaire pour s'aventurer encore plus loin et marquer de nouveaux progrès dans la robotique spatiale de pointe, ceci avec la confiance des pays qui ont un programme spatial.  
Il assure aux milieux scientifiques du Canada un accès privilégié aux conditions de microgravité de l'espace et crée de nouvelles retombées technologiques pour le pays tout entier.  
Aujourd'hui, le Canada recueille les avantages de sa participation au projet en termes de contrats pour l'industrie spatiale canadienne et en termes de prestige, le prestige de participer à l'une des plus grandes prouesses de l'histoire dans le domaine de l'ingénierie. 
Le Canada s'attend à un rendement égal à trois ou quatre fois la valeur de son investissement initial et à la création de nombreux emplois dans le domaine de la technologie de pointe.  
La participation du Canada à la construction de la station spatiale a démarré sous d'excellents auspices. 
En décembre dernier, nous avons assisté à la mise en place du bras spatial et du système de vision artificielle canadiens aux fins du montage des deux premiers modules de la station, le module américain «Unity» et le module russe «Zarya». 
Le savoir-faire canadien a été déterminant pour la mise en route de ce fabuleux projet.  
Il va sans dire que la collaboration canadienne à la réunion de ces deux modules est tout à fait symbolique. 
En mai dernier, c'est avec grand fierté que nous avons vu Mme Julie Payette devenir la première canadienne à monter à bord des deux premiers modules de la station.  
L'année prochaine, M. Marc Garneau effectuera sa troisième mission en qualité de membre de l'équipage du STS-97. 
La mission sera essentiellement consacrée à l'installation des panneaux solaires de la station spatiale internationale et comportera deux sorties dans l'espace que M. Garneau coordonnera. 
Entre autres tâches qui lui sont imparties, M. Garneau sera amené à manipuler le bras spatial canadien dans le cadre des travaux de montage.  
Accompagnant M. Garneau, M. Chris Hadfield effectuera sa deuxième mission. 
Avec tous nos compatriotes, nous assisterons à la première sortie d'un Canadien dans l'espace en la personne de M. Hadfield qui aura pour mission d'installer le nouveau bras robotique du Canada sur la station spatiale internationale.  
Aussitôt cet appareil en place, le Canada et tous les autres pays du monde verront avec joie et fierté comment fonctionnent le bras spatial canadien et le nouveau bras robotique qui serviront à la construction de la plus grande structure spatiale et plus grand laboratoire de microgravité jamais construits.  
Dès que ce système d'entretien sera opérationnel, grâce à l'installation de la main robotique à l'extrémité du bras spatial, les astronautes pourront exécuter en orbite des tâches complexes en pleine efficacité et en toute sécurité à partir du confort relatif des modules de la station spatiale. 
En d'autres mots, si la technologie robotique canadienne n'avait pas existé, cette station n'aurait pu être construite ou entretenue avec l'efficacité et la rentabilité voulues.  
En mars, le gouvernement du Canada a accordé à l'Agence spatiale canadienne un financement stable et permanent au titre du programme spatial du Canada. 
Cette décision témoigne de l'engagement ferme du gouvernement à l'égard de la promotion des sciences et des technologies de pointe sur lesquelles s'appuient l'économie mondiale fondée sur le savoir et les Canadiens pour rester à l'avant-garde dans le domaine.  
Nos investissements dans la technologie spatiale s'inscrivent dans le droit fil de nos engagements en matière de protection de l'environnement, tels qu'énoncés dans le Protocole de Montréal et les accords de Kyoto. 
Le Canada est le créateur de RADARSAT, le formidable satellite qui a permis à notre pays de prendre la première place au monde pour ce qui est de la gestion de la Terre à partir de l'espace. 
Aujourd'hui, nous utilisons RADARSAT pour surveiller les inondations, comme dans le cas de l'inondation de la rivière Rouge au Manitoba, pour appuyer les opérations de gestion des désastres, comme à Kobe au Japon, et pour mieux faire comprendre les effets de la guerre sur les populations locales et leur environnement, comme dans le cas du Vietnam. 
Nos investissements dans le domaine spatial nous permettent de faire avancer les découvertes scientifiques. 
Grâce aux expériences menées par les scientifiques mondiaux de l'espace parmi lesquels les Canadiens sont reconnus pour leur rôle décisif, nous apprenons davantage sur l'univers, sur les effets du soleil sur la Terre et sur la façon d'exploiter les conditions de microgravité uniques, ce qui nous permet d'obtenir des renseignements inestimables sur le système cardio-vasculaire, les os, le cerveau et les effets de la radiation sur l'organisme humain.  
Les expériences canadiennes dans le domaine des sciences spatiales se penchent, par exemple, sur les problèmes de santé dont le cancer et les maladies des os comme l'ostéoporose, une maladie qui touche plus d'un million de Canadiens. 
Les investissements que nous faisons favorisent les innovations et les retombées technologiques dans notre vie de tous les jours. 
Nous ne semblons pas nous rendre compte aujourd'hui que chaque fois que nous allumons la télévision, que nous écoutons les prévisions météorologiques, que nous rendons visite au médecin, que nous allumons notre ordinateur portatif, que nous prenons notre téléphone cellulaire ou que nous laçons nos chaussures de course à semelle absorbante, nous avons recours à des produits et services qui ont joui de la technologie spatiale. 
Cette liste de produits et services est longue et les effets positifs qu'ils ont eus sur notre qualité de vie sont très réels.  
Ces investissements que nous faisons permettent également de favoriser une industrie spatiale qui emploie des milliers de Canadiens et permet de tirer des revenus de plus de 1,4 milliard de dollars, et j'aimerais ajouter que 45 p.100 de ce chiffre d'affaires proviennent des exportations, ce qui est beaucoup plus que toute autre nation à vocation spatiale.  
La stratégie industrielle du Canada a fait de la robotique spatiale et de l'automation un créneau stratégique et a poussé notre industrie à mettre au point des technologies innovatrices qui s'imposent partout dans le monde. 
Par exemple, une entreprise de Terre-Neuve a mis au point un revêtement sensible conçu au départ pour des manipulateurs de robotique spatiale et se sert maintenant de cette technologie pour fabriquer des prothèses et des pare-chocs d'automobiles ainsi que pour contrôler le déploiement des coussins gonflables. 
Tout cela a été rendu possible grâce au sens de l'innovation des Canadiens et au programme spatial du Canada.  
Certains autres des principaux pays industrialisés font appel au savoir-faire canadien dans le cadre de leur apport au projet de station spatiale. 
Ainsi, la société EMS Technologies d'Ottawa a récemment obtenu un marché de 9,5 millions de dollars de la société Mitsubishi du Japon pour la fourniture de matériel électronique qui fera partie de la contribution du Japon à la création de la station spatiale internationale. 
D'autres commandes pourraient faire progresser la valeur totale de ce marché jusqu'à 24 millions de dollars.  
Par-dessus tout, notre engagement dans le secteur dynamique de l'espace est un engagement envers les jeunes du pays, nos futurs scientifiques et ingénieurs et même nos futurs astronautes.  
La ratification du projet de loi C-4 constitue une étape importante dans le cadre d'un engagement international prolongé et avantageux de notre pays. 
Troisième pays au monde à mettre un satellite en orbite, le Canada était le premier pays n'étant pas une superpuissance à le faire. 
C'était il y a 35 ans.  
Le Canada est le premier au pays au monde à s'être doté d'un système intérieur de télécommunications spatiales reliant les Canadiens des quatre coins de notre vaste pays.  
Le Canada est le créateur de RADARSAT, le satellite remarquable qui a permis au Canada de jouer un rôle de chef de file mondial dans la gestion de la Terre à partir de l'espace.  
Le Canada compte une équipe d'astronautes dont l'expérience dans le domaine de l'exploration spatiale est connue partout dans le monde.  
Le Canada est l'architecte du bras canadien de réputation mondiale, une merveille technologique qui est devenue une marque de commerce mondiale de l'excellence du Canada dans le secteur de la haute technologie.  
La liste des réalisations canadiennes est loin de s'arrêter là, et c'est un héritage dont tous les Canadiens peuvent à juste titre être très fiers.  
Que nous réserve l'avenir? 
À bien des égards, l'espace est le chemin de fer national du prochain siècle reliant tous les Canadiens d'un océan à l'autre et unissant tous les Canadiens. 
Avec le projet de loi C-4, nous attirons l'attention sur le dossier remarquable des réalisations du Canada dans l'espace et sur l'avenir qui s'annonce fort prometteur. 
Le programme spatial canadien et la participation active du Canada à la station spatiale internationale demeureront une composante essentielle et, de fait, un élément dominant de l'avenir du bien-être social et économique de notre pays.  
M. Charlie Penson (Peace River, Réf.): 
Madame la Présidente, je suis heureux de participer au débat sur le projet de loi C-4, qui vise à mettre en oeuvre l'accord que le Canada a conclu l'année dernière avec les États-Unis, le Japon, la Russie et onze pays de l'Union européenne.  
L'accord formule la participation du Canada à la station spatiale internationale. 
La station spatiale est un projet audacieux et emballant qui amène plusieurs pays dans le monde à travailler ensemble dans un esprit de coopération plutôt que de rivalité.  
