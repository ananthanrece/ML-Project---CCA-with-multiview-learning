Je suis heureux de dire aujourd'hui que le Parti réformiste appuiera l'adoption rapide du projet de loi. 
Je reconnais qu'il y a une certaine urgence à adopter le projet de loi afin d'honorer les engagements que nous avons pris envers nos partenaires dans ce projet.  
Le chef du Parti réformiste se passionne pour la technologie spatiale. 
Il s'intéresse vivement à ce projet et souhaite qu'il soit mis à exécution. 
Bon nombre de mes collègues manifestent le même intérêt pour ce domaine. 
Quant à moi, j'ai eu l'occasion, il y a deux ans, de visiter le centre spatial de Houston. 
J'ai beaucoup apprécié cette expérience et j'ai été émerveillé par la technologie qui a été mise au point dans le monde en l'espace de quelque 30 années seulement.  
J'aimerais revenir sur un commentaire fait par le secrétaire parlementaire au sujet des chemins de fer nationaux. 
La technologie spatiale fait partie, sous une forme nouvelle et très excitante, des innovations qui ont découlé de l'invention du chemin de fer.  
Avant de parler de contexte, j'aimerais préciser que, si le projet de loi suivait aujourd'hui toutes les étapes, nous serions tout à fait favorables à son adoption rapide. 
En janvier 1984, le président des États-Unis a demandé à la NASA de mettre au point et de placer en orbite une station spatiale habitée en permanence. 
À l'époque, le président Ronald Reagan avait invité amis et alliés des États-Unis à participer au développement de la station et à en partager les bénéfices.  
Au sommet de Québec, en mars 1985, le Canada acceptait cette invitation et, l'année suivante, à la réunion au sommet à Washington, il confirmait son intérêt pour la coopération. 
À la même époque, plusieurs autres pays exprimèrent un intérêt pour le projet et, au fil des ans, ils signèrent des protocoles d'entente.  
On reconnut que la Russie pourrait grandement améliorer les capacités de la station spatiale, car ce pays avait à son actif une longue liste de réalisations dans le domaine des vols spatiaux habités et des missions de longue durée. 
Le 6 décembre 1993, la Russie fut invitée à participer à ce projet. 
On prit alors des dispositions prévoyant de coopérer en matière de vols spatiaux; c'est ainsi qu'est né le programme russo-américain de la navette spatiale Mir en vue de la construction de la station spatiale.  
Le 29 janvier 1998, tous ces pays signèrent l'Accord sur la coopération relative à la station spatiale internationale civile, qui établit un cadre en vue de la conception, du développement, de l'exploitation et de l'utilisation de la station spatiale.  
Aujourd'hui, nous entamons la deuxième lecture du projet de loi C-4 ayant pour objet la mise en oeuvre de l'accord que nous avons signé l'an dernier afin de rendre cette entreprise possible.  
J'aimerais parler un moment de l'accord même et du projet de loi qui le met en oeuvre. 
L'accord international a été signé par le Canada, les États-Unis, le Japon, la Russie et 11 pays européens. 
Il compte 28 articles et une annexe qui énumère succinctement les tâches que les divers pays se sont engagés à accomplir eux-mêmes. 
Dans le cas du Canada, l'Agence spatiale canadienne fournira trois éléments: un centre d'entretien mobile, un manipulateur agile spécialisé, c'est-à-dire une main qui est en fait la nouvelle génération du Canadarm et des éléments au sol spécifiques de la station spatiale.  
Les articles précisent les objectifs et la portée de l'accord, les droits et les obligations internationaux, la propriété des éléments de l'équipement et la gestion de la station spatiale même. 
Il y est également question des aspects de la conception et du développement: le droit de fournir du personnel qualifié, le transport, le droit d'accès à la station spatiale et la fourniture d'un réseau de communications.  
Chaque partenaire prend à sa charge les dépenses nécessaires pour s'acquitter de ses propres responsabilités au titre de l'accord, y compris en partageant équitablement les activités et les coûts communs d'exploitation des systèmes qui auront été agréés et qui se rapporteront à l'exploitation de la station spatiale dans son ensemble.  
Un article important, à mon avis, est l'article 19, qui traite de l'échange de données et de biens. 
Chaque partenaire à l'accord s'engage à transférer toutes les données techniques et tous les biens jugés nécessaires pour que les partenaires s'acquittent de leurs responsabilités.  
L'article 7 du projet de loi C-4 renferme des dispositions qui confèrent au gouvernement le pouvoir d'obliger des sociétés, des personnes et des tiers qui n'ont pas conclu directement un contrat avec la Couronne de communiquer des renseignements concernant la station spatiale. 
D'où la nécessité de l'article 7. 
Pour assurer cette protection, l'accord renferme l'article 21, qui dit ce qui suit: 
Pour l'application du droit en matière de propriété intellectuelle, une activité se déroulant dans ou sur un élément de vol de la Station spatiale est réputée n'avoir eu lieu que sur le territoire de l'État partenaire.  
Cet article accorde une protection aux pays qui effectuent dans la station spatiale des travaux de recherche et de développement concernant de nouveaux produits. 
Ce produit leur appartiendra.  
Cela signifie que la personne ou l'organisme qui enregistrera un brevet le premier en sera propriétaire. 
Le projet de loi contient aussi des modifications au Code criminel. 
Ces modifications font en sorte que, si un membre canadien de l'équipage commet un acte criminel dans l'espace, il sera jugé en vertu du droit canadien. 
Il ne sera pas jugé en vertu des lois des États-Unis ou de la Russie, par exemple.  
Cela m'amène à la participation du Canada au projet de station. 
Récemment, Julie Payette est devenue la première canadienne à monter à bord de la station spatiale, mais elle ne sera certainement pas la dernière. 
En 2000, Marc Garneau participera à sa troisième mission spatiale en tant que membre d'équipage d'une navette. 
Lorsque le Canadarm et la main télémanipulatrice seront en place, nos partenaires pourront construire la station spatiale elle-même. 
Ce sont des éléments d'infrastructure nécessaires pour assembler la station spatiale. 
Une fois installé, le SEM pourra se déplacer autour de la station pour faire des travaux d'assemblage et de réparation.  
Le Canada participe aussi à la conception d'un système de vision artificielle pour les opérateurs du Canadarm, qui doivent le faire fonctionner de l'intérieur de la navette, qui n'a pas de hublots, ce qui, à mon avis, n'est certainement pas une tâche facile. 
Au sol, le complexe du système d'entretien mobile de l'Agence spatiale canadienne, à Saint-Hubert, au Québec, planifiera les missions, surveillera l'état du bras télémanipulateur et donnera aux équipages de la station spatiale la formation pour utiliser ce bras.  
Le coût est d'environ 3 $ par contribuable et par année. 
La contribution canadienne est relativement modeste, soit seulement 2,5 p. 100, mais elle demeure importante.  
Le budget fédéral de 1999 prévoyait 430 millions de dollars en argent frais pour l'Agence spatiale canadienne sur trois ans, ce qui a stabilisé son budget à 300 millions par année à compter de 2002-2003.  
Il s'agit néanmoins d'un aspect important, et je crois que ces avantages seront non négligeables. 
La station spatiale permettra de surveiller la banquise dans l'Arctique canadien, ce qui sera utile aux navires canadiens qui naviguent dans les eaux de l'Arctique.  
Depuis 1987, il y a eu plus de 150 marchés concernant l'automatisation et le développement de la robotique.  
Pendant les dix années de la durée estimative de la station spatiale, le Canada pourra étendre ses recherches en microgravité, recherches qui ont des applications dans des maladies comme l'ostéoporose et le cancer. 
Le Canada poursuivra également ses recherches sur la cristallisation des protéines dans l'espace, ce qui aidera considérablement la recherche médicale au Canada.  
Parmi les technologies déjà mises au point par suite de notre collaboration au projet de la station spatiale, il y a la première station robotisée d'avitaillement en carburant. 
Cette station a été conçue par une firme de la Colombie-Britannique dans le cadre d'un partenariat avec Shell. 
De même, une entreprise québécoise a appliqué l'expertise spatiale pour mettre au point un système d'imagerie numérique pour la radiographie qui élimine le besoin de films photographiques. 
De plus, grâce à une entreprise de Terre-Neuve, une peau sensible d'abord conçue pour les manipulateurs robotiques spatiaux entre maintenant dans la fabrication de membres artificiels.  
De nombreuses firmes canadiennes ont réussi à pénétrer les marchés internationaux en passant des contrats d'atterrissage fondés sur l'expertise qu'elles avaient acquises en travaillant à divers aspects du projet spatial. 
D'autres entreprises aident des partenaires d'autres pays à collaborer au projet de la station spatiale. 
