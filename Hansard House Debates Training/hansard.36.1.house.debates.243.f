LES MALADIES THYROÏDIENNES 
M. Bernard Patry (Pierrefonds-Dollard, Lib.):
Monsieur le Président, je suis heureux d'informer la Chambre que le mois de juin est le Mois des maladies thyroïdiennes au Canada.  
Plus d'un million de Canadiens et de Canadiennes de tous les âges souffrent d'une forme quelconque de maladie thyroïdienne et cette maladie frappe cinq à dix fois plus de femmes que d'hommes. 
Lorsque les troubles thyroïdiens ne sont ni décelés, ni traités, ils entraînent souvent de graves problèmes physiques ou affectifs.  
À travers ses 22 sections, la Fondation canadienne de la thyroïde s'emploie à sensibiliser la population aux affections thyroïdiennes au moyen de publications et de réunions d'information à l'échelle communautaire aux quatre coins du Canada.  
Je profite de l'occasion pour souligner le travail remarquable accompli par la Fondation canadienne de la thyroïde et ses nombreux bénévoles dévoués, et leur souhaiter tout le succès possible pendant le Mois des maladies thyroïdiennes.  
