LE DÉCÈS DE MME SARAH NINGIURUVIK 
M. Guy St-Julien (Abitibi-Baie-James-Nunavik, Lib.):
Monsieur le Président, aujourd'hui, nous déplorons le décès de Mme Sarah Ningiuruvik de Kangiqsujuaq au Nunavik. 
Elle était très connue et impliquée socialement dans sa communauté.  
Lors d'un incident, le 1 er juin dernier, dans un camp de pêche, elle a été brûlée au troisième degré de la tête aux pieds. 
Elle a souffert pendant plus de 20 heures avant de recevoir les soins appropriés aux grands brûlés, et ce, à cause de la réglementation dans les transports. 
Six compagnies aériennes n'ont pu se rendre à Kangiqsujuaq en raison de cette réglementation.  
Au nom de Mme Sarah Ningiuruvik, de sa famille et de la population des régions nordiques, nous devons trouver une solution à ce grave problème qui existe dans le Grand Nord.  
