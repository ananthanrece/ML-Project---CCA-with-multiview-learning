Deuxièmement, il y a eu amplement de temps. 
Les membres du comité de la partie gouvernementale étaient tout à fait prêts à consacrer plus de temps à l'examen des motions présentées par les députés de l'opposition. 
Ils n'ont soumis aucune motion. 
Il y a eu des discussions, mais aucune de ces motions n'a été présentée au comité pour examen. 
Les députés de l'opposition ont choisi de les présenter à la Chambre.  
Il est présomptueux de la part de l'opposition de tenir des propos alarmistes et de dire que cela sera inconstitutionnel. 
Nous ne savons pas si cela sera déclaré inconstitutionnel. 
Aucun expert des questions constitutionnelles n'a comparu devant le comité pour témoigner, à l'exception d'un avocat. 
Je pense que l'on n'aurait pas dû faire cette présomption.  
Le gouvernement ne peut appuyer la motion no 1. 
Elle nuit au côté pratique de la mesure. 
Ce n'est rien de nouveau et on juge ce critère nécessaire pour défendre l'idée du télémandat sur le plan constitutionnel dans le Code criminel.  
Les motions nos 2, 4 et 6 ont trait au fait que l'expression maison d'habitation est employée au singulier dans le projet de loi. 
Certains prétendent que, pour qu'un mandat s'applique à plus d'une maison d'habitation, nous devons employer cette expression au pluriel. 
Mais c'est pourquoi nous avons la Loi d'interprétation. 
La Loi d'interprétation établit déjà que le singulier sous-entend le pluriel. 
Toutes les lois fédérales, et cette loi en particulier, le projet de loi C-16, ont été rédigées en ayant cette règle à l'esprit.  
Il compliquerait l'interprétation des autres statuts et rendrait leur interprétation incertaine pour l'avenir.  
Nous ne pouvons appuyer cette motion, parce qu'elle contredit d'autres dispositions du Code criminel.  
Quant à la motion no 3, nous ne pouvons l'appuyer non plus. 
La raison, c'est que la motion modifie la formule 7, un mandat d'arrestation. 
Toutefois, le gouvernement pourrait se laisser entraîner à appuyer la motion no 5, et je vais commenter cette motion.  
Cet amendement a trait à la formule 7.1 et à l'article 529.1 du Code criminel. 
L'amendement a pour effet d'indiquer clairement dans la formule ce qui est déjà autorisé en vertu de l'article 529.1. 
Cet article permet l'obtention d'un mandat d'entrée pour l'arrestation d'une personne identifiable, plutôt qu'identifiée.  
Je crois que cette modification de la formule, selon la proposition du député, améliorerait le projet de loi en permettant d'indiquer sur la formule elle-même qu'il est possible de procéder ainsi.  
M. Michel Bellehumeur (Berthier-Montcalm, BQ):
Madame la Présidente, il me fait plaisir d'intervenir sur ce projet de loi extrêmement important. 
Comme les intervenants précédents l'ont fait, je pense que je vais glisser quelques mots sur la procédure qu'on a imposée au Comité de la justice et des droits de la personne.  
Je comprends que c'est à la suite d'une décision de la Cour suprême du Canada, l'arrêt Feeney, qu'on a imposé au gouvernement en quelque sorte l'obligation de modifier les dispositions, s'il voulait corriger la situation. 
Le jugement a été rendu le 22 mai 1997, en pleine campagne électorale. 
Cependant, il ne faut pas se le cacher, les fonctionnaires-je l'ai dit en comité, je ne m'en cache pas-la bureaucratie, à bon escient également, ont fait des consultations à travers le Canada et le Québec.  
Ce que je déplore, ce n'est pas d'avoir consulté avant d'avoir saisi l'aspect politique de la question, c'est d'avoir attendu à vendredi de la semaine dernière pour saisir la Chambre du projet de loi C-16, somme toute très important, alors que la Chambre siège depuis huit semaines. 
Ce n'est peut-être pas la faute des députés libéraux, mais c'est sûrement la faute de la lourdeur bureaucratique et administrative du gouvernement fédéral qu'on est obligés d'accélérer le processus d'analyse comme on le fait.  
Cependant, après avoir consulté certaines personnes qui ont intérêt à ce que ce projet de loi améliore le Code criminel, je pense que ce projet de loi, dans son ensemble, est satisfaisant. 
On ne lui donne pas une bénédiction totale, mais compte tenu des délais d'analyse, je pense que, dans un premier temps, c'est quelque chose d'acceptable.  
Je vais commenter les amendements par groupe. 
Je vais parler très brièvement du premier groupe, qui comprend la motion numéro un. 
Je pense que de la façon dont est rédigé l'article 529.5 du projet de loi C-16, il reflète finalement ce qui est inscrit un peu dans d'autres dispositions, d'autres lois statutaires. 
Je ne pense pas qu'on devrait modifier cet article de la façon dont le député du Parti conservateur voudrait le faire. 
Je pense que la rédaction, telle que nous la retrouvons à l'article relativement au télémandat, répond aux critères déjà établis dans ce domaine.  
Je veux vous le lire, parce que je pense qu'il se comprend très bien et on verra qu'il s'agit quand même d'un cas exceptionnel.  
La règle est que quelqu'un qui veut obtenir un mandat se présente devant un juge pour en obtenir un, mais il peut arriver des cas exceptionnels où c'est peu commode, comme on dit dans l'article, où c'est impensable que le policier se présente devant un juge. 
Il peut s'agir également d'une urgence-on a un article qui traite des cas d'urgence-et le policier ne peut pas se présenter devant un juge.  
L'article, qui se comprend très bien, se lit ainsi: 
Je pense que la lecture fait foi de l'objectif visé par cet article, et pour avoir une bonne économie de la loi, de façon générale, je pense qu'on ne devrait pas modifier cet article, puisqu'il est semblable à tous les autres articles traitant des télémandats. 
C'est pourquoi nous serons contre la motion no 1, telle que présentée.  
