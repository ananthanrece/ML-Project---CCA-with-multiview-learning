M. Paul DeVillers (secrétaire parlementaire du président du Conseil privé de la Reine pour le Canada et ministre des Affaires intergouvernementales, Lib.):
Monsieur le Président, je suis heureux de répondre, au nom du gouvernement, à la motion déposée par le député de Nanaïmo-Cowichan.  
Cette motion fait ressortir les divergences d'opinions entre le gouvernement et le député sur la question de l'unité nationale. 
Comme il l'a indiqué clairement dans le discours du Trône, le gouvernement a fait de cette question une des principales priorités de son mandat actuel. 
Nous voulons procéder à une réconciliation nationale. 
Nous croyons que la déclaration de Calgary est un pas important dans cette direction.  
Neuf premiers ministres provinciaux n'appuieraient pas non plus l'esprit de la motion présentée par le député. 
Même si les résultats des consultations qu'ils tiendront auprès de leur population respective ne sont pas encore connus, il semble déjà que la majorité des Canadiens appuient les grands principes de la déclaration de Calgary.  
Le premier élément de cette motion que le député nous invite à appuyer concerne l'égalité des provinces et des Canadiens en général. 
Je tiens à rassurer mon collègue: l'égalité est un principe que les Canadiens de toutes les régions partagent et auquel notre gouvernement souscrit pleinement. 
La déclaration de Calgary lui consacre d'ailleurs ses deux premiers articles.  
Ce principe d'égalité se situe au coeur même de notre identité. 
D'ailleurs, la Charte canadienne des droits et libertés le reconnaît clairement à l'article 15 qui garantit l'égalité des citoyens devant la loi. 
À ce sujet, l'article 1 de la déclaration de Calgary stipule que «tous les Canadiens et les Canadiennes sont égaux et leurs droits sont protégés par la loi».  
Nous sommes aussi en faveur du statut d'égalité des provinces. 
Il n'y a pas deux ou trois types de statuts pour les provinces. 
Dans la fédération canadienne, les provinces sont égales. 
Elles peuvent toutes exercer les mêmes pouvoirs. 
Quoi qu'il en soit, il se peut que des provinces se montrent plus intéressées que d'autres à exercer certains pouvoirs. 
En réalité, au fil des ans, certaines provinces se sont prévalues de certains pouvoirs, alors que d'autres ont préféré ne pas le faire. 
Par exemple, toutes les provinces ont le pouvoir d'administrer la perception de l'impôt sur le revenu des particuliers, mais seul le Québec le fait et a le personnel et l'infrastructure nécessaires à cette fin.  
En 1965, toutes les provinces ont pu se retirer de certains programmes nationaux établis et ont reçu à cet égard une indemnité adéquate, y compris des points d'impôt. 
Seul le Québec a choisi de le faire, ce qui explique pourquoi le taux de la taxe fédérale est plus bas au Québec que dans les autres provinces.  
Seul le Québec a exercé son droit constitutionnel d'établir son propre régime de pensions. 
Les autres provinces ont adhéré au Régime de pensions du Canada.  
Ces pouvoirs que le Québec a exercés ont toujours été à la disposition des autres provinces. 
Le fait que le gouvernement du Québec ait choisi de les exercer, contrairement aux autres provinces, ne confère pas un statut spécial au Québec. 
C'est pourquoi l'article 6 de la déclaration de Calgary stipule que si, dans l'avenir, des modifications constitutionnelles confèrent des pouvoirs à une province, ces pouvoirs devraient être accordés à toutes les provinces. 
Ainsi égalité ne signifie pas uniformité, pas plus qu'égalité de traitement n'est synonyme d'uniformité de traitement.  
Prenons l'exemple qui concerne la province du député de Nanaïmo-Cowichan. 
