Les gens doivent comprendre qu'à un niveau tout à fait fondamental, les Québécois se sentent menacés parce qu'ils vivent dans ce qu'ils perçoivent comme un Canada anglophone, une Amérique du Nord anglophone et, à bien des égards, à l'ère de l'Internet, des communications de masse et des satellites, dans un monde qui pourrait bien devenir anglophone. 
Ce qu'ils désirent, c'est qu'on reconnaisse dans le reste du Canada leur caractère français. 
Ils veulent être certains d'avoir un minimum de protection dans la Constitution. 
À certains égards, c'est une question émotive, mais ils pensent que leur culture, leur patrimoine et, plus particulièrement, leur langue sont gravement menacés.  
J'ai expliqué cela aux gens de St. Thomas. 
Lorsqu'on voit un Pizza Hut ou un magasin vidéo Blockbuster sur la rue principale de St. Thomas, ce n'est pas une question culturelle. 
Lorsque mes enfants jouent sur l'Internet, là encore, ce n'est pas une question culturelle. 
Si je vais voir un film américain, la même chose s'applique. 
Si je me fais bombarder par Much Music à la télévision lorsque mes enfants regardent ce canal, ce n'est pas un grand problème pour moi en tant qu'anglophone. 
C'est à ce chapitre qu'ils estiment avoir besoin de protection. 
De nos jours, il est possible que, dans une collectivité francophone, il n'y ait personne qui parle anglais, ou bien peu de gens qui le parlent. 
Ces gens pensent que, d'ici 30, 40 ou 50 ans, leurs enfants et leurs petits-enfants parleront anglais, à cause de toutes ces pressions venant de l'extérieur. 
Quand nous commençons à comprendre le problème sous son angle émotif, nous pouvons comprendre ce qu'ils cherchent.  
Ils cherchent une reconnaissance, que la déclaration de Calgary réussit bien à leur donner. 
Elle reconnaît que le Québec a un caractère unique. 
Nous pouvons débattre de la question de savoir s'il faudrait employer le terme «distincte», «unique» ou un autre. 
Pour ma part, je considère que c'est réellement une question de sémantique. 
Cela n'a pas vraiment d'importance. 
Ce qui importe, c'est que la majorité des Québécois sont francophones et veulent protéger leur culture.  
Une fois qu'ils ont obtenu cette reconnaissance, ils veulent également avoir un droit de veto concernant les modifications à la Constitution. 
Cela leur procurera essentiellement les deux points d'ancrage de toute modification constitutionnelle. 
Ils ont besoin d'une reconnaissance de même que d'un droit de veto régional.  
Avec un peu d'ouverture d'esprit, nous pouvons nous rendre à l'évidence, comme la population d'Elgin. 
Peu lui importe qu'il y ait un club hongrois à Simcoe ou ailleurs ou un club croate tout à côté de St. Thomas. 
Cela ne le dérange pas que les Québécois veuillent parler français. 
C'est parfaitement légitime.  
Je lui ai expliqué que cela ne changeait en rien ses droits, que cela ne le rendrait pas moins égal, qu'il s'agit au fond de protéger sa langue et sa culture. 
Au fond, il s'agit de respecter les coutumes de ses parents et de ses grands-parents, les traditions transmises. 
Il s'agit de respecter les traditions dans lesquelles on a grandi et qu'on veut transmettre à ses enfants et à ses petits-enfants.  
Nous comprenons tous cela. 
Nous sommes fiers de notre patrimoine. 
Nous sommes fiers de nos ancêtres, quelle que soit leur origine ethnique. 
Au Québec, on se sent particulièrement menacé, et on veut protéger son patrimoine. 
On ne peut qu'être d'accord.  
