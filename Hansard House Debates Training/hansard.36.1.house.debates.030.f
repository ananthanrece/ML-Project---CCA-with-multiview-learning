MOTIONS D'AMENDEMENT 
M. Peter MacKay (Pictou-Antigonish-Guysborough, PC) propose: 
Motion no 1 
Que le projet de loi C-16, à l'article 2, soit modifié par substitution, aux lignes 26 à 36, page 5, de ce qui suit: 
Il y a un problème au ministère de la Justice lorsque nous arrivons au point où d'importantes modifications au Code criminel sont, à toutes fins pratiques, imposées de force au comité puis à la Chambre des communes.  
En raison de la décision de la Cour suprême dans l'affaire La Reine c. Feeney, le ministère de la Justice s'est vu obligé de boucher la brèche ouverte quand la Cour suprême a annulé les articles du Code criminel se rapportant aux pouvoirs des policiers au moment des arrestations.  
La situation se rapproche de la négation du droit du Parlement de participer activement et sérieusement au débat et au processus visant à formuler des recommandations relatives au Code criminel du Canada.  
Il est clair que le temps pressait. 
La Cour suprême, en vertu d'une suspension qui a commencé en juillet 1997, a donné au Parlement le temps de réagir pour combler la lacune créée par la décision rendue dans l'affaire La Reine c. Feeney.  
Le problème, c'est que cinq mois et demi se sont écoulés déjà. 
Le ministère de la Justice a eu amplement le temps de réagir, d'apporter les modifications nécessaires pour corriger la situation. 
Lorsque ces amendements sont arrivés à l'étape de l'étude en comité, les députés, les élus à la Chambre, ont eu moins de cinq jours pour les examiner. 
Pour être plus précis, on leur a donné environ huit heures pour discuter, en présence de témoins et entre eux, ces modifications importantes qui touchent les pouvoirs d'arrestation de tous les agents de la paix d'un bout à l'autre du pays.  
Nous avons aussi entendu les témoignages présentés par un groupe de défense des droits des victimes et par le Centre canadien de ressources pour les victimes de crimes.  
Ce témoin a fait une déclaration très surprenante. 
Cela ne veut pas dire que son opinion est nécessairement juste. 
Cependant, lorsqu'un criminaliste chevronné habitué de plaider devant les tribunaux de première instance et les tribunaux d'appel fait une telle déclaration devant un comité quelques heures à peine avant que les modifications ne soient déposées et ne reçoivent la troisième lecture, cela devrait amener le ministère et tous les membres du comité à réfléchir et à se demander si nous voulons vraiment que cette mesure législative boiteuse soit adoptée, nous exposant ainsi, dans quelques mois, quelques semaines ou même quelques jours, à une autre contestation judiciaire qui pourrait avoir une incidence sur ces modifications ou, au pire, entraîner leur annulation.  
Le premier amendement que j'ai proposé à la Chambre vise à autoriser les télémandats au Canada. 
Cet amendement est le fruit de consultations avec l'Association du Barreau du Québec, qui n'a pas témoigné devant le comité, mais qui a soumis un mémoire.  
Cet amendement fait suite à l'apparition d'une nouvelle technologie de communication à laquelle tous les agents de la paix au Canada devraient avoir accès.  
Le Canada est un pays immense dont la population n'est pas entièrement regroupée dans les grands centres urbains comme Calgary, Toronto ou Halifax. 
En fait, de nombreux Canadiens vivent disséminés dans les régions rurales. 
Dans de nombreuses localités, les postes de police ne comptent qu'un ou deux policiers. 
Les télémandats, cette nouvelle forme de technologie rendue accessible grâce aux télécopieurs, téléphones et téléphones cellulaires, accroissent les possibilités d'action des policiers lorsqu'ils se retrouvent dans des circonstances pressantes. 
L'expression circonstances pressantes est une nouvelle formule issue de la décision rendue dans l'affaire de La Reine c. Feeney. 
Des circonstances pressantes se produisent fréquemment dans les activités quotidiennes des policiers, notamment lorsqu'ils se retrouvent dans des situations très sérieuses.  
Les télémandats permettent aux policiers de communiquer avec des juges de paix, mais encore faut-il qu'il y ait les ressources nécessaires pour que ces derniers soient disponibles. 
Il est facile de décrocher le téléphone ou de composer un numéro de télécopieur, mais s'il n'y a personne au bout du fil pour recevoir l'information et autoriser la délivrance d'un mandat, il y a un problème.  
Cet amendement va plus loin que la modification proposée par le gouvernement puisqu'il permettrait aux agents de la paix d'obtenir un mandat dans des circonstances plus générales. 
J'insiste cependant sur la nécessité de disposer des ressources suffisantes. 
Cela va de pair avec la possibilité d'obtenir des mandats. 
Nous devons faire en sorte qu'il y ait davantage de juges de paix disponibles, en particulier dans les régions rurales.  
J'insiste sur ce que je disais au départ. 
Je trouve regrettable que nous soyons obligés de débattre encore de cette question à la Chambre. 
Nous aurions pu éviter cela si les choses avaient été faites de façon plus professionnelle et sans perte de temps à l'étape de l'étude en comité.  
Tous les députés devraient examiner cet amendement et l'appuyer. 
Il permettra d'accroître la sécurité de tous les Canadiens et facilitera le travail très important des policiers chargés d'appliquer les lois pénales au Canada. 
Les députés, et surtout ceux qui font partie du Comité de la justice, doivent surveiller de très près ce que deviendront les modifications proposées au Code criminel.  
La ministre de la justice, qui est le premier exécutant de la loi au Canada, peut et aurait dû au moins examiner la nécessité de quitter son siège, descendre la rue Wellington et frapper à la porte de la Cour suprême pour demander un délai supplémentaire. 
Cela nous aurait évité de devoir adopter le projet de loi en vitesse à la onzième heure. 
Nous aurions pu en débattre adéquatement à l'étape de l'étude en comité et, si nécessaire, sur le parquet de la Chambre.  
J'appuie cet amendement. 
J'aimerais beaucoup connaître le point de vue des députés qui sont présents à la Chambre aujourd'hui.  
Mme Eleni Bakopanos (secrétaire parlementaire du ministre de la Justice et procureur général du Canada, Lib.):
Madame la Présidente, je désire d'abord réitérer ce qui a déjà été dit dans cette Chambre, soit que le gouvernement était obligé, par la Cour suprême, de s'assurer qu'on pouvait tous ensemble, des deux côtés de cette Chambre, arriver à un échéancier établi par la cour. 
Il ne s'agit pas d'un échéancier établi par le gouvernement, mais par la Cour suprême du Canada, et on doit s'y conformer.  
