INITIATIVES PARLEMENTAIRES 
LA SOCIÉTÉ DISTINCTE 
Que, de l'avis de la Chambre, le gouvernement devrait appuyer l'égalité des provinces et les Canadiens en général en faisant en sorte qu'aucune province ne soit reconnue comme société distincte dans le cadre de la Constitution du Canada.  
À la lumière de la récente déclaration de Calgary, tous les Canadiens devraient comprendre l'importance de cette motion. 
Étant donné que les partis traditionnels souhaitent s'en tenir à la notion de société distincte, je ne me fais aucune illusion quant aux critiques qu'ils vont soulever aujourd'hui.  
Pour commencer, certains vont dire que l'initiative dont nous sommes saisis est anti-Québec. 
Pour ceux qui ont ce sentiment, je tiens à dire que tout comme la déclaration de Calgary, la motion ne s'attaque à rien ni personne. 
Elle est, fondamentalement, en faveur du Canada dans son ensemble. 
Elle est rédigée dans le but de créer un nouveau Canada dans lequel tout le monde est fier d'être Canadien.  
Je dis cela avec beaucoup de confiance. 
Cette motion est dans l'intérêt des Canadiens en ce sens qu'elle reconnaît un principe fondamental cher au coeur de tous les Canadiens, à savoir que nous sommes tous égaux.  
Il s'agit de promouvoir le principe de l'égalité de tous les Canadiens et de préciser clairement le caractère essentiel des droits individuels. 
La protection de ces droits est également importante. 
Un des principaux défauts des accords de Meech et de Charlottetown, c'est qu'ils visaient à insérer un droit collectif dans la Constitution. 
Ainsi, la Constitution canadienne aurait pu subordonner des droits individuels au droit collectif d'une société distincte.  
Cet argument reposait sur la perception répandue et exacte parmi les Canadiens que la reconnaissance du Québec en tant que société distincte aurait conféré un droit collectif aux habitants de cette province, mais pas à ceux des autres provinces. 
C'est là que résident le problème et le dilemme auxquels doivent faire face les rédacteurs constitutionnels et les politiques, ceux-là mêmes qui ont tenté à de nombreuses occasions de forcer la reconnaissance du Québec en tant que société distincte, sans pratiquement aucun égard pour les véritables sentiments des Canadiens. 
Quelle a été la réaction des dirigeants des partis traditionnels aux paliers fédéral et provinciaux? 
Le mieux qu'ils aient pu faire, c'est de qualifier d'antipatriotique toute personne qui s'opposait à cette initiative. 
Ils ont également manipulé les craintes des Canadiens en disant que le pays serait déchiré si cette modification n'était pas apportée à la Constitution. 
Le pays est toujours là.  
Pis encore, ils ont délibérément tenté d'induire les Canadiens en erreur sur la signification véritable de la notion de société distincte. 
À l'époque, l'ancien premier ministre du parti qui s'est retrouvé en dernière place avait dit aux Canadiens de l'extérieur du Québec que la notion de société distincte n'était rien de plus qu'une reconnaissance symbolique de la place de cette province dans la Confédération.  
Je voudrais traiter brièvement de cette reconnaissance symbolique. 
C'est ce qui distingue la déclaration de Calgary des accords de Meech et de Charlottetown. 
C'était sur ce point que les accords du lac Meech et de Charlottetown faisaient problème. 
Les deux initiatives ont tenté de faire de cette reconnaissance historique un pouvoir constitutionnel accordé uniquement au Québec.  
C'est précisément ce que l'ancien premier ministre et le regretté premier ministre Bourassa disaient aux Québécois entre-temps. 
Leurs propos laissaient entendre que cette disposition serait utilisée pour accroître les pouvoirs de la province au sein de la Confédération.  
Les propos ambigus et les mesures du gouvernement fédéral concernant le statut de société distincte seraient comiques, s'ils ne compromettaient pas autant l'avenir du pays.  
Je voudrais traiter de ce que je considère comme un tort possible au droit collectif du statut de société distincte, si jamais ce statut était inscrit dans la Constitution. 
Bien sûr, il s'agit d'une hypothèse parce que, dieu merci, la disposition n'est pas dans la Constitution. 
Il faut attribuer cela à la sagesse des Canadiens plutôt qu'à la prévoyance des politiciens traditionnels, qui pensent même aujourd'hui que le fait d'inscrire le statut de société distincte dans la Constitution réglera le problème de l'unité nationale.  
De toute façon, les arguments sur le tort que pourrait causer l'inscription dans la Constitution de la société distincte sont hypothétiques, mais ils reposent sur un précédent historique.  
Je vais m'arrêter brièvement sur le précédent. 
Dans ce cas, les leçons et les arguments permettent de faire ressortir ce que je qualifie d'embûches en matière de société distincte. 
Dans une certaine mesure, le rapatriement de la Constitution canadienne a été réalisé parce que le gouvernement fédéral a pu obtenir l'accord de neuf provinces sur dix. 
Le gouvernement fédéral avait agi de façon unilatérale, mais la Cour suprême du Canada a jugé qu'il fallait un certain appui des provinces.  
Cette disposition faisait partie du prix que le gouvernement fédéral a eu à payer pour obtenir une entente sur la Constitution.  
On a justifié la clause de dérogation en disant qu'elle imposerait un frein constitutionnel aux pouvoirs du gouvernement fédéral. 
Certains premiers ministres croyaient à l'époque que cette disposition s'imposait notamment pour éviter qu'une mesure législative ou un nouveau programme de dépenses n'empiète sur des domaines de compétence provinciale. 
On croyait également que cette clause contribuerait à convaincre le Québec de ratifier la Constitution.  
À part la justification et les intentions motivant la clause de dérogation, bonnes ou mauvaises, certains ont prévu le danger potentiel que présenterait une constitution permettant à une province de ne pas tenir compte d'une loi fédérale ou d'un jugement de la Cour suprême sur la constitutionnalité d'une loi provinciale.  
Ce danger potentiel est important, car les arguments invoqués pour en minimiser l'importance des conséquences à l'époque sont les mêmes que ceux que l'on invoque maintenant pour faire accepter le concept de société distincte. 
Il n'est donc pas étonnant que les Canadiens pensent que les gouvernements successifs n'ont rien appris des expériences constitutionnelles passées. 
Ils ont peu ou pas confiance en la sincérité des politiciens de faire ce qui convient le mieux ni en leur capacité de régler le problème de l'unité nationale.  
C'est vraiment tragique. 
Comme l'a déjà dit quelqu'un dont le nom m'échappe pour le moment, ceux qui n'apprennent pas des erreurs du passé sont condamnés à les répéter.  
Quelles étaient ces terribles conséquences, comme certains le prévoyaient, qui pouvaient découler de la clause de dérogation? 
Les Canadiens qui avaient suffisamment de clairvoyance ont prévu qu'une province pourrait invoquer cette clause pour passer outre à la charte des droits et que, ce faisant, elle pourrait porter atteinte aux droits de certains de ses citoyens. 
C'est exactement ce qui est arrivé.  
Nul besoin de nous préoccuper pour l'instant des circonstances qui ont mené à l'application de cette clause. 
Nous y reviendrons en temps et lieu. 
Il faut nous concentrer sur les arguments qui ont été avancés à l'époque pour apaiser les craintes de voir un jour une province invoquer la clause de dérogation.  
Ces arguments sont encore frais à notre mémoire. 
Nous ne pouvons oublier les arguments qu'ont utilisés le gouvernement précédent et le gouvernement actuel pour rassurer les Canadiens à propos des répercussions sur la Constitution que pourrait avoir la disposition concernant la société distincte.  
Je le répète, en réponse à la crainte bien-fondée de voir une province invoquer la clause de dérogation, le gouvernement de l'époque a cherché à minimiser les chances d'une telle éventualité. 
Les reporters politiques et, dans une moindre mesure, les universitaires ont dit: «Cela ne se produira jamais. 
Aucune province n'invoquera la clause de dérogation. 
Le prix politique qu'elle aurait à payer serait beaucoup trop élevé. 
L'opinion publique dans cette province empêcherait le gouvernement d'appliquer cette clause», ainsi de suite.  
Essentiellement, les prédictions n'ont pas été retenues et les dirigeants politiques vivaient dans l'assurance que la disposition dérogatoire ne serait jamais, au grand jamais, appliquée. 
Naturellement, ils avaient tort.  
Passons rapidement de 1982 à 1989, année où le gouvernement du Québec a invoqué la clause de dérogation. 
Le Québec a pris cette initiative à la suite d'une décision de la Cour suprême du Canada, qui a déclaré que la loi linguistique du Québec, la loi 178, était anticonstitutionnelle et contraire à la Charte des droits et libertés.  
Je ne tiens pas tant à analyser les raisons pour lesquelles la clause de dérogation a été utilisée. 
Il est beaucoup plus intéressant d'examiner les parallèles entre les dangers potentiels que posait la clause de dérogation et les risques inhérents à une disposition reconnaissant une société distincte.  
Les politiques de l'époque n'avaient pas complètement tort au sujet de la réaction de l'opinion publique au recours, par le Québec, à la disposition dérogatoire. 
Le problème, c'est que le tribunal de l'opinion publique n'a rien changé et que son plein impact ne s'est fait sentir que longtemps après coup.  
Néanmoins, il y a eu pour le gouvernement du Québec des retombées négatives. 
Car, si conforme à la Constitution qu'ait été son recours à la disposition dérogatoire, il n'était guère acceptable pour ceux qui, tant au Québec qu'à l'extérieur, dénonçaient le fait que le gouvernement provincial piétinait les droits des Québécois anglophones.  
Ce qu'il fallait alors, c'était une modification de la Constitution canadienne permettant au Québec de se soustraire à la charte en matière de langue, par exemple, sans mettre la province dans la position inconfortable de devoir invoquer la disposition dérogatoire pour le faire. 
Bref, la Constitution canadienne devrait être interprétée de manière à reconnaître le droit de la province d'adopter ces lois.  
Dès le début est apparue l'idée de société distincte, d'une modification qui permettrait au Québec de passer outre à la Constitution en lui donnant le pouvoir de prendre tous les mesures nécessaires pour préserver et promouvoir le Québec comme société distincte.  
Que peut-on prévoir et quel pourrait être l'impact réel de la disposition sur la société distincte sur la Constitution? 
Les cris d'alarme auxquels on n'a pas prêté attention lorsque la disposition dérogatoire a été adoptée sont les mêmes qui se font entendre au sujet de la société distincte aujourd'hui.  
Si, comme des gouvernements au Québec l'exigent, la Constitution canadienne et la Charte des droits doivent être interprétées de telle sorte qu'elles reconnaissent le Québec comme société distincte, qu'arrive-t-il lorsque des droits individuels comme la liberté d'expression entrent en conflit avec un droit collectif fondé sur la culture et la langue?  
En fait, c'est une question de pure forme, car nous connaissons tous la réponse. 
C'est seulement que des gens ont horreur ou refuse de l'admettre ou ne veulent pas que le reste du pays le comprennent bien.  
Si l'on prend l'exemple de l'étude de la loi 178 du Québec par la Cour suprême, une disposition relative à la société distincte aurait permis à la Cour suprême de déclarer cette loi inconstitutionnelle. 
C'est exact. 
Aucun recours compliqué ou désagréable à une disposition de dérogation. 
Aucune ingérence fédérale, car le gouvernement fédéral dirait qu'il ne peut rien faire, qu'il faut respecter la décision de la Cour suprême.  
Cela a quelque chose de familier. 
Au moins, de cette manière, les électeurs ne peuvent pas reprocher au gouvernement de n'avoir rien fait.  
Afin que tous les députés aient leur mot à dire dans cette affaire, je demande le consentement unanime des députés ici présents pour que cette motion fasse l'objet d'un vote.  
