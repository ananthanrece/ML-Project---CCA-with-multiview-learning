M. Eric Lowther (Calgary-Centre, Réf.): 
Monsieur le Président, je profiterai des deux dernières minutes pour éclaircir rapidement certains des points dont il a été question à la Chambre cet après-midi.  
Cette mesure, soit le programme de protection des témoins de la GRC, sert d'ultime filet de sécurité pour les conjoints dans les cas où le counselling et les mesures de droit criminel se sont révélés inefficaces et où la vie des intéressés est en danger. 
Il existe des mesures de protection spéciales pour les témoins en danger en raison de leur participation une activité policière mais, chose étrange, il n'en existe pas pour les conjoints. 
Nous pouvons protéger des témoins, mais non des conjoints qui font l'objet d'actes de violence. 
Le projet de loi permettrait aux conjoints de bénéficier de ce genre de protection.  
Je lance un appel à la Chambre. 
Manifestement, elle s'impose et je l'appuie. 
La meilleure chose que nous puissions faire lorsque le projet de loi sera mis aux voix, ce qui arrivera, c'est de l'adopter et de le renvoyer à un comité et d'obtenir des éclaircissements afin que nous ne laissions pas la porte ouverte à plus de préjudices à l'endroit des familles et des conjoints.  
