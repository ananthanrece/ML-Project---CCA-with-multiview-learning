-Monsieur le Président, c'est un grand jour pour moi. 
Bien que j'aie présenté des projets de loi d'initiative parlementaire à de nombreuses reprises au cours des six dernières années, c'est mon premier projet de loi qui a été jugé digne de faire l'objet d'un vote.  
Le projet de loi C-223, Loi modifiant la Loi sur le programme de protection des témoins, ou loi sur le changement d'identité, comme je l'ai appelée, a un objectif: sauver des vies.  
Dans mon discours, aujourd'hui, je vais aborder le problème de la violence familiale dans notre société, de l'insuffisance des lois actuelles et du programme actuel de changement d'identité, et je vais expliquer quelles sont les initiatives proposées dans le projet de loi C-223.  
La violence familiale est l'un des problèmes les plus horribles auxquels est confrontée notre société. 
C'est une question qui exige que nous laissions de côté notre esprit de parti et que nous répondions aux véritables besoins des personnes en danger.  
J'aimerais donner quelques chiffres. 
En 1996, 21 901 cas de violence conjugale ont été enregistrés dans 154 services de police au pays. 
En 1996, environ 80 p. 100 des victimes de harcèlement criminel ou de harcèlement avec menaces étaient des femmes. 
Plus de la moitié de ces femmes étaient victimes d'ex-conjoints ou autres partenaires.  
Entre 1977 et 1996, il y a eu 2 048 homicides entre conjoints au Canada. 
Dans plus de 56 p. 100 de ces cas, les policiers avaient déjà été mis au courant de la violence exercée par un des conjoints.  
La lutte contre la violence familiale est très difficile, et je suis conscient qu'il ne sera pas facile de mettre un terme à ce fléau. 
Entre-temps, il y a des femmes et des enfants au pays qui craignent pour leur vie. 
Lorsque j'ai commencé à m'intéresser à la question, j'ai été étonné de constater qu'il y avait très peu de lois qui visaient à prévenir la violence familiale et conjugale. 
Il y a peu de recours dans ce domaine, si ce n'est les dispositions législatives interdisant le harcèlement avec menaces et les ordonnances de non-communication. 
La plupart des lois sont axées sur le châtiment pour des crimes commis, et il est alors souvent trop tard pour les victimes.  
C'est ce qui est arrivé à Mary-Lynne Miller, de Dawson Creek, dans ma circonscription. 
Gravement atteinte à la tête, elle a souffert pendant 21 heures sur le plancher de la cuisine avant d'obtenir de l'aide. 
Elle est toujours dans le coma.  
C'est cette histoire qui m'a vraiment sensibilisé à la violence familiale. 
Malheureusement, il y a eu des milliers d'autres cas tout aussi tragiques.  
En janvier de cette année, je lisais le Province de Vancouver dans l'avion qui me menait à Ottawa. 
Je suis tombé sur un article qui décrivait un programme non conformiste appliqué par des fonctionnaires du ministère du Revenu national conjointement avec le ministère du Développement des ressources humaines, ayant pour objet de fournir de nouvelles identités à des personnes qui se trouvent dans des situations où leur vie est en danger. 
Ce programme leur permet de changer de nom et de numéro d'assurance sociale afin de se cacher d'un conjoint ou ex-conjoint dangereux.  
L'article parlait d'une femme qui avait subi des raclées, qui avait fait l'objet de menaces de mort et qui avait vécu une véritable torture morale. 
Le programme des nouvelles identités l'avait aidée à s'établir ailleurs et à obtenir de nouveaux documents. 
Malheureusement, ses diplômes universitaires n'étaient pas transférables à sa nouvelle identité, ni son curriculum vitae. 
Cela voulait dire qu'elle a dû se contenter de n'importe quel emploi qu'elle a pu trouver afin de subvenir à ses besoins et à ceux de son enfant. 
Quand son mari retrouvait ses traces, il lui fallait fuir et recommencer le même processus panique d'auto-conservation.  
Après avoir lu cet article, j'ai ressenti de l'exaspération et j'ai pris la résolution de faire quelque chose. 
Je me suis alors mis à faire des recherches sur la question afin de trouver une solution. 
J'ai reçu en réponse des lettres d'appui, des histoires personnelles tragiques et horribles et des pétitions signées par des centaines de personnes qui appuyaient le projet de loi. 
Il était rassurant de savoir que des gens qui sont quotidiennement aux prises avec la brutalité ou les mauvais traitements de conjoints approuvent les principes dont s'inspire le projet de loi.  
Cet été, une femme a communiqué avec moi après avoir entendu parler du projet de loi. 
Elle est venue à mon bureau, affublée d'un déguisement, pour me raconter les mauvais traitements dont elle était victime. 
Elle fuit son ex-mari depuis plus de 10 ans. 
Elle et son fils sont constamment prêts à changer de logis. 
Cet automne, son mari, avec l'aide d'un enquêteur privé, les a retrouvés. 
Les menaces de violence sont tellement graves que l'école de son fils a été fermée à clé pour assurer la sécurité de l'enfant.  
Cette femme a été harcelée, suivie, battue, défigurée à jamais et a reçu des menaces de mort. 
Il est difficile de croire que ces citoyens canadiens vivent dans une telle crainte dans notre propre pays.  
À défaut d'une nouvelle identité, cette femme est convaincue que son ex-mari la tuera. 
Même si plusieurs ordonnances de non-communication ont été rendues contre lui, on ne peut pas faire grand-chose tant qu'il ne décidera pas de frapper encore une fois.  
Comme je l'ai mentionné plus tôt dans mes observations, le programme de nouvelles identités est un programme spécial qui offre une aide à ceux qui en ont besoin. 
Cependant, le programme n'est pas bien connu et est appliqué sans mandat ni financement officiel.  
Je voudrais saisir cette occasion pour souligner les services remarquables que rendent les fonctionnaires de ces ministères. 
Ces gens ont largement débordé le cadre de leurs fonctions pour venir en aide à ceux qui en ont besoin. 
Ils montrent qu'ils sont vraiment au service de la population.  
J'estime que le programme de protection des témoins est tout indiqué pour offrir de nouvelles identités. 
Les femmes qui participeront à ce programme bénéficieront du savoir-faire et des connaissances des agents de la GRC, qui relogent déjà des témoins à charge. 
Tous les organismes chargés d'appliquer la loi dans le pays sont reliés électroniquement, offrant un accès à ce programme depuis les coins les plus reculés du Canada.  
Je suis d'avis qu'en lançant le programme des nouvelles identités aux termes de Loi sur le programme de protection des témoins, nous pourrons faire davantage pour ces femmes et ces enfants. 
La Loi sur le programme de protection des témoins définit la protection, incluant le déménagement, le logement et l'établissement d'une nouvelle identité ainsi qu'un soutien psychologique et financier.  
Le programme des nouvelles identités est un dernier recours pour ceux que la loi ne protège pas et qui craignent pour leur vie et celle de leurs enfants.  
J'ai mentionné plus tôt les nombreux sacrifices que suppose un changement d'identité. 
Toutefois, certains pourraient être tentés de recourir à ce programme à d'autres fins. 
Pour empêcher que cela se produise, il y a une liste de critères auxquels il faut répondre pour avoir droit au programme: la nature du risque pour la sécurité de la personne, la possibilité de lui offrir une protection sans la faire entre dans le programme, la nature et la gravité des blessures physiques ou psychologiques infligées par le conjoint ainsi que les antécédents criminels de ce dernier, les circonstances qui amènent la personne à croire que sa vie est en danger, et tout autre facteur que le commissaire de la GRC juge pertinent. 
Je crois que ces critères sont équitables et qu'ils garantiront que ceux qui ont vraiment besoin du programme pourront en bénéficier.  
Le Canada est censé être le meilleur pays du monde à habiter. 
Mais pour la femme qui vit dans la crainte d'un conjoint violent, c'est l'enfer sur terre.  
