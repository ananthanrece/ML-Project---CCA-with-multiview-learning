Mme Michelle Dockrill (Bras d'Or-Cape Breton, NPD):
Monsieur le Président, je remercie la Chambre de l'occasion qu'il m'offre de prendre la parole en remplacement de mon collègue, le député de Regina-Lumsden-Lake Centre.  
Mon collègue et moi sommes tous deux favorables au projet de loi C-5, Loi constituant la Commission canadienne du Tourisme.  
Comme beaucoup de députés le savent déjà, la Commission canadienne du tourisme a été créée en réalité en 1992, à la suite de vastes consultations avec les divers protagonistes de l'industrie touristique d'un bout à l'autre du pays. 
À l'époque, au moment où le gouvernement précédent tâchait de mettre rapidement la commission sur pied et en activité, le gouvernement et l'industrie avaient convenu de constituer la CCT en organisme de service spécial plutôt qu'en société d'État.  
Un organisme de service spécial, cela veut dire essentiellement qu'il a toutes les responsabilités et aucun pouvoir. 
La CCT était chargée d'exécuter le programme, mais c'est le sous-ministre du tourisme qui s'occupait de l'administration. 
À cause de la rapidité avec laquelle la CCT a été créée, son organisation a été privée de tout un ensemble d'éléments importants, et ses activités étaient souvent paralysées à cause du cauchemar bureaucratique qui avait été créé.  
Certaines opérations de commercialisation piétinaient durant des mois parce qu'elles devaient passer par le processus bureaucratique d'approbation qui exigeait 13 signatures. 
Quand on finissait par obtenir toutes les signatures, les circonstances avaient presque toujours changé.  
On pouvait détecter le risque de problèmes à un mille. 
Les processus d'adjudication des contrats du gouvernement étaient tout simplement trop lents pour suivre les changements rapides qui se produisent dans l'industrie touristique. 
L'entreprise n'a pas besoin qu'on lui permette d'agir beaucoup plus rapidement, que ce soit dans le domaine privé ou le domaine public.  
Mes collègues néo-démocrates et moi appuyons la transformation de l'organisme de service spécial en société d'État, pourvu que le gouvernement fournisse à la commission du tourisme le soutien dont elle aura besoin pour remplir ses objectifs.  
La mise en oeuvre et les réalisations de la Commission canadienne du tourisme constituent un modèle de partenariat unique entre le secteur public et le secteur privé. 
L'industrie du tourisme fournit actuellement plus des deux tiers du financement des activités de la Commission.  
Les relations de travail seront désormais régies par le Code canadien du travail plutôt que par la Loi sur l'emploi dans la Fonction publique et les unités de négociation seront fusionnées, mais il y aura une période de transition d'un an qui donnera droit d'admissibilité aux concours de la Fonction publique et aux procédures de grief.  
L'industrie du tourisme appuie ce changement, les provinces appuient ce changement et le personnel appuie ce changement. 
Je crois que nous devrions être en mesure de nous entendre ici aujourd'hui et de faire adopter le projet de loi le plus rapidement possible. 
Toutes les politiques gouvernementales relatives au tourisme devraient être aussi bénéfiques que celle-ci pour l'industrie.  
Je profite de l'occasion pour rappeler à la Chambre les changement subits et brutaux que nos deux compagnies aériennes ont effectués au chapitre des commissions qu'elles tirent da la vente des billets à l'échelle internationale. 
À lui seul, ce changement menace quelque 7 500 emplois dans les petites communautés du pays.  
Fondamentalement, les deux sociétés aériennes se sont unies pour établir la nouvelles structure de prélèvement des commissions et elles en ont informé les agents de voyage. 
Je répète que les deux sociétés aériennes se sont unies pour établir la nouvelle structure de prix. 
Puis, lorsque l'association des agents de voyage a voulu rencontrer les sociétés aériennes pour discuter des taux, ces mêmes sociétés l'ont menacée de l'accuser de pratiques monopolistiques en vertu de la Loi sur la concurrence. 
Les libéraux sont vraiment passés maître dans l'art d'appliquer la loi des deux poids deux mesures.  
Même si je ne souhaite pas nécessairement exprimer mon désaccord avec cette déclaration, je dois quand même exprimer des réserves. 
Ce n'est pas le tourisme qui va garantir notre identité. 
C'est plutôt notre identité socio-culturelle distincte en tant que Canadiens qui va garantir l'intérêt continu des touristes pour le Canada.  
Le tourisme ne sera jamais rien de plus ni rien de moins qu'une partie intégrante de notre économie. 
Une économie bien équilibrée et dynamique ne peut être établie sur le tourisme seulement. 
Je le signale, car il est extrêmement important pour nous au Cap-Breton de se rappeler de cela à une époque où le gouvernement fédéral nous dit que le tourisme va assurer notre survie.  
Le tourisme est sensible aux caprices de l'économie et aux petites variations saisonnières. 
Il est important de noter que même si nous reconnaissons que cette année, le tourisme a rapporté 230 millions de dollars comparativement à 211 millions de dollars l'année précédente au Cap-Breton, cela fait du tourisme un des facteurs importants de reconstruction de notre économie dévastée, mais ce n'est absolument pas la seule réponse.  
Comme certains de mes collègues le savent peut-être déjà, les députés néo-démocrates de la Nouvelle-Écosse collaborent avec les exploitants d'entreprises touristiques dans toutes les provinces maritimes. 
Nous ferons face à une situation de monopole chez les fournisseurs de denrées si la fusion entre les groupes Sobey et Oshawa voit le jour. 
Cette fusion pourrait être catastrophique pour les restaurants, les gîtes touristiques et les hôtels qui doivent accepter les prix exigés de leurs fournisseurs de denrées. 
Si la fusion se matérialise, il n'y aura pratiquement aucune concurrence dans ce secteur. 
Je suis persuadée que pratiquement tous les députés reconnaîtront qu'une situation où il n'y a ni contrôle ni concurrence pourrait être catastrophique pour les gens qui dépendent des fournisseurs.  
Même si j'ai le sentiment de devoir appuyer l'adoption du projet de loi C-5, je ne le fais pas sans quelques réserves à l'égard des politiques du gouvernement libéral relativement au tourisme et aux exploitants d'entreprises touristiques. 
Je ne crois pas que le gouvernement libéral appuie les exploitants d'entreprises touristiques, surtout ceux qui sont chefs de petites entreprises, ainsi que les employés.  
