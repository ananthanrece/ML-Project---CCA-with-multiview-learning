Il est devenu une arme importante dans l'arsenal des techniques d'enquête aux fins de l'application de la loi. 
Il sert avant tout à protéger le témoin qui est peut-être en danger parce qu'il a facilité une enquête policière.  
Le programme a été créé, en vertu d'une loi de 1996, dans le cadre d'une initiative de partenariat entre le gouvernement fédéral et les gouvernements provinciaux et territoriaux en vue de lutter contre le crime. 
Depuis fort longtemps, les programmes de protection des témoins sont étroitement liés aux enquêtes sur le crime organisé. 
Comme nous le savons pour la plupart, le crime organisé couvre un large éventail d'activités criminelles dont le trafic massif de stupéfiants, le meurtre, les voies de fait graves, le blanchiment d'argent, l'extorsion et le vol à main armée. 
Plus souvent qu'autrement, ces délits supposent le recours à la violence et à l'intimidation pour s'assurer le silence des témoins et des informateurs potentiels.  
Toutefois, la protection des témoins a pris aujourd'hui un sens plus large. 
Une tendance troublante qui se dessine depuis quelques années est le recours à la violence et à l'intimidation par des criminels solitaires. 
Ces gens sont prêts à tout pour éviter la prison ou pour se venger des témoins. 
Il s'ensuit qu'un nombre croissant de personnes ont besoin de protection par suite du rôle qu'elles ont joué dans des affaires qui n'ont rien à voir avec le crime organisé.  
