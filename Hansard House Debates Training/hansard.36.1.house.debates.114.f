L'ENVIRONNEMENT 
M. Rick Laliberte (Rivière Churchill, NPD):
Monsieur le Président, le 12 mars, j'ai demandé à la ministre de l'Environnement si Environnement Canada avait suffisamment de ressources pour assumer ses responsabilités légales et faire appliquer la réglementation de la présente Loi canadienne sur la protection de l'environnement.  
La ministre a répondu que son ministère avait suffisamment de ressources pour s'occuper de tous les aspects de la protection de l'environnement dans le cadre de la loi actuelle. 
Le comité permanent a fait un constat contraire le 26 février. 
Le sous-ministre d'Environnement Canada a répondu aux questions du comité en disant que le programme de mise en oeuvre de la réglementation sur l'environnement n'avait pas les ressources humaines et financières nécessaires.  
Le rapport du comité permanent, intitulé «L'intérêt public d'abord! 
L'application des lois canadiennes sur la pollution», soulève une préoccupation du fait que la responsabilité de la Couronne, si elle néglige d'appliquer la législation gouvernementale, pourrait entraîner des poursuites contre Environnement Canada pour négligence réglementaire. 
Le rapport souligne aussi la nécessité d'avoir plus de ressources humaines et financières.  
La ministre de l'Environnement a affirmé à la télévision nationale qu'il n'y avait pas eu de compressions, récemment, dans le programme d'application de la législation environnementale. 
Après l'entrevue, les Canadiens ont pu lire les chiffres.  
En 1995, on comptait 28 enquêteurs chargés de protéger l'environnement du Canada contre la pollution et divers impacts environnementaux et infractions à la loi. 
Les Canadiens ne savent pas dans quel état critique se trouve vraiment l'environnement au Canada. 
La plupart des Canadiens seraient surpris d'apprendre que des pans entiers de la LCPE ne sont pas appliqués dans certaines provinces.  
Le lendemain, les médias ont état d'au moins vingt cas où le gouvernement n'a pas appliqué la loi relativement à ces infractions dans cette province.  
La région du Pacifique et du Yukon, où le gouvernement s'attend à ce que 16 personnes couvrent 17 200 endroits où la réglementation fédérale pourrait s'appliquer, pourrait perdre le tiers de son budget cette année.  
Le nombre des agents surveillants du stationnement sur la colline parlementaire est plus élevé que celui des inspecteurs itinérants pour toute la région des Prairies et du Nord, un territoire plus vaste que l'Europe.  
Avant les examens des programmes qui ont contribué à réduire d'environ 40 p. 100 les budgets d'Environnement Canada depuis l'arrivée des libéraux au pouvoir, un rapport interne du ministère disait qu'il faudrait 300 agents d'exécution de la loi pour protéger l'environnement. 
En réalité, il y en a moins de 70 aujourd'hui.  
La ministre de l'Environnement a parlé des avantages des programmes d'observation volontaire de la loi et de déclaration volontaire. 
Les Canadiens devraient savoir que des rapports ont conclu autrement les uns après les autres. 
L'autodiscipline à elle seule ne marche pas. 
L'industrie observe la loi quand les règlements fédéraux sont appliqués.  
Nous ne voulons pas dire qu'il y a beaucoup de mauvais éléments, mais il y en a quelques-uns qu'il faut surveiller. 
Par exemple, dans ce merveilleux pays que nous appelons le Canada, le climat rigoureux ne nous permet pas de laisser une fenêtre ou une porte ouverte sans compromettre le bien-être ou la sécurité de tout le monde.  
Le comité permanent a reçu des milieux écologistes canadiens de nombreux mémoires qui s'accordent à dire que les programmes volontaires ne suffisent pas pour assurer la protection de l'environnement. 
Pourquoi la ministre continue-t-elle d'appliquer une politique qui, de l'aveu de son propre ministère, ne marche pas? 
Qui donc le gouvernement essaie-t-il de protéger?  
On a souligné un manque de leadership et de volonté politique... 
