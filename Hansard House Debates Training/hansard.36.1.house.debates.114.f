LE CONCOURS INTERNATIONAL DE LABOURAGE ET LE SALON DE LA MACHINERIE AGRICOLE 
M. Larry McCormick (Hastings-Frontenac-Lennox and Addington, Lib.):
Monsieur le Président, cette année, le 85e concours international de labourage, CIL 98, se tiendra du 15 au 19 septembre à Sunbury, près de Kingston, dans ma circonscription, Hastings-Frontenac-Lennox and Addington.  
C'est la plus grande foire agricole en plein air d'Amérique du Nord. 
Cette manifestation de 5 jours a pour thème la qualité de la vie, un partenariat, un thème qui met l'accent sur l'interdépendance entre les communautés urbaines et les communautés rurales.  
Plus de 125 000 personnes sont attendues à cette exposition d'un millier de stands sur des éléments divers, allant du matériel agricole au matériel de loisir en passant par le matériel de jardinage, les objets anciens, les objets d'artisanat; de magnifiques animaux d'élevage seront exposés et les participants pourront voir également une présentation didactique et assister en direct à un concert de musique country.  
Des laboureurs âgés de 7 à 70 ans prendront part à ce concours. 
En plus des épreuves ordinaires, il y aura le championnat canadien de labourage. 
Pour ma part, j'ai hâte d'assister au concours qui opposera des élus provinciaux et fédéraux.  
