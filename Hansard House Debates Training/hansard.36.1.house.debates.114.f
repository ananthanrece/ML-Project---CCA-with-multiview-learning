L'honorable secrétaire parlementaire du premier ministre a la parole.  
M. Rey D. Pagtakhan (secrétaire parlementaire du premier ministre, Lib.):
Monsieur le Président, les Canadiens tiennent à ce que leur environnement soit sain à tous les égards car ils savent que c'est une condition sine qua non d'une bonne santé. 
C'est pourquoi les Canadiens veulent des lois fortes et efficaces pour protéger l'environnement et en assurer la qualité. 
C'est pour cette raison que, le 12 mars, la ministre de l'Environnement a présenté le projet de loi C-32, qui renouvelle la Loi canadienne sur la protection de l'environnement. 
Il met l'accent sur la prévention plutôt que sur les efforts coûteux en vue d'éliminer la pollution une fois qu'un accident est arrivé.  
Le projet de loi prévoit que toutes les substances utilisées au Canada seront étudiées pour en déterminer les effets sur l'environnement et donc sur la santé humaine. 
Des lignes directrices très strictes seront mises en place. 
Le gouvernement est déterminé à s'engager sur la voie de l'élimination quasi totale des substances toxiques les plus dangereuses. 
La prévention de la pollution est excellente pour l'environnement et les affaires. 
Les sociétés qui ont volontairement opté pour la prévention ont constaté une amélioration de leurs résultats financiers. 
Par ailleurs, une saine application de la loi est essentielle si l'on veut que les règles soient les mêmes pour tous et que les chefs de file soient soutenus tandis que les pollueurs sont pénalisés.  
La nouvelle loi améliorera l'application en donnant aux inspecteurs de nouveaux pouvoirs, notamment ceux de rendre des ordonnances sur place et de veiller à ce que les ressources financières et humaines soient disponibles.  
La nouvelle loi est fondée sur le principe du partenariat, non sur la dévolution de pouvoirs fédéraux aux provinces et territoires, mais sur une étroite coopération. 
Le gouvernement fédéral reste fermement résolu à préserver la santé des Canadiens contre la menace de la pollution en protégeant mieux l'environnement.  
