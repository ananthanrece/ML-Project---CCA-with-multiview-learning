LA PLACE TIANANMEN 
M. Jason Kenney (Calgary-Sud-Est, Réf.): 
Monsieur le Président, il y aura neuf ans demain qu'a eu lieu le massacre de la place Tiananmen.  
Au cours de la nuit du 3 juin 1989, l'Armée Populaire Chinoise de Libération a entouré les étudiants et les travailleurs qui s'étaient rassemblés de façon pacifique sur la place Tiananmen en faveur de la démocratie. 
À l'aube du 4 juin, les troupes et les policiers ont ouvert le feu. 
Les blindés se sont amenés. 
Les balles perforantes ont eu raison de la foule.  
En ce jour d'anniversaire, les Chinois réclament une nouvelle Chine, une Chine démocratique où régnerait la liberté de presse, une Chine qui respecterait la dignité humaine. 
Mais leur voix est bien vite étouffée et de nouveaux dissidents sont arrêtés chaque semaine.  
Malheureusement, le gouvernement communiste de la Chine a fait savoir aujourd'hui que le massacre des étudiants qui manifestaient en faveur de la démocratie sur la place Tiananmen était une mesure justifiée et qu'il n'y a aucune raison de rouvrir ce dossier. 
N'oublions jamais les braves étudiants et travailleurs qui ont perdu la vie ou qui sont toujours emprisonnés pour nulle autre raison que d'avoir exprimé leur croyance en la démocratie.  
