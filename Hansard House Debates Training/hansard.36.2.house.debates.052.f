La Chambre reprend l'étude de la motion 
M. Gary Lunn (Saanich-Gulf Islands, Réf.): 
Monsieur le Président, je suis heureux de prendre part au débat.  
J'ai beaucoup de respect pour le député de Pictou-Antigonish-Guysborough. 
Il est impérieux que je lise la motion ainsi que la recommandation no 73 du rapport sur la mine Westray qui a incité le député à présenter sa motion.  
La motion no 79 dit ceci: 
Que, de l'avis de la Chambre, le Code criminel ou d'autres lois fédérales pertinentes devraient être modifiées conformément à la recommandation no 73 de la Commission d'enquête de la province de la Nouvelle-Écosse sur la tragédie de la mine Westray, de sorte que les cadres d'entreprise et administrateurs soient tenus responsables de la sécurité au travail.  
Le recommandation no 73 du rapport d'enquête sur la mine de Westray dit ceci: 
Le gouvernement canadien, par l'intermédiaire du ministère de la Justice, devrait instituer une étude...  
J'insiste sur le mot «étude» 
...de la responsabilité des cadres et des dirigeants d'entreprise relativement aux actes fautifs ou à la négligence de leur entreprise et qu'il dépose au Parlement du Canada les modifications législatives nécessaires pour que les cadres et les dirigeants aient des comptes à rendre par rapport à la sécurité au travail.  
La recommandation no 73 lance clairement un appel au Gouvernement du Canada afin que, par l'intermédiaire du ministère de la Justice, il entreprenne une étude sur la responsabilité des cadres et des dirigeants d'entreprise. 
Je ne vois pas de problème là-dedans.  
Regardons la question de plus près. 
La motion no 79 demande la modification immédiate du Code criminel ou d'autres lois fédérales pertinentes sans étude préalable des paramètres de la responsabilité des entreprises. 
Avec tout le respect que je dois au député de la Nouvelle-Écosse qui est très sincère dans ce qu'il essaie de faire, je trouve que c'est un peu prématuré. 
Je voudrais parler un peu des détails et faire une critique constructive.  
Les ramifications de la modification du Code criminel sont immenses, voire dans certains cas définitives. 
L'objection que j'oppose à cette initiative du député n'a pas du tout pour but de minimiser ce qu'il essaie de faire. 
Nous ne pouvons minimiser l'étendue de cette tragédie qui a coûté la vie à 26 personnes.  
Nous devrions suivre la recommandation no 73 avant de modifier le Code criminel.  
La question de la responsabilité des entreprises est un paradigme en évolution. 
La composition des entreprises et le rôle des administrateurs sont en constante évolution. 
Je ne sais pas vraiment de quelle preuve empirique et de quelles ressources nous disposons pour bien comprendre la responsabilité des entreprises, notamment celle des administrateurs.  
La loi énonce des principes directeurs et établit des paramètres légaux pour le mode d'organisation des entreprises. 
La constitution de l'entreprise en personne morale est un mode d'organisation qui permet d'éviter aux actionnaires d'être tenus personnellement responsables des obligations civiles de l'entreprise. 
Les dirigeants, les administrateurs ou autres cadres et employés de l'entreprise ne bénéficient d'aucune immunité spéciale en matière de responsabilité criminelle ou quasi criminelle. 
Ils sont légalement responsables lorsqu'ils commettent personnellement une faute ou une négligence. 
Les entreprises dotées de la personnalité morale peuvent être tenues criminellement responsables à titre distinct.  
En cas d'infraction comportant une responsabilité absolue ou stricte, l'entreprise est tenue criminellement responsable des actes ou omissions perpétrés illégalement par elle-même ou par ses employés et ses mandataires dans l'exercice de leurs fonctions. 
En cas d'infraction pénale, l'entreprise est tenue responsable des actes ou omissions perpétrés par des personnes qui, du fait de leur position d'autorité dans l'entreprise, sont réputées être son «âme dirigeante».  
Dans le rapport de novembre 1997 sur la tragédie de la mine Westray, le juge Peter Richard recommande la révision et la modification des lois fédérales et provinciales applicables pour nous assurer que les cadres d'entreprise et les administrateurs soient tenus responsables de la sécurité au travail. 
Je ne cherche nullement à minimiser ce drame, mais il est important de veiller à ce que les modifications qui seront apportées soient celles qu'il faut.  
Le député propose dans sa motion d'aller plus loin. 
Sa motion voudrait ostensiblement qu'il soit plus facile de tenir les entreprises criminellement responsables en réclamant la création d'une nouvelle infraction pénale. 
Le juge Richard n'est pas allé aussi loin. 
Il voulait procéder par étapes. 
Le juge Richard a demandé une étude sur la responsabilité de la société, reconnaissant la nature changeante et évolutive des sociétés, leur composition et le rôle des directeurs.  
Permettez-moi d'identifier certains des problèmes réels que présente, selon moi, cette motion. 
Les sociétés auraient des difficultés importantes à attirer de bons candidats acceptant de siéger au conseil si de telles sanctions du Code criminel pouvaient leur être imposées, comme le demande la motion no 79. 
Je dirais que personne ne voudrait faire ce travail, mais les implications pour les sociétés sans conseil sont évidentes en vertu du droit commercial et du droit des sociétés. 
Les sociétés plus petites qui luttent pour survivre seraient très désavantagées si de telles normes de responsabilité leur étaient imposées.  
Je ne dis pas qu'il ne devrait pas y avoir de responsabilité et que les administrateurs devraient pouvoir échapper à leurs devoirs et à leurs responsabilités sociales. 
Toutefois, je ne souhaite pas voir d'obstacles à la croissance, punitifs et affaiblissants, se dresser devant eux et, de ce fait, devant leurs sociétés.  
Personnellement, je crois que la majorité des sociétés servent un objectif social et sont préoccupées par la sécurité du milieu de travail. 
Je dis cela en connaissance de cause. 
J'ai été responsable de la sécurité pour une compagnie forestière de la Colombie-Britannique et je peux attester que, bien que des accidents surviennent, la sécurité est prise très au sérieux. 
Je suis certain que les sociétés ne souhaitent pas voir des accidents se produire.  
Une modification générale du Code criminel ne favoriserait pas la croissance économique et l'emploi. 
Un certain équilibre entre la réalisation de profits et l'exploitation de façon responsable sur le plan éthique et social des entreprises est nécessaire. 
De plus, je dois m'informer des conséquences et des responsabilités que cela entraînerait pour les délégués syndicaux. 
Je ne veux pas dire par là que nous devrions songer à leur imposer des sanctions pénales, mais ils jouent également un rôle très important au chapitre de la sécurité dans le milieu de travail. 
Il faudra évaluer cet aspect également.  
J'aimerais que le député de Pictou-Antigonish-Guysborough nous dise s'il a songé aux responsabilités des inspecteurs provinciaux et fédéraux des lieux de travail. 
La motion no 79 pourrait entraîner des conséquences désastreuses pour les affaires au Canada. 
Elle pourrait avoir des répercussions importantes sur les investissements et augmenter considérablement les coûts d'opération et réduire d'autant les profits et la motivation à prendre de l'expansion. 
Les niveaux d'emploi dans les sociétés en seraient très certainement réduits. 
Moins il y a de gens qui travaillent, moins il y a de risques d'accidents. 
Toutefois, les niveaux de production n'en seraient-ils pas également réduits?  
Il ne serait pas raisonnable de s'attendre à ce que tous les administrateurs de sociétés connaissent tous les aspects de la santé et de la sécurité relatifs à une société en particulier. 
Il importe que tous les administrateurs connaissent bien le fonctionnement quotidien de la compagnie.  
Je comprends que l'exemple de la Westray représente un problème de taille. 
Toutefois, si chacun des administrateurs devait assumer ce genre de responsabilité, il n'y aurait plus personne pour siéger aux divers conseils.  
Je partage le véritable intérêt que voue le député au bien-être des mineurs et son espoir qu'une telle tragédie ne se répétera pas. 
Personne ne peut minimiser la gravité de la tragédie humaine qui a frappé cette localité et ses habitants.  
Cependant, il est important de reconnaître que l'État a actuellement le pouvoir, sous le régime du Code criminel, de porter des accusations contre les gestionnaires négligents de la mine qui étaient sur place et qui étaient responsables. 
Il faut donner suite à l'avis énoncé dans la recommandation no 73 du rapport de la commission d'enquête sur la tragédie de la mine Westray et examiner les responsabilités des administrateurs avant d'apporter des modifications au Code criminel.  
En résumé, ne sautons pas trop vite aux conclusions. 
Une étude très approfondie s'impose, car une telle mesure aura de graves répercussions sur les entreprises partout au Canada. 
Assurons-nous que ce que nous faisons est absolument juste. 
Il existe actuellement des dispositions concernant les personnes occupant des postes de gestion courante.  
Je félicite le député de Pictou-Antigonish-Guysborough d'avoir proposé cette motion. 
C'est une motion très importante, mais nous tenons à nous assurer que ce que nous faisons est inattaquable et juste car cela pourrait avoir des répercussions très négatives sur les milieux d'affaires au Canada. 
Cela semble être l'orientation que prenait également le député d'Erie-Lincoln.  
