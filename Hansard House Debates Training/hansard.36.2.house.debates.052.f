INITIATIVES PARLEMENTAIRES 
LA MINE WESTRAY 
M. Peter MacKay (Pictou-Antigonish-Guysborough, PC) propose: 
Que, de l'avis de la Chambre, le Code criminel ou d'autres lois fédérales pertinentes devraient être modifiés conformément à la recommandation no 73 de la Commission d'enquête de la province de la Nouvelle-Écosse sur la tragédie de la mine Westray, de sorte que les cadres d'entreprise et administrateurs soient tenus responsables de la sécurité au travail.  
Brièvement, je voudrais rappeler bien entendu que, le 9 mai 1992, il y a eu à la mine Westray de Plymouth, en Nouvelle-Écosse, une explosion qui a entraîné la mort de 26 hommes. 
C'était une terrible tragédie qui a touché toute la province de la Nouvelle-Écosse et, en fait, tout le pays.  
De nombreux habitants de la Nouvelle-Écosse ont agi de façon tout à fait héroïque en cette sombre journée de mai 1992. 
Nous serons éternellement reconnaissants à ces dragueurs créatifs qui ont participé si directement aux efforts pour retrouver les 26 mineurs qui avaient perdu la vie. 
Beaucoup de ces dragueurs venaient de toute la Nouvelle-Écosse et, en fait, de tout le pays. 
Ils ont travaillé en vain malheureusement pour sauver ces 26 hommes.  
Les mines de charbon et l'industrie houillère ont été extrêmement importantes pendant des générations pour le nord-est de la Nouvelle-Écosse. 
Le livre Pictou County Colliers , de James M. Cameron, raconte l'histoire des mines de charbon dans cette région du Canada.  
La catastrophe à la mine Westray, survenue le jour de la Fête des mères il y a près de huit ans, a laissé un souvenir douloureux dans le coeur et l'esprit des mineurs, et des gens en général, dans toute la province.  
L'explosion à la mine de charbon Westray a transmis un message très inquiétant. 
Ce message, c'était que la hâte et la poursuite de gains financiers peuvent mettre la vie de gens en danger. 
Beaucoup de gens qui gagnaient leur vie dans ces mines sont morts sous terre au cours des 150 dernières années.  
Beaucoup de gens en Nouvelle-Écosse en particulier se rappellent de la secousse à Spring Hill ou d'éboulements et d'explosions dans le comté de Pictou et au Cap-Breton. 
Des artistes comme Rita MacNeil et Men of the Deep chantent des chansons poignantes en souvenir de ces âmes perdues.  
C'est pourquoi il nous incombe en tant que législateurs de nous assurer qu'il y ait des milieux de travail sûrs pour tous les travailleurs, qu'il s'agisse d'une mine ou d'une usine de transformation de poisson, d'une usine de fabrication d'automobiles ou de tout autre milieu de travail. 
Ils nous incombe en tant que législateurs de faire en sorte d'assurer la sécurité dans ces milieux de travail.  
Quelques jours après l'explosion à la mine Westray, soit le 15 mai, le gouvernement de la Nouvelle-Écosse nommait le juge Peter Richard à la tête d'une commission chargée, en vertu du Public Inquiries Act, d'enquêter sur ce qui s'était passé à la mine.  
Le mandat de cette commission d'enquête était très vaste, visant à faire toute la lumière sur l'explosion et toutes les circonstances à l'origine de la catastrophe. 
En fait, le premier ministre de la Nouvelle-Écosse en poste à l'époque, Donald Cameron, avait été clair sur ce mandat. 
Il avait dit: 
L'enquête menée par le juge Richard ne se limitera pas aux événements qui se sont produits en début de matinée le 9 mai. 
Rien ni personne qui soit susceptible d'aider à faire la lumière sur cette stratégie n'échappera à l'examen minutieux de cette enquête.  
Les travaux de la Commission d'enquête ont donc débuté presque immédiatement en préparation des audiences publiques devant commencer en octobre 1992.  
La compagnie Curragh Resources Inc. et la direction de Westray ont contesté la validité de l'arrêté en conseil établissant la commission d'enquête. 
Cela a donné lieu, comme le député peut s'imaginer, à de nombreuses procédures judiciaires. 
Conséquence de ces délais, le rapport Richard n'a été déposé que cinq ans plus tard, en novembre 1997.  
Il y a eu beaucoup de disputes au pénal et les accusations portées en vertu de la loi sur la sécurité des travailleurs ont beaucoup contribué aux retards et à la frustration entraînés par ces disputes.  
Il s'agit d'une question très importante pour tous et j'apprécierais d'avoir l'attention des députés présents.  
La tragédie de la mine Westray est très complexe, et on a pu voir en contexte toutes sortes de mesures, d'omissions, d'erreurs et d'exemples d'incompétence, d'indifférence, de cynisme et de pure stupidité. 
Ces incidents apparemment isolés ont été la conséquence d'une mentalité et d'une philosophie d'exploitation qui semblaient placer l'opportunisme avant la planification intelligente, banalisant ainsi les préoccupations en matière de sécurité. 
En effet, les gestionnaires de la mine Westray ont démontré un certain mépris de la sécurité et ont semblé considérer les travailleurs conscients des questions de sécurité comme des poules mouillées.  
Les gestionnaires de la mine Westray ont, par incompétence ou par ignorance, perdu de vue le principe de base des houillères, à savoir que la sécurité dans l'exploitation est une bonne pratique d'affaires, et cela jette un certain discrédit sur eux. 
Je tire cela directement du rapport du juge Richard, intitulé «The Westray Story:
A Predictable Path to Disaster». 
Il contenait 74 recommandations et concluait, dans son analyse finale, que la tragédie aurait pu être évitée si les normes de sécurité minimales avaient été respectées.  
Il y a toujours des moyens d'éviter les tragédies. 
Parfois, elles ne peuvent pas l'être, mais il existe des façons d'éliminer le risque et l'environnement qui coûtent des vies ou laissent les gens avec des blessures pour le reste de leur vie. 
Il existe des moyens d'imposer des responsabilités criminelles à ces actions qui mettent la vie des gens en danger. 
Quelquefois, les sociétés et ceux qui les dirigent devraient, selon moi, se voir imposer un certain degré de culpabilité et de responsabilité en vertu de notre loi, en particulier du Code criminel.  
Cela peut se faire par des modifications et des mesures législatives. 
Certaines ont été suggérées par le juge Richard. 
Il a proposé en particulier la création d'une nouvelle infraction criminelle qui imposerait une responsabilité criminelle aux directeurs, ou autres personnes concernées, qui ne font pas respecter dans leur entreprise des normes convenables en matière de santé et de sécurité au travail. 
C'est exactement l'objet de la motion à l'étude.  
En Nouvelle-Écosse, l'activité minière est régie par trois lois: sur les ressources minérales, sur la santé et la sécurité au travail et sur les mines de charbon. 
Dans son rapport, le juge Richard passe en revue toutes ces lois provinciales et conclut que leur principal objectif est la sécurité.  
Les dirigeants des sociétés minières se doivent de veiller à ce que le travail se fasse en toute sécurité.  
Nous voulons que les particuliers oeuvrant à l'intérieur et l'extérieur des entreprises canadiennes fassent l'objet d'un traitement équitable en vertu de la loi, mais nous désirons également qu'il y ait un certain degré de responsabilité, et que les dirigeants ne puissent se cacher derrière le paravent de la société et du titre de leur poste dans l'exercice de leurs fonctions.  
Les entreprises canadiennes ont naturellement deux fonctions liées: faire un bénéfice et créer de l'emploi. 
Un bénéfice est une bonne chose, mais il faut atteindre un équilibre entre le bénéfice et le coût qui est parfois attribuable à un comportement téméraire. 
Il doit y avoir un équilibre entre la réalisation d'un bénéfice et les moyens pris pour y arriver.  
L'article 220 du Code criminel parle précisément de négligence criminelle causant la mort, et des paragraphes de l'article 234 du Code criminel parlent d'homicide involontaire coupable. 
Il y a peut-être lieu de présenter des amendements à ces articles en vue d'élargir la portée de la notion de culpabilité ou peut-être d'aller jusqu'à parler précisément des dirigeants, administrateurs ou gestionnaires lorsqu'il est question de la commission d'actes aboutissant à la perte de vies.  
J'ai présenté cette motion dans l'espoir qu'on n'oublie jamais la catastrophe de la mine Westray. 
Je peux donner l'assurance à la Chambre et aux Canadiens en général que les gens du comté de Pictou et, en fait, de toute la Nouvelle-Écosse, se rappellent avec horreur de la période qui s'est écoulée depuis mai 1992.  
Il est tout aussi important aujourd'hui de veiller à ce que les recommandations découlant de l'enquête ne soient pas oubliées. 
Malheureusement, tous les Canadiens ont été témoins de rapports qui ont été complétés, de livres blancs qui ont été présentés par des commissions royales d'enquête qui avaient accompli leur travail. 
Ces rapports aboutissent sur une tablette où ils accumulent de la poussière sans qu'aucune mesure ne soit adoptée.  
Les belles paroles ne suffisent pas lorsqu'il est question de protéger les vies dans ce contexte. 
Il est important que nous donnions suite aux recommandations du rapport et que nous agissions effectivement de façon énergique en réaction au travail qui a déjà été accompli.  
La responsabilité fondamentale de l'exploitation sûre d'une mine de charbon souterraine incombe intimement sur le plan industriel aux propriétaires et aux gestionnaires. 
La direction de la mine Westray, à commencer par le président directeur général, était tenue en vertu de la loi, des bonnes pratiques commerciales et de leur bonne conscience de concevoir une mine sûre et de l'exploiter de façon sûre. 
La direction de la mine Westray a été pointée du doigt dans les jours qui ont suivi l'explosion parce qu'elle avait fait preuve de laxisme dans l'exercice de cette responsabilité.  
On ne saurait exagérer l'ampleur de cette négligence. 
Que d'autres aient éludé leur responsabilité n'est pas une réponse satisfaisante. 
On peut dire que les recommandations qui se sont dégagées du rapport parlent implicitement d'un partage de la responsabilité.  
Non seulement dans l'industrie minière, mais aussi dans toute entreprise, les cadres semblent parfois moins intéressés par la sécurité au travail et davantage par la quête du profit. 
C'est précisément à cause de cette mentalité que la sécurité au travail laissait tellement à désirer dans la mine Westray.  
Les entreprises doivent également veiller à ce que leurs employés soient bien supervisés et constamment informés des nouvelles pratiques de sécurité au travail. 
Dans le cas de la mine Westray, les normes minimales de sécurité au travail n'ont pas été respectées comme elles auraient dû l'être.  
Si nous examinons la question dans un contexte plus large, nous constatons que le gouvernement doit également reconnaître le rôle qu'il a à jouer pour veiller au respect des normes appropriées. 
Si les normes ne sont pas respectées, il doit intervenir en faisant appel à ses inspecteurs et ordonner la fermeture du lieu de travail jusqu'à ce que ces normes minimales soient respectées.  
Bien souvent, il ne faisait aucun doute que la direction était au courant ou aurait dû être au courant du fait que les travailleurs n'utilisaient pas des pratiques sûres d'extraction minière souterraine.  
Comme on peut le lire dans le rapport du juge Richard, tout porte à croire que la direction de Westray connaissait les niveaux souterrains de méthane auxquels les mineurs étaient exposés et savaient que ces niveaux étaient très dangereux et pouvaient exploser. 
En vertu de l'article 72 de la Coal Mines Regulation Act, les travailleurs auraient dû être retirés des endroits concernés. 
Bien sûr, c'était un moyen d'éviter la tragédie qui s'est produite.  
Dans le cas de Westray, le fait qu'il y avait des subventions gouvernementales augmentait la pression.  
C'est à ce moment aussi qu'il faut tenir compte de l'élément de sécurité. 
Les dirigeants d'entreprise sont trop souvent tentés de faire passer la rentabilité avant la sécurité des travailleurs. 
Cette rentabilité à court terme pour un mal à long terme, comme on l'a vu dans le cas de Westray, doit être évitée, et c'est l'objet de la motion. 
Bien sûr, je fais allusion au mal que cause la mort pouvant résulter de la décision de privilégier la rentabilité. 
C'est un scénario fort regrettable dont il faut tirer des leçons.  
Les temps difficiles que traverse le pays augmentent la pression sur les travailleurs. 
C'est pour cela que la motion est, à mon sens, si opportune. 
Quand il faut fermer des entreprises, les conséquences économiques se répercutent inévitablement sur tous les habitants de la région. 
Les employés, les cadres, les administrateurs, tous ceux qui sont liés à l'entreprise subissent les conséquences d'une fermeture.  
C'est le prix à payer en affaires. 
S'il faut fermer trois entreprises pour protéger des vies, il faut le faire.  
Les entreprises ainsi que nous, en tant que législateurs, devons faire tout ce qui est possible pour nous attaquer aux lieux de travail dangereux d'une manière proactive et non réactive, comme on l'a vu dans le cas de Westray. 
Si les entreprises n'agissent pas correctement, si elles ne respectent pas les lois provinciales et fédérales, elles doivent en subir les conséquences.  
Des dirigeants d'entreprises se trouvent dans une position où ils n'ont pas à assumer les conséquences de leur possible négligence et du contrôle laxiste exercé sur ce qui se passe sur le terrain. 
Les dirigeants d'entreprises doivent favoriser et encourager une éthique du travail qui assure la sécurité; ils doivent avoir une attitude ouverte et être facilement accessibles pour leurs employés. 
Personne ne veut revivre les conséquences des événements survenus à la mine Westray de Plymouth.  
Je suis très satisfait du ton non partisan que nous avons adopté lors des précédents débats sur cette motion. 
Nous avons les connaissances, le savoir-faire et le pouvoir requis pour promulguer des lois qui peuvent avoir un effet très réel sur la sécurité au travail. 
Nous pouvons exercer notre pouvoir discrétionnaire pour veiller à ce que ceux qui exploitent des entreprises et ceux qui déterminent ce qui se passe dans le milieu du travail respectent des pratiques favorisant la sécurité.  
Je remercie tous les députés qui ont appuyé cette motion jusqu'à maintenant. 
