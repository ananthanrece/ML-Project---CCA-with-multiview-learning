M. Brent St. Denis (secrétaire parlementaire du ministre des Ressources naturelles, Lib.):
Monsieur le Président, je suis heureux de prendre la parole sur la motion no 79. 
Je félicite le député de Pictou-Antigonish-Guysborough de ses observations ainsi que la députée de Bras d'Or-Cape Breton de ses sages remarques.  
Il est demandé dans la motion que les lois fédérales, y compris le Code criminel, soient modifiées afin de régler la question de la responsabilité des dirigeants et des administrateurs des sociétés relativement à la sûreté du milieu de travail, conformément à la recommandation 73 du rapport de la Commission d'enquête de la province de la Nouvelle-Écosse sur la tragédie de la mine Westray. 
Personne à la Chambre ne partage pas les préoccupations du député relativement aux victimes de la tragédie de la mine Westray et à leur famille.  
Le désastre de la mine Westray est une tragédie qui a secoué le pays tout entier. 
Bien qu'elle se soit produite il y a quelque temps déjà, elle reste toujours présente dans nos esprits et souligne la nécessité pour nous tous, représentants publics, d'être sensibles aux préoccupations des travailleurs et de garantir la sûreté du milieu de travail.  
Les travailleurs sont la pierre angulaire de notre économie. 
Les accidents mortels, les blessures et les maladies au travail sont très coûteux pour les familles des victimes tant sur le plan humain que pécuniaire. 
On estime qu'en plus des terribles coûts humains, les accidents de travail coûtent jusqu'à 10 millions de dollars par an à notre économie. 
Ces coûts sont trop élevés et il faut trouver le moyen de les réduire.  
La voie législative est certes une option, mais la sensibilisation l'est aussi.  
Ayant moi-même travaillé dans des usines et dans les chemins de fer, je sais à quel point il est important que les travailleurs sachent quelles sont les meilleures pratiques à adopter dans leur lieu de travail et puissent compter sur leurs gestionnaires. 
Beaucoup de spécialistes du domaine de la santé et de la sécurité en milieu de travail estiment que la prévention par l'éducation et la formation est tout aussi importante que les lois. 
Ces spécialistes comprennent la valeur que prend la promotion de l'éducation et de la formation dans la réduction de l'incidence des maladies et des accidents en milieu de travail.  
Le Jour de deuil national a été souligné le 28 avril de l'an dernier et le sera à nouveau cette année. 
Je suis fier de dire que dans ma ville, Elliot Lake, dans ma circonscription, Algoma-Manitoulin, nous profitons chaque année de cette journée spéciale pour rendre hommage à la mémoire des travailleurs qui ont perdu la vie.  
Nous demandons aux Canadiens de se souvenir des travailleurs tués ou blessés lors d'accidents de travail ou frappés par des maladies professionnelles parfois mortelles. 
Le drapeau canadien a été mis en berne sur la colline du Parlement en mémoire des travailleurs qui ont perdu la vie ou ont contracté une maladie au travail.  
En outre, chaque année, dans toute l'Amérique du Nord, des organismes collaborent à l'organisation de la Semaine nord-américaine de la sécurité et de la santé au travail. 
L'an dernier, cet événement s'est déroulé du 17 au 23 mai et je prévois qu'il sera à nouveau tenu cette année.  
À cette occasion, le gouvernement du Canada s'unit au gouvernement des États-Unis et à celui du Mexique pour sensibiliser à la sécurité au travail toute l'Amérique du Nord.  
Par ailleurs, des organismes canadiens comme la Société canadienne de la santé et de la sécurité au travail et le Centre canadien d'hygiène et de sécurité au travail, travaillent en partenariat avec le gouvernement fédéral et d'autres intervenants au sein de la société canadienne à la promotion de la sécurité au travail.  
Ces organismes produisent d'excellents documents visant à aider les travailleurs, les entreprises, les gouvernements et toutes les autres parties intéressées à la santé et à la sécurité au travail à apprendre à travailler de concert à la découverte et à la mise en oeuvre de nouveaux moyens de promotion de la sécurité au travail.  
Il faut rappeler l'importance de l'éducation en matière de sécurité au travail.  
Le Programme du travail apporte un soutien important à ces activités. 
D'ailleurs, depuis quelques années le programme joue un rôle de premier plan en matière d'éducation et de sensibilisation à l'importance de la sécurité en milieu de travail.  
Bien que la motion ne fasse pas référence à l'utilisation de l'information pour améliorer la sécurité au travail, il faut se rappeler que l'éducation et la sensibilisation constituent un élément important de notre politique de promotion de la sécurité au travail.  
Le second élément de cette entreprise est, bien entendu, de nature législative.  
Au fil des années, les lois et la réglementation fédérales concernant la sécurité et la santé au travail ont été regroupées dans le Code canadien du travail.  
En étudiant la motion dont nous sommes saisis, nous devons aussi tenir compte des dispositions que contient le Code canadien du travail, en particulier la partie II, qui régit la sécurité et la santé au travail pour les employés qui travaillent dans des organisations de compétence fédérale.  
Le code contient également un ensemble de règles relatives à la sécurité et à la santé au travail, qui énoncent les normes et procédures que doivent suivre les employeurs et les employés.  
Les inspecteurs du gouvernement fédéral se rendent en milieu de travail, répondent aux plaintes, effectuent des enquêtes, interdisent l'accès à des milieux de travail jugés dangereux et peuvent imposer des amendes en cas de non-observation. 
Autrement dit, le Code canadien du travail nous offre déjà un modèle dont nous pouvons nous inspirer pour assurer la sécurité en milieu de travail.  
Le problème, c'est que le Code canadien du travail ne vise que les employés qui travaillent dans des industries et organisations de compétence fédérale. 
Cela ne vise donc qu'une petite partie de la population active du Canada. 
Par conséquent, bien que nous ayons un modèle efficace dans le Code du travail, il ne couvre pas la totalité des travailleurs, dont la plupart sont couverts par des lois provinciales et territoriales.  
La difficulté que présente l'idée d'élargir le modèle fédéral afin d'inclure les travailleurs non couverts par la loi fédérale est que, constitutionnellement, la sécurité du milieu de travail relève également des provinces. 
Toute action au niveau fédéral visant la protection des droits des travailleurs et pouvant empiéter sur le domaine provincial ou territorial peut ne pas être vue d'un bon oeil par les autres niveaux de gouvernement.  
Nous devons trouver une approche qui accommoderait à la fois les intérêts du gouvernement fédéral et des gouvernements provinciaux et qui combinerait également les approches éducatives et législatives. 
Cette question nécessite d'être étudiée de façon plus approfondie par la ministre de la Justice, comme le recommande lui-même le rapport sur la tragédie de Westray.  
Je voudrais mettre l'accent sur un point soulevé par ma collègue, la secrétaire parlementaire de la ministre de la Justice, qui a déclaré que la responsabilité criminelle des entreprises fait partie d'un objectif plus large d'amélioration de la sécurité des lieux de travail. 
Le projet de loi C-12, dont la Chambre est maintenant saisie, modifie la partie II du Code canadien du travail pour accroître les obligations des employeurs et les droits des employés relativement à la sécurité dans les lieux de travail sous réglementation fédérale.  
Même si l'accent est mis sur des mesures préventives, divers recours, y compris des sanctions, sont prévus dans le code du travail pour toute violation des normes. 
Le projet de loi C-12 mérite d'être étudié le plus rapidement possible par la Chambre, alors que le gouvernement continue d'examiner des modifications législatives possibles relativement à la responsabilité criminelle des entreprises.  
Cette motion, qui part des meilleures intentions du monde, demande simplement à la Chambre de souscrire à l'idée de modifier le Code criminel, mais ne précise pas le type de modifications nécessaires. 
En tant que telle, la motion ne fait pas avancer de façon importante l'étude de la question complexe de la responsabilité criminelle des entreprises.  
Nous, de ce côté-ci, souscrivons à l'idée d'une étude. 
Je pense que le député qui a proposé cette motion, qui l'a fait dans les meilleures intentions du monde, ne reconnaît pas les compétences importantes des provinces en ce qui concerne la sécurité et la santé en milieu de travail. 
Je l'encourage à coopérer avec le gouvernement. 
Je suis persuadé qu'une étude donnera les meilleurs résultats possibles pour les travailleurs de tout le pays. 
Nous savons que les travailleurs sous réglementation fédérale sont bien protégés. 
Nous espérons que nos homologues provinciaux jugeront bon de participer à toute étude destinée à améliorer le sort des travailleurs de tout le pays.  
