Les statistiques sont toujours incroyables cependant. 
Trois travailleurs canadiens perdent la vie tous les jours. 
Mille Canadiens par année perdent la vie au travail et des centaines de milliers d'autres doivent s'absenter par suite d'accidents de travail.  
Il faut se pencher sur les raisons morales et éthiques pour lesquelles il importe de mettre un frein à tout cela. 
Passons maintenant aux raisons financières.  
Tous les ans, dans ma province du Manitoba, 50 000 jours-personnes de travail sont perdus en raison de grèves et de lock-out et les gens de la Chambre de commerce, qui sont de la droite, répètent à qui veut l'entendre que les syndicats sont responsables d'un manque de productivité. 
Au cours de la même période, 500 000 jours-personnes sont perdus par suite de blessures, d'accidents, d'absence au travail, de maladies causées par des blessures et autres. 
Si nous nous soucions vraiment de la productivité, si nous nous soucions vraiment des travailleurs ou des bénéfices liés à la productivité, il faut commencer par faire le ménage dans le milieu de travail.  
Il y a toutes sortes de bonnes raisons pour lesquelles la motion devrait obtenir l'appui unanime de la Chambre. 
Dans la foulée de la motion, nous pourrions adopter le projet de loi d'initiative parlementaire portant sur la mine Westray, en incorporer les dispositions dans le Code criminel et modifier la Partie II du Code canadien du travail. 
Personne ne devrait se faire blesser, massacrer ou estropier dans notre pays au nom d'un calendrier arbitraire de production.  
