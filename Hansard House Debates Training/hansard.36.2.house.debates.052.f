J'ai hâte d'entendre les propos des députés à cet égard et j'espère obtenir l'appui de tous les députés à la Chambre afin que nous puissions progresser et faire tout notre possible pour que la sécurité au travail soit une priorité au pays. 
C'est une question qui préoccupe au plus haut point les parlementaires et nous sommes disposés à prendre des mesures concrètes pour apporter une amélioration.  
M. John Maloney (secrétaire parlementaire du ministre de la Justice et procureur général du Canada, Lib.):
Monsieur le Président, je suis heureux de prendre la parole au sujet de la motion no 79.  
Présentée par le député de Pictou-Antigonish-Guysborough, la motion propose que, de l'avis de la Chambre, le Code criminel devrait être modifié, de sorte que les cadres d'entreprise et administrateurs soient tenus responsables de la sécurité au travail. 
Cette motion fait suite, de toute évidence, à l'explosion tragique qui a tué 26 mineurs dans la mine Westray, le 9 mai 1992. 
L'enquête publique que le gouvernement de la Nouvelle-Écosse a instituée par la suite a établi clairement que la mauvaise gestion était à l'origine d'un milieu de travail dangereux, lequel était la cause directe du désastre. 
Tout au long de l'enquête et des poursuites qui ont suivi l'enquête sur la tragédie, la responsabilité des cadres de la mine Westray et de la compagnie minière Westray elle-même est apparue comme un facteur important et comme une source de controverse.  
La commission d'enquête, présidée par le juge Peter Richard, a produit un rapport en quatre volumes intitulé «The Westray Story:
A Predictable Path to Disaster». 
Le rapport contient 74 recommandations qui visent à améliorer la sécurité dans les mines, pour faire en sorte que des incidents comme celui de la mine Westray ne se reproduisent plus et que les opérations minières ne conduisent plus jamais, selon les mots du juge, au désastre.  
Je voudrais lire la recommandation no 73 du rapport du juge Richard, car elle constitue le fondement de la motion dont la Chambre est saisie aujourd'hui: 
Le gouvernement du Canada, par le biais de son ministère de la Justice, devrait entreprendre une étude portant sur la responsabilité des dirigeants et directeurs d'entreprises pour les actes de négligence posés par ces derniers et introduire devant le Parlement du Canada les amendements législatifs nécessaires afin d'assurer que ces dirigeants et directeurs soient tenus responsables de la sécurité des lieux de travail.  
Je reconnais le bien-fondé d'un examen de la législation pénale en ce qui a trait à la responsabilité criminelle des entreprises. 
Toutefois, je pense qu'il faudra plutôt avoir recours à une combinaison de mesures préventives, correctives et punitives pour prévenir de nouveaux désastres. 
Aussi, les solutions au problème de la responsabilité des entreprises relèveront à la fois de la législation pénale, du code du travail et de la réglementation de l'activité des entreprises.  
Avant de parler du rôle que le droit criminel joue dans un tel système, je donnerai un exemple de mesures préventives et correctrices extérieures au Code criminel qui peuvent contribuer à atteindre les objectifs de sécurité en milieu de travail.  
Le 28 octobre dernier, la ministre du Travail a présenté le projet de loi C-12, apportant diverses modifications à la partie II du Code canadien du travail afin d'améliorer la sécurité en milieu de travail des entreprises relevant de la compétence fédérale.  
Cette mesure législative élargit les responsabilités des employeurs et des employés pour ce qui est de créer un environnement de travail sûr. 
Elle établit trois droits fondamentaux des employés: le droit de savoir les risques qu'ils courent en milieu de travail, le droit de contribuer à l'enlèvement de ces risques et le droit de refuser un travail dangereux.  
Un droit ne sert à rien s'il n'y a pas de solution d'apportée. 
Par conséquent, le projet de loi C-12 élargit le rôle des comités d'orientation en matière de santé et de sécurité en milieu de travail pour ce qui est d'inspecter les lieux de travail et d'enquêter sur les plaintes. 
De la même manière, lorsqu'un employé refuse d'accomplir des tâches qui sont considérées comme dangereuses, le projet de loi prévoit l'accélération du processus de traitement de sa plainte.  
Conformément au Code canadien du travail, quiconque contrevient à ces règles commet une infraction et est passible d'une amende ou d'une peine d'emprisonnement.  
Nous pouvons attendre à un autre jour pour débattre du projet de loi C-12, mais j'exhorte mes collègues à ne pas oublier que des initiatives comme le projet de loi C-12 visent beaucoup des mêmes objectifs que les modifications que le juge Richard a recommandé d'apporter au droit criminel dans son rapport sur l'affaire de la mine Westray.  
La motion dont nous sommes saisis aujourd'hui ne vise pas à apporter une modification en particulier au Code criminel. 
Par conséquent, je voudrais signaler des éléments de ce qui constitue un comportement criminel de l'entreprise employeure. 
Tout examen du Code criminel dans cette optique doit tenir compte de la responsabilité de la société elle-même et de celle des personnes qu'elle emploie.  
En ce qui concerne le premier point, il faut comprendre qu'en vertu de la loi actuelle, il est déjà possible de porter des accusations criminelles contre une société. 
L'article 2 du Code criminel inclut les sociétés dans la définition de «personne», et il est arrivé que des accusations criminelles soient portées contre des sociétés. 
En fait, dans l'affaire de la mine Westray, des accusations d'homicide involontaire coupable et de négligence criminelle causant la mort ont été portées contre Curragh, Inc., le propriétaire et l'exploitant de la mine, ainsi que contre certains employés de la société.  
Il importe aussi de souligner que la Cour suprême du Canada a soutenu, relativement à une affaire dont elle avait été saisie en 1985, qu'une société sera généralement tenue responsable d'une infraction criminelle si un de ses directeurs ou responsables commet une infraction dans l'intérêt de la société, dans l'exercice de ses fonctions.  
En 1993, un sous-comité du Comité permanent de la justice et du Solliciteur général de la Chambre des communes, comme il s'appelait à l'époque, a publié un rapport sur la recodification de la partie générale du Code criminel. 
Il s'agit de la partie qui traite des aspects fondamentaux des infractions criminelles comme la culpabilité et les moyens de défense.  
Le sous-comité s'est dit d'avis qu'il faudrait inclure, dans la partie générale, des règles explicites sur la responsabilité des sociétés, de manière à établir une distinction nette entre la responsabilité de la société et celle de ses employés. 
Le comité a fait la recommandation suivante: 
Une personne morale est responsable de la conduite de ses administrateurs, dirigeants et employés agissant en son nom et dans les limites de leurs pouvoirs, et dont on peut supposer qu'ils sont investis de pouvoirs quant à l'élaboration ou à la mise en oeuvre des politiques de la personne morale, même si un administrateur, dirigeant ou employé ne peut être tenu pour personnellement responsable de la même infraction.  
Si nous voulons modifier la loi en vigueur, il y a d'autres possibilités qui méritent d'être examinées.  
La loi australienne prévoit qu'il est possible qu'une entreprise soit accusée d'infractions liées à l'intention, à la connaissance, à l'insouciance ou à la négligence. 
Elle s'intéresse aux actes du conseil d'administration ou de ses agents qui, tacitement ou expressément, autorisent la perpétration d'une infraction criminelle.  
Je parlerai maintenant brièvement de la responsabilité criminelle des employés individuels d'une société par opposition à celle de la société. 
Sur le plan individuel, les employés peuvent être accusés de toute infraction criminelle perpétrée par eux et pour lesquelles ils sont moralement responsables. 
L'article 33 du Code criminel prévoit également que commet une infraction quiconque a participé à une infraction, c'est-à-dire qu'il a fait ou omis de faire quelque chose pour aider quelqu'un à commettre ladite infraction. 
L'article prévoit aussi que le fait de conseiller quelqu'un à participer à une infraction constitue une infraction. 
Par conséquent, il est déjà possible d'accuser des employés individuels dont les actions fautives dans l'exercice de leurs fonctions équivalent à une infraction.  
Ces dispositions du Code criminel sont importantes car si la société est, de façon générale, tenue de se conformer à la loi, ses administrateurs et ses dirigeants ont également le devoir de tenir compte des intérêts qui vont au-delà de ceux des actionnaires et du bilan.  
Je sais qu'après l'enquête sur la tragédie de la mine Westray, le procureur général de la Nouvelle-Écosse a demandé à la ministre de la Justice du Canada de donner suite à la recommandation 73 du rapport sur la mine Westray et que la ministre a accepté de le faire. 
Cela ayant été fait, je suis d'avis que la motion dont nous sommes saisis ne fait pas avancer les choses.  
Enfin, les lois pénales adoptées par le Parlement devraient en outre reconnaître le rôle des provinces dans le domaine de la sûreté du milieu de travail. 
À cet égard, je remarque que le rapport Westray recommande que la province de la Nouvelle-Écosse entreprenne un examen de sa législation en matière de santé et de sécurité professionnelles et prenne les mesures qui s'imposent pour veiller à ce que les dirigeants et les administrateurs des sociétés exerçant leur activité dans la province soient tenus responsables lorsqu'ils manquent à leur obligation de maintenir un milieu de travail sûr.  
Pour ces raisons, je m'oppose à cette motion puisqu'elle ne fait pas progresser l'étude du droit pénal en ce domaine.  
