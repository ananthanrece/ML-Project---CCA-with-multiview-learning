LOI SUR LA CITOYENNETÉ AU CANADA 
M. Andrew Telegdi (secrétaire parlementaire de la ministre de la Citoyenneté et de l'Immigration, Lib.):
Madame la Présidente, je suis heureux d'avoir l'honneur d'ouvrir ce débat sur le projet de loi C-16 concernant la citoyenneté canadienne, au nom de la ministre de la Citoyenneté et de l'Immigration.  
À mon avis, comme à celui de bon nombre de Canadiens, la citoyenneté canadienne représente l'appartenance à notre société. 
La citoyenneté est donc beaucoup plus qu'un simple document, plus qu'une case à cocher sur un formulaire, plus qu'un document qui s'avère commode quand on voyage à l'étranger. 
C'est pourquoi la loi sur la citoyenneté doit donc être considérée comme l'une de nos plus importantes lois. 
Notre loi sur la citoyenneté établit les règles fondamentales qui doivent régir ceux qui peuvent se considérer comme des Canadiens à part entière. 
Elle reflète ce que les Canadiens retirent vraiment de leur citoyenneté.  
En 1947, le gouvernement libéral a consacré le statut de citoyen canadien dans la loi pour la première fois. 
Cette loi de 1947 a marqué le début d'une ère de grands changements dans la société canadienne, ère qui s'est poursuivie sur 30 ans.  
En 1977, alors que M. Robert Andras était ministre de la Main-d'oeuvre et de l'Immigration, le gouvernement libéral a adopté une nouvelle loi sur la citoyenneté. 
Cette nouvelle loi s'appuyait sur l'expérience tirée de la première loi canadienne sur la citoyenneté. 
Cette deuxième loi nous a été très utile au cours des vingt dernières années, qui ont été marquées par une évolution encore plus rapide.  
Toutefois, le temps est venu de la moderniser. 
Le temps est venu d'adopter une loi qui reconnaisse pleinement les répercussions de la Charte canadienne des droits et libertés. 
Le temps est venu d'adopter une loi qui traite des nombreuses questions juridiques et administratives qui ont surgi avec l'expérience au cours des années. 
Le temps est venu d'adopter une loi qui rehausse la valeur de la citoyenneté canadienne. 
C'est ce que fait le projet de loi C-16.  
Permettez-moi de souligner d'abord que nos principes de base concernant la citoyenneté ont résisté à l'épreuve du temps. 
La signification que les Canadiens donnaient à la citoyenneté en 1947 est essentiellement la même que celle qu'ils lui donnent encore aujourd'hui. 
Les enfants nés au Canada seront encore d'office des citoyens canadiens. 
Les enfants nés dans d'autres pays, d'un parent né au Canada, ont toujours droit à la citoyenneté canadienne. 
Ceux qui viennent au Canada plus tard doivent être résidents permanents avant de pouvoir demander la citoyenneté. 
Ils doivent encore prouver leur attachement au Canada. 
Ils doivent prouver qu'ils connaissent notre société et nos valeurs. 
Ils doivent prouver qu'ils connaissent au moins l'une de nos deux langues officielles.  
Je tiens à rappeler aux députés de cette Chambre que l'ancien ministre de la Citoyenneté et de l'Immigration a déposé le projet de loi C-63 lors de la dernier session parlementaire. 
Ce projet de loi se voulait à la fois une tentative de moderniser la Loi sur la citoyenneté et de renforcer la valeur de la citoyenneté canadienne. 
Le projet de loi est mort au Feuilleton lors de la prorogation de la Chambre, mais pas avant d'avoir été l'objet de nombreuses discussions en cette Chambre ainsi qu'en comité.  
Ce projet de loi renferme essentiellement le même objectif et les mêmes dispositions que le projet de loi C-63. 
Il reflète le large consensus qui était manifeste lors des débats précédents.  
Mais ce projet de loi n'est pas identique à celui que nous avons examiné lors de la dernière session. 
Nous n'avons pas tout simplement apposé un nouveau numéro sur un ancien projet de loi.  
Permettez-moi d'aborder quelques-unes des modifications majeures. 
Par exemple, nous comptons toujours renforcer les critères de résidence, mais nous avons prévu une modification sur la façon de répondre à ces critères. 
Mais pour accumuler le temps de présence physique requis, les demandeurs disposeront maintenant de six ans au lieu de cinq comme le prévoyait le projet de loi C-63.  
Nous avons aussi décidé de recommencer à créditer le temps de présence physique qu'a accumulé une personne qui possède un statut juridique au Canada autre que celui de résident permanent. 
Par exemple, la moitié du temps de présence accumulé par quelqu'un qui est arrivé au Canada à titre de réfugié au sens de la convention, de visiteur ou de détenteur d'un permis ministériel pourrait compter, jusqu'à concurrence d'un an, dans les trois ans exigés pour l'obtention de la résidence permanente.  
Nous avons déjà dit clairement que nous voulons que les enfants adoptés à l'étranger obtiennent beaucoup plus simplement la citoyenneté canadienne. 
Mais nous voulons aussi répondre aux préoccupations qu'ont suscitées les propositions que nous avons déjà présentées. 
Par exemple, dans les cas d'adoption, seuls les mineurs obtiendront automatiquement la citoyenneté. 
Les nouvelles règles seront rétroactives à 1977. 
Il devra également être prouvé que l'adoption est dans l'intérêt supérieur de l'enfant.  
Permettez-moi de parler de quelques autres changements.  
Nous proposons toujours un nouveau serment de citoyenneté, un peu modifié par rapport à l'ancien. 
Ils continueront de prêter allégeance au Canada et à Sa Majesté la reine.  
Nous modifierons les pouvoirs d'annulation et de révocation proposés. 
Nous voulons également qu'il soit clair que l'on ne peut révoquer la citoyenneté que dans le cas où la personne a cherché sciemment à nous tromper. 
Qui plus est, on ne prendra aucune mesure pour révoquer la citoyenneté des enfants.  
En ce qui a trait à tous ces changements importants, il faut souligner que les grandes orientations en matière de citoyenneté que notre gouvernement a énoncées en 1998 restent en vigueur. 
Elles sont inscrites dans ce projet de loi.  
Nous voulons également limiter le nombre de générations des personnes nées à l'extérieur du Canada qui peuvent prétendre à la citoyenneté canadiens et clarifier les conditions à respecter pour conserver la citoyenneté canadienne.  
Nous continuons de croire que la décision d'enlever au processus d'attribution ses aspects quasi-judiciaires pour le rendre purement administratif est pleine de bon sens. 
Pour ce faire, nous nous proposons de recourir à des critères et à des tests qui soient clairs et cohérents. 
Nous avons l'intention de nous en remettre, pour une large part, aux agents de la citoyenneté dans ce domaine.  
Nous maintenons le rôle des juges de la citoyenneté, ces hommes et ces femmes que ce projet de loi désigne sous le nom de «commissaires à la citoyenneté». 
Ils auront de nouvelles attributions en matière de promotion de la citoyenneté active au sein de la collectivité. 
Ils conseilleront le ministre dans ce domaine et continueront de présider les cérémonies de la citoyenneté. 
Ils seront les ambassadeurs de la citoyenneté dans leurs collectivités.  
Le projet de loi vise à simplifier le processus d'examen à l'égard des personnes qui souhaitent contester les décisions rendues. 
Une personne qui estime qu'il y a eu erreur de fait pourra demander au ministre de la Citoyenneté et de l'Immigration qu'il soit procédé à un examen des faits à l'origine de cette décision. 
Si la personne pense qu'il y a eu une erreur d'interprétation de la loi, elle pourra s'adresser à la Cour fédérale pour obtenir un contrôle judiciaire. 
Aux termes du nouveau système, un juge de la Cour fédérale peut exiger que la cause soit réexaminée. 
Toutes ces propositions figurent encore dans ce projet de loi, car elles sont judicieuses et appropriées.  
Permettez-moi de terminer mon intervention d'aujourd'hui avec quelques observations. 
L'histoire de la citoyenneté au Canada est marquée, d'une part, par l'évolution et, d'autre part, par des principes durables. 
La loi de 1947 était fidèle à son temps. 
Elle reflétait la situation relative des hommes et des femmes à l'époque. 
Elle reflétait un monde dans lequel nous entretenions des liens plus étroits avec d'autres pays dont les citoyens étaient des sujets britanniques.  
En 1977, l'évolution de la vie dans la société canadienne a exigé une modernisation. 
La loi de 1977 supprimait de nombreuses iniquités dans l'ancienne loi. 
Elle apportait des critères plus cohérents et égalitaires en matière de citoyenneté. 
Elle reconnaissait que les femmes méritaient d'être traitées de la même façon que les hommes et d'avoir les mêmes droits afférents à la citoyenneté.  
En ce nouveau millénaire, nous avons besoin d'une loi encore plus moderne, plus simple, plus claire et plus cohérente.  
Ce projet de loi satisfait à ces exigences. 
La nouvelle Loi sur la citoyenneté du Canada, qui fait aujourd'hui l'objet d'un débat, va renforcer la valeur de la citoyenneté canadienne.  
Il va intégrer l'évolution dont notre société a été témoin, ainsi que notre cadre législatif moderne et nos processus gouvernementaux.  
Elle continuera de respecter les éléments fondamentaux de ce que la citoyenneté signifie pour nous. 
Les principes au coeur de notre loi sur la citoyenneté ont résisté à l'épreuve du temps depuis l'époque où l'honorable Paul Martin père a donné au Canada sa première Loi sur la citoyenneté, en 1946, il y a plus d'un demi-siècle.  
Sous la direction du très honorable William Lyon Mackenzie King, le Canada venait de prouver qu'il était un pays égal à tout autre sur la planète. 
Les Canadiens avaient combattu en tant que Canadiens pour libérer l'Europe de la terrible agression nazie. 
D'autres Canadiens avaient combattu en Asie et dans le Pacifique pour défendre nos valeurs et nos intérêts. 
Ici, au Canada, les Canadiens avaient contribué de bien des façons à l'effort de guerre.  
La création d'une citoyenneté unique bien à nous était une façon tout à fait appropriée de souligner ce que notre pays et nos citoyens venaient de réaliser. 
C'était une bonne façon pour le Canada de donner à ses citoyens un sentiment d'appartenance enraciné dans notre pays et ses habitants.  
Les Canadiens de naissance et les Canadiens naturalisés ont tous fait leur part pour nous mériter notre place sur la scène mondiale à l'époque. 
Ils sont tous devenus des citoyens de ce grand pays en 1947 pour la première fois dans notre histoire. 
Les Canadiens de naissance et les Canadiens naturalisés en font autant aujourd'hui.  
Elle nous rassemblera tous, que nous soyons Canadiens de naissance ou Canadiens naturalisés.  
À tous les ans, naissent environ 350 000 bébés au pays, et environ 160 000 personnes font une demande de citoyenneté canadienne. 
Notre Loi sur la citoyenneté permet à plus d'un demi-million de personnes de se joindre à la grande famille canadienne en tant qu'égaux, en tant que Canadiens. 
Une nouvelle loi sur la citoyenneté sera une façon appropriée d'aider à célébrer un nouveau millénaire. 
Nous comptons sur l'appui de la Chambre au projet de loi C-16.  
M. Leon E. Benoit (Lakeland, Réf.): 
Madame la Présidente, je suis très heureux d'intervenir sur le projet de loi C-16 qui, une fois adopté, remplacera la Loi sur la citoyenneté en vigueur. 
Il y a dans ce projet de loi certains éléments que j'appuie et d'autres non. J'en parlerai au cours de mon intervention. 
Entre temps, je pense qu'il y a d'autres questions à discuter aujourd'hui.  
Présenter une mesure législative visant à remplacer la Loi sur la citoyenneté actuelle avant l'adoption par la Chambre de celle remaniant la Loi sur l'immigration est manifestement une erreur de jugement de la part du gouvernement. 
Le gouvernement promet depuis quatre ans, en réalité depuis mon arrivée à Ottawa il y a six ans, de présenter immédiatement, l'an prochain, une mesure législative remaniant entièrement la Loi sur l'immigration. 
Cela fait six ans qu'il promet de le faire plus tard au cours de cette année ou l'an prochain. 
On attend toujours et on a du mal à expliquer ce retard.  
On ne devrait pas modifier la Loi sur la citoyenneté avant de modifier celle sur l'immigration. 
La Loi sur la citoyenneté fait référence à la Loi sur l'immigration à plusieurs endroits.  
Je pense savoir pourquoi cela ne s'est pas fait. 
Le gouvernement n'est pas prêt à prendre les décisions difficiles qui s'imposent pour modifier la Loi sur l'immigration, il modifie donc la Loi sur la citoyenneté pour pouvoir dire qu'il a fait quelque chose. 
Je le comprends mais, à mon avis, c'est inacceptable.  
Comme l'a souligné mon collègue d'en face, la citoyenneté est pour nous tous un objet de très grande fierté. 
Elle est perçue partout dans le monde comme fort enviable. 
De nombreuses personnes caressent le rêve d'obtenir la citoyenneté canadienne. 
Il n'y a pas que les Canadiens qui estiment que la citoyenneté canadienne est précieuse, et je sais que tous les Canadiens pensent de la sorte; d'autres personnes aussi voudraient obtenir la citoyenneté canadienne.  
Au moment de mettre en place une nouvelle loi sur la citoyenneté, ce qui est le but du présent projet de loi, il est important de bien faire les choses. 
Le gouvernement a présenté le projet de loi C-63 au cours de la dernière législature; c'était sa façon de bien faire les choses. 
Il a cependant abandonné cette mesure législative parce qu'elle était si imparfaite que même lui ne voyait pas comment il aurait pu l'adopter. 
Cette situation est devenue très claire en comité.  
Le projet de loi C-16 est la réponse du gouvernement aux problèmes perçus en comité. 
C'est bien, sauf que le gouvernement n'a apporté aucune solution à bon nombre des problèmes qui ont été soulevés à maintes reprises en comité. 
Je vais traiter de certains de ces problèmes, mais je ne peux prétendre tous les aborder. 
Je mettrai l'accent sur trois ou quatre des principaux.  
Le projet de loi C-63 a franchi tout le processus de travail en comité au cours de la dernière législature. 
Des témoins ont suggéré de nombreux changements. 
D'excellentes suggestions ont été faites. 
Le gouvernement a donné suite à quelques-unes d'entre elles, mais il n'en a pas retenu suffisamment. 
C'est inacceptable dans le cas d'une loi qui régit un aspect aussi important que la citoyenneté canadienne, quelque chose dont nous sommes tous si fiers.  
J'aimerais avoir devant moi un projet de loi sur la citoyenneté que je pourrais appuyer. 
Ce n'est pas le cas, à moins que soient apportés en comité des changements qui permettront d'aboutir à un document que les Canadiens et moi pourrons appuyer. 
Ce que j'appuie comme simple citoyen n'a pas tellement d'importance, mais il est important que le grand public canadien appuie dans l'ensemble la mesure législative. 
À mon avis, cette mesure législative ne peut être appuyée par les Canadiens. 
Le projet de loi constitue une deuxième tentative, et des changements ont été apportés. 
Pour ce qui est de la mesure législative antérieure, je tiens à souligner très clairement que non seulement les témoins mais aussi les membres du comité de l'immigration chargé d'étudier cette mesure ont exprimé de très fortes réserves au sujet du projet de loi C-63, l'ancêtre du projet de loi C-16. 
Des changements ont été souhaités.  
Quelques changements ont été apportés, mais pas beaucoup. 
En fait, le projet de loi C-16 ne diffère du projet de loi C-63 qu'en trois points; pourtant le gouvernement avait renoncé à ce dernier, prétextant qu'il était lacunaire. 
Je ne comprends pas pourquoi il n'y a pas remédié avant de nous le resservir sous la forme du projet de loi C-16. 
Certains changements sont utiles. 
À titre d'exemple, mon collègue de Dewdney-Alouette peut se féliciter du changement qui a été proposé à l'étape du comité par la communauté mennonite. 
Cette communauté avait demandé l'inclusion dans le nouveau texte de dispositions visant à faciliter l'obtention de la nationalité canadienne aux Mennonites communauté qui souhaitaient rentrer au pays ou qui l'avaient déjà fait.  
En quelques mots, l'article 57 accorde aux personnes nées entre 1947 et 1977, hors du Canada mais de parents canadiens, un délai de trois ans pour demander et obtenir la naturalisation. 
Les mennonites se sont fort bien expliqués à ce sujet devant le comité. 
Le député de Dewdney-Alouette leur a accordé son soutien. 
Voilà un changement utile. 
Le comité a fait là du bon travail, comme il le devrait.  
En conséquence d'un autre changement, on a supprimé du nouveau projet de loi la disposition qui, dans le projet de loi C-63, habilitait les bureaucrates ou le ministre à redéfinir la notion d'époux. 
Il y a deux semaines environ, quand j'ai pris connaissance de ce texte, j'ai pensé que l'initiative était valable. 
C'était une chose que mes collègues et moi avions réclamée très énergiquement au cours du débat sur le projet de loi C-63. 
C'était une chose que certains des membres libéraux du comité avaient réclamée eux aussi. 
Cette disposition a dont été retirée du projet de loi, et j'ai cru que nous avions fait du progrès.  
Puis, la semaine dernière, le projet de loi C-23 a été déposé à la Chambre, mais pas d'une façon acceptable. 
Je comprends alors pourquoi on a retiré cette disposition du projet de loi sur la citoyenneté. 
Elle n'était plus nécessaire, puisque le projet de loi omnibus reconnaîtrait certains avantages aux conjoints de même sexe dans plusieurs projets de loi. 
La Loi sur la modernisation de certains régimes d'avantages et d'obligations, proposée par la ministre de la Justice, modifie 68 lois fédérales, touche 20 ministères et organismes et accorde des avantages aux couples de conjoints de même sexe mis sur le même pied que les couples de conjoints de sexe opposé vivant en union de fait.  
Le gouvernement a décidé d'accorder des avantages fondés sur l'activité sexuelle privée d'une personne tout en excluant d'autres types de relations de dépendance. 
Voilà exactement ce qui s'est passé.  
On a apporté au projet de loi sur la citoyenneté une modification qui a enlevé aux fonctionnaires ou au ministre le pouvoir d'effectuer un changement. 
