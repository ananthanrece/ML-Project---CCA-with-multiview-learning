L'objectif du juge Richard et de sa commission est clair. 
À titre de parlementaires fédéraux, nous devons prendre l'initiative et présenter des modifications au Code criminel pour garantir la responsabilité des cadres et administrateurs d'entreprises.  
Cette motion nous permet de nous élever au-dessus de tout le raffut causé par la politique partisane dernièrement. 
Si cette motion est adoptée par la chambre, ce sera la première étape pour s'assurer que les entreprises, minières ou autres, qui fonctionnent de façon responsable sont récompensées. 
Leur récompense viendra des peines imposées aux entreprises qui n'assurent pas la sécurité au travail, dans le seul but répréhensible de réduire les prix. 
Ces entreprises qui mettent leurs travailleurs en danger n'auront plus le droit de fonctionner.  
Soyons parfaitement clairs. 
Il ne s'agit pas de critiquer les entreprises, mais bien de légiférer afin d'imposer des pratiques commerciales responsables. 
Des pratiques responsables signifient que les employeurs, surtout ceux qui exploitent des entreprises présentant certains dangers pour l'intégrité physique ou mentale de leurs employés, doivent rendre des comptes si quelque chose tourne mal. 
