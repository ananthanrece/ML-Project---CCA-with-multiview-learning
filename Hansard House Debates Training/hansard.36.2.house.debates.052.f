Après 76 jours de témoignages sur une période de 14 mois, la commission d'enquête sur la mine Westray dirigée par le juge Richard de la Cour suprême de la Nouvelle-Écosse a rendu public son rapport.  
Ce n'est rien d'autre qu'une condamnation de la façon dont les dirigeants de la mine Westray ont exploité cette mine avant l'explosion. 
Le juge Richard a précisé très clairement dans son rapport que les actions et les attitudes de la direction de la mine Westray ont envoyé le message que la sécurité des travailleurs n'était pas une priorité. 
Les mineurs de la mine Westray, y compris ceux qui ont été tués dans l'explosion de 1992, devaient produire aux dépens de leur sécurité.  
Cette motion tend fondamentalement à faire adopter la recommandation no 73 du rapport sur la mine Westray. 
