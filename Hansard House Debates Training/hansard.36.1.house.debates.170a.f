CHAMBRE DES COMMUNES 
Le jeudi 10 décembre 1998 
La séance est ouverte à 21 heures.  
Le Président:
J'informe la Chambre que, en vertu du paragraphe 28(4) du Règlement, j'ai rappelé la Chambre aujourd'hui à seule fin de donner la sanction royale à certains projets de loi.  
LA SANCTION ROYALE 
Le Président:
J'ai l'honneur d'informer la Chambre que j'ai reçu le message suivant: 
Résidence du Gouverneur général, 
Le 10 décembre 1998 
Monsieur le Président, 
J'ai l'honneur de vous aviser que l'honorable Charles Gonthier, juge puîné de la Cour suprême du Canada, en sa qualité de suppléant du Gouverneur général, se rendra à la salle du Sénat aujourd'hui, le 10 décembre 1998, à 21 heures, afin de donner la sanction royale à certains projets de loi.  
Le sous-secrétaire, 
Politique, programme et protocole, 
* * * 
MESSAGE DU SÉNAT 
Le Président:
J'ai l'honneur d'annoncer à la Chambre que le Sénat lui a adressé un message pour l'informer qu'il a adopté certains projets de loi sans amendements.  
L'huissier du bâton noir apporte le message suivant: 
Monsieur le Président, c'est le désir de l'honorable suppléant du Gouverneur général que cette honorable Chambre se rende immédiatement auprès de lui dans la salle de l'honorable Sénat.  
En conséquence, le Président et les députés se rendent au Sénat.  
Et de retour: 
Le Président:
J'ai l'honneur de faire savoir à la Chambre que, lorsqu'elle s'est rendue au Sénat, il a plu au suppléant du Gouverneur général de donner, au nom de Sa Majesté, la sanction royale aux projets de loi suivants: 
Projet de loi C-3, Loi concernant l'identification par les empreintes génétiques et modifiant le Code criminel et d'autres lois en conséquence-Chapitre 37.  
Projet de loi S-21, Loi concernant la corruption d'agents publics étrangers et la mise en oeuvre de la Convention sur la lutte contre la corruption d'agents publics étrangers dans les transactions commerciales internationales, et modifiant d'autres lois en conséquence-Chapitre 34.  
Projet de loi C-25, Loi modifiant la Loi sur la défense nationale et d'autres lois en conséquence-Chapitre 35.  
Projet de loi C-38, Loi modifiant la Loi sur les parcs nationaux (création du parc Tuktut Nogait)-Chapitre 39.  
Projet de loi C-42, Loi modifiant la Loi sur le tabac-Chapitre 38.  
Projet de loi C-53, Loi visant à accroître la disponibilité du financement de l'établissement, de l'agrandissement, de la modernisation et de l'amélioration des petites entreprises-Chapitre 36.  
Projet de loi C-60, Loi portant octroi à Sa Majesté de crédits pour l'administration publique fédérale pendant l'exercice se terminant le 31 mars 1999-Chapitre 40.  
Le Président:
Comme il est 21 h 20, la Chambre s'ajourne au lundi 1 er février 1999, à 11 heures, aux termes des paragraphes 28(2) et 24(1) du Règlement.  
(La séance est levée à 21 h 20.) 
