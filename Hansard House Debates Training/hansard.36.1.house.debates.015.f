LOI MARITIME DU CANADA 
Projet de loi C-9. 
L'ordre du jour appelle:
Initiatives ministérielles 
2 octobre 1997-le ministre des Transports-Deuxième lecture et renvoi au Comité permanent des transports du projet de loi C-9, Loi favorisant la compétitivité du réseau portuaire canadien par une rationalisation de sa gestion, prévoyant la création des administrations portuaires et l'aliénation de certains ports, régissant la commercialisation de la Voie maritime du Saint-Laurent et des traversiers et des questions connexes liées au commerce et au transport maritimes, modifiant la Loi sur le pilotage et abrogeant et modifiant certaines lois en conséquence.  
L'hon. David M. Collenette (ministre des Transports, Lib.) propose: 
-Monsieur le Président, je suis très heureux de prendre la parole au sujet du projet de loi à l'étude, intitulé Loi maritime du Canada. 
Un des principaux objectifs du gouvernement est de renforcer l'économie canadienne et d'instaurer un climat propice à la création d'emplois et à l'investissement. 
Seuls des fondements économiques solides nous permettront de conserver le niveau de vie et les avantages sociaux que les Canadiens tiennent maintenant pour acquis.  
Des transports rapides, fiables et économiques sont indispensables à l'économie canadienne. 
Nous devons disposer d'un système de transport sûr, efficace, compétitif et exploité selon des pratiques commerciales saines.  
Depuis 1993, nous travaillons à moderniser le réseau de transport du Canada, en commercialisant les opérations de transport, en réduisant les subventions et en modernisant la législation.  
Dans tous les modes, aérien, terrestre et maritime, nous nous dirigeons vers une plus grande efficacité en donnant aux utilisateurs du réseau une plus grande voix au chapitre, et aux collectivités et régions, une plus grande économie.  
Notre objectif est d'avoir un système de transport national intégré pour le nouveau millénaire, un système qui aidera le Canada à relever les défis de la concurrence sur les marchés mondiaux. 
Un réseau de transport multimodal moderne permettra de créer des emplois et favorisera la croissance puisqu'il facilitera les échanges commerciaux internes et internationaux et stimulera l'industrie du tourisme.  
Les transports maritimes revêtent une importance vitale pour la santé économique du Canada et jouent un rôle considérable dans nos échanges commerciaux internationaux, notre industrie touristique et la création d'emplois. 
Ce secteur d'activité assure 40 p. 100 du traitement des marchandises que le Canada expédie chaque année. 
Cela représente 200 millions de tonnes de nos échanges commerciaux avec d'autres pays, ce qui se traduit par des recettes de plus de 2,5 milliards de dollars par année et la création de plus de 45 000 emplois au Canada.  
La modernisation du secteur maritime aura des répercussions directes sur la création d'emplois et la croissance. 
Un système de transport maritime plus fort et plus efficient va améliorer nos résultats sur le marché international, ce qui entraînera la création d'un plus grand nombre d'emplois pour les Canadiens. 
L'impulsion donnée à tout le processus par mon secrétaire parlementaire à l'heure actuelle, le député de Hamilton-Ouest, était évidente. 
En fait, le rapport sur le secteur maritime est maintenant connu sous le nom de rapport Keyes.  
De nombreuses recommandations destinées à améliorer le système maritime sont ressorties de ce rapport.  
À la suite de la présentation de ce rapport, le ministère a tenu des réunions régionales avec des expéditeurs et des représentants de l'industrie pour discuter des recommandations et il a entrepris ensuite une série de consultations avec les principaux intervenants du secteur maritime. 
Ensemble, ces activités ont aidé à forger la politique maritime nationale que le gouvernement a annoncée en décembre 1995.  
La politique est conçue pour accroître l'efficience du secteur maritime, réduire les coûts et donner aux collectivités un plus grand contrôle sur leurs ports. 
Elle aidera à s'assurer que le secteur maritime du Canada continue de favoriser le commerce international. 
Elle nous aidera également à continuer de respecter nos principales priorités: la sûreté, la sécurité et la protection de l'environnement.  
On n'y a absolument rien changé. 
Si on doit y apporter des modifications, le comité envisagera cette possibilité.  
Nous considérons qu'il s'agit d'une mesure importante. 
C'est pourquoi nous l'avons présentée sous sa forme initiale. 
Elle est essentielle à la compétitivité de notre secteur maritime.  
J'ai accueilli avec plaisir la coopération des autres partis qui se sont entendus avec moi pour dire que ce projet de loi devrait être renvoyé au comité le plus tôt possible pour qu'on puisse l'étudier en détail.  
Nous savons que l'industrie appuie ce projet de loi et veut qu'on améliore le système maritime.  
La loi révisée consolidera et simplifiera la réglementation maritime, réduira la paperasserie et permettra la prise de décisions commerciales plus rapide. 
Elle permettra aux ports de répondre plus efficacement aux besoins de leurs clients et de réduire la bureaucratie. 
Dans l'ensemble, elle améliorera la compétitivité de notre secteur maritime.  
Le projet de loi répond à sept grands objectifs. 
Premièrement, il permet de promouvoir et de préserver la compétitivité du Canada et ses objectifs commerciaux. 
Deuxièmement, il fonde l'infrastructure maritime et les services sur des pratiques internationales et des approches compatibles avec celles de nos partenaires commerciaux. 
Troisièmement, il permet de veiller à ce que les services de transport maritime soient organisés de façon à satisfaire les besoins des utilisateurs et qu'ils leur soient offerts à un coût raisonnable. 
Quatrièmement, il fournit un niveau élevé de sécurité et de protection de l'environnement. 
Cinquièmement, il offre un niveau plus élevé d'autonomie aux administrations du réseau des installations portuaires. 
Sixièmement, il assure une gestion de l'infrastructure maritime conforme aux bonnes pratiques commerciales. 
Septièmement, il prévoit le transfert de certains ports et installations portuaires.  
Quatre grands éléments composent ce projet de loi. 
Je pense que je n'aurai pas le temps de discuter en détail de chacun d'eux. 
Je crois qu'on avait un consensus sur cette question à la dernière législature. 
Il traite aussi de la voie maritime et des ports eux-mêmes.  
Il faut moderniser le réseau portuaire. 
Cela ne fait aucun doute.  
Nous devons commencer le nouveau millénaire avec une structure portuaire complète et adaptable à tous les besoins de ses utilisateurs.  
Nous devrons accroître la discipline commerciale dans le réseau portuaire et simplifier le processus de prise de décisions.  
La loi nous aidera à commercialiser les ports publics et permettra l'élimination graduelle de la Société canadienne des ports.  
Les ports jugés essentiels au commerce intérieur et international formeront un réseau national administré par les administrations portuaires canadiennes, groupes composé de représentants des usagers et des gouvernements.  
Tout port peut demander à devenir une administration portuaire-ou APC, comme nous les appelons affectueusement. 
Pour qu'on accède à sa demande, un port doit respecter quatre critères. 
Il doit être financièrement autonome, avoir des activités diversifiées, présenter une importance stratégique pour le commerce et être rattaché à une ligne principale de chemins de fer ou à des axes routiers importants.  
Les APC seront constituées en tant que sociétés sans capital-actions dotées d'un conseil d'administration composé de représentants nommés par les gouvernements fédéral, provincial et municipal ainsi que de représentants des utilisateurs. 
Elles ne recevront pas de financement du gouvernement fédéral. 
L'ère des subventions fédérales est révolue.  
Les APC auront le statut de mandataire de la Couronne. 
Ce statut permettra aux autorités portuaires de verser des subventions tenant lieu d'impôts fonciers et il renforcera l'immunité des ports à l'égard des impôts et des règlements provinciaux. 
Le statut de mandataire se limitera aux activités portuaires essentielles.  
Les APC devront trouver leur financement dans le secteur privé. 
Le gouvernement fédéral ne sera plus là pour éponger leurs dettes.  
Une deuxième catégorie de ports, celle des ports régionaux et locaux, sera transférée aux gouvernements provinciaux et municipaux, aux organisations communautaires, à des intérêts privés et, dans certains cas, à d'autres ministères fédéraux d'ici cinq ans. 
Le processus de cession de ces ports est déjà bien en marche. 
Pour faciliter la transition, le gouvernement a établi un fonds de dessaisissement de 125 millions de dollars sur six ans.  
Le gouvernement du Canada continuera de garantir le fonctionnement des ports dans les régions éloignées. 
Ces ports ont été déterminés selon des critères correspondant au degré d'isolation des collectivités dans lesquelles ils se trouvent. 
Les activités maritimes et les transports sont essentiels à la survie de ces collectivités et nous continuerons de les appuyer.  
Cette politique sera appliquée de façon équitable et uniforme partout au Canada.  
Je m'arrête en particulier sur des questions délicates que des députés du Canada atlantique m'ont signalées concernant l'avenir des ports dans leur région. 
C'est d'ailleurs la première région maritime du Canada. 
La mer, les ports et le trafic maritime sont indispensables à toutes les collectivités de cette région. 
Un grand nombre de ces inquiétudes ont été formulées à l'autre endroit au moment de l'étude en comité du projet de loi C-44. 
De nombreux collègues m'ont aussi fait part de certaines préoccupations depuis quelques semaines.  
Au Cabinet, mes collègues et moi seront sensibles à ces préoccupations. 
Nous tenterons de corriger tous les problèmes légitimes d'une façon juste et équitable pour que ce projet de loi traduise vraiment le consensus national.  
Je n'ai pas le temps d'aborder d'autres aspects, mais d'autres députés assis de ce côté-ci traiteront du reste du projet de loi et de l'importance que revêt son adoption rapide par la Chambre.  
