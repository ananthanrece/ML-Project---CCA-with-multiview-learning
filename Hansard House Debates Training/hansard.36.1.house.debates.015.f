M. Stan Keyes (secrétaire parlementaire du ministre des Transports, Lib.):
Le projet de loi a été déposé à la Chambre pour la première fois en juin 1996. 
Elle a ensuite fait l'objet de discussions auxquelles ont participé divers intervenants et d'un examen de la part du Comité permanent des transports, qui a tenu des audiences partout au pays.  
Le comité a proposé un certain nombre d'amendements après avoir recueilli divers points de vue auprès des parties intéressées, puis d'autres amendements ont été proposés à l'étape du rapport ici même à la Chambre des communes avant que le projet de loi soit renvoyé au Canada. 
Comme nous le savons tous, le Parlement a été dissous et nous n'avons pas eu le temps de faire adopter le projet de loi.  
D'abord, le gouvernement fédéral visera les ports qui sont essentiels au commerce intérieur et international et tentera de préserver l'accès aux régions éloignées. 
Les autres ports seront transférés à des intérêts locaux, qui sont mieux placés pour les gérer efficacement et en fonction des besoins locaux.  
Le projet de loi instituera les administrations portuaires canadiennes, les APC, comme nous aimons les appeler, et la majorité des membres de leur conseil d'administration seront nommés en consultation avec les usagers des ports.  
Le projet de loi C-9 abroge la Loi sur les ports et installations publics. La Société canadienne des ports sera dissoute.  
L'administration portuaire sera constitué par des lettres patentes aux fins de l'exploitation du port en cause. 
Elle pourra exercer des activités liées à l'expédition, à la navigation, au transport de voyageurs et de marchandises, à la manutention et au stockage de marchandises de même que d'autres activités considérées comme nécessaires à l'exploitation du port.  
Chacun des conseils d'administration sera composé d'un nombre d'administrateurs variant de sept à onze. 
La majorité des membres de chacun des conseils seront nommés par le gouvernement fédéral seulement après la tenue de consultations auprès des usagers, qui inscriront ensuite les noms sur une liste de candidats soumise au ministre des Transports.  
Le reste des administrateurs seront nommés par la ou les municipalités adjacentes aux installations portuaires, avec la participation des provinces et du gouvernement du Canada.  
Le mécanisme d'imputabilité le plus important du projet de loi est sans doute la disposition suivant laquelle les ports doivent faire appel au secteur privé pour se financer. 
Tous les plans d'expansion des ports nécessitant des investissements devront donc faire l'objet d'une évaluation des risques commerciaux.  
Lors de la dernière législature, le plus gros changement introduit par le comité permanent a consisté à donner aux nouvelles administrations portuaires le statut de mandataire de Sa Majesté. 
Cela les exempte d'une partie des impôts fonciers et les rend admissibles à la Loi sur les subventions aux municipalités.  
Les administrations verseront donc aux municipalités des subventions tenant lieu de taxes au même taux que les autres installations fédérales. 
Pour la plupart des ports qui deviendront des administrations portuaires canadiennes ce sera le statu quo.  
Pratiquement toutes les nouvelles obligations découlant du statut de mandataire de Sa Majesté incomberont au gouvernement fédéral et non aux mandataires. 
Par exemple, le statut de mandataire de Sa Majesté ne vise que les activités principales des ports et non les activités connexes ou secondaires qu'ils pourraient mener.  
Pour que les tiers sachent bien qu'ils traitent avec un mandataire de Sa Majesté, la loi oblige les administrations portuaires à le faire savoir clairement dans toutes les affaires qu'elles traitent avec des non mandataires.  
Le projet de loi oblige les administrations portuaires à contracter leurs emprunts en leur propre nom plutôt qu'au nom de Sa Majesté. 
Cela vise à bien faire comprendre aux prêteurs que l'État ne se tient pas responsable des prêts consentis.  
Je parlerai aussi de la partie du projet de loi qui nous permet de commercialiser les activités de la Voie maritime du Saint-Laurent.  
Cette voie maritime est une voie d'eau capitale, mais elle est aussi une entreprise. 
De grands défis se posent à cet égard, notamment le coût élevé du renouvellement des infrastructures.  
Le projet de loi C-9 permet au ministre de conclure des ententes avec des sociétés sans but lucratif et d'autres entreprises privées pour exploiter et entretenir la totalité ou des partie de la voie-maritime. 
Le gouvernement fédéral conservera le pouvoir de réglementation sur la navigation dans la voie maritime et l'administration actuelle de la voie maritime sera dissoute au moment opportun.  
La nouvelle société sans but lucratif serait responsable de l'exploitation de la voie maritime et, dans une certaine mesure, du renouvellement de l'infrastructure. 
Il y aurait des incitatifs pour encourager cette société à accroître l'efficacité et à réduire les coûts. 
