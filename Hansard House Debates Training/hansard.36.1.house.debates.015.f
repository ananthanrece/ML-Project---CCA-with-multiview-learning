Transports Canada négocie actuellement le transfert de l'exploitation de la voie maritime à un groupe représentant les principaux expéditeurs et transporteurs qui utilisent la voie maritime. 
Nous croyons que ce groupe d'utilisateurs est le mieux placé pour prendre en mains l'exploitation de la voie maritime parce que les utilisateurs veulent réduire les péages au minimum et assurer l'intégrité de la voie maritime à long terme. 
Les industries représentées, particulièrement l'industrie sidérurgique et les transporteurs maritimes, ont besoin de la voie maritime pour leur survie à long terme.  
Pour ce qui est du pilotage, le projet de loi C-9 permet au ministre des Transports d'améliorer la façon dont les administrations de pilotage fonctionnent au Canada. 
Évidemment, la sécurité et la protection de l'environnement continueront d'être la plus grande priorité du gouvernement, et tout changement apporté contribuera à assurer le maintien de nos normes élevées en matière de sécurité.  
En conclusion, le projet de loi C-9 respecte les objectifs de la politique maritime nationale et crée un équilibre dans notre façon de gérer nos institutions et nos installations maritimes. 
Ce projet de loi sert de complément aux autres initiatives en matière de transport mises en oeuvre par le gouvernement et constitue un élément important de notre stratégie visant à préparer notre système de transport à relever les défis du XXIe siècle. 
Cependant, quels que soient les changements qui sont apportés ou quel que soit le nombre de services qui sont commercialisés, Transport Canada continuera de faire de la sécurité du système de transport au Canada sa plus grande priorité.  
