La mort tragique de Shaughnessy remet tout notre travail en perspective. 
Au nom de l'opposition officielle, j'offre mes condoléances à ses très nombreux amis et à sa famille, particulièrement à son mari Jerome et à sa belle-fille Dena.  
Les mots sont un bien faible réconfort dans de tristes circonstances comme celles-ci et les expressions d'amitié manifestées par les collègues ne pourront combler entièrement le grand vide laissé dans tant de coeurs. 
Si j'exprime mes sentiments aujourd'hui, c'est pour bien faire comprendre au Canada tout ce qu'il a perdu hier soir.  
J'aimais le style de Shaughnessy. 
J'aimais son énergie. 
J'admirais sa ténacité. 
Je respectais l'ardeur farouche avec laquelle elle défendait les causes en lesquelles elle croyait ainsi que les électeurs qu'elle représentait. 
J'appréciais également sa gentillesse et son merveilleux sens de l'humour. 
Voilà ce qu'il y avait d'étonnant chez cette femme de Windsor. 
Elle semblait représenter l'impossible mariage d'un sens de parti aigu et d'une amitié universelle. 
Nous tous, les députés de ce côté-ci de la Chambre, en savons quelque chose puisque nous avons goûté aux deux.  
Il n'y en aura plus une autre personne comme elle. 
Shaughnessy a mené une vie de fonctionnaire dévouée. 
Elle a été procureure de la Couronne pendant de nombreuses années et, en 1993, elle apporta son amour de la justice au Parlement. Elle devint plus tard présidente du comité de la justice.  
Bien que nous appartenions à des partis différents, Shaughnessy a su mériter le respect des députés de ce côté-ci de la Chambre. 
J'ai compris alors que c'était une femme qui faisait passer la résolution des problèmes avant la politique politicienne.  
À un moment où l'un de nos députés avait des problèmes et que d'autres se seraient tenus à l'écart, Shaughnessy s'est assise à ses côtés, lui tenant tranquillement compagnie avec bonté et compassion.  
Elle avait l'esprit vif. 
Tous ceux qui ont eu la chance de la rencontrer s'en sont vite aperçu. 
Elle présidait le Comité de la justice avec une saine dose d'humour et traitait toujours les témoins avec respect et dignité. 
Elle les mettait à l'aise et elle nous mettait nous aussi à l'aide.  
Sa dévotion pour la cause publique lui est venue naturellement. 
Née de parents engagés, qui avaient fondé une école dans leur localité et qui faisaient du bénévolat à la banque d'alimentation, Shaughnessy avait hérité d'eux son profond engagement envers autrui.  
Je forme le voeux que l'esprit de Shaughnessy, la ténacité et le dévouement avec lesquels elle servait ses électeurs et sa joie de vivre ne quittent pas cet endroit avant longtemps. 
Shaugnessy Cohen va cruellement nous manquer.  
M. Michel Bellehumeur (Berthier-Montcalm, BQ):
Monsieur le Président, hier, le 9 décembre, notre collègue, Shaughnessy Cohen, s'effondrait ici même. 
Cette femme engagée, profondément sensible aux valeurs démocratiques, avait choisi de consacrer ses énergies et ses compétences à servir ses concitoyens et ses concitoyennes.  
La députée de Windsor-St. 
Clair n'est plus, laissant derrière elle une famille, une profession et, je le sais fort bien, un parti qu'elle aimait.  
Mme Cohen est née à London, en Ontario, le 11 février 1948. 
Après avoir complété un baccalauréat en études anglaises à l'Université de Windsor en 1969, elle obtenait une maîtrise en sociologie à la même université en 1973, puis un baccalauréat en droit en 1977.  
De 1971 à 1974, elle fut professeur au Collège des arts appliqués et de la technologie de St. Clair. 
De 1979 à 1993, elle a pratiqué le droit, plus particulièrement dans les domaines du droit criminel et du droit du travail, en plus d'agir à titre de procureur adjoint de la Couronne.  
Élue pour la première fois en 1993, elle représentait depuis la circonscription de Windsor-St. 
Clair et présidait le Comité de la justice et des droits de la personne depuis 1996. 
Elle a également fait sa marque comme coprésidente du comité du caucus du Parti libéral dans le dossier du contrôle des armes à feu.  
Ayant eu le privilège de travailler étroitement avec Mme Cohen au Comité permanent de la justice, très sincèrement, je peux facilement imaginer combien sa perte se fera sentir au sein du caucus libéral.  
Bien que nous n'ayons pas toujours eu la même vision des choses, j'ai toujours respecté Shaughnessy à cause de son professionnalisme et, surtout, de la passion qu'elle démontrait à l'égard du droit et de la justice. 
Sur bien des points, on partageait les mêmes idéaux et les mêmes valeurs. 
Encore une fois, malgré certaines divergences que nous avions, à l'occasion, nous nous sommes toujours beaucoup respectés.  
À son époux Jerome et à sa fille Dena, en mon nom et au nom de mes collègues du Bloc québécois, j'offre mes plus sincères condoléances.  
Je crois profondément que c'est la mémoire des vivants qui fait reculer les frontières de la mort. 
Shaughnessy, ceux qui ont eu le privilège de vous côtoyer se rappelleront de vous comme d'une femme de parole et d'action. 
Nous ne vous oublierons pas. 
Merci, Shaughnessy, pour ce que tu nous as donné par ta présence et ton travail acharné qui étaient fort appréciés.  
