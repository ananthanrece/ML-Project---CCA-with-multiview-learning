Le Président:
Chers collègues, nous avons parmi nous, en cette triste journée, des membres de la famille de Shaughnessy Cohen qui sont à la tribune, avec ses collaborateurs et les amis très proches qu'elle a eus tout au cours de sa vie.  
Aujourd'hui, nous allons suivre la procédure suivante. 
Il y aura des hommages de la part des porte-parole de chaque parti. 
Ensuite, je donnerai davantage de renseignements. 
Il est inutile de rappeler que cette journée est unique dans l'histoire du Parlement. 
Le très hon. Jean Chrétien (premier ministre, Lib.):
Monsieur le Président, au nom des membres de mon parti et au nom des députés de la Chambre des communes, je veux rendre hommage à la députée de Windsor-St. 
C'est une merveilleuse famille à laquelle nous appartenons. 
La députée de Windsor-St. 
Clair était la meilleure parmi nous. 
C'était une personne extrêmement dévouée et engagée dans toutes les causes sociales dans sa ville, Windsor. 
Elle est venue ici avec les mêmes préoccupations. 
Elle était présidente du comité de la justice et a passé toute sa vie à veiller à ce que les plus démunis de notre société puissent avoir une vie un peu plus belle. 
C'était sa préoccupation quotidienne, aux comités, à la Chambre des communes et au caucus.  
Comme tout le monde le sait, c'était une personne qui avait son franc-parler. 
Elle n'avait absolument pas peur de dire les choses comme elles étaient, comme elles les voyaient. 
Elle avait un grand sens de l'humour. 
Elle était devenue l'amie de tout le monde.  
C'est incroyable la grande tristesse qui m'a envahi hier soir lorsque j'ai appris son décès et lorsque j'ai vu mes collègues pleurer. 
Nous avons perdu une soeur.  
Ce que les gens ne comprennent pas, c'est que nous pouvons avoir nos divergences politiques, mais nous voulons tous faire de notre mieux pour réaliser une société meilleure. 
C'était le cas de Shaughnessy. 
Elle débordait d'énergie. 
Elle faisait avancer les choses; c'était une pousseuse. 
Elle était très partisane de bien des façons. 
Elle croyait dans le parti et dans les valeurs de notre parti. 
Elle pouvait également critiquer vertement certaines mesures prises, car parfois, elle n'était pas complètement satisfaite. 
Cela arrive de temps à autre.  
Elle le faisait avec la plus grande vigueur.  
Je tiens à dire en mon nom et au nom de mes collègues que nous avons perdu une soeur extraordinaire et une parlementaire remarquable.  
J'ai eu l'occasion, il y a un instant, de parler avec son mari et sa fille pour leur exprimer mes condoléances. 
Ce doit être un choc terrible et une perte incroyable pour eux.  
Mais ils savent que son séjour au Parlement a été remarqué. 
Elle avait des convictions profondes et elle savait aussi se faire des amis. 
La vie, pour elle, était de travailler le jour, et aussi être capable de profiter de la vie, avoir du plaisir de temps en temps, de parfois nous créer des problèmes, mais de me faire rire tout de suite après. 
Au nom des membres de mon parti, au nom des citoyens du Canada, je désire la remercier pour ce qu'elle a fait pour ses électeurs et pour tous les citoyens du Canada.  
Je tiens à dire aux députés que, de bien des façons, Shaughnessy Cohen a laissé sa marque et a fait du Canada un bien meilleur pays.  
M. Randy White (Langley-Abbotsford, Réf.): 
Monsieur le Président, c'est le coeur lourd que je rends hommage à Shaughnessy Cohen dont la mort tragique survenue hier nous a tous secoués.  
