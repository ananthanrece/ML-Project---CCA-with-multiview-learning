Par ailleurs, la Chambre devrait maintenir le droit prioritaire à bénéficier de la présence de ses membres et devrait pouvoir exercer son pouvoir dans des circonstances exceptionnelles.  
Je vais parler de l'immunité d'arrestation. 
Le Privilège parlementaire au Canada, de Joseph Maingot, résume le privilège de l'immunité d'arrestation de la façon suivante: une protection contre une arrestation à l'occasion d'un procès civil, notamment en cas de désobéissance à une ordonnance ou à un jugement de la cour, y compris en cas d'outrage de nature civile au tribunal. 
L'immunité d'un parlementaire ne s'étend pas aux affaires criminelles et un parlementaire peut être emprisonné pour une infraction criminelle ou quasi criminelle, par exemple un outrage pénal au tribunal.  
Par contre, la page 161 laisse entendre que la Chambre a le pouvoir d'intervenir si elle estime que les circonstances sont extraordinaires. 
On peut lire: 
Aucune des Chambres n'a renoncé à son droit d'intervenir dans le cas où l'un de ses membres serait condamné et emprisonné pour outrage; les Chambres peuvent donc théoriquement considérer chaque cas à titre individuel, mais il est peu vraisemblable qu'une Chambre puisse prendre en considération une intervention concernant une action au civil, à moins de circonstances extraordinaires. 
Donc, si la Chambre a le droit d'intervenir dans des circonstances extraordinaires pour protéger ses privilèges, les parlementaires n'ont pas besoin d'un privilège de protection automatique contre l'arrestation. 
Ce serait conforme à la plupart des autres cas relatifs aux privilèges des parlementaires.  
Lorsque les parlementaires estiment que leurs privilèges ont été bafoués, ils soumettent le cas au Président qui détermine s'il y a, à première vue, une atteinte aux privilèges. 
Si oui, la Chambre étudie le cas et prend une décision.  
Il y a le privilège de ne pas être tenu de comparaître comme témoin. 
C'est le privilège visé par la motion no 53. 
Au lieu de cela, la Chambre devrait examiner les circonstances et décider au cas par cas.  
Si nous abolissons le privilège d'un député de refuser de témoigner devant un tribunal, nous devrions, logiquement, nous assurer que la Chambre se réserve le droit d'intervenir dans des circonstances extraordinaires. 
Cela empêcherait qu'un député n'abuse de son privilège tout en préservant le droit prioritaire du Parlement à bénéficier de la présence de ses membres.  
Et le privilège d'exemption de l'obligation de faire partie d'un jury? 
Ce privilège est conforme au principe du droit prioritaire du Parlement à bénéficier de la présence de ses membres. 
Si cette exemption par rapport à la loi est moins choquante que les autres, elle place malgré tout les députés au-dessus des autres citoyens.  
Si nous voulons être logiques, nous devrions appliquer à ce privilège les mêmes normes que nous appliquons aux privilèges susmentionnés. 
Le droit à l'immunité parlementaire devrait être retiré aux députés et relever de la Chambre.  
Ce sont des points sur lesquels le Comité de la procédure et des affaires de la Chambre devrait peut-être se pencher.  
Le privilège de la liberté de parole est une autre question. 
Il tombe dans une catégorie différente et compte probablement parmi les libertés essentielles dont jouissent les députés. 
Selon Maingot, le privilège de la liberté de parole n'a pas tellement pour but de donner aux députés l'avantage personnel d'une protection contre les poursuites; il vise plutôt à mieux garantir les droits de la population en permettant à ses représentants de s'acquitter des fonctions de leur charge sans crainte de poursuites au civil et au pénal.  
D'après l'ouvrage de Bourinot, la liberté de parole est un des premiers et plus grands privilèges des députés. 
L'auteur dit qu'un des avantages des corps législatifs tient au droit de dévoiler et de dénoncer les abus au moyen de la liberté de parole.  
Souvent au cours des débats et au cours de la période des questions, les membres du Cabinet, dont le premier ministre, accusent les députés d'opposition d'abuser de leur liberté de parole. 
Cela arrive ici de temps à autre. 
Ces accusations sont non seulement peu judicieuses et inopportunes, elles sont très révélatrices des problèmes qu'éprouve le gouvernement actuel. 
Les ministres tolèrent la liberté de parole tant qu'elle ne sert pas à les critiquer. 
Le gouvernement considère abusive la liberté exercée de cette façon.  
À la page 28 de son ouvrage sur le privilège parlementaire, Joseph Maingot parle des origines de la liberté d'expression: 
Au Royaume-Uni, jusqu'au XIXe siècle, la communication de ce qui se disait au Parlement était considérée comme un outrage; jusqu'alors, le privilège ne servait qu'à protéger les membres du Parlement contre les poursuites royales.  
