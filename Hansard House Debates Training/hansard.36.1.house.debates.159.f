LOI SUR LA PROTECTION DES RENSEIGNEMENTS PERSONNELS (NUMÉROS D'ASSURANCE SOCIALE) 
-Monsieur le Président, de nos jours, chaque fois qu'on veut louer un appartement, ouvrir un compte bancaire ou présenter une demande pour pratiquement n'importe quoi, la première chose qu'on nous demande, c'est notre numéro d'assurance sociale.  
Il y a des millions et des millions de numéros d'assurance sociale non utilisés qui circulent un peu partout au Canada. 
En fait, le vérificateur général a signalé récemment le nombre alarmant de numéros d'assurance sociale non utilisés qui sont en circulation.  
Non seulement il est immoral, mais il est également injuste de demander à quelqu'un son numéro d'assurance sociale à moins qu'on soit tenu de le faire aux termes de la loi.  
Je veux m'assurer que cela devienne illégal. 
À cette fin, ce projet de loi va faire en sorte qu'il soit illégal pour quiconque de demander le numéro d'assurance sociale à moins que la loi ne l'exige. 
Ainsi, nous pourrons résoudre ce problème. 
Une personne ne devrait pas pouvoir exiger qu'une autre personne lui donne son numéro d'assurance sociale à moins que la demande ne soit prévue par la loi ou qu'elle ne signale à l'autre personne en question par écrit qu'il n'est pas nécessaire de donner suite à cette requête et qu'elle n'est passible d'aucune sanction si elle n'y consent pas.  
(Les motions sont adoptées, le projet de loi est lu pour la première fois et imprimé.)  
