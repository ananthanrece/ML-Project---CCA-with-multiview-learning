Je signale que le Canada est un de plusieurs pays à s'être dotés de privilèges parlementaires. 
On trouve l'équivalent dans les parlements d'autres pays. 
Le Royaume-Uni et l'Australie reconnaissent que la présence des parlementaires dans les Chambres du Parlement doit avoir priorité sur leur comparution devant un tribunal.  
Le droit des parlementaires de refuser de témoigner devant un tribunal est étroitement lié aux privilèges qui les exemptent de l'obligation de siéger comme jurés et qui les protègent de l'arrestation et d'actes de brutalité. 
Je crois que ces privilèges ont encore leur raison d'être.  
La Loi sur le Parlement du Canada reconnaît que ces privilèges «sont partie intégrante du droit général et public du Canada et n'ont pas à être démontrés, étant admis d'office devant les tribunaux et juges du Canada».  
Ce privilège découle du droit prioritaire de chaque chambre de bénéficier de la présence et des services de ses membres. 
Les députés sont protégés par le privilège dans les limites de leurs fonctions à la Chambre des communes.  
Le privilège vise à faire en sorte que les députés ne soient pas empêchés d'exercer leurs fonctions, mais ses applications comportent cependant des limites. 
Le privilège n'est pas censé être utilisé pour empêcher la justice de suivre son cours. 
Il ne soustrait pas les députés aux poursuites de nature pénale. 
Ainsi, indépendamment du privilège, les députés peuvent faire l'objet de poursuites.  
En d'autres termes, le privilège vise à garantir le fonctionnement efficace de la Chambre. 
Le travail de la Chambre est tributaire de la participation de tous les députés des diverses régions de notre pays. 
