INITIATIVES MINISTÉRIELLES 
LA LOI SUR LE TABAC 
Mme Elinor Caplan (secrétaire parlementaire du ministre de la Santé, Lib.):
Monsieur le Président, je suis heureuse d'avoir l'occasion de prendre part au débat sur ce projet de loi d'une importance capitale.  
C'est un honneur pour moi que d'appuyer le projet de loi C-42 parce qu'il constitue une autre mesure prise par le gouvernement pour placer le Canada à l'avant-garde internationale de la lutte contre le tabagisme.  
Comme les députés le savent, au printemps 1997, la Chambre a adopté le projet de loi C-71, Loi sur le tabac, qui prévoyait un certain nombre de restrictions strictes en matière de commercialisation des produits du tabac. 
C'était une loi ferme et elle a grandement contribué à protéger le public canadien contre les ravages du tabagisme. 
C'était une loi nécessaire parce que le tabagisme est un problème de santé publique d'une extrême gravité.  
Les modifications présentées dans le projet de loi dont nous sommes saisis aujourd'hui viendraient renforcer cette loi. 
En éliminant progressivement sur cinq ans toute promotion de commandite d'événements culturels et sportifs par les compagnies de tabac, le projet de loi C-42 renforce l'engagement du gouvernement de protéger les Canadiens, les jeunes notamment, qui sont plus influençables, contre les effets néfastes de la commercialisation des cigarettes.  
En tant que parlementaires nous adresserons un puissant et historique message aux Canadiens et, surtout, aux fabricants de cigarettes par ce projet de loi. 
Nous dirons de manière non équivoque que le tabagisme est le principal ennemi de la santé publique. 
Nous dirons que nous ne tolérerons pas que l'industrie multinationale du tabac vise nos jeunes en tentant de les inciter à commencer à fumer.  
Permettez-moi de décrire brièvement la teneur du projet de loi C-42. 
Les députés se souviendront qu'un article de la Loi sur le tabac prévoit des restrictions quant à ce que peuvent faire les compagnies de tabac en matière de publicité et de promotion dans leur commandite financière d'événements comme la course automobile, les compétitions de saut à ski, des concerts, etc. 
L'industrie du sport automobile, par exemple, craignait que la perte soudaine de commanditaires privés ne mette en danger la capacité du Canada à être l'hôte de courses internationales.  
Le projet de loi C-42 répond à ces préoccupations. 
Il propose une approche progressive qui retardera de deux ans l'entrée en vigueur des dispositions de la Loi sur le tabac concernant les restrictions en matière de publicité. 
Pendant les trois prochaines années, les compagnies de tabac auront le droit de continuer à commanditer des manifestations. 
Toutefois, les message publicitaires en dehors du site de la manifestation seront soumis à la limite de taille de 10 p. 100 stipulée dans la Loi sur le tabac.  
Après cinq ans, soit en 2003, les commandites par des fabricants de cigarettes seront interdites. 
C'est ce qui me fait dire que le projet de loi C-42 renforce la Loi sur le tabac. 
Au lieu de restreindre les activités de promotion, le projet de loi les interdira carrément.  
Ces modifications législatives mettront le Canada en tête des pays qui se soucient grandement de la santé de leurs citoyens. 
Nous progressons plus vite que l'Australie et l'Union européenne, qui prévoient de mettre en oeuvre un régime interdisant les commandites similaire au nôtre d'ici l'an 2006, soit trois ans après nous.  
Il ne fait aucun doute que certains groupes culturels ou sportifs seront gênés financièrement lorsque les géants du tabac seront obligés d'arrêter de pomper des millions de dollars dans la promotion de commandite. 
Cependant, le fait est que les promoteurs de manifestations sportives et les organisateurs de manifestations artistiques nous ont dit et répété qu'ils avaient besoin de temps pour trouver d'autres sources de financement. 
En bref, tel est l'objet du projet de loi C-42.  
Ces groupes nous ont affirmé qu'ils voulaient aussi être traités équitablement. 
Ainsi, en vertu des modifications proposées, tous les groupes, de l'Orchestre symphonique de Terre-Neuve au Festival international de jazz de Victoria, seront traités sur le même pied. 
Pas un plus que l'autre n'aura droit à l'argent de la commandite des cigarettes.  
Il est également important de souligner qu'en proposant le projet de loi C-71, tout comme le projet de loi C-42 à l'étude aujourd'hui, le gouvernement a cherché à protéger la santé publique tout en tenant compte des préoccupations légitimes des organismes sportifs et culturels. 
À ce titre, le projet de loi C-42 constitue un bon compromis, un équilibre délicat entre les désirs des partisans de l'interdiction complète et rapide de la commandite et de ceux qui sont d'avis qu'il est également nécessaire de tenir compte des événements sportifs, culturels et de divertissement qui dépendent des commanditaires.  
Nous en sommes arrivés à ce juste milieu à la suite de longues consultations auprès des groupes de la santé et des représentants des arts et de l'industrie du spectacle. 
J'aimerais d'ailleurs reconnaître l'importante contribution du Comité permanent de la santé qui a mené le processus de consultation et peaufiné le projet de loi à l'étude aujourd'hui. 
Le gouvernement a écouté les arguments présentés au cours des audiences du comité et a adopté certaines mesures en vue de donner plus de force au projet de loi C-42 .  
Tout d'abord, on a clairement établi le début de la période d'application progressive du projet de loi qui a été fixée au 1 er octobre 1998. Cela signifie que si la mesure législative proposée était adoptée, la période de cinq ans serait déjà entamée.  
Deuxièmement, la clause des droits acquis que l'on retrouve dans le projet de loi C-42 ne s'appliquerait qu'à des événements qui étaient déjà tenus au Canada. 
Autrement dit, les promoteurs ne pourraient pas décider de tenir au pays des événements qui avaient autrefois lieu aux États-Unis ou ailleurs simplement dans le but de tirer avantage des dispositions sur l'application progressive des restrictions relatives à la publicité de commandite.  
Troisièmement, les nouveaux amendements n'exempteraient que les manifestations qui ont eu lieu au Canada au cours des 15 mois qui ont précédé le 25 avril 1997. 
Cela empêcherait les commanditaires de ressusciter d'anciens festivals uniquement en raison de leur importance comme moyen de promouvoir la vente de produits du tabac. 
Ces modifications apportées au projet de loi C-42 ont été proposées par le secteur de la santé, ont été adoptées par le gouvernement et sont conformes à notre approche en ce qui concerne la santé publique.  
En terminant, nous avons tous entendu les faits alarmants. 
Le tabagisme est de loin la principale cause de décès et de maladies évitables au Canada. 
On estime que près d'un décès sur cinq peut être imputable à l'usage du tabac, ce qui représente un nombre plus élevé que celui des décès attribuables aux suicides, aux accidents de la route, au sida et aux meurtres, mis ensemble. 
Chaque année, 45 000 Canadiens meurent d'un cancer, d'une maladie cardiaque ou d'une maladie pulmonaire attribuables à l'usage du tabac. 
La qualité de vie d'un grand nombre d'autres Canadiens est compromise en raison de l'emphysème et d'autres insuffisances respiratoires.  
Nous savons que beaucoup de gens commencent à fumer à l'adolescence et que les jeunes sont particulièrement vulnérables à l'influence de leurs camarades et aux messages, parfois subliminaux, qui les incitent à fumer. 
De toute évidence, en tant que société compatissante, nous avons une obligation morale d'agir. 
Nous avons une responsabilité à assumer envers les générations qui nous suivront et un devoir d'aider les jeunes, qui sont impressionnables, à résister à l'attrait de cette habitude mortelle.  
Les groupes du secteur de la santé de tout le pays nous ont exhortés à mener la lutte contre le tabagisme. 
Nous ne les avons pas déçus. 
La Loi sur le tabac, dont nous proposons la modification, donnerait au gouvernement des armes utiles dans la bataille contre le tabagisme. 
Cette mesure législative nous donne, en tant que société, le pouvoir de refuser le cadeau qu'on nous offre. 
Nous aurons les moyens de dire aux fabricants de produits du tabac: «Non, merci. 
Nous attachons trop d'importance à la santé de nos enfants pour accepter votre argent afin de commanditer des manifestations.»  
J'exhorte donc tous les partis à appuyer ce projet de loi, de sorte que, petit à petit, nous puissions remporter la bataille contre le tabagisme et atteindre notre objectif, soit une société sans tabac et des Canadiens en meilleure santé. 
