LES AGRICULTEURS CANADIENS 
M. John Maloney (Erie-Lincoln, Lib.):
Monsieur le Président, les éleveurs de porcs sont en train d'être ruinés par l'effondrement des prix. 
Le prix du porc est maintenant égal à la moitié de son coût de production. 
Vendredi dernier, j'ai rencontré 70 éleveurs de porc dans mon bureau de circonscription, à Smithville. 
Ce sont des producteurs travaillants et efficients qui connaissent leur affaire, mais ils ont été confrontés à des circonstances indépendantes de leur volonté.  
Les éleveurs de porc savent que leur secteur d'activité est cyclique et ils se préparent à affronter les fluctuations normales, mais la situation dans laquelle ils se trouvent maintenant n'est pas normale. 
Les éleveurs de porc et d'autres agriculteurs qui exportent leurs produits subissent les contrecoups de la crise économique asiatique. 
Les exportateurs canadiens ont perdu leur marché et la surabondance de l'offre a entraîné une chute des prix mondiaux.  
La situation des revenus agricoles est urgente. 
Le ministre de l'Agriculture et de l'Agroalimentaire a travaillé avec les dirigeants du secteur agricole et ses homologues provinciaux à la recherche d'une solution au problème. 
Je l'invite à créer un programme national d'aide en cas de désastre, qui prendrait le relais lorsque le filet de sécurité ne suffirait pas.  
