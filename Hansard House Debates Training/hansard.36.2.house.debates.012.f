LE MOIS DE L'HISTOIRE DES FEMMES 
M. Jacques Saada (Brossard-La Prairie, Lib.):
Monsieur le Président, octobre est le Mois de l'histoire des femmes. 
En cette année de la Francophonie, qui est particulièrement vibrante au Québec, j'aimerais rendre un hommage très particulier aux militantes qui ont lutté pour le droit de vote des femmes au Québec.  
En 1907, Marie Gérin-Lajoie et Caroline Beïque fondent la Fédération nationale Saint-Jean-Baptiste afin de promouvoir les droits civiques et politiques des femmes. 
Elles réclament alors une réforme du Code civil et exigent une commission d'enquête sur les droits des femmes.  
N'ayant toujours pas obtenu le droit de vote en 1922, Marie Gérin-Lajoie se rend à Québec avec un groupe de femmes afin de faire pression auprès du premier ministre. 
Au cours des années suivantes, Idola St-Jean et Thérèse Casgrain prennent le relais. 
Après plusieurs années de lutte acharnée, les femmes québécoises obtiennent enfin le droit de vote le 25 avril 1940.  
Depuis, grâce aux convictions profondes de ces militantes, plusieurs femmes québécoises ont été élues à l'Assemblée nationale du Québec et à la Chambre des communes. 
Mais malgré cela, le ratio hommes-femmes est loin d'être représentatif de l'ensemble de la population canadienne.  
