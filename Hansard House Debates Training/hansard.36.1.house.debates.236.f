Monsieur le Président, je suis heureux de participer à l'examen à l'étape du rapport du projet de loi C-54, notamment au débat sur le groupe de motions no 1.  
Ce projet de loi est très important car il traite de la fine pointe de la technologie. 
Il se rapporte à l'explosion de la technologie de l'information que connaît le monde aujourd'hui, aux défis auxquels nous faisons face en tant que nation et aux moyens de protéger les renseignements personnels recueillis sur les individus.  
Les bloquistes agissent ainsi parce qu'ils pensent, et ils n'ont pas tout à fait tort, que les questions se rapportant à la vie privée sont relèvent de la province. 
C'est vrai.  
Le Parti réformiste cherche à faire en sorte que les questions de la vie privée qui relèvent du fédéral soit fusionnées avec celles qui relèvent des provinces et constituent un complément aux règlements provinciaux en place. 
Malheureusement, le Québec est la seule province à avoir de tels règlements. 
Les autres provinces n'en ont pas faute principalement de ressources.  
