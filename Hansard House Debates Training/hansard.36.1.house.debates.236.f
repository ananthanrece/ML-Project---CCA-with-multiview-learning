Seule Canadienne honorée, Rossana est l'une des 50 femmes entrepreneurs sélectionnées pour assister au troisième gala annuel à Monaco. 
L'idée d'organiser un gala annuel dans une ville différente chaque année était de souligner les accomplissements des femmes entrepreneurs dans le monde entier ainsi que leur impact sur l'économie mondiale et de présenter ces femmes comme des modèles de comportement.  
Rossana Magnotta est sans nul doute un modèle de comportement. 
En huit ans, Magnotta Winery est devenue la troisième fabrique de vin en importance en Ontario pour ce qui est du volume et des ventes. 
Cette fabrique de vin a remporté 800 médailles. 
Magnotta est maintenant implantée sur le marché international et exporte aux États-Unis, au Japon, à Taiwan et en Chine.  
C'est vraiment une réussite typiquement canadienne. 
Nous adressons nos félicitations à Rossana.  
