Monsieur le Président, j'aimerais parler aujourd'hui d'un sujet qui préoccupe les Canadiens au plus haut point. 
Il y a donc un grand nombre de Canadiens qui ont besoin de nous. 
J'aimerais dire quelques mots sur ces maladies et sur les répercussions qu'elles entraînent pour les Canadiens. 
La polysensibilité chimique ou maladie environnementale, est une maladie chronique dont les symptômes se manifestent par suite d'une faible exposition à de nombreux produits chimiques non reliés et s'améliorent ou disparaissent lorsque la personne n'est plus en contact avec les produits chimiques en cause. 
Il s'agit d'un malaise qui touche plusieurs organes et qui est lié de près à la fatigue chronique et à la fibromyalgie. 
Il y a d'ailleurs un certain chevauchement des symptômes dans ces trois cas.  
En 1994, le U.S. Centre for Disease Control en est arrivé à la conclusion que le syndrome de fatigue chronique est une maladie reconnue qui est caractérisée par une fatigue constante et une variété de symptômes qui touchent plusieurs systèmes. 
Les principaux symptômes sont une fatigue excessive, des douleurs musculaires et articulaires généralisées, l'impossibilité de se concentrer et souvent des problèmes gastro-intestinaux. 
Au nombre des autres symptômes, citons la fatigue déclenchée par des activités stressantes, des maux de tête, un mal de gorge, des troubles du sommeil, un peu de fièvre et un état dépressif. 
Les symptômes varient en intensité et peuvent durer très longtemps.  
Les causes exactes du syndrome de fatigue chronique ne sont pas encore connues. 
Selon les théories étiologiques actuelles, il pourrait être attribuable à des troubles neuro-endocriniens, à un virus, à des toxines environnementales, à une prédisposition génétique, à un traumatisme crânien et au stress. 
Cette maladie est plus commune chez les femmes que chez les hommes.  
Par ailleurs, la fibromyalgie est une douloureuse maladie musculaire qui se traduit par l'épaississement ou le rétrécissement de l'enveloppe musculaire. 
Elle est également connue sous le nom de fibrosite. 
Cette maladie, qui présente un grand nombre de symptômes similaires à ceux du syndrome de fatigue chronique, est également plus courante chez les femmes que chez les hommes.  
La triste réalité est que ces maladies détruisent non seulement la santé des personnes qui en souffrent, mais également la vie des familles. 
Imaginez ce que ça serait si votre enfant était rendu grabataire par des réactions allergiques déclenchées par la moquette nouvellement posée dans son école, ou si votre conjointe ou votre compagne devenait invalide après que son bureau ait été rénové. 
Pour les personnes atteintes de polysensibilité chimique, c'est la réalité.  
Il se peut que nous ne soyons pas au courant des épreuves que traversent ces personnes et du fait qu'elles sont incapables de prendre soin d'elles-mêmes et de leur famille une fois qu'elles sont atteintes. 
Le problème est qu'il n'existe pas de normes d'admissibilité en ce qui concerne ces maladies.  
Supposons qu'une personne atteinte de cette maladie fasse une demande d'aide, une demande de prestations d'invalidité auprès du Régime de pensions du Canada, par exemple. 
Selon la province ou le territoire où elle habite, on accédera ou non à sa demande. 
Le problème, c'est qu'il n'existe aucune norme. 
Par conséquent, des personnes frappées d'une incapacité sont traitées de façon inéquitable.  
Ces gens sont malades et dans bien des cas on leur refuse des prestations. 
On leur dit non au moment où ils ont besoin de soutien. 
Ils sont ainsi conduits à la pauvreté, au désespoir et, dans quelques cas tragiques, au suicide. 
