M. Peter Adams (secrétaire parlementaire du leader du gouvernement à la Chambre des communes, Lib.):
Monsieur le Président, c'est avec plaisir que je répondrai au député de Halifax-Ouest, pour le compte du ministre de la Santé, concernant des allégations relatives au ministère de la Santé.  
Après deux jours de témoignages et la présentation d'une foule de renseignements, le ministère a conclu que les scientifiques n'avaient pas réussi, par leur témoignage ou des documents, le bien-fondé de leurs plaintes. 
En fait, la CRTFP a rejeté toutes les plaintes d'employés de Santé Canada.  
Il a déclaré au comité qu'il y avait, à Santé Canada, 6 000 héros, 6 000 fonctionnaires dévoués qui travaillent tous les jours et bien des soirées et des week-ends pour protéger la santé et la sécurité des Canadiens. 
Je veux qu'on comprenne bien que le produit dont il est question aujourd'hui, la STBr, a été analysée par les scientifiques de Santé Canada pendant plus de neuf ans et a également fait l'objet d'évaluations au niveau international. 
Certains pays l'ont approuvé, d'autres pas. 
Santé Canada a émis un avis de non-conformité dans le cas de la STBr le 14 janvier dernier. 
On avait certaines réserves quant à la santé et à la sécurité des animaux ayant reçu des injections de STBr.  
La consigne du silence n'a pas été donnée. 
À preuve, les longs témoignages que les scientifiques ont livrés au comité sénatorial au cours de ces deux audiences. 
En fait, le ministère nous a fait savoir très clairement qu'il avait l'obligation de le faire.  
