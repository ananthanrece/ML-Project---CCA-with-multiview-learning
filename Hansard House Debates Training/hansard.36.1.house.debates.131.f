À présent, les Canadiens ont les mains liées, mais pas les Américains, au détriment de l'agriculture canadienne et au détriment des petites, moyennes et grandes entreprises de notre pays. 
Nous constatons cette semaine les conséquences de la limite imposée au débat sur ce projet de loi concernant l'OMC.  
Aujourd'hui, les libéraux et le Parti réformiste se prêtent à nouveau à des jeux stupides à l'égard des affaires de la nation. 
À mon avis, les deux partis négligent leur responsabilité envers les contribuables du pays. 
Ils négligent les petits entrepreneurs en ne voulant pas que ce projet de loi fasse l'objet d'un débat en bonne et due forme. 
Il ne veut pas qu'il reste une seule petite entreprise au pays.  
Lorsque les gens d'affaire verront le véritable programme du Parti réformiste, son programme favorisant la grosse entreprise aux dépens des petites entreprises, ils lui feront payer cher lors des prochaines élections. 
Ça ne rapportera rien aux libéraux car ce sont eux qui incitent le Parti réformiste à se prêter à ces jeux stupides.  
Venant de la Saskatchewan, je partage en partie les commentaires du député de Souris-Moose Mountain, un réformiste qui vient aussi de la Saskatchewan. Il s'inquiète du problème agricole dans l'ouest du Canada et dans le reste du pays. 
Je suis aussi très inquiet au sujet de cette importante question. 
Ces dernières semaines, j'en ai discuté par téléphone et de vive voix avec des agriculteurs et leurs familles. 
Les agriculteurs en Saskatchewan et dans les autres provinces de ce pays sont dans une situation désespérée.  
Les prix des produits ont chuté. 
Les revenus des fermes familiales ont baissé. 
Beaucoup prévoient qu'ils baisseront d'environ 40 p. 100 cet hiver seulement. 
Cela signifie que le tiers des agriculteurs, qui possèdent très peu de marge de manoeuvre, risquent de perdre leur exploitation agricole.  
Que se passe-t-il ici? 
Le gouvernement libéral et le Parti réformiste empêchent tout débat sur un projet de loi portant sur les petites entreprises. 
Ils empêchent la tenue de grands débats et l'échange d'idées à la Chambre des communes au sujet des programmes d'aide aux agriculteurs, qui s'imposent. 
Une des questions clés que la Chambre devrait examiner n'est pas uniquement celle des petites entreprises et de leur financement, mais également la nécessité d'assurer aux fermes familiales au moins un soutien équivalent de la part du gouvernement national.  
Le Canada est le seul pays de l'OCDE à ne pas être doté d'une politique agricole nationale ou d'un programme d'aide aux agriculteurs. 
Lorsque le gouvernement fédéral a supprimé la subvention du Nid-de-Corbeau, on nous avait dit que cette décision était conforme aux exigences du GATT et de l'OMC.  
Je me suis rendu devant le Conseil de l'Europe, à Strasbourg, en France, et j'ai soulevé cette question auprès des agriculteurs et des anciens agriculteurs, aujourd'hui députés, de 36 pays européens qui subventionnent leurs produits agricoles et leurs fermes familiales. 
J'ai dit que le gouvernement libéral du Canada supprimait la subvention du Nid-de-Corbeau, une subvention au transport de produits agricoles, en raison de l'OMC. 
Ils ont dit que le GATT et l'OMC leur accordaient cinq ans pour régler les questions des subventions aux agriculteurs. 
Le gouvernement libéral a supprimé ces subventions à la première occasion. 
Cependant, les parlementaires européens et le comité de l'agriculture du Conseil de l'Europe ont dit qu'en aucun cas ils abandonneraient leurs agriculteurs au profit des États-Unis d'Amérique, qui veulent des subventions réduites pour les agriculteurs de l'étranger, mais pas pour les leurs.  
Il y a trois ans que je me suis entretenu avec ces politiciens de l'Europe. 
Ils m'ont dit que les subventions étaient intactes, mais que les agriculteurs étaient tout de même en difficulté, quoique pas aussi gravement que ceux du Canada, parce qu'ils bénéficiaient d'un programme d'aide de base offert par leurs gouvernements nationaux, ce que ne fait pas notre gouvernement.  
À mon avis, le projet de loi C-53 est un ouvrage en cours d'élaboration. 
Il devrait être renvoyé au Comité permanent de l'industrie pour un examen détaillé, afin que les petites entreprises aient accès à du financement.  
Le secteur des petites entreprises et certains de ses représentants, dont la Fédération canadienne de l'entreprise indépendante, nous ont dit qu'une des conditions importantes pour les petites entreprises est l'accès au capital.  
Nous tenons beaucoup à ce que les petites entreprises continuent d'avoir accès au capital. 
Ce projet de loi va donner aux petites entreprises l'occasion d'obtenir des prêts garantis qui seront entièrement financés par les entreprises en question. 
Il ne s'agit pas d'un programme de subventions, mais d'un programme de prêts garantis que j'estime fort nécessaire, surtout pour les petites entreprises.  
Je tiens à formuler deux ou trois réserves à propos du projet de loi. 
À ma connaissance, les paramètres de base de la LPPE demeurent inchangés dans ce projet de loi. 
On offrira encore du financement, par emprunt fondé sur les biens, aux entreprises ayant un chiffre d'affaires annuel inférieur à 5 millions de dollars. 
Les prêts demeurent plafonnés à 250 000 $ par entreprise. 
Le montant maximal admissible au financement sera égal à 90 p. 100 du coût des biens. 
Il faudra aussi verser au gouvernement un droit d'enregistrement unique de 2 p. 100 pour demander un prêt, une fois qu'il sera accepté. 
La durée du prêt demeure à peu près la même.  
Cette initiative gouvernementale est importante pour les petites entreprises, car, chaque année, 30 000 entreprises présentent une demande en vertu de ce programme. 
Ce ne sont pas des prêts de 250 000 $ dans tous les cas. 
La moyenne se situe à 68 000 $ environ. 
Certaines personnes trouveront peut-être que ce n'est pas beaucoup, mais il faut comprendre qu'il existe des structures très intéressantes dans le secteur canadien des petites entreprises.  
Par exemple, la maison Thompson Lightstone & Company Limited vient de compléter, en 1998, une étude sur les petites entreprises. 
Le Parti réformiste donne de la misère aux petites entreprises qui essaient de créer de l'emploi et de vendre leurs produits et services, au pays et à l'étranger. 
Il s'attend quand même à obtenir l'appui du milieu de la petite entreprise. 
Les petits entrepreneurs seront très intéressés par les commentaires des députés réformistes au sujet du projet de loi et de la petite entreprise. 
Ceux-ci seront diffusés d'un bout à l'autre du pays par tous les députés qui sont favorables à une certaine forme de mécanisme de financement des petites entreprises.  
Vingt-sept pour cent d'entre eux croient que la disponibilité du financement pose un problème. 
Si le Parti réformiste signe l'arrêt de mort de ce projet de loi, 27 p. 100 des entreprises seront très inquiètes, parce que leurs possibilités de financement seront à peu près inexistantes en cas de difficulté.  
Ce chiffre ne coïncide pas avec ceux de Lightstone. 
Pour ce sondage, on appelait des petits entrepreneurs très occupés à essayer de faire leur travail et de faire marcher le commerce. 
C'est très important, mais pour ce qui est des enjeux fondamentaux pour la viabilité des petites entreprises, je pense que 27 p. 100 est un chiffre beaucoup plus conforme à ce que j'ai vu au fil des ans, d'une étude à l'autre.  
Le rapport Lightstone présente aussi de l'information intéressante au sujet des entreprises. 
Il indique que 18 p. 100 des centaines de milliers de petites entreprises ont eu recours à une garantie de prêt semblable à ce qui est prévu dans la LPPE, et qu'une autre tranche de 6 p. 100 ont eu recours à quelque autre forme de garantie, en vertu d'un programme de prêt gouvernemental, fédéral ou provincial.  
Quelque 26 p. 100 des PME sont établies à la campagne. 
Le député de Souris-Moose Mountain a dit plus tôt que l'agriculture pose problème et que cela n'aidera pas vraiment les gens d'affaires de sa circonscription. 
Il a souligné un point intéressant, mais je ne pense pas qu'il ne comprend pas que 26 p. 100 de ces PME sont rurales.  
C'est une proportion élevée qui est confirmée dans le registre national de Statistique Canada.  
Le Parti réformiste s'en prend à ces entreprises à domicile et aux autres PME en les privant de la possibilité d'obtenir des prêts garantis, qui sont, je vous le rappelle, pour l'essentiel autofinancés par les gens d'affaires qui y ont recours.  
D'aucuns diront que le vérificateur général avait certaines préoccupations. 
C'est vrai, le vérificateur général avait bien des préoccupations relatives à la Loi sur les prêts aux petites entreprises du Canada. 
Il se trouve que j'ai des observations du vérificateur général à ce sujet. 
Il s'inquiétait beaucoup de l'obligation de rendre compte relativement à certains de ces prêts. 
Il s'inquiétait beaucoup du fait que certaines entreprises abusaient du programme avec l'aide de certaines institutions financières. 
Il avait des préoccupations au sujet de la vérification, du suivi des chiffres et de la responsabilité possible du gouvernement.  
Ce sont des préoccupations légitimes que tous les membres appuieraient sans doute. 
C'est pourquoi le projet de loi devrait être renvoyé à un comité pour que ces préoccupations soient soulevées de nouveau et que le gouvernement puisse donner l'assurance qu'on tiendra compte des préoccupations du vérificateur général et que les abus très mineurs de la part des institutions financières et de certaines entreprises, seront corrigés et réprimés.  
Dans le cadre du programme de prêts aux petites entreprises, 177 000 nouveaux prêts ont été consentis entre 1993 et 1997. 
Le total des garanties s'élevait à 11,2 milliards de dollars, ce qui n'est pas négligeable.  
Ces deux projets nous inquiètent. 
Nous voudrions discuter avec le président du Conseil du Trésor et le ministre des Finances de notre crainte de voir l'approbation de ces prêts inscrite dans le processus de réglementation. 
Ils ne sont pas aussi transparents qu'ils devraient l'être. 
Le vérificateur général a recommandé plus de transparence dans l'approbation de ces prêts, et certainement en ce qui concerne leur réglementation.  
Ce programme prévoit le recouvrement des coûts. 
Nous sommes d'avis que, s'il y avait une composante de recouvrement des coûts dans les règlements, la loi favoriserait vraiment les petites entreprises. 
