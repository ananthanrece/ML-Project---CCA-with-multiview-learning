L'hon. Don Boudria (leader du gouvernement à la Chambre des communes, Lib.):
Monsieur le Président, je suis heureux de prendre part au débat sur cette excellente mesure législative.  
Nous savons bien sûr le vif intérêt que le ministre de l'Industrie, comme tous les députés, porte à la petite entreprise au Canada. 
Hier encore, l'opposition officielle parlait des petits entrepreneurs au Canada, des cotisations à l'assurance-emploi et de plusieurs autres questions, chantant les louages de la petite entreprise.  
Hier, le chef de l'opposition à la Chambre a aussi parlé des problèmes économiques actuels dans le monde et a demandé si le gouvernement avait une annonce à faire à égard dans la déclaration d'hier concernant les travaux de la Chambre. 
Je l'ai bien sûr remercié pour cette question et j'ai immédiatement mentionné l'excellente mesure législative que nous proposons afin d'aider les petites entreprises et les entreprises en général au Canada à créer richesse et emplois. 
C'est là un élément essentiel du programme du gouvernement.  
Donc, compte tenu de l'intérêt des députés d'en face, et de tous les députés, envers l'économie canadienne-du moins les discours et l'intérêt manifesté dans cette Chambre hier le démontrent-on doit se poser la question de savoir pourquoi l'opposition officielle a choisi, dans sa sagesse...  
Une voix:
C'est beaucoup dire.  
L'hon. Don Boudria: ...dans sa prétendue sagesse, pourquoi l'opposition officielle a-t-elle choisi d'invoquer une procédure de la Chambre pour faire en sorte que le projet de loi n'aille pas de l'avant?  
L'opposition a demandé de retarder un projet de loi visant à aider les petites entreprises, à le retarder de six mois.  
L'hon. Lucienne Robillard:
Quelle contradiction!  
L'hon. Don Boudria:
Quelle contradiction, en effet, me dit la ministre de la Citoyenneté et de l'Immigration. 
Elle a raison, comme d'habitude d'ailleurs, elle est tellement éloquente.  
Il est important de nous assurer que ce projet de loi visant à aider les petites entreprises aille de l'avant.  
Il importe que l'opposition ne retarde pas l'adoption de ce projet de loi en proposant des motions dilatoires et des motions de renvoi ou en faisant appel à d'autres astuces procédurières. 
Je ne pense pas qu'elle devrait recourir à de telles astuces. 
Je propose donc: 
Que la question soit maintenant mise aux voix.  
La présidente suppléante (Mme Thibeault):
On passe donc à la période de questions et commentaires.  
M. Keith Martin (Esquimalt-Juan de Fuca, Réf.): 
Madame la Présidente, je remercie mon collègue d'en face de son discours, mais je suis déçu d'entendre qu'il désire mettre cette importante question aux voix dès maintenant.  
Quoi qu'il en soit, j'ai une question pour le député. 
Son gouvernement prend des sommes importantes dans la caisse de l'assurance-emploi pour les mettre dans les recettes générales. 
Cet argent est en excès des besoins de la caisse pour les paiements aux chômeurs.  
Les renseignements actuels montrent que le prélèvement d'argent dans le secteur privé a un effet néfaste sur la capacité de celui-ci à créer des emplois. 
Le député sait très bien que le rapport publié il y a tout juste 48 heures par la Fédération canadienne de l'entreprise indépendante montre clairement que la chose la plus importante pour elle, celle qui gêne le plus la création d'emplois, c'est le niveau d'imposition.  
J'aimerais que le député nous dise s'il est prêt, oui ou non, à demander au ministre des Finances de réduire de 50 p. 100 les cotisations à l'assurance-emploi?  
L'hon. Don Boudria:
Madame la Présidente, tout d'abord, je ne tiens pas à donner de leçon de procédure au député d'en face, mais les mots «que la question soit mise aux voix maintenant», ne signifient pas que le débat cesse maintenant.  
Une motion portant les mots «que la question soit mise aux voix maintenant» vise à faire en sorte qu'aucun député ne puisse demander le renvoi du projet de loi et empêcher qu'il fasse l'objet d'un vote. 
C'est ce que signifie la motion.  
M. Philip Mayfield:
Puis-je savoir sur quoi porte le vote?  
L'hon. Don Boudria:
Madame la Présidente , je ne pensais pas devoir donner une leçon de procédure à un autre député d'en face. 
Je les renverrai simplement à Beauchesne, et cela réglera la question. 
Nous pourrions tenir un séminaire. 
D'ailleurs, il y en a eu un à l'intention de tous les députés après les élections.  
La question soulevée par le député est très sérieuse. 
Ce dernier a déclaré que le gouvernement prend les cotisations d'assurance-emploi et les verse dans le Trésor. 
Je demanderais au député de justifier cette allégation. 
Rien de tel n'a été fait. 
Personne n'a utilisé les cotisations d'assurance-emploi à quelque fin que ce soit. 
Cette affirmation est foncièrement inexacte. 
Le député veut sans doute dire que nous tenons actuellement un débat pour déterminer si la meilleure ligne de conduite devrait être, et non pas est, de réduire les cotisations d'assurance-emploi ou d'autres formes de perception exercées auprès des contribuables, par exemple l'impôt général, autrement dit déterminer la meilleure façon de remettre de l'argent dans leurs poches. 
Voilà l'objet du débat. 
Il ne s'agit pas de déterminer si quelqu'un a utilisé ou utilise les cotisations d'assurance-emploi.  
Quant au second volet de la question, le député a dit que l'argent était versé dans le Trésor. 
Je rappelle qu'en 1986, le vérificateur général du Canada a présenté un rapport dans lequel il recommandait d'abolir la réserve spéciale de l'assurance-emploi, dans laquelle se trouvaient des cotisations. 
La réserve a été abolie une année plus tard, en 1987. 
Il y a presque 12 ans qu'il n'y a plus de caisse distincte d'assurance-emploi. 
Je sais que l'information est lente à circuler ici parfois, mais il y a déjà 12 ans que ce fonds n'existe plus. 
Le débat ne porte pas sur cette question, mais sur la meilleure ligne de conduite à suivre.  
Le premier ministre a écouté les doléances du député d'en face et d'autres de ses collègues. 
Il s'agit de déterminer quelle est la meilleure façon de remettre de l'argent dans les poches des contribuables, et non pas de savoir si cela va se produire. 
C'est une toute autre question. 
Il s'agit de voir quelle réserve nous devons conserver dans l'éventualité d'un ralentissement de l'économie, ce que j'espère ne pas voir se produire.  
Il est insensé de présenter la chose en ces termes et cela ne rend service à personne ici.  
Je recommande à tous les Canadiens de lire la politique du Parti réformiste. 
Je sais que, dans la meilleure des situations possible, la chose est difficile.  
Des voix:
Oh, oh!  
L'hon. Don Boudria:
Permettez-moi de reformuler cela. 
Les Canadiens devraient lire une très petite partie des politiques réformistes, car je sais que s'ils en lisaient trop, cela leur causerait une indigestion, des brûlements d'estomac et d'autres troubles du genre. 
Nous aurions peut-être à recourir aux services d'un médecin bien connu à la Chambre si nous lisions trop de cela.  
C'est la politique que le Parti réformiste défendait dans ses propres documents.  
M. Ken Epp (Elk Island, Réf.): 
Madame la Présidente, je vous remercie de m'avoir donné la parole afin que je puisse répondre.  
M. Antoine Dubé:
Madame la Présidente, j'invoque le Règlement. 
