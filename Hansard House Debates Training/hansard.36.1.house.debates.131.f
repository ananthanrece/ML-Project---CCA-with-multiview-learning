Le gouvernement libéral ne songe qu'à jouer des jeux encore plus imbéciles, soit mettre fin au débat, ou à tout le moins en limiter la durée, concernant un projet de loi d'une telle importance pour les propriétaires de petite entreprise et pour leurs employés. 
Le NPD est tout à fait désolé de voir ces partis politiques jouer à de tels jeux avec l'avenir de ces entreprises et des familles qui en dépendent.  
Ce n'est pas la première fois que nous voyons le Parti libéral essayer de limiter ou de clore un débat sur des questions ayant une importance économique pour notre pays. 
Nous l'avons vu imposer la clôture tant de fois que j'en ai perdu le compte.  
