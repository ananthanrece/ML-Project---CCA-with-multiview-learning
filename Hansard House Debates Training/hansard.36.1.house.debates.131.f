La présidente suppléante (Mme Thibeault):
Un député a fait un rappel au Règlement. 
Vous avez la parole maintenant.  
M. Ken Epp:
Ce que nous voulons faire, et ce que les libéraux ne font pas, c'est améliorer le projet de loi. 
Il est faux de sa part de laisser entendre que nous ne voulons pas que le secteur privé puisse jouer le rôle qui lui revient. 
Il affirme que nous voulons prolonger l'étude de ce projet de loi et étouffer cette mesure. 
Nous souhaitons plutôt l'améliorer. 
Nous avons constaté que le gouvernement libéral n'est pas ouvert à des amendements raisonnables. 
Nous voulons apporter certains amendements, nous voulons qu'on accorde une certaine période pour améliorer ce projet de loi avant qu'il ne soit adopté.  
Les libéraux ne veulent pas de débat, que ce soit à la Chambre ou au sein de la population canadienne. 
Lorsqu'ils se font une idée, ils ne veulent pas en changer. 
Cela nous donne des mesures législatives bien inférieures à ce qu'elles pourraient être.  
Comment a réagi le gouvernement? 
Il a seulement dit qu'il ne changerait rien, qu'il allait procéder à sa façon et il a présenté un motion d'attribution de temps. 
C'est une mesure bâclée.  
Pourquoi les libéraux s'opposent-ils à ce qu'on améliore une mesure législative? 
Ils ne sont certainement pas assez arrogants pour penser que du premier coup ils peuvent faire mieux que quiconque ne pourra jamais faire au Canada? 
Pourquoi refusent-ils d'entendre d'autres points de vue, comme ceux exprimés par les Canadiens par l'entremise de l'opposition ou directement? 
Pourquoi insistent-ils pour appliquer la clôture? 
C'est vraiment inacceptable.  
Mon vis-à-vis pourrait-il expliquer à tous les Canadiens, et pas simplement à votre serviteur et à notre parti, pas simplement à l'opposition, pourquoi son parti doit avoir recours à ces tactiques draconiennes pour faire adopter envers et contre tous une mesure législative qu'il considère parfaite, alors que nous savons fort bien qu'on pourrait l'améliorer?  
L'hon. Don Boudria:
Madame la Présidente, je recommande au député de lire le commentaire 521 de Beauchesne. 
C'est la première chose que j'ai à lui dire.  
Le député affirme vouloir modifier le projet de loi, mais la motion qu'il a présentée plus tôt cette semaine proposait d'interrompre l'étude du projet de loi pendant six mois, non pas de l'amender. 
Cette motion aurait empêché le renvoi du projet de loi au comité où les propres amendements du député auraient pu être entendus.  
Ce que le député vient de dire est la meilleure expression du contraire de la vérité qu'il m'ait été donné d'entendre.  
M. Antoine Dubé (Lévis-et-Chutes-de-la-Chaudière, BQ):
Madame la Présidente, je veux intervenir dans le cadre de ce débat sur le projet de loi C-53, mais compte tenu ce qui vient de se passer, je pense qu'il faut clarifier une certaine situation.  
Le leader du gouvernement à la Chambre, apparemment en réaction à une motion du Parti réformiste dont on aura à disposer, je suppose, mardi prochain, propose en quelque sorte un bâillon. 
Ne voulant pas accepter la motion du Parti réformiste de retarder l'étude du projet de loi de six mois, il propose d'y aller immédiatement. 
C'est ce qu'il a dit ce matin.  
Il faut clarifier cela. 
Le leader du gouvernement se sert du Règlement pour clarifier, mais il se met à parler de l'assurance-emploi et de différentes autres choses, de sorte que nos auditeurs à la maison doivent être complètement confus.  
Nous disons également que le projet de loi C-53 n'est pas parfait. 
Cependant, les principes du projet de loi, l'importance des fonds pour les petites entreprises, tant au Québec qu'au Canada, ne justifient pas qu'elles soient laissées pour compte pendant six mois.  
L'an dernier, lorsqu'on a adopté le projet de loi C-21, c'était pour accorder des crédits supplémentaires, environ un milliard de dollars de plus. 
Le leader du gouvernement demande d'y aller rapidement, alors que ce projet de loi n'est pas assez bien rédigé ou ficelé pour répondre aux besoins des petites entreprises.  
Cependant, il y a urgence de maintenir des fonds. 
Par contre, il ne faut pas faire comme lors des trois dernières années, c'est-à-dire proposer chaque année un projet de loi qui prolonge la période d'un an et qui accorde des crédits supplémentaires. 
On fonctionne une année à la fois, et cela n'a pas de sens.  
C'est très important, il faut absolument qu'on arrête de jouer, j'allais dire à la petite semaine, mais là, c'est à la petite année, avec les petites entreprises canadiennes et les laisser sur le qui-vive constamment.  
Par cette façon de gérer, on produit la chose suivante: les gens, craignant que le programme ne soit aboli ou que les limites de crédit soient atteintes rapidement, vont tout de suite à la banque pour être certains de pouvoir bénéficier du programme. 
Souvent, les projets, et ce fut démontré, ne sont pas toujours prêts et sont plus vulnérables. 
Donc, ils risquent d'être refusés.  
Le gouvernement, lorsque vient le temps d'indemniser les banques sur les pertes, a plusieurs demandes. 
C'est une situation comme celle-là qui s'est produite par le passé, parce qu'on agissait à la petite année, une année à la fois. 
Le vérificateur a fait une étude là-dessus et a constaté que oui, il y avait peut-être un certain problème de contrôle et qu'il n'y avait pas toute la rentabilité nécessaire. 
Je crois que pour la dernière année, on parle d'indemnisations de près de 200 millions de dollars. 
C'est quand même un montant important.  
Cependant, 95 p. 100 des entreprises du Canada sont des petites entreprises. 
Ce sont elles qui créent près de 50 p. 100 des emplois actuellement. 
Ce sont les petites entreprises de 50 employés ou moins qui utilisent une grande partie de la main-d'oeuvre et qui font le plus, il faut le dire, preuve d'innovation et d'imagination. 
Ce sont elles qui travaillent le plus, à mon avis, au développement économique. 
C'est très important.  
C'est pour cette raison que nous sommes pour le principe du projet de loi et que mardi, le Bloc québécois votera en faveur du projet de loi. 
On comprend un peu les arguments du Parti réformiste, mais on sait pourquoi le Parti réformiste veut en retarder l'étude de six mois; apparemment, c'est pour mieux étudier la situation. 
Si on acceptait leur proposition, on risquerait de priver, pendant une assez longue période, des petites entreprises de financement. 
Nous ne pouvons accepter cela.  
La meilleure façon de faire serait que le Parti réformiste, comme les autres partis de l'opposition, comme les députés du parti ministériel, s'attellent à la tâche au Comité permanent de l'industrie. 
Le secrétaire parlementaire est ici et nous a dit qu'il se montrait réceptif aux changements, aux bonifications. 
Pour une fois! 
Ce gouvernement ne nous a pas nécessairement habitués à beaucoup d'ouverture face aux changements proposés par l'opposition.  
Généralement, il a tendance à penser que la vérité est exclusivement de son côté. 
Nous, nous pensons que la vérité est mieux répartie que cela. 
Souvent, on la retrouve aussi dans les partis de l'opposition.  
Le processus habituel est que, par la suite, on fasse rapport à la Chambre. 
Là encore, les partis d'opposition peuvent présenter des amendements. 
Comme il s'agit d'un sujet très important, il faudrait peut-être que, cette fois-ci, le gouvernement se montre à l'écoute des citoyens et des citoyennes qui s'expriment aussi par l'intermédiaire des partis de l'opposition.  
C'est ce que j'ai entendu jusqu'à maintenant dans le cadre du débat sur ce projet de loi, mais aussi dans le cadre de celui sur le projet de loi C-21, et lors du débat de l'année précédente, puisque à peu près à chaque année le gouvernement revient avec cela.  
Je fais partie du Comité permanent de l'industrie et encore, hier matin, on nous a présenté presque un pied de documents et d'études statistiques. 
On nous propose des séminaires, des colloques, différents moyens, des revues de presse à n'en plus finir, et il y a aussi des groupes qui ont demandé à être entendus. 
L'Association des entreprises indépendantes, des consommateurs, les plus grandes entreprises ont un avis sur le sujet. 
Il y a actuellement aussi tout le débat sur les fusions de banques. 
Ce n'est pas rien, c'est important.  
Actuellement, il y a sept grandes banques, plus les caisses populaires au Québec. 
On sait que les caisses populaires sont extrêmement importantes au Québec. 
J'en sais quelque chose puisque le siège social est dans ma circonscription. 
Lévis est le lieu de création du mouvement Desjardins. 
Cela s'est fait à Lévis en 1900 et dans deux ans on fêtera son centenaire.  
C'est très important.  
Oui, on parle de petites entreprises, de financement des petites entreprises mais, en même temps, on parle du fonctionnement des banques. 
C'est un des sujets qui est le plus d'actualité actuellement discuté au niveau fédéral. 
Tous les partis doivent terminer leur réflexion sur le sujet. 
Tous ont des positions de base et de principe, mais la situation évolue rapidement à l'échelle mondiale quant aux institutions financières qu'il faut inscrire le débat et l'évaluation des besoins de financement des petites entreprises dans ce cadre. 
Je sais que le Comité permanent des finances étudie cela. 
Il y a à cet égard le Rapport MacKay.  
Il faut prendre le temps nécessaire, mais pas trop de temps non plus, ce qui priverait nos petites entreprises du financement dont elles ont besoin.  
Ce projet de loi a des défauts. 
Il veut réagir aux recommandations du vérificateur général qui, à bon droit je pense, est allé dans une optique de comptabilité et de contrôle comptable. 
C'est son travail et il l'a bien fait. 
Il propose que le gouvernement ajoute des mécanismes, que le ministre ait davantage de moyens pour contrôler l'efficacité de ce programme. 
Nous sommes d'accord. 
Cependant, il ne faut pas seulement répondre à cette situation.  
L'avenir n'est absolument pas sécuritaire et garanti et les grandes entreprises-je donne l'exemple du dossier de la compagnie GM à Montréal-tardent à annoncer leurs investissements, leur planification et leurs orientations. 
Pendant ce temps, il y a des gens qui sont laissés pour compte.  
Il y a une grande industrie chez nous, les industries Davie. 
Il y a des contrats pour 300 millions de dollars mais les travailleurs sont laissés dans l'insécurité, parce qu'actuellement, il y a un manque de garantie de financement. 
Il faut regarder cela sérieusement, parce qu'il s'agit de centaines de millions de dollars. 
