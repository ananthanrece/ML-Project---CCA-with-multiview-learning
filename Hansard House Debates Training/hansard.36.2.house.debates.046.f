M. Rahim Jaffer (Edmonton-Strathcona, Réf.): 
Madame la Présidente, je suis heureux d'intervenir sur le projet de loi C-20, la Loi sur la clarté, comme nous l'appelons.  
Le projet de loi C-20 a essentiellement pour but de donner effet à l'exigence de clarté formulée en 1998 par le Cour suprême du Canada dans son avis sur le Renvoi sur la sécession du Québec. 
Le texte prévoit que la Chambre des communes détermine si la question référendaire sur la sécession d'une province est claire et énonce certains des facteurs dont la Chambre tient compte dans le cadre de sa décision. 
Je reviendrai plus tard sur cette question qui forme une importante partie de ce débat.  
Le texte interdit en outre au gouvernement du Canada d'engager des négociations sur les conditions auxquelles une province pourrait cesser de faire partie du Canada si la Chambre décide que la question n'est pas claire. 
Le projet de loi C-20 prévoit aussi que la Chambre des communes, après la tenue d'un référendum dans une province, détermine si une majorité claire de la population de la province a clairement déclaré qu'elle voulait que celle-ci cesse de faire partie du Canada et énonce les facteurs dont la Chambre tient compte dans le cadre de sa décision.  
Ce projet de loi permettrait au gouvernement du Canada d'engager des négociations avec la province au cas où une majorité claire de la population a clairement déclaré qu'elle voulait que celle-ci cesse de faire partie du Canada.  
Dans le cas où toutes ces conditions seraient remplies et où un vote affirmatif serait exprimé dans un référendum sur la question, le texte confirme que la sécession d'une province requiert une modification à la Constitution du Canada, à l'issue de négociations avec les gouvernements provinciaux et le gouvernement fédéral.  
Le projet de loi reconnaît qu'il n'existe aucun droit, au titre de la Constitution du Canada, d'effectuer unilatéralement la sécession d'une province du Canada et que, par conséquent, la sécession d'une province requerrait la modification de la Constitution à l'issue de négociations auxquelles participeraient notamment les gouvernements de l'ensemble des provinces et du Canada.  
En quelques mots, ladite loi sur la clarté aurait pour objet de veiller à ce que le libellé de la question sur la sécession d'une province du Canada soit sans équivoque, et que la décision de sécession soit l'expression de la volonté d'une majorité nette de la population ayant participé au référendum. 
Pourtant, dans son projet de loi, le gouvernement n'a pas donné de précision quant à l'une ou l'autre de ces questions.  
