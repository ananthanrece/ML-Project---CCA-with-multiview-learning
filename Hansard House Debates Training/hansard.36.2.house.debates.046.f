On ne peut modifier un projet de loi par la voie d'un amendement motivé. 
Un amendement motivé ne peut modifier autre chose que la motion de lecture d'un projet de loi, et certainement pas le projet de loi lui-même. 
Cela doit se faire soit en comité soit à l'étape du rapport. 
Par conséquent, selon le Règlement, l'amendement que propose le député est une impossibilité.  
Je veux citer notre nouveau manuel de procédure, qui est maintenant connu comme le M et M. Cet ouvrage dit que l'amendement motivé, un type d'amendement qui peut être proposé à l'étape de la deuxième lecture, permet aux députés de présenter les raisons pour lesquelles ils s'opposent à la deuxième lecture du projet de loi. 
Je pense que cela règle la première question parce qu'on ne peut modifier un projet de loi au moyen d'un amendement à l'étape de la deuxième lecture. 
Un amendement à cette étape-là est un amendement motivé, qui a simplement pour effet de s'opposer au projet de loi.  
Il ne devient loi qu'après avoir reçu la sanction royale, et il faut ensuite promulgation par Son Excellence aux termes d'un décret, à moins que cela ne figure dans le projet de loi, pour que cette loi prenne effet. 
