À ce moment-là, les séparatistes faisaient partie du camp du non et disaient à qui voulait l'entendre qu'une majorité de 50 p. 100 plus un ne pouvait être suffisante. 
Pourquoi? 
Parce que, comme le disait M. Landry, il fallait tenir compte des anglo-allos, un vote qu'il considérait d'ores et déjà perdu. 
La victoire du oui dans ce cas-là n'aurait été justifiée, selon lui, que si un tel niveau d'appui avait pu être constaté chez les francophones.  
Ce n'est la première fois que nous avons l'occasion de nous pencher sur des déclarations de ce type qui, admettons-le, comportent un certain cynisme. 
«Tenir compte des anglo-allos» équivaut, à toutes fins pratiques, à peser les votes, c'est-à-dire que le vote d'un francophone vaut forcément plus que celui d'un Québécois issu d'une communauté culturelle.  
Or, j'ai toujours cru, pour ma part, qu'à partir du moment où ils jouissent de la citoyenneté canadienne, les voteurs étaient tous soumis aux mêmes règles et que leurs votes possédaient tous le même poids politique. 
Considérer les citoyens en vertu de leur race, de leur langue, de leur religion, c'est jouer dangereusement avec des sentiments qui mènent à l'intolérance.  
Ce genre de déclaration en dit long sur la conception péquiste de la démocratie.  
Il y aura toujours, aux yeux des séparatistes, deux types de Québécois: les vrais, qui favorisent l'option indépendantiste, et les faux, composés d'une part du groupe francophone qui rejette la séparation et, d'autre part, des Québécois issus de tous les horizons de la communauté internationale.  
Voilà pourquoi ces derniers ne peuvent s'identifier au projet indépendantiste et refusent d'écouter le chant de la sirène qu'on leur sert à chaque référendum.  
Le projet de loi que nous avons devant nous, comme son nom l'indique, vise à clarifier les règles qui guideraient la conduite du gouvernement du Canada si une province proposait à ses citoyens un projet de sécession. 
Ce projet s'impose d'emblée, et c'est pourquoi nous sommes si résolus à le faire adopter.  
Des déclarations comme celle de Bernard Landry, que j'ai déjà citées, ne peuvent laisser indifférents ceux qui sont véritablement attachés à la démocratie. 
Il est à nos yeux inadmissible de dire que la séparation ne nécessite qu'une majorité de 50 p. 100 plus un, alors qu'un projet de réforme constitutionnelle nécessiterait, comme le disait M. Landry en vertu de je ne sais trop quelle logique «tripatouillée», un appui de 58 p. 100. 
C'est là un reproche que nous font les séparatistes eux-mêmes. 
Ils nous reprochent de soutenir qu'une majorité requise pour faire sécession doit être supérieure à celle nécessaire pour entrer, par exemple, dans une fédération.  
En effet, c'est ce que nous croyons, et je vais illustrer mon propos avec un exemple que les séparatistes utilisent fréquemment pour illustrer leur thèse, soit celui de Terre-Neuve. 
Convenons tout d'abord d'une chose. 
En 1949, Terre-Neuve était une colonie de la Grande-Bretagne. 
Il n'existait pas, pour ainsi dire, de liens d'interdépendance aussi solides entre Terre-Neuve et la Grande-Bretagne qu'entre le Québec et le Canada. 
C'est ainsi que la sécession du Québec serait beaucoup plus complexe que ne l'a été le rattachement de Terre-Neuve au Canada. 
Les risques d'injustice seraient donc, eux aussi, plus grands.  
Il faut éviter de confondre les 52 p. 100 de Terre-Neuviens déjà mentionnés et la majorité très importante d'entre eux qui a voté en faveur de la brisure du lien avec le Royaume-Uni. 
En effet, les séparatistes ne disent jamais qu'un premier référendum, tenu le 3 juin 1948, offrait trois options aux Terre-Neuviens: le prolongement du statut de dépendance pour cinq années additionnelles; l'indépendance sans l'aide financière de Londres; ou l'entrée dans la fédération canadienne.  
À peine 14 p. 100 des électeurs ont voté pour le prolongement du statut de dépendance. 
En d'autres mots, 86 p. 100 des électeurs ont voté en faveur de la cassure du lien colonial avec Londres. 
Il faut dire que c'était assez clair.  
Un autre référendum fut alors tenu entre les deux options qui restaient, soit d'opter pour l'indépendance sans l'aide financière de Londres ou joindre la Confédération canadienne. 
Le 22 juillet 1948, 52 p. 100 des Terre-Neuviens ont opté pour l'un des deux changements radicaux, soit la Confédération canadienne. 
Dans les circonstances, les autorités canadiennes ont décidé d'accueillir Terre-Neuve et, aujourd'hui encore, on doit s'en féliciter.  
On vient de le voir, la différence essentielle entre Terre-Neuve en 1948 et l'option séparatiste au Québec de nos jours, c'est que Terre-Neuve, en se joignant au Canada, ne brisait pas un pays. 
Elle mettait fin à un lien colonial temporaire. 
La sécession du Québec du Canada aurait quant à elle pour effet de briser le Canada, et ce, de façon permanente. 
Voilà une raison essentielle pourquoi le même pourcentage ne peut être exigé dans deux cas aussi différents l'un de l'autre.  
