Que fait donc au juste le projet de loi sur la clarté? 
Il prévoit que la Chambre des communes détermine si la question référendaire sur la sécession d'une province est claire et énonce certains des facteurs dont la Chambre tient compte dans le cadre de sa décision. 
Il interdit au gouvernement du Canada d'engager des négociations sur les conditions auxquelles une province pourrait cesser de faire partie du Canada si la Chambre décide que la question n'est pas claire.  
Il prévoit que la Chambre des communes, après la tenue d'un référendum dans une province, détermine si une majorité claire de la population de la province a clairement déclaré qu'elle voulait que celle-ci cesse de faire partie du Canada et énonce les facteurs dont la Chambre tient compte dans le cadre de sa décision. 
Il interdit au gouvernement du Canada d'engager des négociations avec la province, à moins qu'une majorité claire n'ait clairement exprimé sa volonté de faire sécession.  
Enfin, le projet de loi confirme que la sécession d'une province requiert une modification à la Constitution du Canada, à l'issue de négociations avec tous les gouvernements de l'ensemble des provinces et du Canada. 
Il énumère également certaines des matières, comme la répartition de l'actif et du passif, la modification des frontières de la province, les droits des peuples autochtones et d'autres minorités, dont il doit être traité avant qu'une modification constitutionnelle ne soit proposée par un ministre d'État.  
Le projet de loi sur la clarté traite d'honnêteté. 
Il vise à offrir les conditions permettant d'obtenir un résultat honnête dans tout référendum à venir. 
Si on prend le temps de lire le jugement de la Cour suprême, on constate que ce document raconte notre riche histoire constitutionnelle et analyse nos plus importants principes constitutionnels. 
L'objet intrinsèque de notre Constitution, c'est d'assurer et de protéger les droits fondamentaux dans le cadre du fédéralisme, de la règle de droit et de la démocratie, de protéger les minorités et le constitutionnalisme.  
