M. Paul DeVillers (Simcoe-Nord, Lib.):
La seule motivation des chefs séparatistes à cet égard réside dans leur conviction que le faible écart qui séparait le oui du non, le 30 octobre 1995, pourrait être comblé lors d'un prochain référendum. 
Vous en conviendrez avec moi, ce n'est certainement pas la meilleure des raisons. 
Je vais donc expliquer, durant le temps qui m'est alloué, pourquoi un autre référendum ne devrait pas être tenu au Québec.  
Convenons d'abord d'une chose. 
Dans l'esprit des séparatistes, la tenue d'un autre référendum s'impose avant tout parce que, prétendent-ils, la situation du Québec est intenable au sein du Canada. 
C'est faux. 
Le Québec a su se développer dans toutes les sphères de l'activité humaine et affirmer la spécificité notamment sur les plans linguistiques et culturels. 
Le Québec est devenu, au sein du Canada, une société dynamique et moderne. 
Bref, le Québec peut être lui-même et se développer au sein du Canada.  
Depuis son arrivée au pouvoir, notre gouvernement a mis de l'avant plusieurs initiatives qui ont permis de moderniser la fédération canadienne. 
En voici quelques exemples.  
On peut penser tout d'abord à la limitation du pouvoir fédéral de dépenser, à laquelle notre gouvernement s'est engagé dans le discours du Trône de 1996. 
L'Entente sur l'union sociale, conclue le 4 février dernier, est venue concrétiser cet engagement et circonscrire le pouvoir fédéral de dépenser.  
Cette entente, à laquelle le gouvernement Bouchard n'a pas voulu souscrire, assurera néanmoins la viabilité de nos programmes sociaux. 
Elle met en évidence des principes qui traduisent les valeurs canadiennes fondamentales: l'égalité de tous les Canadiens, le respect de la diversité, l'équité, la dignité de l'être humain, la responsabilité individuelle et la solidarité.  
L'entente prévoit notamment que les nouvelles politiques sociales ne créeront pas d'obstacle à la mobilité. 
Les gouvernements s'engagent également à accroître leur transparence et leur imputabilité vis-à-vis des Canadiens. 
Pensons aussi à l'adoption de la Loi sur les veto régionaux, ainsi qu'à l'adoption de la résolution sur la société distincte. 
Cette résolution reconnaît que le Québec forme au sein du Canada une société distincte qui comprend notamment une majorité d'expression française, une culture unique et une tradition de droit civil.  
Rappelons-nous également la réduction du caractère conditionnel du principal transfert fédéral aux provinces, via la création du Transfert canadien en matière de santé et de programmes sociaux.  
L'Accord sur la libéralisation du commerce intérieur constitue une autre réalisation dont notre gouvernement est fier, tout comme du Programme des travaux d'infrastructures, qui s'est avéré un franc succès.  
Dans le domaine des échanges avec l'extérieur, il convient de souligner la formule Équipe Canada, qui a valu à nos entreprises des contrats évalués à des centaines de millions de dollars.  
La modification constitutionnelle relative aux commissions scolaires du Québec démontre que nous n'avons pas hésité à emprunter la voie constitutionnelle lorsque la situation le permettait.  
On le voit bien, nous ne sommes pas restés les bras croisés en regardant passer le train. 
Nous avons agi et les Québécois le reconnaissent. 
Ce qui est très clair, c'est que les Québécois ne veulent pas d'un autre référendum. 
Si un autre devait être tenu, les Québécois veulent une question claire. 
C'est ce que reflètent de récents sondages.  
Selon les résultats d'un sondage d'opinion publique CROP publié par le gouvernement fédéral le 30 octobre dernier, 93 p. 100 des Québécois estiment qu'il est raisonnable d'exiger une question claire et 72 p. 100 souhaitent une majorité claire. 
Soixante et un pour cent des répondants croient que la question de 1995 n'était pas claire et 60 p. 100 estiment qu'une majorité de 50 p. 100 des voix plus une ne constitue pas une majorité claire. 
Voilà qui en dit long sur l'opinion des Québécois au sujet d'un futur référendum.  
D'autres résultats du sondage font la lumière sur le soi-disant droit des Québécois de déclarer l'indépendance unilatéralement. 
Le 23 novembre, M. Bouchard a soutenu que le jugement de la Cour suprême ouvrait la porte à la possibilité d'une déclaration d'indépendance unilatérale en cas de mauvaise foi de la part du gouvernement canadien et que le désir d'Ottawa de voir respecter l'exigence de clarté était un exemple de mauvaise foi.  
Quoi qu'il en soit, 66 p. 100 des répondants du sondage CROP jugent raisonnable que le Québec signe une entente avec le reste du Canada avant de déclarer l'indépendance. 
Seulement 23 p. 100 pensent le contraire.  
Quelque 68 p. 100 des répondants estiment que les partis d'opposition à l'Assemblée nationale du Québec devraient avoir leur mot à dire dans la formulation de la question. 
Cinquante-huit pour cent des répondants croient que le gouvernement canadien a un rôle à jouer à cet égard, comparativement à 56 p. 100 dans le reste du Canada. 
On le voit, ces données illustrent bien la confusion que génère le projet séparatiste. 
Puisque ceux-ci refusent de lever l'hypothèque référendaire, il appartient au gouvernement fédéral de bien s'assurer qu'un éventuel référendum ne soit pas tenu dans un climat d'ambiguïté. 
C'est dans ce contexte que s'inscrit la volonté de notre gouvernement de clarifier les circonstances dans lesquelles il serait tenu de négocier la séparation d'une province.  
M. Bouchard a renouvelé son engagement d'aller de l'avant avec ses intentions référendaires. 
M. Facal a déclaré y consacrer tout son temps. 
Dans son discours du 28 novembre dernier, le premier ministre a invité le gouvernement Bouchard à mettre de côté son projet référendaire pour les quatre prochaines années.  
Le gouvernement péquiste et le Bloc québécois n'ont pas été long à réagir et à repousser cette proposition. 
Pourtant, les Québécois sont fortement opposés à la tenue d'un autre référendum, dans une proportion de 71 p. 100, selon un sondage CROP mené en septembre dernier.  
Les Québécois ne veulent ni d'un nouveau référendum, ni de la séparation. 
Ils sont en droit d'exiger que les gouvernements se consacrent aux questions qui les préoccupent dans la vie de tous les jours. 
Notre gouvernement est résolument engagé dans cette voie, mais l'obsession référendaire des chefs séparatistes nous oblige à nous pencher sur cette question qui concerne la survie de notre pays.  
Nous croyons que notre pays mérite d'être préservé et que nous devons veiller au bien-être des Canadiens, non pas dans un esprit de division mais en adoptant une approche réaliste et constructive. 
Un référendum ne ferait que diviser la population. 
Nous devons plutôt consacrer toutes nos énergies aux enfants, à l'éducation, à l'environnement et à tous les défis qui nous attendent au prochain siècle.  
Nous avons l'obligation de définir avec précision les conditions auxquelles le gouvernement fédéral devrait se sentir obligé de négocier la sécession d'une province. 
Je suis convaincu qu'à une question claire, les Québécois répondraient, une troisième fois, qu'ils ne veulent pas se séparer du Canada.  
