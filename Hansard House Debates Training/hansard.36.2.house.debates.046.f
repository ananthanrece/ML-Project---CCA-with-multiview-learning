À l'époque, la leucémie équivalait à toutes fins pratiques à une sentence de mort. 
Depuis, les traitements ont évolué, et nous soignons aujourd'hui les leucémiques avec de la chimiothérapie et des greffes de moelle osseuse. 
En juin dernier, à l'occasion du 25e anniversaire de notre sortie de l'école de médecine, mon condisciple, le docteur Mark Minden, a présenté un exposé. 
Cet éminent chercheur sur la leucémie et spécialiste de la greffe de moelle osseuse, préside aujourd'hui le groupe d'examen scientifique de la fondation canadienne de recherche sur la leucémie.  
Sa communication s'est révélée édifiante pour ceux d'entre nous qui ne s'étaient pas rendu compte des progrès réalisés depuis les 25 dernières années. 
La devise même de la fondation canadienne de recherche sur la leucémie annonce que nous nous approchons inexorablement de l'objectif visé. 
Avec éloquence, le docteur Minden a su exprimer à quel point nous sommes près de notre but.  
Malheureusement, plus de 3 000 Canadiens découvriront cette année qu'ils sont ateints de leucémie et pous de 2 000 mourront. 
Il importe que les Canadiens comprennent que le taux de guérison est de 70 p. 100 chez les enfants et que 50 p. 100 des adultes atteints de cette maladie connaissent une rémission d'un à cinq ans et parfois plus. 
D'une façon générale, disons simplement que la leucémie est une maladie des globules blancs. 
Le nom de cette maladie vient de deux mots grecs signifiant blanc et sang. 
Il s'agit d'un cancer des globules blancs ou des tissus de notre corps qui forment le sang, de la moelle osseuse, de la rate et des ganglions lymphatiques.  
La leucémie frappe des personnes de tous les âges, quel que soit leur sexe et leur milieu. 
Ce n'est pas une maladie contagieuse ni héréditaire, mais plus nous comprendrons les troubles génétiques, le mieux ce sera. 
Nous connaissons maintenant le chromosome Philadelphia comme une cause de l'une des formes de leucémie chronique. 
Nous savons que cette maladie est plus courante chez les personnes atteintes du syndrome de Down, lequel est évidemment une maladie génétique. 
Nous savons que l'exposition à certains produits chimiques et à certaines radiations peut accroître les risques de souffrir de cette maladie. 
Autrement dit, nous pouvons non seulement découvrir les origines génétiques de cette maladie, mais nous pourrions aussi trouver des moyens de prévention si nous comprenions les problèmes liés aux produits chimiques et aux radiations.  
La chimiothérapie, la radiothérapie et la greffe de moelle osseuse sont efficaces, mais la leucémie continue de faire plus de victimes chez les enfants que toute autre maladie.  
Une des formes de leucémie les plus courantes chez l'enfant est la leucémie lymphoblastique aiguë, ou LAL. 
Elle représente les trois quarts des cas de leucémie chez les jeunes. 
La maladie se développe dans les lymphoblastes immatures ou les jeunes lymphocytes. 
Elle semble être causée par des facteurs immunologiques. 
Dans ce nouveau millénaire, l'immunologie est probablement le domaine où il nous faut travailler le plus.  
Les gens atteints de troubles d'immunodéficience semblent exposés à des risques élevés. 
Il semble y avoir des concentrations de cas de cette forme de leucémie vers l'âge de deux ou trois ans. 
Les particularités de ce sous-ensemble tendent à montrer que nous ne savons toujours pas s'il s'agit d'un trouble dans le développement initial du système immunitaire ou d'une réaction immunitaire inhabituelle à des agents infectieux. 
Il est certain que, si nous parvenons à une meilleure compréhension, nous pourrons traiter beaucoup mieux beaucoup de problèmes causés par des systèmes immunitaires anormaux.  
Ces jeunes ont habituellement besoin de chimiothérapie pendant au moins deux ou trois ans. 
Il s'agit d'un traitement qui vise à assurer une rémission par l'élimination de toutes les cellules leucémiques. 
Comme nous le savons, les agents qui servent à détruire les cellules leucémiques tuent également des cellules saines. 
Des cellules sont détruites dans les cheveux, la peau, la couche intérieure de l'estomac et de l'intestin. 
Par conséquent, une thérapie fructueuse a parfois des conséquences terribles, même si la maladie est guérie. 
Pour l'enfant, les effets secondaires sont vraiment pénibles. 
Il faut parfois une radiothérapie au cerveau et à la moelle épinière, et les enfants éprouvent des malaises très pénibles.  
Avant 1970, peu d'enfants ou d'adolescents guérissaient de la leucémie. 
Le taux de survie a augmenté lorsque la monochimiothérapie a été remplacée par la polychimiothérapie. 
Mais, lorsqu'il subsiste des cellules leucémiques dans la région du cerveau et de la colonne vertébrale, la chimiothérapie est impuissante.  
Ce traitement a considérablement amélioré le taux de guérison des enfants, et 95 p. 100 des enfants atteints de leucémie aiguë connaissent maintenant une rémission après un mois de chimiothérapie. 
Au Canada, les trois quarts des jeunes à qui l'on a diagnostiqué cette maladie entre 1985 et 1988 ont survécu pendant cinq ans et la plupart ont été considérés comme guéris.  
Il est prouvé que celle-ci améliore les chances de survie et elle est offerte à beaucoup d'enfants atteints de leucémie aiguë à partir de leur deuxième rechute.  
Il faut que les Canadiens acceptent de donner leur moelle osseuse. 
Comme il faut des donneurs qui satisfont aux six marqueurs immunologiques, les frères et soeurs ont une chance sur quatre d'être de parfaits donneurs. 
Sinon, les patients qui sont parfois mourants doivent s'adresser rapidement à d'autres parents, des registres de donneurs et à la population en général par l'intermédiaire des médias. 
Ces recherches sont vaines dans 40 p. 100 des cas.  
Les scientifiques ont trouvé une nouvelle façon de transplanter de la moelle osseuse d'un donneur non compatible, de telle sorte que n'importe qui ou presque peut bénéficier de cette technique de survie.  
Cela fait presque cinq ans que le meilleur ami de notre famille, Phillip Borsos, est décédé de la leucémie. 
Phillip était atteint de la maladie de Hodgkin, et c'est le traitement qu'il a reçu pour cette maladie qui a causé sa leucémie, un rare effet secondaire qui s'est malheureusement produit dans son cas. 
Son épouse, Barrett, et ses deux fils, Angus et Silas, que j'ai eu le privilège de mettre au monde, espèrent maintenant qu'au Canada, on empêchera que d'autres pères disparaissent alors que nous sommes si près d'une cure.  
Phillip Borsos était l'un des meilleurs cinéastes du pays. 
Il a ensuite réalisé The Grey Fox et Un été pourri, ainsi que Un noël magique, Bethune et Yellow Dog, avant de mourir à l'âge de 40 ans.  
Déjà, la Fondation sur la recherche sur la leucémie a fait d'énormes progrès et elle poursuit la lutte pour sensibiliser davantage les gens. 
En juin dernier, durant le mois désigné par le Leukemia Research Fund of Canada comme le mois de sensibilisation à la leucémie, les drapeaux de l'organisme flottaient au-dessus des mairies de toutes les grandes villes du pays.  
Nous croyons que si ce mois de sensibilisation était désigné par la Chambre, il deviendrait encore plus important. 
On sensibiliserait davantage les gens. 
L'objectif de cette stratégie de sensibilisation est de faire savoir aux gens que la leucémie doit être éliminée et que le Leukemia Research Fund of Canada existe justement à cette fin.  
Le fait pour nous de désigner le mois de juin comme mois de sensibilisation à la leucémie sera très utile à cet égard. 
J'espère que les députés appuieront cette mesure.  
