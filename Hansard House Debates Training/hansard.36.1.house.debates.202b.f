-Monsieur le Président, je prends la parole pour présenter le projet de loi prévoyant la reprise et le maintien des services gouvernementaux. 
Mais je désire d'abord informer la Chambre que les appels du gouvernement à la raison ont été entendus.  
Je suis extrêmement heureux d'informer la Chambre que les efforts conjoints de nos négociateurs et de ceux de l'Alliance de la fonction publique du Canada ont conduit ce soir à une entente de principe pour les 14 000 cols bleus...  
