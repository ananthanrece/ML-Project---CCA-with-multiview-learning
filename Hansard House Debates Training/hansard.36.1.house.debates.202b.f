L'hon. Marcel Massé:
Je suis très heureux d'annoncer à la Chambre que, ce soir, il y a tout juste quelques heures, grâce aux efforts des négociateurs du gouvernement et du syndicat, nous avons conclu une entente de principe avec les cols bleus en grève.  
Cet accord de principe me paraît équitable et généreux. 
Cet accord de dernière minute ne doit pas cependant nous détourner des raisons qui nous font siéger à une heure aussi tardive. 
Depuis 10 semaines déjà, les Canadiens de toutes les régions du pays sont victimes des grèves tournantes menées par les membres de l'Alliance. 
Les effets de ces grèves non seulement préoccupent le gouvernement, ils ont fait l'objet d'un débat d'urgence dans cette Chambre, la semaine dernière.  
Cet accord de principe ne garantit pas que les grèves prendront fin. 
Les membres du syndicat pourront entériner cet accord, comme le rejeter. 
C'est le prix à payer pour le respect du droit de grève, un droit démocratique qui fait partie des mécanismes de la négociation collective.  
Un gouvernement responsable ne peut toutefois pas attendre la décision des syndiqués et nous devons donc poursuivre notre travail pour assurer les Canadiens du retour à la normale des services du gouvernement fédéral assurés par les cols bleus, ainsi que le maintien de ceux fournis par les agents correctionnels dans les pénitenciers fédéraux.  
Au cours des derniers mois, le Secrétariat du Conseil du Trésor a signé de nombreuses ententes collectives avec plus de 87 p. 100 de ses employés. 
Le gouvernement du Canada a fait preuve, à maintes reprises, et encore ce soir, de son respect pour le processus de la négociation collective. 
Et ce soir, le gouvernement demande au Parlement d'imposer un retour au travail et une convention collective à ses 14 000 cols bleus.  
Nous demandons aussi au Parlement l'adoption de mesures qui pourraient s'avérer nécessaires pour que les quelque 4 500 agents correctionnels demeurent à leur poste et reprennent la négociation le plus rapidement possible.  
Nous voulons éviter une grève, et l'absence de services correctionnels fonctionnels constituerait une menace autant à la sécurité des prisonniers qu'à celle des Canadiens. 
Après 10 semaines, l'impact de ces débrayages sur l'ensemble des Canadiens et sur les opérations du gouvernement se fait sentir.  
Ni les Canadiens ni le gouvernement ne pouvaient tolérer plus longtemps que le transport des passagers soit perturbé dans nos aéroports. 
Nous ne pouvons pas accepter de voir la perception de l'impôt et de la TPS devenir encore plus difficile. 
Les députés devraient savoir qu'un million de contribuables recevront leur remboursement d'impôt en retard en raison de ces grèves.  
En termes simples, de nombreuses familles à revenu modeste et de nombreuses petites entreprises devront attendre les remboursements auxquels ils ont droit et dont ils ont un urgent besoin.  
Cela, c'est sans parler des plus vulnérables de notre société, qui comptent chaque année sur les services gratuits des cliniques d'impôt de Revenu Canada. 
Les grèves tournantes ont empêché bien des Canadiens de recourir à ces services.  
Les piquets de grève et l'interruption des services par les travailleurs en grève ont passablement nui aux activités de la défense nationale, de la garde côtière et des travaux publics. 
La grève s'attaque aussi à nos exportations de grains, menaçant un secteur important de l'économie canadienne ainsi que nos relations commerciales internationales. 
Dans le port de Vancouver, des dizaines de bateaux attendent d'être chargés...  
