LA LOI SUR LA DÉFENSE NATIONALE 
-Je serai très bref, monsieur le Président. 
Le projet de loi C-25, Loi modifiant la Loi sur la défense nationale et d'autres lois en conséquence, nous a été renvoyé par le Sénat avec des amendements.  
Ce projet de loi a été présenté à la Chambre il y a un an aujourd'hui. 
Au nom du gouvernement et des hommes et des femmes qui font partie du ministère de la Défense nationale et des Forces armées canadiennes, je tiens à féliciter les sénateurs pour leur examen rigoureux du projet de loi.  
Cette mesure législative, qui est un élément clé du programme de transformation complet du ministère et de l'armée, est très attendu par les hommes et les femmes qui font partie de ces organisations.  
Le Sénat a proposé un amendement à l'article 96 du projet de loi C-25. 
Cet article prévoit un examen des dispositions et de l'application de la loi dans les cinq ans suivant son entrée en vigueur et le dépôt du rapport d'examen devant chaque chambre du Parlement. 
L'amendement du Sénat modifie cette disposition de manière à préciser qu'il y aura un examen indépendant tous les cinq ans et que le rapport sera déposé devant chaque chambre du Parlement. 
Le gouvernement est prêt à appuyer cet amendement.  
M. Art Hanger (Calgary-Nord-Est, Réf.): 
Monsieur le Président, je me réjouis de l'occasion qui m'est offerte de participer au débat sur le projet de loi C-25 et la proposition d'amendement présentée par le Sénat.  
Comme les députés le savent sans doute, le projet de loi C-25 constitue la première modification apportée à la Loi sur la défense nationale depuis 1950.  
Il est temps que le gouvernement introduise des changements importants afin de permettre au ministère de la Défense nationale de consolider ses fondements déjà bien ancrés qui en font une des institutions les plus importantes du Canada. 
Le projet de loi C-25 est au mieux une mesure d'ordre administratif. 
Il ne s'attaque pas aux problèmes inhérents au système de justice militaire. 
Il ne prend pas en compte les graves lacunes qu'a fait ressortir l'enquête sur la Somalie. 
Il ne prévoit pas d'inspecteur général. 
Il y aura tout au plus une modeste surveillance par un organisme indépendant des opérations des Forces canadiennes.  
Il y a des décennies que les Forces canadiennes sont victimes de la négligence des gouvernements libéraux qui se sont succédé. 
Sous-financement chronique, pénurie d'équipement, bureaucratisation excessive, rectitude politique, système de justice militaire obsolète, voilà autant de facteurs qui contribuent à saper les fondements de cette fière institution.  
Je n'entrerai pas dans les détails aujourd'hui, mais j'exhorte les députés de la Chambre et tous ceux et toutes celles qui suivent ce débat de lire le rapport minoritaire de l'opposition officielle sur la qualité de vie dans l'armée. 
Notre position y est clairement exposée.  
L'opposition officielle ne souscrit pas au projet de loi C-25 parce qu'il ne traite pas des vrais problèmes que connaissent les Forces canadiennes.  
Le projet de loi C-25 est une occasion manquée de renouveler les Forces canadiennes. 
Nous ne trouvons rien à redire à la proposition d'amendement de l'autre Chambre. 
Le problème, c'est que le projet de loi laisse grandement à désirer.  
M. René Laurin (Joliette, BQ):
Monsieur le Président, lors de la présentation du projet de loi, le Bloc québécois s'était prononcé contre celui-ci pour plusieurs raisons.  
La principale était qu'il nous apparaissait que ce projet de loi n'améliorait pas suffisamment la situation pour permettre aux militaires de voir leurs droits respectés devant les tribunaux, particulièrement en ce qui concerne la procédure sommaire des tribunaux militaires et de la cour martiale. 
Un militaire, par exemple, n'avait que 24 heures pour décider s'il choisissait la procédure sommaire ou la procédure devant la cour martiale. 
Ces éléments-là se trouvent toujours dans le projet de loi et n'ont pas été modifiés par l'amendement. 
Les droits de la personne des militaires sont, à notre avis, largement bafoués.  
On en a eu un autre exemple cette semaine. 
Je lisais dans un journal qu'un militaire s'est vu traduire devant une cour militaire parce qu'il a refusé de recevoir un vaccin contre l'anthrax. 
Si c'est cela respecter les droits de la personne, nous ne sommes pas d'accord.  
Il doit le discipliner et il doit souvent porter un jugement sur une décision qu'il a lui-même prise à l'égard d'un militaire. 
Il peut s'agir d'une situation dans laquelle il est lui-même impliqué.  
Ces situations ne sont pas corrigées par l'amendement. 
Tout ce que fait l'amendement proposé aujourd'hui et que le gouvernement est prêt à adopter, c'est d'augmenter le nombre de rapports qui seront déposés à la Chambre. 
Il augmente le nombre de rapports, ce qui est déjà une amélioration, on le reconnaît.  
On parle d'un rapport indépendant, sans qualifier ou préciser ce que cela veut signifie. 
Qui nommera les personnes devant préparer ce rapport? 
Seront-elles nommées par le ministère ou par le gouvernement? 
Encore là, il peut s'agir d'un rapport qui ne sera pas vraiment indépendant, qui sera fait par le gouvernement. 
Y aura-t-il des militaires qui siégeront sur le comité chargé de produire ces rapports?  
Nous n'avons aucune garantie que le terme «indépendant» viendra aider les militaires à obtenir un plus grand respect de leurs droits lorsqu'ils sont impliqués dans une situation litigieuse.  
Pour ces raisons, nous ne pourrons malheureusement pas appuyer l'amendement. 
Les mêmes problèmes que nous avions évoqués lors de la présentation du projet de loi ne sont pas corrigés par cet amendement. 
Par conséquent, le Bloc québécois votera contre cet amendement.  
