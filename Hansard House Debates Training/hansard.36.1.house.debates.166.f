Mme Eleni Bakopanos (secrétaire parlementaire du ministre de la Justice et procureur général du Canada, Lib.):
Monsieur le Président, je suis heureuse de lancer le débat sur la motion de troisième lecture du projet de loi C-57, Loi portant modification de la Loi sur le Nunavut relativement à la Cour de justice du Nunavut et modifiant diverses lois en conséquence.  
Le projet de loi vise à tenir la promesse que le gouvernement fédéral a faite aux gens du Nunavut de présenter en temps opportun une mesure législative tendant à instituer un tribunal de première instance à palier unique au Nunavut.  
L'établissement d'une cour de ce genre conçue précisément pour répondre aux besoins des habitants du Nord s'inscrit dans le rêve plus large des Inuit de l'est de l'Arctique de décider de leur propre avenir. 
Je suis très fière aujourd'hui de participer à un processus qui va aider les gens du Nunavut à réaliser ce rêve. 
Nous participons tous à un nouveau processus historique aujourd'hui.  
Les témoins qui ont comparu devant le Comité permanent de la justice et des droits de la personne de la Chambre des communes ont vanté le processus de consultation qui a conduit à l'élaboration du projet de loi C-57.  
Comme Mme Rebecca Williams, sous-ministre adjointe désignée pour le Nunavut l'a dit si éloquemment: 
Le processus que nous avons utilisé pour établir la Cour de justice du Nunavut était révolutionnaire, car il faisait participer les Inuit. 
Ces derniers ont pu préciser de quels styles de systèmes ils avaient besoin pour faire face aux conflits et pour travailler pour la paix et des collectivités sûres à l'avenir. 
Le processus lui-même m'a donné espoir dans l'avenir de notre gouvernement public au Nunavut. 
J'ai appris, dans le cadre de ce processus, des façons de mettre fin à notre silence.  
Il faut louer leur travail.  
L'élaboration du projet de loi C-57 marque également le début d'une nouvelle phase dans le partenariat entre le gouvernement fédéral et les gens du Nunavut. 
On reconnaît de façon concrète que la réforme du système de justice pénale au Nunavut maintenant et à l'avenir doit répondre aux besoins et aux souhaits des gens du Nunavut.  
Je suis très heureuse d'annoncer que lorsque la ministre de la Justice s'est présentée devant le comité permanent, la semaine dernière, le Parti réformiste, le Bloc québécois et le Parti progressiste-conservateur ont indiqué à l'honorable ministre qu'ils appuyaient le projet de loi C-57.  
Je tiens à demander à tous les députés de cette Chambre d'appuyer ce projet et d'aider ainsi le peuple du Nunavut à réaliser son rêve d'établir, avant le 1 er avril 1999, une cour de première instance à palier unique.  
Étant donné l'examen exhaustif du projet de loi C-57 par le Comité permanent de la justice et des droits de la personne de la Chambre des communes, je n'ai pas l'intention de discuter en détail des dispositions du projet de loi. 
Toutefois, j'aimerais indiquer les grandes lignes du projet, puisqu'elles font ressortir certains aspects qui sont uniques à la Cour de justice du Nunavut.  
Une des caractéristiques très importantes de la Cour de justice tient à ce que, à la différence des autres juridictions au Canada, les juges de cette cour seront habilités à entendre toutes les instances. 
Une modification à la Loi sur le Nunavut prévoit qu'un juge de la Cour de justice du Nunavut peut exercer les pouvoirs et fonctions que toute loi applicable au Nunavut peut conférer à un représentant de l'autorité judiciaire.  
Les modifications apportées dans une partie distincte du Code criminel prévoient qu'un juge de la Cour de justice du Nunavut aura tous les pouvoirs et fonctions de toutes les cours et tous les représentants de l'autorité judiciaire prévus au Code criminel.  
Un autre aspect important du nouveau tribunal tient à ce que l'on confère à la Cour de justice du Nunavut la compétence d'une cour supérieure. 
En raison des exigences de la prestation de la justice dans le Nord, les juges de la Cour de justice du Nunavut vont nécessairement s'acquitter de la plupart des fonctions normalement confiées aux autres représentants de l'autorité judiciaire. 
Toutefois, ces juges exerceront ces fonctions en leur qualité de juges de la Cour supérieure et auront tous les pouvoirs inhérents aux juges de la Cour supérieure.  
Comme les juges résideront au Nunavut et auront de fréquents contacts avec les diverses collectivités du Nunavut, je ne doute pas que les habitants du Nunavut constateront qu'ils ont plus facilement accès à la justice. 
Je m'attends à ce que les gens constatent aussi qu'ils peuvent confier leurs problèmes à des juges qui connaissent bien leur culture, leurs valeurs et leurs besoins.  
Je suis heureuse de dire que les modifications proposées au Code criminel en vue d'établir un tribunal de première instance à palier unique ne restreignent nullement les droits des parties devant la Cour.  
Ce respect de l'équité se reflète par exemple tant dans les dispositions relatives aux appels que dans le nouveau régime de révision judiciaire conçu pour assurer la révision de certaines décisions clés du processus de justice pénale.  
Le projet de loi C-57 maintient toute l'étendue et la portée des droits d'appel en matière sommaire pour toutes les poursuites en matière sommaire instruites au Nunavut. 
Pour ce qui est des modifications apportées aux lois, les honorables députés qui suivent de près les aspects les plus techniques liés au tribunal à palier unique se rendront compte qu'il ne sera pas possible de réviser les décisions du tribunal du Nunavut au moyen de brefs de prérogative, contrairement aux décisions des tribunaux provinciaux et territoriaux, étant donné qu'il s'agira d'un tribunal supérieur.  
Pour compenser, les dispositions de ce projet de loi créeront un nouveau mode de révision judiciaire pour certaines décisions importantes dans la procédure de justice pénale. 
Comme pour les appels en matière de procédure sommaire, l'examen sera effectué par un seul juge de la Cour d'appel et on pourra en appeler de sa décision à une section de la Cour d'appel composée de trois juges.  
En décrivant en détail les caractéristiques du mode de révision judiciaire, j'insiste sur l'un des objectifs importants de cette révision, soit celui d'assurer que les habitants du Nunavut ne soient pas traités comme des citoyens de deuxième classe, en ce qui concerne les droits qui leur sont reconnus par les tribunaux.  
Ce sont ces caractéristiques qui permettent au gouvernement d'affirmer, dans le sommaire figurant à la première page du projet de loi C-57, que: 
...le texte institue un tribunal de première instance à palier unique pour le territoire du Nunavut, afin d'y établir un système judiciaire efficace, accessible et propre à répondre aux besoins particuliers du territoire, tout en garantissant à ses justiciables les mêmes droits substantiels et procéduraux que ceux des autres Canadiens. 
Un dernier thème du projet de loi C-57 a trait aux modifications de la Loi sur les juges nécessaires pour permettre le fonctionnement d'un tribunal de première instance à palier unique au Nunavut, au niveau de la cour supérieure.  
Les modifications prévues à la Loi sur les juges prévoient la nomination de trois juges de la Cour supérieure à la Cour de justice du Nunavut. 
Comme je le disais tout à l'heure, tous ces juges habiteront le territoire.  
La ministre de la Justice tient à nommer à la cour du Nunavut des candidats qualifiés, expérimentés et qui s'intéressent au Nord. 
La ministre s'est engagée à la Chambre à consulter la population du Nord pour s'assurer que les nominations répondent aux exigences particulières du Nunavut et tiennent compte de la culture et des conditions propres à la région.  
L'établissement d'un tribunal de première instance à palier unique au Nunavut est le point de départ de l'élaboration d'un système de justice pénale répondant aux besoins des gens qu'il vise à servir.  
Le ministère de la Justice tient à travailler en étroite collaboration avec la commission provisoire du Nunavut, les organismes du Nunavut et le nouveau gouvernement du Nunavut, quand il sera établi, pour favoriser le plus possible l'adaptation du système judiciaire aux besoins des habitants du Nunavut.  
Par exemple, un point très important a trait à la formation des juges de paix afin qu'ils soient capables de compléter le travail de la Cour de justice du Nunavut en résolvant des affaires moins graves au niveau local. 
Bien que la formation des juges de paix constitue une responsabilité territoriale, le ministère de la Justice fera tout ce qu'il peut pour aider le nouveau territoire dans cette tâche importante.  
L'utilisation de comités de justice communautaire et de comités de justice pour la jeunesse est un autre aspect de l'administration de la justice qui continuera d'être important dans le Nord. 
Il incombera au nouveau territoire d'assurer le maintien du financement des deux types de comité.  
Les comités de justice pour la jeunesse sont beaucoup utilisés dans l'île de Baffin et s'occupent des jeunes au cours de la période précédant l'inculpation. 
Ils sont également reconnus comme mesures de rechange prévues par la Loi sur les jeunes contrevenants. 
Les comités de justice pour la jeunesse et les comités de justice communautaire ont été et continueront d'être des éléments clés du système judiciaire au Nunavut.  
En établissant un tribunal de première instance à palier unique, on espère énormément que cette structure judiciaire répondra mieux aux besoins des habitants du Nunavut, les gens qu'il est censé servir.  
Le nouveau système proposé dans le projet de loi C-57 est sans précédent. 
Il sera par conséquent nécessaire de surveiller et d'évaluer le système dans les années à venir pour veiller à ce qu'il atteigne l'objectif d'assurer un système judiciaire efficace et accessible dans le Nord. 
Le ministère de la Justice collabore avec les services du commissaire intérimaire pour mettre au point un système de surveillance et d'évaluation visant à identifier les problèmes et les changements qu'il pourrait être nécessaire d'apporter au système judiciaire.  
La création du Nunavut, dans quatre mois à peine, constitue un événement considérable dans l'histoire canadienne. 
Elle marque la réalisation d'un rêve vieux de 25 ans pour les Inuit de l'Arctique oriental. 
Elle marque la création du premier tribunal à un seul niveau pour les questions criminelles, civiles et familiales au Canada. 
Elle marque un nouveau chapitre dans le partenariat entre les Inuit et le gouvernement fédéral. 
Comme un témoin l'a fait remarquer au comité permanent la semaine dernière, elle marque le début d'une nouvelle ère empreinte d'espoir pour les habitants du Nunavut.  
Le témoignage livré devant le comité permanent la semaine dernière par Mlle Leena Evic-Twerdin, conseillère politique de la société Nunavut Tunngavik Incorporated, est digne de mention à cet égard. 
Je cite: 
Aujourd'hui, nous amorçons le processus de réaffirmation de notre identité et de nos coutumes. 
C'est l'occasion qui s'offre à nous qui nous permet d'amorcer le processus. 
