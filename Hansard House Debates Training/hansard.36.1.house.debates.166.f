Je trouve admirable de voir qu'il a encore la force de se tenir sur ses jambes le vendredi pour participer au débat sur sa motion d'initiative parlementaire. 
Le député de Davenport et moi divergeons d'opinion sur la manière d'assurer un environnement sans pollution, mais nous nous entendons cependant sur l'importance d'avoir un environnement propre.  
La motion est la suivante: 
Que, de l'avis de la Chambre, le gouvernement devrait agir avec détermination, devant les résultats du Rapport de l'évaluation des contaminants dans l'Arctique canadien, afin d'éliminer les polluants organiques persistants, en faisant progresser le protocole sur les POP.  
Pour bien comprendre la motion, je crois qu'il convient d'expliquer l'origine du protocole sur les POP. 
On peut dire que ce protocole a été ébauché au moment de la création de la Commission économique des Nations Unies pour l'Europe, un forum où les pays d'Amérique du Nord, d'Europe de l'Ouest, d'Europe centrale, d'Europe de l'Est et d'Asie centrale se réunissent pour se doter d'outils de coopération économique.  
Ce grand regroupement de pays qu'unissent des liens historiques étroits, compte pour 64 p. 100 de la production mondiale et est également responsable de 60 p. 100 des émissions mondiales de dioxyde de carbone. 
Ces faits illustrent les responsabilités de chaque région envers ses propres habitants et envers ceux des autres régions du monde. 
C'est dans ce contexte que la Commission économique pour l'Europe des Nations Unies exerce ses activités. 
La CEE se veut une tribune pour un dialogue visant à amener les pays membres à s'entendre sur des lignes directrices et politiques communes. 
Lorsque des accords sont négociés et que des activités d'aide sont préparées, sa principale mission consiste à harmoniser les politiques et pratiques de ses pays membres.  
La CEE compte plusieurs divisions différentes, dont l'une s'occupe de l'environnement et des établissements humains. 
Un des nombreux organes de cette division est l'organe exécutif pour la Convention sur la pollution atmosphérique transfrontalière à longue distance. 
Cette convention a été rédigée après que des scientifiques ont examiné le lien entre les émissions de soufre en Europe continentale et l'acidification des lacs en Scandinavie et ont ensuite étudié la possibilité que les polluants atmosphériques parcourent plusieurs milliers de kilomètres avant de se déposer et de causer des dommages. 
Cela voulait dire qu'une collaboration à l'échelle internationale était nécessaire pour résoudre les problèmes tels que l'acidification.  
La convention a été le premier instrument international entraînant des obligations juridiques visant à résoudre les problèmes de pollution atmosphérique sur une grande échelle. 
Elle a été signée en 1979 et est entrée en vigueur en 1983. 
Depuis son entrée en vigueur, on y a ajouté sept protocoles, dont celui sur les polluants organiques persistants. 
L'organe exécutif a adopté le protocole sur les polluants organiques persistants le 24 juin 1998, au Danemark. 
Ce protocole est fondé sur une liste de 16 substances qui ont été choisies d'après des critères de risque convenus. 
La liste compte onze pesticides, deux produits chimiques industriels et trois sous-produits contaminants. 
L'objectif ultime est l'élimination de toute décharge, émission ou déversement de POP.  
Il restreint sévèrement l'utilisation du DDT, du HCH et des PCB. 
Il comporte des dispositions concernant les rebuts des produits qui deviendront interdits. 
Il oblige en outre les parties à réduire les émissions de dioxine, de furanes, de HPA et de HCB sous les seuils de 1990. 
Il établit des valeurs limites précises concernant l'incinération de déchets dangereux ou biomédicaux par les municipalités.  
Le Canada a été l'hôte de la première de cinq séances de négociation devant se tenir au cours des deux prochaines années, et qui aboutiront éventuellement à un traité d'application obligatoire d'ici l'an 2000. 
Qu'est-ce que cela signifie pour le Canada? 
La région canadienne de l'Arctique a déjà été considérée comme une zone vierge en raison de son éloignement et de sa faible densité de population. 
Toutefois, au cours des 50 dernières années, le Nord a été exposé à des contaminants provenant de sources locales, par exemple l'industrie lourde, et de régions lointaines du monde, en raison des vents et des courants marins.  
Nous sommes donc aux prises avec un gros dilemme. 
Beaucoup de contaminants préoccupants dans le cas de l'Arctique sont des pesticides et des produits chimiques industriels qu'on n'utilise plus au Canada et qui, bien souvent, sont interdits ou d'usage restreint dans la plupart des pays développés du monde. 
Toutefois, on continue à s'en servir dans les pays en développement, et l'on peut ainsi les trouver dans les lacs, les rivières et la neige du Canada. 
Il importe que les pays en développement soient signataires de tout protocole de réduction de la pollution dans le monde. 
Le Canada a agi en chef de file dans l'évaluation de la pollution atmosphérique à longue distance, mais il faudra une coopération à l'échelle mondiale pour évaluer adéquatement le problème de la pollution.  
Les écosystèmes de l'Arctique sont très fragiles. 
La nature de ces produits chimiques est telle qu'ils ont tendance à s'accumuler dans les tissus des organismes vivants. 
On a trouvé dans les animaux qui se situent au sommet de la chaîne alimentaire des niveaux très élevés de ces produits, ce qui n'est pas sans inquiéter les Inuit. 
Ceux-ci dépendent beaucoup des produits de leur chasse et de leur pêche, comme le poisson, le phoque et le caribou, non seulement pour leur subsistance, mais aussi pour le maintien de leur culture.  
S'il fallait qu'ils s'en passent, les conséquences socio-économiques seraient énormes. 
À cause de leur grande dépendance envers les aliments traditionnels récoltés dans la nature, les autochtones du Nord sont susceptibles d'être exposés aux effets nocifs possibles de ces polluants. 
Bien que le rapport sur l'évaluation des polluants dans l'Arctique canadien ait conclu qu'il n'y avait pas de menace immédiate pour la santé des adultes, on craint pour la santé des foetus et des jeunes enfants.  
Cette motion n'est pas la bonne façon de régler le problème. 
Le Nord canadien n'est pas contaminé seulement par les POP. 
La motion n'est qu'une solution très partielle à un problème beaucoup plus grand. 
Le gouvernement n'a pas encore dit aux Canadiens ce qu'il compte faire des sites contaminés situés sur les terres fédérales.  
La semaine dernière, le vérificateur général a révélé que le gouvernement n'avait pas la moindre idée de ce qu'il allait faire avec les quelque 5 000 sites contaminés situés sur les terres publiques. 
Il n'a pas de vue d'ensemble des risques éventuels que présentent ces sites pour la santé et l'environnement. 
Il n'a même pas une idée précise et complète du passif éventuel ou réel relatif à ces sites.  
