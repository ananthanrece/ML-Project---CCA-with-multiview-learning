M. Ghislain Fournier (Manicouagan, BQ):
Monsieur le Président, en tant que député membre associé du Comité permanent de la Chambre des communes sur les affaires indiennes et du Nord pour le Bloc québécois, il me fait plaisir aujourd'hui de prendre de nouveau la parole sur le projet de loi C-57, Loi portant modification de la Loi sur le Nunavut relativement à la Cour de justice du Nunavut et modifiant diverses lois en conséquence.  
Ma position et mes impressions par rapport au projet de loi C-57 ne diffèrent pas vraiment des commentaires déjà émis lors de la deuxième lecture de ce projet de loi. 
Le projet de loi C-57 vise à modifier le Code criminel afin qu'il prenne en compte les réalités du nouveau territoire du Nunavut et permette l'établissement d'un gouvernement opérationnel avant le 1 er avril 1999, date consacrant la naissance officielle du territoire. 
On comprend donc toute l'importance de rendre cette loi effective afin que la population du Nunavut puisse être prête et dispose de tous les outils nécessaires pour relever le défi que représente un nouveau gouvernement.  
Le projet de loi C-57 s'inscrit dans le processus enclenché en 1992 par l'Accord sur les revendications territoriales qui fixaient le cadre juridique et politique du nouveau territoire du Nunavut. 
Sanctionnée en 1993, la création de ce dernier s'est vu appuyer par le projet de loi C-39 dont nous avons traité à la Chambre l'an dernier et qui permet la tenue d'élections législatives, tout en facilitant la transition et en légitimant le processus. 
Le projet de loi C-57 s'inscrit fort bien dans ce continuum. 
Il constitue, en quelque sorte, la dernière pierre à l'édifice politico-légal permettant aux habitants de ce territoire d'être fin prêts pour avril 1999. 
Nous sommes au seuil de 1999. Il est grand temps de remettre tous les outils politiques et, plus particulièrement pour le cas qui nous intéresse aujourd'hui, les instruments législatifs, entre les mains des habitants du Nunavut. 
Ceci leur permettra d'avoir une instance judiciaire répondant à leurs besoins et étant plus prêts d'eux. 
La mise en place de ce tribunal de première instance que prévoit le projet de loi C-57 est une composante essentielle des efforts pour l'obtention d'une pleine autonomie gouvernementale.  
Depuis l'entrée de Terre-Neuve dans la Confédération, en 1949-j'ai dit Confédération, j'aurais dû dire fédération, parce que les gouvernements fédéraux qui se sont succédé ici ont détruit, et sans consultation, la Confédération pour instaurer et mettre en place une fédération.  
De toute façon, les frontières du Canada, depuis 1949, n'ont subi aucune modification. 
Ceci démontre bien à quel point la création du Nunavut est un moment historique et explique l'importance que l'établissement de ce nouveau territoire revêt pour la population qui y vit. 
Les territoires du Grand Nord ouest se retrouvent ainsi divisés en deux entités distinctes.  
Le Nunavut couvre environ deux millions de kilomètres carrés et se situe au nord du 60e parallèle. 
Il est divisé en trois régions et comprend 28 collectivités. 
Quatre-vingt pour cent de la population est inuit. 
Nunavut est, d'ailleurs, un joli mot de leur langue qui signifie, «Notre terre».  
Il va sans dire que les réalités du Grand Nord canadien et des collectivités qui y habitent sont différentes de celles que nous connaissons dans nos grandes villes. 
La superficie du territoire, à elle seule, explique la nécessité d'adopter des outils légaux plus adaptés à leurs besoins et à leurs réalités. 
Je crois fermement que le projet de loi C-57 répond à cette nécessité.  
En instituant un tribunal de première instance à palier unique, et en lui attribuant des compétences élargies, les magistrats seront plus polyvalents et plus aptes à répondre aux besoins spécifiques des populations au Nunavut.  
Je tiens donc à redire que le Bloc québécois est favorable au projet de loi C-57 qui permet à ces milliers d'autochtones de franchir d'autres étapes vers une autonomie gouvernementale solide, viable et plus près de leurs besoins.  
On sait que le projet de loi C-39, adopté par la Chambre l'an dernier, permet aux Inuits du Nunavut d'administrer leurs terres via une assemblée législative élue au suffrage universel. 
Par le projet de loi C-39, une transition est assurée et les pouvoirs de l'administration des gouvernements fédéral et territorial sont dévolus au territoire du Nunavut. 
Cette loi a aussi modifié la Loi constitutionnelle de 1867, afin que la population du territoire soit représentée à la Chambre des communes et au Sénat.  
Les représentants des Inuits du Nunavut pourront ainsi, dès la création effective du territoire, servir leur population à l'aide d'une assemblée législative opérante.  
Malgré les nombreuses facettes du projet de loi C-39, on peut aisément comprendre que certains aspects doivent encore être traités, afin de favoriser le bon fonctionnement politique et législatif du nouveau territoire. 
Afin d'être fin prêt pour le 1 er avril 1999, le territoire du Nunavut doit maintenant avoir en main tous les outils législatifs. 
C'est ce que vise le projet de loi C-57, et nous l'appuyons. 
Le transfert de certaines juridictions des gouvernements territorial et fédéral vers le Nunavut n'est pas simple, mais ce transfert est néanmoins essentiel et répond à la réalité du Grand Nord.  
La législation institue un tribunal de première instance à palier unique pour le territoire du Nunavut. 
L'objectif de la création de ce territoire, qu'on appelle désormais la Cour de justice du Nunavut, est d'établir un système judiciaire efficace, essentiel et apte à répondre aux besoins particuliers du territoire, tout en garantissant à ses justiciables les mêmes droits que ceux dont jouissent les autres Canadiens. 
Concrètement, dans le nouveau territoire du Nunavut, il y aura désormais la Cour de justice du Nunavut. 
Cette cour de justice remplacera la Cour suprême des Territoires du Nord-Ouest, à titre de cour de juridiction supérieure, et remplacera la Cour territoriale, à titre de cour de juridiction inférieure. 
Le projet de loi C-57 se trouve donc à modifier, une fois de plus, la Loi sur le Nunavut, adoptée en 1993, lorsque le Parti progressiste-conservateur était au pouvoir.  
On modifie aussi la Loi sur les juges, afin de prévoir la nomination de trois juges de juridiction supérieure à la Cour de justice.  
Cette législation insère aussi au Code criminel des mécanismes de procédure adaptés à la Cour de justice et précise une panoplie d'éléments légaux. 
Nous croyons que la création de cette Cour de justice permettra un traitement judiciaire souple et efficace sur l'ensemble du territoire ciblé. 
En rendant cette Cour compétente pour entendre toute affaire, tant les affaires mineures que les infractions criminelles graves, on permet à la population du territoire d'avoir accès à un service plus près de ses réalités.  
Dorénavant, lorsqu'un magistrat se rendra dans un petit village du Nunavut, il sera plus polyvalent. 
Il faut comprendre que la multiplicité des juridictions, c'est-à-dire la complexité de l'organisation judiciaire qui est utile dans les grands centres urbains à forte densité de population, ne sert pas nécessairement à la bonne administration de la justice sur un territoire comme celui du Nunavut. 
C'est pour cette raison qu'il importe de légiférer sur la question et de permettre les modifications nécessaires aux lois qui, jusqu'à présent, octroyaient à des juridictions différentes les compétences pour entendre les diverses causes.  
Le projet de loi C-57 apporte les changements nécessaires à la mise en place et au bon fonctionnement de cette Cour de justice et répond bien aux volontés des artisans de la mise en place de l'autonomie gouvernementale.  
J'ajoute que nous sommes favorables au projet de loi et que nous continuerons à appuyer le principe des démarches qui, comme le projet de loi C-57, permettent aux peuples de retrouver les outils nécessaires à leur plein épanouissement.  
