L'ENVIRONNEMENT 
L'hon. Charles Caccia (Davenport, Lib.) propose: 
Que, de l'avis de la Chambre, le gouvernement devrait agir avec détermination, devant les résultats du Rapport de l'évaluation des contaminants dans l'Arctique canadien, afin d'éliminer les polluants organiques persistants, en faisant progresser le protocole sur les POP.  
Pour répondre brièvement, certaines substances polluantes nocives, qui se déplacent sur de longues distances, se retrouvent dans l'Arctique. 
Le gouvernement du Canada collabore de façon constructive avec d'autres gouvernements pour en arriver à des ententes visant à réduire ces substances polluantes, ainsi qu'à protéger la santé humaine, l'environnement, la faune et les habitants de l'Arctique.  
On appelle POP ces substances polluantes. 
Il s'agit de pesticides utilisés dans l'agriculture, de produits chimiques comme les BPC, de produits comme les dioxines chlorées, les furanes, les hydrocarbures aromatiques polycycliques ou HAP, qui découlent de la combustion de combustibles fossiles, du brûlage et de la transformation de bois et d'autres matières.  
On pourrait se demander pourquoi la signature du protocole sur les polluants organiques persistants ou POP est importante et urgente? 
C'est que 16 pays sont tenus de ratifier le protocole pour assurer l'entrée en vigueur de l'entente visant à réduire les polluants organiques. 
On me dit que les pays intéressés pourront le signer après le 21 décembre 1998. 
Le Rapport de l'évaluation des contaminants dans l'Arctique canadien, produit l'année dernière, décrit la situation qui existe dans l'Arctique.  
Dans ce rapport, nous lisons que les polluants organiques persistants qui ne proviennent pas du Canada et de l'Arctique se déplacent dans l'atmosphère pour arriver jusque dans l'Arctique. 
Ces polluants s'infiltrent dans la chaîne alimentaire de l'Arctique et entrent dans les aliments traditionnels des autochtones. 
Ainsi, des niveaux élevés des pesticides toxaphène et chlordane ont été trouvés dans le muktuk de bélouga et dans la viande de phoque. 
Bien souvent nos Inuit, même s'ils mangent très peu de viande provenant de sources traditionnelles, peuvent ingérer une quantité de contaminants qui dépasse le seuil jugé tolérable par Santé Canada.  
Il est important de noter que ni le toxaphène ni le chlordane ne sont utilisés au Canada.  
Il est aussi troublant de lire dans ce rapport que la concentration de BPC dans le lait maternel des Inuit est une des plus élevées au monde. 
La teneur en BPC du sang d'au moins 40 p. 100 des femmes inuit est jusqu'à cinq fois le niveau jugé inquiétant par le ministère de la Santé.  
De toute évidence, la santé de l'être humain dépend de ce que nous faisons des polluants et, dans le cas de l'Arctique, de ces POP. 
Dans le sud du Canada, le professeur Jules Blais de l'Université d'Ottawa, dont les résultats de recherche sont parus dans la revue Nature , en octobre 1998, fait état de quantités troublantes de pesticides aéroportés jusqu'aux Rocheuses. 
Le professeur Blais a relevé, dans le champ de glace Columbia et au lac Louise, d'infimes quantités de lindane, un insecticide agricole utilisé dans les Prairies; d'endosulfan, un insecticide populaire dans l'est du Canada; de chlordane, un termicide interdit; et même de DDT, un pesticide interdit au Canada, mais encore utilisé en Chine, au Mexique et ailleurs dans le monde.  
En Suisse, des scientifiques se disent aussi alarmés, parce que certaines villes puisent leur eau dans des lacs situés dans les hautes montagnes.  
Tout cela reflète les problèmes causés par les POP et auxquels sont confrontés les populations et les gouvernements sur divers continents.  
Pour le gouvernement du Canada il devient donc nécessaire de contrôler les émissions de ces POP que nous continuons de produire à la maison, comme les dioxines et les furanes. 
Tant que nous continuerons de les produire, les POP entreront dans l'écosystème et dans la chaîne alimentaire.  
Plus nous polluons, plus le prix à payer est élevé en termes de coûts de santé. 
Beaucoup de POP, nous dit-on, causent des défauts du système endocrinien, un dysfonctionnement du système immunitaire, des anomalies du système reproducteur et des problèmes de développement chez les humains. 
Certains POP peuvent provoquer ou favoriser des cancers. 
Quand nous aurons fait le ménage chez nous, au Canada, nous pourrons bien sûr exhorter nos voisins à faire le ménage chez eux.  
Il devient souhaitable de ratifier le protocole sur les POP afin que les autres pays sachent que nous tenons sérieusement à réduire ces polluants organiques.  
Nous avons besoin de montrer l'exemple dans la mise en application de la nouvelle entente mondiale. 
Des spécialistes en polluants organiques réunis à Vancouver en 1995 ont produit des recommandations. 
L'entente à laquelle je fais allusion est celle que le PNUE, le Programme des Nations Unies pour l'environnement, a produite pour faire en sorte qu'on établisse des objectifs fermes et des échéanciers pour l'élimination de ces POP autour du globe.  
Il faudra beaucoup de volonté politique pour convaincre les pays en voie de développement que l'élimination des POP a des avantages à long terme pour la santé humaine. 
Il faudra également des ressources financières importantes pour aider ces pays à changer leurs processus et leurs pratiques en matière agricole et de lutte antiparasitaire.  
Apparemment, la Banque mondiale et d'autres prêteurs internationaux forcent souvent les agriculteurs des pays en voie de développement à utiliser des pesticides contenant des POP, comme condition à l'obtention d'une aide financière. 
Cette politique a mené à une rencontre tenue il y a trois ans à Vancouver, où des experts internationaux se sont réunis et en sont arrivés aux recommandations suivantes.  
Premièrement, que les pratiques bancaires nationales et internationales favorisent des solutions de rechange sûres à l'emploi des POP afin que cet emploi soit réduit et supprimé.  
Deuxièmement, que des stimulants encouragent les collectivités des pays en développement à employer des pesticides et des produits chimiques plus sûrs que les POP afin que l'emploi de ceux-ci soit réduit et supprimé.  
Troisièmement, que l'on applique des restrictions plus sévères en ce qui concerne les résidus de pesticides sur les aliments et que l'on décourage les pays d'employer les POP dans l'agriculture.  
Quatrièmement, que l'on tienne compte de la santé humaine et de la protection de l'environnement dans les décisions concernant le commerce et l'investissement.  
La nécessité de tenir compte des conséquences pour la santé humaine des polluants organiques qui aboutissent dans l'Arctique est établie par le Programme de lutte contre les contaminants dans le Nord. 
Celui-ci sera financé par le gouvernement fédéral pendant cinq ans encore à raison de 6 millions de dollars par année, pour un total de 30 millions de dollars.  
La suppression de ces polluants organiques fait aussi ressortir la nécessité pour le Canada de prendre des mesures sur plusieurs fronts.  
Premièrement, nous devons poursuivre les recherches sur les effets des POP. 
Deuxièmement, nous devons promulguer et appliquer une loi sévère pour supprimer l'emploi des POP. 
Troisièmement, nous devons réglementer le secteur industriel et protéger la santé humaine. 
Quatrièmement, nous devons interdire les substances qui sont néfastes pour la santé humaine et, si certaines substances ne peuvent pas être interdites, recourir à des mesures de prévention de la pollution.  
Nous savons que plus de 23 000 substances sont enregistrées pour être utilisées au Canada. 
Jusqu'à maintenant, seulement une poignée d'entre elles ont été interdites. 
Il reste à en examiner des milliers d'autres pour déterminer si elles sont toxiques et à mettre au point des plans pour les gérer, les contrôler et les supprimer.  
Il faut mettre en oeuvre une politique qui fasse passer la santé avant l'économie car, comme on peut le constater à long terme, une société saine et un environnement sain sont le gage d'une économie saine. 
C'est pourquoi, en tant que parlementaires, nous tenons à promouvoir et à accélérer la conclusion de traités visant à réduire la pollution internationale.  
Les polluants organiques internationaux, les POP ou polluants organiques rémanents, doivent être considérés comme une menace pour la santé et l'économie. 
Les polluants qui viennent de loin peuvent causer des dommages à notre environnement, et ils le font. 
Des ententes sont donc nécessaires entre les gouvernements pour veiller à ce que les pays se comportent bien les uns envers les autres du point de vue environnemental et économique.  
Les parlementaires ont un rôle à jouer par le truchement du Conseil de l'Arctique. 
Certains de nos collègues parlementaires en Scandinavie, aux États-Unis et en Russie s'intéressent à la prévention de la pollution. 
Le député de Lac-Saint-Louis et moi l'avons découvert il y a quelques années et avons, à toutes les occasions s'offrant à nous, fait avancer la cause de la prévention et de la réduction de la pollution par la conclusion de protocoles. 
Dans le cas des polluants organiques, nous l'avons fait à Iqaluit, à Salekhard, en Russie et au Conseil de l'Europe.  
L'an prochain, nous avons l'intention d'intensifier nos efforts à Bruxelles à l'occasion d'une réunion du Conseil de l'Arctique ainsi qu'au Conseil de l'Europe, à Strasbourg.  
En guise de conclusion, j'exhorte les députés à se prononcer fermement en faveur de la réduction et de la prévention de la pollution ainsi que de toute initiative susceptible de réduire, voire d'éliminer complètement les polluants qui sont si dommageables à la santé humaine, à l'économie et à l'environnement.  
À mesure que la population terrestre augmente et que s'intensifient les activités humaines, il apparaît de plus en plus évident que nous devons accélérer le processus de réduction et, si possible, de prévention de la pollution par des changements des processus et des pratiques industriels afin de maintenir la vie sur cette planète à longue échéance.  
La question des POP est un exemple classique d'interdépendance internationale. 
Les polluants que nous émettons dans l'atmosphère peuvent être préjudiciables à la santé de gens habitant loin de nous, et vice versa. 
Nous devons renouveler la ferme volonté qui nous a guidés au cours des années 70 et 80 et déployer de nouveaux efforts communs vers l'objectif d'une société sans pollution. 
Tel est le sens véritable du débat d'aujourd'hui sur les POP.  
