LE DON DE VIE DE SANDRINE 
M. Ian Murray (Lanark-Carleton, Lib.):
Monsieur le Président, en mai dernier, la région de la capitale nationale a été affligée par le décès tragique de la petite Sandrine Craig, âgée de 11 ans, qui a été victime d'un accident d'autobus scolaire. 
Des amis de sa famille ont lancé une intense campagne de sensibilisation au don d'organes d'une durée de deux semaines par suite de la décision des Craig de faire don des organes de Sandrine, geste qui a grandement contribué à améliorer la vie de six personnes.  
Aujourd'hui, on a lancé une campagne nationale de sensibilisation au don d'organes intitulée «Le don de vie de Sandrine». 
Des bénévoles de la succursale de la Fondation canadienne du rein de l'est de l'Ontario dirigeront la campagne, et l'Association des collèges communautaires du Canada organisera par le biais de ses collèges des campagnes locales dans 900 collectivités partout au Canada. 
Au cours de cette campagne, on encouragera les membres des familles à partager leurs désirs et à convaincre d'autres personnes de faire de même. 
Cette campagne s'adressera et profitera à des millions de Canadiens. 
J'encourage tous les Canadiens à signer leur carte de don d'organe afin qu'une perte tragique comme celle qu'a connue la famille de Sandrine puisse offrir de l'espoir à d'autres familles.  
