LES ENFANTS 
Mme Jean Augustine (Etobicoke-Lakeshore, Lib.):
Monsieur le Président, la période des Fêtes est synonyme de joie et de bonheur pour de nombreux enfants canadiens, mais pour un jeune enfant inconnu, ce ne sera pas le cas. 
Mardi dernier, les gens d'Etobicoke-Lakeshore ont été choqués d'apprendre que le corps d'un enfant non identifié a été trouvé démembré dans le Colonel Samuel Smith Park, un parc local.  
La mort de cet enfant est un exemple du type de violence qui nous touche tous. 
La vie ne reprendra pas son cours normal dans nos collectivités tant que nous n'aurons pas identifié cet enfant et trouvé les coupables de ce meurtre. 
La vie de nos enfants est trop précieuse pour ne pas mériter notre protection.  
Alors que nous pleurons la mort tragique de cet enfant, prenons le temps de réfléchir aux choses qui menacent la vie de nos enfants et d'adopter des mesures pour s'y attaquer. 
Nous devons nous assurer que les enfants vivent en sécurité dans Etobicoke-Lakeshore et dans toutes les collectivités de notre merveilleux pays.  
