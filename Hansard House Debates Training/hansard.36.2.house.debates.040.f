LE DÉCÈS DE VICTOR QUELCH 
M. David Chatters (Athabasca, Réf.): 
Monsieur le Président, j'interviens à la Chambre aujourd'hui pour réparer un oubli commis par inadvertance il y a bien des années.  
M. Victor Quelch a été député de 1935 à 1958. 
Au cours de ces 23 ans, il a servi le pays en tant que député créditiste de la circonscription d'Acadia, dans le sud de l'Alberta. 
Parlementaire tenu en haute estime, il a exercé plusieurs fonctions dont celle de porte-parole de son parti pour les finances, l'agriculture et les pêches. 
En 1950, le premier ministre Louis St-Laurent l'a nommé conseiller du personnel canadien auprès de l'Assemblée des Nations Unies.  
Avant d'embrasser la carrière politique, M. Quelch avait pris part à la Première Guerre mondiale et reçu la Croix militaire pour bravoure.  
Le sens du devoir envers son pays et ses citoyens a guidé toute la vie de M. Quelch, et pourtant, à sa mort survenue en 1975, aucun hommage ne lui a été rendu à la Chambre.  
Même si cela n'a que trop tarder, j'aimerais souligner les bons et loyaux services de M. Quelch. 
Ses contributions au Canada datent un peu peut-être, mais elles n'ont pas été oubliées.  
