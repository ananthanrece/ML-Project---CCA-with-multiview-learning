Je désire procéder ainsi encore une fois.  
À l'heure actuelle, l'industrie céréalière est confrontée à des défis et des possibilités importantes à exploiter. 
La concurrence mondiale et les forces naturelles mènent aux changements et aux défis et en accélèrent l'émergence. 
L'industrie céréalière évolue rapidement pour réagir aux changements. 
La législation doit également évoluer pour aider l'industrie à relever ces défis et à profiter des possibilités à exploiter. 
Ce projet de loi exprime bien la volonté du gouvernement de répondre aux besoins de l'industrie céréalière et d'appuyer les efforts déployés. 
Le gouvernement a comme principal objectif de rendre l'économie concurrentielle et innovatrice, et créer ainsi encore plus d'emplois et améliorer le bien-être des Canadiens. 
Le secteur de l'agriculture et l'agroalimentaire est l'un des piliers de l'économie canadienne. 
Il apporte toujours une contribution très importante à notre économie. 
L'industrie est en plein essor. 
Une des façons de réaliser l'objectif d'une croissance prospère et continuelle du secteur, c'est de travailler étroitement avec les intervenants. 
Notre gouvernement prévoit être le catalyseur des partenariats avec les intervenants du secteur. 
Nous nous sommes engagés à consulter à fond l'industrie agroalimentaire et les provinces. 
Je crois que nous réussissons à atteindre ce but. 
Nous reconnaissons la valeur immense des citoyens du milieu rural et des communautés rurales au Canada.  
Le projet de loi C-26 renforce notre engagement envers le Canada rural. 
Le secteur des cultures spéciales deviendra plus accessible, ce qui aura des répercussions très favorables sur les possibilités d'emploi en milieu rural.  
Dans l'esprit de la politique fédérale sur le milieu rural, nous voulons accroître la participation des Canadiens en milieu rural à l'élaboration des programmes et services fédéraux. 
Les éléments principaux du projet de loi visent cet objectif, surtout par rapport aux dispositions qui portent sur les cultures spéciales. 
Les producteurs et transformateurs de cultures spéciales ont participé à toutes les étapes du processus menant à l'étude de ce projet de loi par la Chambre. 
Le secteur des cultures spéciales a connu un essor impressionnant au cours des dix dernières années. 
Des cultures qui étaient autrefois d'une importance négligeable pour l'économie agroalimentaire de l'ouest du Canada sont maintenant produits en quantité considérable. 
Devant l'expansion du secteur des cultures spéciales, on a également reconnu le besoin d'un régime réglementaire spécialement adapté. 
La réglementation actuelle semble draconienne et n'a qu'un faible effet de dissuasion. 
La Loi sur les sanctions administratives pécuniaires en matière d'agriculture et d'agroalimentaire est une loi bien fondée qui offre diverses possibilités d'application.  
Le secteur des opérations à terme sur les grains a également connu des changements importants récemment. 
La Bourse des marchandises de Winnipeg a l'intention d'offrir des contrats sur des marchandises autres que les grains. 
Lorsque la Bourse offrira ces contrats, la Loi sur les marchés de grain à terme, en vigueur depuis 1939, sera périmée. 
L'abrogation de cette loi permettra au gouvernement du Manitoba de réglementer toutes les activités d'une bourse moderne offrant des contrats à terme et des contrats d'option.  
Le projet de loi C-26 a été élaboré par la Commission canadienne des grains avec la collaboration des parties intéressées représentant les producteurs et les négociants des cultures spéciales ainsi que le marché à terme des grains. 
Les modifications contenues dans le projet de loi serviront à réaliser cinq objectifs.  
Elles mettraient en place un régime d'octroi de licences au coût raisonnable pour le commerce des cultures spéciales dans l'ouest du Canada. 
Elles institueraient un régime d'assurance financé par les producteurs. 
Elles constitueraient un comité consultatif des cultures spéciales qui relèverait du ministre de l'Agriculture et de l'Agroalimentaire. 
Elles prévoiraient une procédure équitable et efficace pour appliquer la Loi sur les grains du Canada et son Règlement. 
Et elles transféreraient la responsabilité de réglementer le marché à termes des grains à la province du Manitoba.  
J'aimerais parler des trois premiers objectifs proposés dans le projet de loi C-26, à savoir l'institution d'un régime d'assurance des producteurs, la mise en place d'un régime d'octroi de licences et la mise sur pied d'un comité consultatif.  
Le projet de loi prévoit la création d'une nouvelle licence pour le commerce des cultures spéciales dans l'ouest du Canada. 
Cette licence permettra aux entreprises d'effectuer le commerce des cultures spéciales et d'utiliser les noms de grades officiels. 
Plutôt que d'offrir une garantie, les négociants percevront des primes auprès des producteurs à qui ils achètent des cultures spéciales.  
Après déduction des frais administratifs, la commission remettra les primes à l'assureur. 
La Société pour l'expansion des exportations a accepté d'être l'assureur initial du régime. 
Il sera possible de changer d'agent et d'assureur à l'avenir sur recommandation du Comité consultatif des cultures spéciales.  
On propose une prime de 38c. pour chaque centaine de dollars de grain vendu. 
La prime correspondra au total de la prime d'assurance et des frais administratifs. 
Dans un premier temps, la prime d'assurance se chiffrera à 20c. pour chaque centaine de dollars de ventes et les frais administratifs exigés par la commission seront de 18c.  
Les exigences actuelles visant les garanties ont été conçues en fonction des cultures traditionnelles produites à grande échelle qui sont achetées par les grandes sociétés céréalières. 
On ne tenait pas compte des cultures spéciales, dont la production est beaucoup plus restreinte et qui sont surtout manutentionnées par des usines de nettoyage de semences, des négociants en cultures spéciales et des transformateurs, dont le chiffre d'affaires est moins élevé. 
Le résultat, c'est que des entreprises qui seraient autrement agréées se voient exclues du marché. 
Encore d'autres entreprises, malheureusement, achètent du grain sans licence et exposent ainsi les producteurs au risque de perte. 
Notre gouvernement, par le truchement du projet de loi C-26, donne suite aux préoccupations et recommandations formulées par le secteur des cultures spéciales.  
Cette mesure encouragera aussi les entreprises qui font actuellement le commerce sans licence à en faire la demande.  
Même s'il est plus facile de se procurer une licence, cela ne veut pas dire pour autant que tous les demandeurs en obtiendront une. 
Les demandeurs devront prouver qu'ils disposent des ressources financières convenant au commerce des cultures spéciales avant d'obtenir une licence.  
La société de consultants dont les services ont été retenus par la Commission canadienne des grains, Kelly and Associates, a estimé qu'environ 125 entreprises de l'Ouest pourraient se procurer des licences en vertu du projet de loi proposé. 
Les modifications qu'il contient pourraient faire augmenter le nombre de points de commerce pour les producteurs et encourager une saine concurrence.  
Je voudrais maintenant parler du régime d'assurance facultatif proposé, dont les frais seront assumés par les producteurs. 
Les producteurs de cultures spéciales bénéficieront toujours d'une couverture malgré l'élimination des dispositions actuelles obligeant les négociants à fournir une garantie.  
Si une entreprise titulaire de licence ne respecte pas ses obligations envers les producteurs à qui elle a acheté des cultures spéciales, ou encore si cette entreprise devient insolvable, les producteurs qui adhèrent au régime pourront réclamer une indemnisation.  
Les producteurs pourront se retirer du régime d'assurance. 
S'ils le font, cependant, il ne bénéficieront d'aucune protection si les négociants agréés manquent à leurs obligations.  
Les producteurs qui choisiront de ne pas adhérer au régime seront tenus de payer les primes, qui leur seront remboursées par l'administrateur du programme.  
Les modalités du régime prévues au départ obligeaient les producteurs qui s'étaient retirés de présenter une demande remboursement. 
Les témoins qui ont comparu devant le Comité permanent de l'agriculture et de l'agroalimentaire ont fait part de leurs réticences à l'égard de cet aspect du régime, qui impose un fardeau administratif aux producteurs.  
Le comité a pris les devants en approuvant un amendement pour que le remboursement se fasse automatiquement. 
On a également pris des démarches proactives pour modifier les dispositions qui permettaient des ajouts et des retraits dans la liste des cultures spéciales visées par le régime.  
On craignait que les cultures traditionnelles, comme le blé, l'avoine, l'orge, le seigle, le lin et le canola, puissent ainsi devenir admissibles. 
Bien que cette possibilité n'ait jamais été envisagée par les rédacteurs du projet de loi, le comité a approuvé des amendements qui excluent formellement du régime les six cultures traditionnelles.  
La perception obligatoire de la prime auprès de tous les producteurs, adhérents ou non au régime, a fait l'objet d'un examen sérieux au Comité consultatif intérimaire des cultures spéciales.  
La décision du comité s'appuyait sur les raisons suivantes. 
Le régime doit être efficace sur le plan administratif afin de limiter les coûts que doivent absorber les producteurs. 
Il est beaucoup plus efficace et rentable de percevoir des primes de tous les producteurs et de rembourser par la suite les producteurs non adhérents. 
On s'attend à recouvrer les coûts administratifs du régime au moyen des frais imposés pour les services offerts. 
Les frais proposés ont été calculés en fonction des estimations de facteurs variables comme le taux de participation et le volume des livraisons.  
Lorsque le régime sera opérationnel, l'administrateur sera mieux placé pour évaluer les coûts et faire les changements nécessaires. 
Si la part de la prime réservée aux frais administratifs est trop élevée, on la modifiera pour tenir compte des coûts réels.  
Au cours des consultations, les parties intéressées ont réclamé un mécanisme officiel qui leur permettrait de s'exprimer à l'égard du régime. 
Ils voulaient s'assurer que leurs préoccupations concernant l'octroi de licences et l'offre de garanties seraient transmises au ministre.  
On a pu donner suite à cette demande en proposant des modifications à la Loi sur les grains du Canada, modifications qui prévoient la constitution d'un comité consultatif des cultures. 
Les neuf membres du comité seront nommés par le ministre de l'Agriculture et de l'Agroalimentaire. 
Les membres seront choisis parmi les producteurs et les négociants du secteur des cultures spéciales des provinces de l'Ouest.  
Le comité conseillera le ministre sur le régime d'octroi de licences et d'offre de garanties visant les cultures spéciales. 
Il pourra recommander au ministre de remplacer l'administrateur ou l'assureur du régime. 
Il pourrait également faire des recommandations concernant l'ajout ou le retrait de cultures dans la liste des cultures admissibles ou encore concernant d'autres questions liées aux cultures spéciales auxquelles s'intéresse le ministre.  
Je vais conclure en parlant des répercussions que le projet de loi pourrait avoir. 
Les transformateurs de cultures spéciales sont d'importants employeurs dans les localités où ils sont installés. 
Par exemple, le village de Sedley, en Saskatchewan, qui compte 342 habitants, est délimité par les municipalités rurales de Francis et Lajord. 
Sedley Seeds, situé à sept milles du village, emploie huit personnes de façon permanente, dont quatre habitent au village. 
Son effectif est d'une quinzaine de personnes durant la saison préparatoire aux semailles et la période de pointe qui suit la récolte. 
Vigro Seed & Supply, installé à Sedley, emploie 12 personnes de façon permanente, dont sept habitent au village. 
On y embauche une trentaine de personnes durant les périodes de pointe.  
Ces deux entreprises ne semblent pas très importantes, auprès de celles de villes comme Toronto, Montréal ou Ottawa, mais ce sont les deux employeurs les plus importants de la région. 
Elles fournissent de précieux services à cette collectivité rurale. 
Comme on est en train de moderniser le réseau des silos et ferme des installations, les transformateurs de cultures spéciales comme Sedley Seeds ou Vigro Seed & Supply, installés un peu partout dans l'Ouest, seront en mesure d'offrir d'autres possibilités de livraison et d'autres services aux producteurs.  
Le projet de loi est avantageux pour le secteur des cultures spéciales et le milieu rural de l'Ouest. 
