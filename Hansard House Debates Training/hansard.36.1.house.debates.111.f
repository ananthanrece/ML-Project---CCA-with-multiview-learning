L'article 3 du projet de loi C-29 dit ceci: «Est constituée l'Agence canadienne des parcs, dotée de la personnalité morale et exerçant ses attributions uniquement à titre de mandataire de Sa Majesté du chef du Canada .» [C'est nous qui soulignons]. 
Étant donné que le paragraphe 3(1) de la Loi sur les langues officielles précise que «tout autre organisme désigné par la loi à titre de mandataire de Sa Majesté du chef du Canada (...)» est une institution fédérale, l'Agence sera, c'est évident, considérée comme une institution fédérale et pleinement visée par la Loi sur les langues officielles .  
Ainsi, l'Agence respecterait également cet autre critère et serait donc, c'est évident, considérée comme une institution fédérale aux termes de la Loi sur les langues officielles .  
De plus, l'article 50 du projet de loi C-29 dit: «L'annexe II de la Loi sur la gestion des finances publiques est modifiée par adjonction, selon l'ordre alphabétique, de ce qui suit: «Agence canadienne des parcs». 
Cette annexe identifie les «établissements publics» aux termes de la Loi sur la gestion des finances publiques et en vertu de l'article 2 de cette loi, les établissements publics sont considérés comme des « ministères ». [C'est nous qui soulignons.]  
Aux termes de l'article 3 de la Loi sur les langues officielles , la définition des «institutions fédérales» englobe donc les «ministères fédéraux». 
L'Agence sera donc considérée comme un «ministère» aux termes de l'article 3 de la Loi sur les langues officielles et de l'article 2 de la Loi de la gestion des finances publiques et elle sera, de ce fait, assujettie aux obligations et aux devoirs prévus dans la Loi sur les langues officielles de ce fait.  
En résumé, étant donné la définition d'institutions fédérales décrite au paragraphe 3(1) de la Loi sur les langues officielles , nous croyons que l'Agence sera une institution fédérale aux termes de cette loi parce que: (i) elle est un mandataire de Sa Majesté; (ii) elle est placée sous la tutelle d'un ministre fédéral; et (iii) elle est un ministère fédéral. 
Ainsi, elle sera pleinement assujettie aux devoirs et aux obligations linguistiques prévus par la Loi sur les langues officielles.  
Si l'agence décide de confier à des sous-traitants des services à la population qui doivent, aux termes de la Loi sur les langues officielles, être fournis dans les deux langues officielles, l'article 25 de la loi obligerait l'agence à veiller à ce que les services fournis en son nom le soient dans les deux langues officielles.  
Dans son sens général, l'expression «au nom de» signifie qu'une partie s'engage à faire quelque chose dans l'intérêt d'une autre partie ou à titre de représentant de l'autre partie, généralement à la demande de cette autre partie. 
Selon le dictionnaire, l'expression veut dire qu'une partie agit dans l'intérêt d'une autre personne, ou d'un principe, ou en tant que représentant (d'un client par exemple).  
À notre avis, certains types d'ententes sont clairement visés par l'article 25, comme le cas d'une institution fédérale qui a l'obligation légale, aux termes d'une loi, d'un contrat ou de tout autre principe juridique, de gérer ou d'organiser une activité particulière ou encore de fournir des services ou de diffuser des renseignements à la population. 
Si l'institution fédérale décide de confier ces services à une tierce partie, celle-ci agira au nom de l'institution fédérale. 
Par conséquent, l'article 25 s'appliquera et l'institution fédérale devra veiller à ce que le sous-traitant qui fournit les services ou diffuse les renseignements respecte les exigences linguistiques de la partie IV de la Loi sur les langues officielles.  
Une autre sorte d'entente qui, à notre avis, serait également visée par l'article 25 est le contrat appelé «mandat» en droit civil et « agency » en common law. 
Le mandataire a normalement l'obligation de représenter le mandant. 
En effet, le mandat a pour objet et effet d'investir le mandataire du pouvoir d'agir au nom du mandant, de sorte que les actes du mandataire engagent juridiquement le mandant envers les tiers ( C.E.D .(Ont.)3rd 
ed. p.49). 
Par conséquent, dans les cas où le contrat entre l'institution fédérale et le tiers est considéré comme un mandat ou agency , le tiers agirait au nom de l'institution fédérale et l'article 25 exigerait que ses services et ses communications au public soient offerts dans les deux langues officielles, conformément à la partie IV de la Loi sur les langues officielles.  
Cela étant dit, l'expression «au nom de» ne devrait pas forcément se limiter aux sortes d'ententes énoncées plus haut, car une interprétation trop étroite de l'article 25...  
