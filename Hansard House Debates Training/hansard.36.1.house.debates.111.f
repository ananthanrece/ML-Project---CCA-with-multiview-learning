INITIATIVES PARLEMENTAIRES 
LES ÉMISSIONS DE GAZ CARBONIQUE 
L'hon. Charles Caccia (Davenport, Lib.) propose: 
Que, de l'avis de la Chambre, le gouvernement devrait, dans le cadre d'un effort mondial de réduction des changements climatiques, élaborer une stratégie pour réduire les émissions de gaz carbonique au Canada, peut-être de 20 p. 100 par rapport à celles de 1988, d'ici 2005.  
Il s'agit d'un problème classique de développement durable.  
Notre géographie, notre territoire peu densément peuplé, notre climat et les distances sont autant de facteurs contribuant à un niveau élevé de production par habitant de gaz à effet de serre, qui n'est dépassé que par celui qu'on enregistre aux États-Unis d'Amérique. 
Cependant, les politiques fédérales actuelles exigent qu'on s'en préoccupe parce qu'elles favorisent une production accrue de gaz à effet de serre.  
En même temps, une réflexion et des politiques innovatrices au Canada sont venues surtout des municipalités. 
Par exemple, on retrouve, au sein du club des 20 p. 100, des municipalités qui se sont engagées à réduire, d'ici l'an 2000, leurs niveaux de 1990 de gaz à effet de serre. 
Font partie de ce club les villes d'Ottawa, Toronto, Vancouver, Regina, Edmonton et 32 autres municipalités.  
Il faut remarquer que la question n'est pas nouvelle pour nous au Canada. 
Une délégation canadienne a participé aux conférences sur le changement climatique de 1991, à Genève, de 1995, à Berlin, et bien avant Kyoto. 
Il est peut-être bon de se rappeler aussi qu'à la conférence de 1986 sur l'énergie et le climat, à Toronto, la collectivité scientifique a adopté une résolution visant à réduire les émissions de gaz à effet de serre de 20 p. 100 avant 2005.  
La question n'est pas nouvelle et les engagements politiques à réduire les émissions de gaz à effet de serre ont bientôt suivi. 
Dans son programme électoral de 1988 et à nouveau dans son programme électoral de 1993, le Parti libéral s'est engagé à réduire avant 2005 les émissions de 20 p. 100 par rapport à 1988.  
Aujourd'hui, après nous être engagés à Kyoto à réduire de 60 p. 100 avant 2010 le niveau actuel des émissions alors que nous sommes déjà à 13 p. 100 au-dessus de ce niveau, il faut reconnaître franchement que nous sommes en retard de cinq ans et de 19 p. 100 sur nos objectifs. 
Néanmoins et comme si de rien n'était, nous semblons maintenir béatement une politique qui ajoute au problème plutôt que de le résoudre. 
Voici des exemples.  
En 1996, le ministre des Finances a offert un allégement fiscal au secteur des sables bitumineux, un secteur qui produit plusieurs fois plus de gaz à effet de serre que l'exploitation du pétrole classique. 
Non seulement cet allégement fiscal rendra plus difficile de réduire les émissions de gaz à effet de serre, mais il risque de coûter jusqu'à 800 millions de dollars en manque à gagner aux contribuables canadiens. 
Comment pouvons-nous espérer atteindre les objectifs de Kyoto avec des stimulants fiscaux aussi absurdes?  
En outre, les Canadiens qui empruntent régulièrement les transports en commun ont besoin d'être reconnus. 
Le ministre des Finances ne semble pas comprendre qu'il importe de faire des laissez-passer mensuels d'autobus fournis par l'employeur un avantage non imposable. 
Par contre, les permis mensuels de stationnement offerts par les employeurs à leurs employés sont exempts d'impôt. 
La Fédération canadienne des municipalités fait valoir cela chaque année depuis 1990, mais en vain.  
Un autre exemple embarrassant réside dans les dépotoirs qui produisent du méthane et d'autres gaz qu'on laisse s'échapper dans l'atmosphère et aggraver l'effet de serre au lieu de les capter et de les convertir en énergie. 
Les municipalités ont besoin de l'aide des gouvernements fédéral et provinciaux pour capter ces gaz et les utiliser dans les installations de chauffage centralisé au lieu de les laisser s'échapper dans l'atmosphère.  
Pour permettre au Canada d'atteindre facilement l'objectif de Kyoto, nous avons donc besoin d'un fonds national de protection de l'air. 
Il ressemblera à celui qu'on a établi pour Toronto en 1992.  
Le fonds national de protection de l'air a bien besoin de capitaux de lancement. 
Dans le cas du Canada, cela pourrait facilement exiger 300 millions de dollars qui serviraient à accorder des prêts pour des projets comme des initiatives touchant les gaz d'enfouissement. 
L'intérêt gagné sur les prêts et d'autres investissements pourrait être ensuite utilisé pour des projets utiles.  
À Toronto, le fonds a augmenté de plus de 100 p. 100 depuis 1992, et il est presque certain que Toronto respectera son objectif de 20 p. 100 d'ici l'an 2000. 
C'est vraiment remarquable.  
Dans le cas des chemins de fer, le ministre des Transports est responsable, à l'heure actuelle, de la fermeture de lignes de chemin de fer dans toutes les Prairies. 
Les silos à grain sont desservis de plus en plus par des camions diesel qui émettent quatre fois plus de gaz à effet de serre que le train.  
Soit dit en passant, il faut remarquer qu'au Canada, l'utilisation des camions diesel a augmenté de 30 p. 100 entre 1990 et 1995. 
Il est évident qu'il faut revenir sur la politique de fermeture de lignes de chemin de fer si nous voulons vraiment respecter nos objectifs de Kyoto. 
Cela va certes poser un problème difficile.  
De plus, le rendement énergétique des automobiles est une faible priorité pour le ministère des Transports. 
Il ne semble prendre aucune mesure concrète pouvant encourager le secteur automobile à fabriquer des véhicules à plus faible consommation.  
On n'impose pas des taxes progressives sur les grosses voitures qui dévorent l'essence pour envoyer un message clair aux consommateurs. 
Il n'y a aucune norme obligatoire quant à la consommation de carburant. 
En bref, on prend peu de mesures concrètes.  
Au fil des ans, il est devenu évident que le gaz naturel va être la solution. 
Nous en avons une grande quantité et il faut procéder à la conversion au gaz naturel et s'éloigner des autres combustibles fossiles, car ils produisent beaucoup plus de gaz à effet de serre.  
Ainsi, étant donné ces faits, l'Alberta pourrait être la grande gagnante de la course vers la réduction des gaz à effet de serre pourvu, bien entendu, que les législateurs albertains jouent un rôle de chef de file plutôt que d'attendre, comme ils semblent le faire maintenant, que les électeurs leur donnent le feu vert.  
Les députés se rappellent qu'au moment de la crise pétrolière, nous avons découvert une chose que nous avions, semble-t-il, oublié. 
On a mis sur pied à la fin des années 1970 et au début des années 1980 des programmes qui visaient à encourager les Canadiens à mieux isoler leurs maisons, à remplacer leurs moteurs actionnés à l'essence par des moteurs actionnés au propane et à prendre toutes sortes de mesures pour économiser l'énergie. 
Ces programmes ont remporté beaucoup de succès, mais ils ont malheureusement été abandonnés pendant le règne de Mulroney.  
Puis, on passe à l'interminable saga des sources d'énergie renouvelable, cousins pauvres des ressources non renouvelables. 
Depuis 1985, les adeptes des énergies renouvelables ont demandé à tous les ministres des Finances qui se sont succédés à ce poste de les traiter sur le même pied que les autres, c'est-à-dire de leur accorder le même traitement préférentiel qu'on accorde aux pétrolières et aux industries exploitant d'autres carburants fossiles. 
C'est un secteur qui exige une attention bien spéciale.  
J'aimerais maintenant aborder la question du nouveau secrétariat sur les changements climatiques qui constitue sans nul doute un pas dans la bonne direction et une mesure avantageuse. 
Le secrétariat doit permettre de respecter les engagements de Kyoto grâce aux efforts combinés de deux ministères, soit le ministère des Ressources naturelles et le ministère de l'Environnement.  
Aussi importants qu'ils puissent être, ces deux ministères ne peuvent atteindre les résultats escomptés tout seuls. 
Le secrétariat doit aussi obtenir la participation d'autres ministères comme les Finances, les Transports, les Travaux publics, l'Agriculture et l'Industrie. 
Le secrétariat devra engager une action concertée avec ces ministères.  
Pourquoi? 
Parce que le secteur du transport à lui seul est responsable de près du tiers des émissions de gaz à effet de serre. 
Pourquoi? 
Parce que le ministère des Finances est responsable de l'octroi des avantages fiscaux parfois pervers et parfois avantageux. 
Autrement dit, la portée et l'autorité du secrétariat proposé et maintenant mis sur pied doivent être étendues et renforcées pour qu'il puisse mener sa mission à bien.  
Il faut comprendre par là que ce sont des paroles en l'air, des propos creux.  
Selon moi, un système d'échange de droits d'émission devrait être assorti d'au moins quatre conditions. 
Premièrement, il devrait être accompagné d'une limitation des émissions, faute de quoi, il ne produira pas les effets désirés. 
Si nous voulons réduire les gaz à effet de serre, la limite doit être progressivement rabaissée. 
Deuxièmement, les réductions doivent être réelles et non fictives. 
Troisièmement, elles doivent être quantifiables et vérifiables. 
Quatrièmement, le système doit être applicable. 
Si les réductions sont en deçà de ce qui était prévu, il doit y avoir des sanctions sévères comme, par exemple, des amendes progressives augmentant avec chaque tonne d'émissions au dessus du niveau autorisé.  
Par mise en commun, et je déteste utiliser des termes techniques, mais dans le domaine du changement climatique c'est inévitable, on entend généralement le fait de porter à son compte dans un pays des réductions effectuées dans un autre pays. 
Il y a des limites à l'utilité de cette pratique. 
En effet, réduire les émissions dans un pays ne remplace pas la réduction d'émissions au Canada, par exemple. 
Toutefois, cela ne devrait pas nous empêcher de prendre des mesures importantes au Canada.  
Dans ce contexte, il est important de signaler que, répondant à une question sur les mérites de la mise en commun des crédits de pollution, le commissaire à l'environnement et au développement durable a dit cette semaine qu'il faudra attendre longtemps avant que ce concept ne soit intégré dans un plan d'ensemble.  
En conclusion, je dirai que nous avons déjà trop attendu. 
C'est pourquoi il est probable que nous ne respecterons pas les engagements que nous avions pris avant Kyoto et, si nous voulons respecter ceux que nous avons pris à Kyoto, nous devons agir sans plus tarder. 
Si nous produisons de telles quantités de gaz à effet de serre, c'est pour des raisons très précises. 
J'en ai parlé au début de mon intervention; le moment est venu de modifier nos habitudes et nos politiques.  
L'engagement que nous avons pris à Kyoto pourrait être extrêmement bénéfique pour notre économie, car il nous forcera à consommer l'énergie de façon plus judicieuse et économique, et à supprimer les incitatifs fiscaux inutiles et coûteux. 
Par ailleurs, il nous forcera à concentrer nos efforts sur la production d'énergie à partir de ressources renouvelables à long terme et à convertir au gaz naturel, dont nous avons d'immenses réserves. 
Je suis certain que, ce faisant, notre économie deviendra plus compétitive et nous gagnerons le respect de la communauté internationale.  
Nous devons donc nous attaquer aux cause du changement climatique et lancer l'un des plans les plus difficiles et les plus passionnants qu'un gouvernement du Canada ait jamais eu à mettre en oeuvre.  
