M. John Godfrey (secrétaire parlementaire de la ministre du Patrimoine canadien, Lib.):
Monsieur le Président, je vais essayer d'aborder les cinq motions et de parler des divers arguments soulevés par la députée.  
Je commence avec la première motion qui vient d'être proposée. 
Nous sommes contre cette modification, parce que nous la trouvons redondante. 
La modification proposée reflète ce qui apparaît déjà à l'alinéa l) et au nouvel alinéa m) du préambule. 
Plus précisément, l'esprit de la modification proposée est déjà saisi par le paragraphe 6(1) qui précise que, et je cite: 
6.(1) L'Agence est responsable de la mise en oeuvre de la politique du gouvernement du Canada dans le domaine des parcs nationaux, des lieux historiques nationaux, des autres lieux patrimoniaux protégés et des programmes de protection du patrimoine.  
Les politiques de cet ordre sont déjà globalement traitées dans le document de Parcs Canada intitulé: «Principes directeurs et politiques de gestion», déposé au Parlement et approuvé en 1994. 
Ce document traite des principales responsabilités de Parcs Canada en ce qui concerne le respect de l'intégrité écologique et commémorative, et non pas culturelle, et comment ces responsabilités sont respectées au niveau des parcs et des lieux, tout en tenant compte de l'utilisation, par les visiteurs, du tourisme et de l'aménagement des parcs.  
Quant à la motion no 3, une fois de plus, nous sommes contre, parce que nous trouvons cette modification redondante. 
L'Agence Parcs Canada est définie comme un établissement public au sens de la Loi sur la gestion des finances publiques. 
En tant que tel, elle doit se conformer aux politiques du Conseil du Trésor sur les marchés et au règlement sur les marchés de l'État. 
Les limites et les formalités d'adjudications sont clairement établies dans ce règlement.  
Il faut remarquer que l'article 9 a simplement pour but de permettre à l'Agence, avec l'approbation du Conseil du Trésor et du gouverneur en conseil, de se procurer des biens et des services auprès d'autres organisations que les organisations de services communs du gouvernement, telles que Travaux publics et Services gouvernementaux Canada, lorsque cela est avantageux du point de vue économique.  
Toutes les activités contractuelles de l'Agence, que ce soit avec les organisations de services communs ou d'autres organisations, continueront à être sujettes au règlement sur les marchés de l'État.  
Cet article remplace l'ancien paragraphe 12(4). 
En termes plus simples, cela veut dire qu'il ne s'agit plus de réunions publiques convoquées par le directeur général de la nouvelle Agence Parcs Canada, mais par le ministre. 
Cette mesure garantit la tenue d'une table ronde au moins tous les deux ans. 
Elle prévoit également que l'on répondra aux recommandations dans un délai raisonnable. 
Nous avons choisi d'accorder 180 jours au ministre pour répondre parce que c'est le délai prévu pour d'autres réponses à des propositions environnementales.  
Ce qui m'amène à la motion no 4 par laquelle nous proposons: 
Que le projet de loi C-29, à l'article 12, soit modifié par substitution, aux lignes 33 à 38, page 7 et à la ligne 1, page 8 de ce qui suit: 
«(4) Le directeur général peut déléguer à» 
Cette proposition ne fait qu'annuler le paragraphe 12(4) pour le remplacer par le nouvel article dont je viens de parler qui prévoit de convoquer une table ronde au moins une fois tous les deux ans. 
Cette mesure a tout simplement pour but de rendre la chose possible.  
La motion no 5 va de pair avec les deux motions dont je viens de parler. 
Comme je l'ai déjà souligné, cela signifie qu'il y aura dépôt d'un rapport dans les 180 jours, ce qui correspond à ce qui se fait par exemple dans le cas de l'évaluation environnementale. 
Je crois que cela fait le tour de la question.  
