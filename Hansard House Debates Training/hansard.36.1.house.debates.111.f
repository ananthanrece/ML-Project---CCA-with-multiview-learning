À supposer que le projet de loi C-29 soit promulgué sous sa forme actuelle, l'Agence sera une «institution fédérale» aux termes de la Loi sur les langues officielles et, par conséquent, elle sera assujettie à toutes les obligations linguistiques établies dans cette loi.  
