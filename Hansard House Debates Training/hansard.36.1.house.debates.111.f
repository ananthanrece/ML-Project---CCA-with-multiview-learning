Si nous savons une chose à propos des Américains, c'est que, lorsqu'ils prennent une initiative, elle est habituellement couronnée de succès. 
S'ils décident de rendre leurs industries beaucoup plus éconergétiques et beaucoup plus concurrentielles et que le Canada ne possède pas des initiatives semblables dans son économie dirigée par le ministre des Finances, les répercussions en seront très négatives sur la compétitivité de notre pays, car nos industries ne seront tout simplement pas en mesure de livrer concurrence à long terme.  
Ce qu'il faut, c'est le leadership d'un premier ministre, comme nous en avons été témoins lors du Sommet de la Terre, avec l'ancien premier ministre Brian Mulroney et le ministre de l'Environnement de l'époque, Jean Charest. 
Nous avons besoin d'une participation de ce genre.  
Bien que nous sachions que la tâche est très décourageante, nous avons déjà dû relever un défi de ce genre. 
Je parle ici des pluies acides. 
Au début, le secteur industriel a déclaré qu'il serait économiquement suicidaire de tenter de modifier les méthodes d'exploitation des industries. 
En fin de compte, une foule de ces industries ont pu devenir beaucoup plus éconergétiques et beaucoup plus rentables en changeant leurs méthodes d'exploitation. 
Les pluies acides constituent un exemple dont nous pouvons nous inspirer pour promouvoir l'efficacité énergétique.  
Le ministre des Finances se plaît à parler de notre bilan. 
J'aime beaucoup parler de notre bilan en matière d'environnement. 
Je déplore que le ministre des Finances ne puisse pas écouter ces observations.  
J'applaudis à nos initiatives au sujet des pluies acides et à l'initiative de Jean Charest concernant le Plan vert, au leadership de l'ancien premier ministre, M. Mulroney, lors du Sommet de la Terre, à un Protocole national sur l'emballage, et je félicite Tom MacMillan et Jean Charest, qui ont présenté la Loi canadienne sur la protection de l'environnement.  
Ce sont des initiatives de ce genre que j'aime me remémorer au sujet de notre bilan en matière d'environnement. 
Je veux être le moins sectaire possible, pour que nous puissions nous s'attaquer à ce problème très grave des changements climatiques. 
Je félicite le député de Davenport de continuer l'alimenter ce débat.  
