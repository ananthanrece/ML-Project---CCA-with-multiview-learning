LOI SUR LA SÉCURITÉ FERROVIAIRE 
L'hon. Alfonso Gagliano (au nom du ministre des Transports, Lib.) propose que le projet de loi C-58, Loi modifiant la Loi sur la sécurité ferroviaire et une autre loi en conséquence, soit lue pour la deuxième fois et renvoyée à un comité.  
M. Stan Dromisky (secrétaire parlementaire du ministre des Transports, Lib.):
Monsieur le Président, c'est avec plaisir que je prends la parole à la Chambre pour entamer le débat sur le projet de loi C-58, à l'étape de la deuxième lecture.  
En septembre 1997, à la suite du déraillement d'un train près de Biggar, en Saskatchewan, le ministre des Transports s'est assuré que la compagnie ferroviaire prenne immédiatement plusieurs mesures de sécurité. 
Par la suite, le ministère s'est assuré de la mise en oeuvre d'une recommandation intérimaire du Bureau de la sécurité des transports du Canada afin que les Canadiens puissent bénéficier d'une sécurité accrue.  
Peu de temps après le déraillement, le ministre annonçait qu'il retardait le dépôt à la Chambre des communes des modifications à la Loi sur la sécurité ferroviaire. 
Il demanda alors aux fonctionnaires de son ministère d'étudier quelles autres améliorations pourraient être apportées à la loi et comment améliorer les mécanismes de surveillance du respect des règlements et des consignes de sécurité.  
Un comité d'étude de la sécurité ferroviaire réunissant des spécialistes de la réglementation, de la gestion des risques et de la sécurité ferroviaire, fut immédiatement constitué. 
Le comité avait pour mandat, entre autres, de consulter l'industrie ferroviaire et autres intervenants afin de recommander comment améliorer davantage la sécurité ferroviaire.  
Les fonctionnaires remirent leur rapport au ministre en janvier et, en mars, ce dernier annonçait qu'il avait accepté les recommandations du comité d'étude de la sécurité ferroviaire et qu'il rendait son rapport public.  
Le travail débuta au printemps 98.  
Aujourd'hui, j'ai le plaisir d'informer la Chambre que les modifications à la loi imposées par ce projet de loi comprennent plusieurs nouvelles dispositions qui ont été recommandées par le comité d'étude et qui vont accroître la sécurité au sein de l'industrie ferroviaire canadienne.  
Ces nouvelles modifications à la Loi sur la sécurité ferroviaire ont été préparés après de longues consultations avec les entreprises ferroviaires, les syndicats des chemins de fer, la Fédération canadienne des municipalités, le Conseil canadien de la sécurité, Transports 2000 et les responsables provinciaux.  
Il y a eu des consultations jusqu'à la fin d'octobre de cette année, de manière à donner aux intéressés la possibilité de parvenir à un consensus sur l'intention des modifications proposées dans le projet de loi C-58.  
Ce cadre législatif permettra au ministère d'exiger des compagnies ferroviaires relevant de la compétence fédérale qu'elles adoptent des systèmes formels de gestion de la sécurité. 
Le ministère aurait le pouvoir de s'assurer que les compagnies se conforment aux exigences et de prendre les mesures nécessaires pour faire respecter la loi.  
Le projet de loi répond également à des recommandations antérieures du Bureau de la sécurité des transports en ce qui concerne des moyens efficaces de vérification de la sécurité dans les chemins de fer.  
La mise en place d'un système de gestion de la sécurité devrait avoir pour effet de renforcer la sécurité ferroviaire en sensibilisant les compagnies de chemin de fer à la sécurité, en leur permettant de faire preuve de leur engagement à l'égard de la sécurité et en démontrant que les compagnies de chemin de fer respectent les règlements.  
Les consultations avec les intéressés ont fait ressortir que la majorité d'entre eux désiraient avoir un comité consultatif sur les questions de sécurité. 
Je suis heureux de dire que, comme le ministre l'a annoncé en mars 1998, le ministère s'est engagé à créer un comité consultatif permanent qui sera composé de fonctionnaires du ministère et de personnes qu'intéressent la sécurité ferroviaire.  
L'objectif de ce comité, au sein duquel seront représentées toute les parties concernées, est de veiller à ce que les décisions concernant les questions de sécurité ferroviaire soient prises en consultation. 
Cela suppose une communication active dans les deux sens pour mieux comprendre les questions et les régler. 
Ce comité s'ajoutera en complément aux dispositions prévues dans ce projet de loi pour renforcer la consultation.  
La première réunion devrait avoir lieu au début de l'année prochaine.  
Chaque année, de nombreux Canadiens trouvent la mort aux passages à niveau ou parce qu'ils ne respectent pas les interdictions. 
C'est le plus gros problème au niveau de la sécurité ferroviaire et d'importants efforts sont faits pour y remédier. 
Le ministère des Transports a mis en place un programme et plusieurs initiatives à cet effet, et cette loi renferme des dispositions pour accroître la sécurité aux passages à niveau.  
Le programme mis en place par le ministère peut être défini en quatre mots: élimination, ingénierie, exécution et éducation.  
Le premier objectif a pour nom élimination. 
Une autre disposition lui permettra de réglementer l'ouverture de nouveaux franchissements sur les voies rapides.  
Le deuxième objectif est un meilleur contrôle. 
Le troisième objectif porte sur l'exécution. 
La surveillance globale exercée par l'inspecteur de la sécurité ferroviaire pourra donner lieu à diverses interventions lorsque des lacunes seront décelées.  
Le dernier objectif est l'éducation de la population. 
L'éducation est un aspect essentiel à l'atteinte des trois autres objectifs. 
C'est pourquoi le ministère des Transports est un partenaire à part entière de l'opération Gareautrain, un programme destiné à renseigner le public sur diverses questions concernant la sécurité.  
Les modifications proposées déboucheront sur un programme national appelé direction 2006 qui vise à réduire de moitié d'ici cette date le nombre de collisions aux franchissements routiers et les entrées interdites sur la propriété des compagnies de chemin de fer. 
Ce programme bénéficie de l'appui actif de tous les intervenants.  
Le gouvernement doit relever l'énorme défi de la réduction des émissions de gaz à effet de serre. 
Le projet de loi proposé prévoit le pouvoir de prendre des règlements pour restreindre les émissions du matériel ferroviaire. 
Il n'existe actuellement au niveau fédéral aucun pouvoir de cette nature, et la Loi canadienne sur la protection de l'environnement qui est proposée ne vise pas le matériel ferroviaire. 
Le pouvoir prévu ici permettra d'assainir l'environnement et aidera le Canada à respecter les quotas d'émission fixés à l'échelle mondiale.  
Les modifications législatives proposées donneront au système de sécurité ferroviaire de meilleurs moyen de garantir raisonnablement le maintien de la sécurité ferroviaire au Canada et de faire en sorte que les transports soient respectueux de l'environnement.  
En conclusion, la première priorité de Transports Canada est et sera toujours la sécurité des transports au Canada. 
J'estime que les modifications apportées à la Loi sur la sécurité ferroviaire renforceront le cadre réglementaire qui régit la sécurité dans ce mode de transport crucial. 
Il donnera au ministère le moyen d'assurer que les chemins de fer du Canada continueront d'améliorer leurs résultats en matière de sécurité à l'aube du XXIe siècle.  
M. Lee Morrison (Cypress Hills-Grasslands, Réf.): 
Monsieur le Président, je suis ravi de traiter de ce projet de loi, que tous les députés, j'en suis sûr, trouvent extrêmement fascinant.  
Le Comité permanent des transports fera peut-être l'étude de ce projet de loi dès mardi prochain, soit huit jours de séance après sa première lecture. 
Une prorogation serait-elle dans l'air?  
Lorsque j'ai donné à quelques intéressés une idée de ce qui se passait, ils ont été fort étonnés et n'ont pas trouvé cela drôle du tout. 
Je crois savoir que certains d'entre eux sont déjà disposés à comparaître à court délai et qu'ils ont communiqué avec le greffier du comité. 
Le ministre ne fait rien au sujet de ce projet de loi depuis plusieurs semaines, comme une poule qui couve un oeuf, et tout à coup, on se précipite pour le faire adopter.  
Cela me rappelle ce qui s'est passé, il y a un an, avec le projet de loi C-9, Loi maritime du Canada, qui était bon en substance, mais qui contenait à quelques endroits de sérieuses lacunes au sujet du financement. 
Certains intéressés en étaient extrêmement bouleversés, mais on ne les a pas entendus. 
Aujourd'hui, un an plus tard, les défauts de ce projet de loi viennent déjà nous hanter, surtout en ce qui concerne le port de Halifax.  
Il signifie différentes choses pour diverses personnes. 
Dans le secteur des transports, on emploie fréquemment le terme «sécurité» pour faire valoir certaines propositions, exigences et stratégies douteuses. 
D'aucuns ne comprennent pas que la sécurité absolue n'existe pas.  
Si nous voulons une sécurité absolue dans le secteur des transports, je présume que nous pourrions tous voyager à pied, quoiqu'une personne risquerait toujours de se casser une jambe. 
On pourrait aussi envisager une autre mesure moins draconienne mais tout aussi farfelue, soit de réduire les limites de vitesse s'appliquant à tous les modes de transport ou encore d'accroître le prix des billets d'avion ou des véhicules pour rendre ces produits inabordables pour la plupart des consommateurs. 
Certes, nous serions plus en sécurité, mais la société moderne qui met l'accent sur la commodité et l'économie de temps et d'argent ne tolérerait jamais de telles restrictions. 
Il faut alors se demander quel niveau de sécurité nous semble acceptable.  
La Loi sur la sécurité ferroviaire est une mesure législative relativement nouvelle. 
Le projet de loi C-58 vise à insérer dans la loi les modifications recommandées par le comité chargé de l'examen quinquennal prévu dans la loi.  
Le gouvernement a présenté au mois de mai 1996 le projet de loi C-43 qui reprenait 60 des 69 recommandations du comité, mais le projet de loi est mort au Feuilleton lorsque des élections ont été déclenchées en avril 1997.  
Dans son rapport de 1995, le comité affirmait que le transport ferroviaire était un mode de transport des marchandises et des passagers extrêmement sûr. 
En fait, il soutenait que les compagnies de chemins de fer du Canada avait un très bon bilan en matière de sécurité comparativement aux autres modes de transport et aux services ferroviaires d'autres pays. 
Le comité signalait également que les compagnies de chemins de fer avaient grandement accru la sécurité au travail et amélioré leur méthode de travail. 
Le portrait brossé par le Bureau de la sécurité des transports est un peu plus obscur.  
Ces neuf dernières années, les déraillements sur les voies principales ont augmenté de 75 p. 100, passant de 101 en 1988 à 177 en 1997. 
Les trains semblent courir davantage sur les droits de passage que sur les voies ferrées au Canada. 
Le nombre de collisions sur les voies principales a augmenté de 44 p. 100. 
Il y a également de bonnes nouvelles. 
Au cours de la même période, les accidents aux passages à niveau ont diminué de 39 p. 100. Les bonnes nouvelles ne s'arrêtent pas là. 
Les blessures corporelles découlant de tous les genres d'accidents ferroviaires ont diminué de près de 80 p. 100 depuis 1988. 
Le nombre total de décès est assez faible et n'a pas beaucoup varié au cours des neuf dernières années.  
Par suite du déraillement mortel d'un train de voyageurs survenu en 1997 à Biggar, en Saskatchewan, le ministre avait annoncé le report d'un projet de loi modifiant la Loi sur la sécurité ferroviaire, pour permettre d'approfondir l'étude sur les améliorations à apporter.  
Contrairement à la mesure qui l'a précédé, le projet de loi C-58 exige que les compagnies de chemin de fer élaborent et mettent en place des systèmes de gestion de la sécurité. 
Ces systèmes devront être soumis au ministre et approuvés par ce dernier. 
Le ministre pourra aussi enjoindre à une compagnie de chemin de fer de prendre les mesures nécessaires pour corriger un système qu'il juge défectueux.  
Malheureusement, aucun mécanisme n'est prévu pour en appeler d'un arrêté ministériel devant un tribunal indépendant, le Tribunal de l'aviation civile par exemple. 
La rumeur veut que Transports Canada planifie la mise sur pied d'un tribunal canadien sur les transports par chemin de fer, avion, navire et pipeline. 
J'espère sincèrement que cette rumeur se concrétise.  
Une chose surprend dans le projet de loi quand on le compare à son prédécesseur, le C-43, c'est qu'on ait éliminé la modification accordant explicitement aux chemins de fer le droit de passage aux franchissements routiers. 
Le projet de loi C-58 va plus loin que son prédécesseur en ce qu'il augmente les pouvoirs des inspecteurs de la sécurité ferroviaire, en particulier pour ce qui est des franchissements routiers. 
Cela suppose l'élargissement du pouvoir d'accès des inspecteurs et leur pouvoir d'obtenir des documents afin d'exécuter la loi. 
On s'inquiète encore de l'absence de mécanisme indépendant pour en appeler des décisions des inspecteurs de la sécurité ferroviaire.  
Aux termes d'une autre modification, on inscrit directement dans la loi un énoncé de politique décrivant les objectifs visés. 
Ces objectifs visent notamment à pourvoir à la sécurité du public et du personnel et à la protection des biens, et en faire la promotion; à encourager la collaboration et la participation des parties intéressées, à reconnaître la responsabilité des compagnies de chemin de fer en ce qui a trait à la sécurité de leurs activités. 
La souplesse, l'efficience et la modernité sont les nouveaux mots d'ordre du système de réglementation de la sécurité dans le transport ferroviaire au Canada.  
Dans son rapport de 1995, le comité d'examen recommandait que le système de sécurité du transport ferroviaire au Canada soit à la fois non normatif et contrôlé par l'industrie. 
Il préconisait d'améliorer le cadre de sécurité dans son entier, mais de laisser aux autorités compétentes le soin de s'occuper des détails. 
Le projet de loi C-58 contient donc un nombre de modifications de forme visant à rationaliser la réglementation et à réduire les tracasseries administratives.  
Le système rationalisé de réglementation de la sécurité qui est proposé dans le projet de loi C-58 peut se résumer ainsi: le gouvernement continue de fixer les normes en matière de sécurité; les sociétés ferroviaires décident, après consultation avec les autres intervenants, quelles sont les règles de sécurité nécessaires et quelles mesures permettent le mieux de respecter ces normes; le gouvernement approuve les règles, autorise les exemptions, surveille le respect de la loi et, au besoin, prend des mesures d'exécution.  
Le nouveau régime accordera également aux sociétés ferroviaires des exemptions temporaires de la réglementation afin qu'elles puissent effectuer des tests liés au transport par rail. 
Le système de réglementation proposé exigera également que les sociétés ferroviaires consultent les organisations intéressées dans le cadre de leur processus d'établissement de règles de sécurité opérationnelles et également lorsqu'elles demandent des exemptions.  
Le projet de loi C-58 propose des améliorations à la sécurité des propriétés des sociétés ferroviaires. 
Il crée le nouveau poste d'agent de contrôle, dont la tâche consistera à inspecter les personnes et les biens avant l'embarquement dans les trains ou l'entrée dans les zones restreintes, par exemple, une gare de marchandises. 
Le mandat des agents de contrôle est très semblable à celui des agents de contrôle des sociétés aériennes et des aéroports.  
La nouvelle loi est quelque peu alambiquée, mais toute simplification du processus sera bien accueillie par les collectivités qui exigent qu'on cesse d'utiliser le sifflet dans les secteurs résidentiels, pourvu bien sûr que d'autres exigences en matière de sécurité soient respectées.  
Le projet de loi C-58 va également renforcer et préciser les pouvoirs du gouvernement sur les franchissements routiers, y compris le pouvoir du ministre en ce qui concerne la construction, la modification et l'entretien de ces franchissements routiers et la possibilité pour le gouverneur en conseil de prendre des règlements touchant le contrôle de la circulation des véhicules et des piétons aux abords des franchissements routiers. 
Le Cabinet aura le pouvoir d'exiger d'une compagnie de chemin de fer, de l'autorité responsable du service de voirie ou de la personne qui est titulaire de droits sur un franchissement routier d'un examen de la sécurité de celui-ci à la suite d'un accident.  
Un changement important aux règles existantes est l'article du projet de loi C-58 qui va donner la possibilité aux autorités responsables du service de voirie d'avoir accès à tout terrain situé à proximité des franchissements routiers pour y abattre les arbres ou enlever les broussailles pouvant nuire à la sécurité des opérations ferroviaires. 
Cependant, le gouvernement n'a pas corrigé certaines lacunes que les intéressés ont signalées au comité la dernière fois. 
Plus particulièrement, les municipalités ont exprimé des craintes au sujet des responsabilités possibles. 
Elles voulaient également savoir qui allait payer pour le débroussaillage, les contribuables locaux ou les compagnies de chemin de fer. 
C'est une bonne question.  
En conclusion, même si le projet de loi C-58 est nécessairement d'une portée limitée, puisqu'en fait, si on y regarde bien, la majeure partie des dispositions semblent s'appliquer d'une façon ou d'une autre aux franchissements routiers, c'est quand même un pas dans la bonne direction pour parvenir à une meilleure sécurité du transport ferroviaire. 
Même si les modifications proposées semblent être excellentes, le projet de loi est extrêmement complexe. 
Je veux entendre ce qu'en pensent les intéressés au comité avant de me prononcer. 
J'espère qu'on nous donnera le temps de le faire.  
M. Paul Mercier (Terrebonne-Blainville, BQ):
Monsieur le Président, le 3 septembre 1997, en Saskatchewan, un train déraillait et cet accident provoquait un mort. 
Cet accident a incité le gouvernement à accélérer le dépôt d'un projet de loi visant à rafraîchir et à améliorer la sécurité existante en matière ferroviaire et ainsi modifier la loi qui datait de 1989.  
Un comité d'experts a été nommé pour préparer ce nouveau texte législatif et c'est ce projet de loi que nous étudions pour le moment.  
On peut évidemment déplorer qu'il ait fallu un accident et un mort pour inciter le gouvernement à accélérer le dépôt d'un projet de loi dont la nécessité se faisait sentir depuis longtemps. 
Quoi qu'il en soit, si c'est un bon projet de loi qui améliore la sécurité ferroviaire, on ne peut être contre la vertu. 
Il y a cependant un point sur lequel nous ne sommes pas d'accord et sur lequel nous déposerons un amendement. 
Je vais d'ailleurs en parler tantôt.  
Je résumerai, en quelques mots, les objectifs de ce projet de loi. 
Premièrement, il s'agit d'augmenter la capacité du gouvernement de faire corriger, par les compagnies de chemin de fer, les irritants et les facteurs de risques en matière de sécurité et d'environnement.  
Deuxièmement, il améliore également les voies de concertation entre les différents acteurs au niveau des transports ferroviaires, c'est-à-dire les compagnies, le gouvernement, les autorités municipales, les syndicats du secteur ferroviaire et les propriétaires ou locataires de matériel ferroviaire utilisé sur les voies ferrées.  
Troisièmement, le projet de loi vise à réduire les émissions nocives des locomotives et ce, dans l'intérêt de l'environnement.  
Nous partageons complètement l'objectif de ce projet de loi. 
Je me félicite également de l'article 18 qui permettra à mon avis de régler, dans notre région et dans pas mal d'autres, un problème qui préoccupe les citoyens. 
Il s'agit du sifflet des trains. 
L'article en question dit notamment ceci, et je cite: 
Il est interdit d'utiliser le sifflet d'un train sur toute partie du territoire d'une municipalité lorsque les conditions suivantes sont réunies: 
