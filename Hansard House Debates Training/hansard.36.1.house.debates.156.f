Cet endroit était resté un passage à niveau non protégé et, comme je l'ai expliqué, c'est devenu un endroit fréquenté par les amoureux. 
Ce passage non protégé dérangeait tout le voisinage.  
Cela m'a fait comprendre l'importance de mettre en place des mécanismes de protection adéquats aux passages à niveau.  
Je viens de Mississauga. 
Comme cette ville commémore l'anniversaire du déraillement de train qui est survenu il y a vingt ans dans notre localité et qui a donné lieu à la plus importante opération d'évacuation de notre histoire, en temps de paix, la question de la sécurité ferroviaire nous intéresse de très près.  
J'espère que le gouvernement a une vision d'avenir en ce qui concerne l'amélioration des services ferroviaires. 
Je pense, par exemple, au débat qui a cours actuellement quant à savoir si la gare Union devrait être acquise par la ville de Toronto, la province ou le gouvernement fédéral, par un promoteur du secteur privé ou par une combinaison de ces éléments, afin que la préservation de sa structure architecturale soit assurée. 
L'histoire et l'architecture sont certes importantes, mais je pense que l'essentiel, ce sont les installations comme telles. 
J'entrevois le jour où la gare de l'Union sera reliée à l'aéroport Pearson en passant, espérons-le, par le centre-ville de Mississauga. 
J'envisage une ligne peut-être pour assurer le transfert des passagers qui arrivent par vols nolisés de l'aéroport de Hamilton à la gare de l'Union et au centre-ville de ma localité, voire à l'aéroport Pearson.  
Nous pourrions envisager, moyennant une très bonne infrastructure, l'expansion des services ferroviaires pour assurer notamment la navette entre la maison et le travail pour les banlieusards, mais aussi la liaison avec l'aéroport international de Hamilton, l'aéroport Pearson, la gare de l'Union et d'autres points intermédiaires. 
J'espère que ce n'est pas la fin du rail dans ce pays. 
Il est très important que nous examinions sérieusement les questions entourant la sécurité.  
Des lignes de chemin de fer sur courtes distances ont commencé à être mises en place un peu partout dans le pays à la suite de l'abandon de certaines sections de chemin de fer. 
Les entreprises privées se mettent dans certains cas ensemble pour former une coopérative. 
Je pense à la liaison sur courte distance entre Collingwood, Stayner et Barrie dans la partie sud du nord de l'Ontario. 
Cette ligne vise surtout à servir les entreprises locales et à assurer la liaison entre elles. 
Si l'exploitation de telles lignes est confiées au secteur privé, il est primordial d'accorder toute l'attention à la sécurité.  
J'espère que le rôle du service ferroviaire dans notre pays pourra un jour s'apparenter à celui qu'il joue en Europe et au Japon. 
Le train superexpress japonais, le Shinkansen, dessert tout le pays. 
Par exemple, un métro ne serait pas rentable dans les régions où il n'y a pas une forte densité de population permettant que des voyageurs montent et descendent un peu partout. 
La même chose pourrait se produire dans le secteur des chemins de fer. 
On n'envisage pas de le faire aujourd'hui, mais je crois qu'on devrait étudier sérieusement la possibilité.  
Les preuves sont là, le gouvernement réagit aux problèmes de sécurité des services ferroviaires. 
Je crois que les partis d'opposition appuient cette mesure. 
La réponse du gouvernement au rapport du comité d'examen de la Loi sur la sécurité ferroviaire a été déposée au Parlement en juin 1995. 
Il proposait un certain nombre d'amendements.  
Un amendement visait à rationaliser le processus administratif, réduire la paperasserie et assurer un meilleur engagement des organisations intéressées dans la détermination des règles, ce qui, d'après moi, est une réaction très démocratique.  
Un autre amendement visait à réduire le dérangement causé par le bruit des trains, dans les localités. 
Cela concerne les passages à niveau sans barrière. 
À ces intersections, il faut aménager la structure pour faire passer la route en-dessous ou au-dessus des rails, ce qui coûte je crois 7 ou 8 millions de dollars, en chiffres d'aujourd'hui, pour un aménagement aussi simple. 
Les municipalités se préoccupent de l'interaction entre l'automobile et le train et elles préféreraient que les deux soient séparés, mais pas à leurs frais bien entendu.  
