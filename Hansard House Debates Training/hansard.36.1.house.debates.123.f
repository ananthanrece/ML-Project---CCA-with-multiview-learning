La Loi sur les armes à feu contient, à l'article 118, des dispositions sans précédent qui exigent que tous les règlements adoptés en vertu de cette loi soient déposés devant les deux Chambres du Parlement le même jour et que chaque Chambre renvoie ces règlements à un comité compétent, qui peut tenir des audiences publiques.  
Deux grandes séries de règlements ont été traitées de cette manière, la première série ayant été déposée en novembre 1996 et la deuxième, en octobre 1997. 
Le comité permanent a examiné la première série de règlements et a formulé 39 recommandations, dont 38 ont été acceptées intégralement ou en partie. 
En ce qui concerne la deuxième série de règlements, le comité permanent a encore une fois formulé 39 recommandations, dont 35 ont été acceptées intégralement ou partiellement.  
L'objet de mon bref survol est de demander à l'opposition, étant donné la participation considérable des parlementaires à l'élaboration du projet de loi et des règlements, et vu le nombre de modifications et de compromis qui ont été faits lorsque la Chambre a été saisi du projet de loi, pourquoi nous devrions ne serait-ce qu'envisager de remplacer le projet de loi C-68. 
Pourquoi voudrions-nous le faire? 
Rappelons-nous que cette mesure législative bénéficie de l'appui de 82 p. 100 des Canadiens. 
Les partisans de cette motion voudraient nous faire croire que ce projet de loi n'empêche aucunement d'utiliser des armes à feu à des fins criminelles. 
Les députés d'opposition voudront peut-être consulter le Code criminel à cet égard. 
Un nombre considérable d'infractions au Code ont été modifiées pour prévoir une peine minimale de quatre ans d'emprisonnement. 
Ces infractions au Code criminel se trouvaient sous les rubriques: négligence criminelle entraînant la mort, homicide involontaire coupable, tentative de meurtre, fait de causer intentionnellement des lésions corporelles, agression sexuelle armée, agression sexuelle grave, enlèvement, prise d'otage, vol qualifié et extorsion.  
On y trouve d'autres sortes d'infractions criminelles, y compris des activités comme le trafic d'armes, la possession d'armes aux fins de trafic, l'importation et l'exportation d'armes à feu automatiques, sachant qu'elles sont interdites, et la modification du numéro de série d'une arme à feu.  
Nous avons porté une attention très sérieuse aux activités criminelles lorsque nous avons formulé les dispositions du projet de loi C-68 concernant les infractions.  
S'ils prenaient vraiment le temps d'étudier le dossier, les députés d'opposition constateraient également que les peines minimales de quatre ans qui ont été imposées ces deux dernières années ont fait l'objet d'un certain nombre d'appels. 
L'opposition, dans son entêtement, propose que le gouvernement abandonne la politique de l'enregistrement des armes à feu. 
Il y a d'excellentes raisons pour lesquelles l'enregistrement des armes à feu est une bonne chose. 
Je voudrais en aborder quelques-unes.  
Tous les ans, quelque 70 000 armes à feu sont vendues en privé au Canada. 
Par ailleurs, un grand nombre d'armes sont volées, perdues ou autrement comptées pour disparues, car les armes à feu circulent au Canada. 
L'enregistrement de toutes les armes à feu, des carabines et des fusils de chasse aussi bien que des armes de poing contribue à réduire les ventes d'armes sur le marché noir et protège tant les vendeurs que les acheteurs.  
La délivrance de permis aux utilisateurs d'armes à feu constitue un élément central de cette loi. 
Seules les personnes responsables qui, au cours des cinq années précédentes, n'ont pas été trouvées coupables d'une infraction criminelle pourront utiliser des armes à feu. 
Si elles ont été trouvées coupables d'une infraction comprenant de la violence contre une personne, une infraction liée à une activité criminelle, une infraction à la Loi sur les aliments et drogues ou à la Loi sur les stupéfiants ou si elles ont déjà été traitées pour une maladie mentale caractérisée par un comportement violent, elles seront identifiées par le système de délivrance de permis.  
Les gens qui vendent des armes à feu devraient savoir à qui ils vendent. 
Si l'acheteur d'une arme à feu détient un permis, le vendeur aura tout lieu de croire qu'il est un citoyen responsable et respectueux des lois.  
En outre, les détenteurs de permis devront avoir suivi et réussi le cours de maniement des armes à feu et devraient avoir une connaissance élémentaire du maniement et de l'utilisation des armes à feu en toute sécurité.  
En somme, l'enregistrement contribue à la sécurité publique en maintenant les armes à feu hors de portée de ceux qui ne devraient pas en avoir. 
Bon nombre des armes à feu perdues, volées ou disparues finissent par être signalées à la police. 
Un système d'enregistrement peut aider les policiers à rendre ces armes à feu à leurs légitimes propriétaires. 
L'enregistrement peut aider à la restitution de la propriété privée aux propriétaires légitimes qui ont été victimes d'acte criminel. 
Comme les utilisateurs autorisés auront montré qu'ils ne sont pas impliqués dans des activités criminelles et qu'ils sont responsables et comme les armes à feu seront enregistrées, les policiers disposeront d'un outil précieux pour les aider dans leur lutte contre la criminalité.  
Les opposants à la loi prétendent que les criminels ne feront pas enregistrer leurs armes à feu. 
Nous le reconnaissons. 
Cependant, grâce à ses dispositions en matière de délivrance de permis et d'enregistrement, la loi aidera les policiers en leur fournissant des outils additionnels pour porter des accusations contre les criminels et pour lutter contre le crime organisé.  
Le système d'enregistrement fournira aux policiers un précieux outil pour retrouver la trace des armes à feu en remontant la filière de leurs propriétaires précédents. 
La possibilité de retracer les armes à feu constitue un outil précieux pour enquêter sur les crimes. 
Cela aidera à identifier les trafiquants d'armes à feu illégales. 
L'enregistrement permettra d'identifier les entreprises illicites et, grâce aux données balistiques, fournira un moyen d'identifier les armes à feu utilisées lors de crimes jusque-là non résolus.  
Grâce à l'enregistrement des armes à feu, on réussira probablement davantage à porter des accusations contre les auteurs de crimes et à les en trouver coupables. 
Quel parlementaire pourrait s'opposer à cela?  
Personne ne sera étonné d'apprendre que l'entrée illégale d'armes à feu au Canada constitue un problème de grande ampleur. 
Le système d'enregistrement permettra d'enregistrer les armes à feu qui entrent au Canada et qui en sortent, de même que le déplacement de ces armes à l'intérieur du pays. 
Il sera plus facile d'intercepter les cargaisons illégales. 
Les douaniers seront en mesure d'identifier les cargaisons en consultant la base de données du système d'enregistrement. 
On pourra retracer les armes à feu importées au Canada pour la vente grâce à leur histoire au Canada.  
Il importe de souligner qu'en réduisant la contrebande d'armes à feu, la Loi sur les armes à feu et le système d'enregistrement peuvent contribuer à réduire la criminalité.  
La Loi sur les armes à feu vise essentiellement à réglementer des biens qui peuvent causer la mort, tout en permettant leur utilisation légitime par des propriétaires responsables. 
Le système d'enregistrement dont la Loi sur les armes à feu prévoit l'établissement est un aspect important des efforts que déploie le Canada pour faire en sorte que notre mode de vie et nos valeurs soient ceux d'un pays pacifique et sûr.  
L'opposition prétend que la Loi sur les armes à feu confisque la propriété privée. 
Nous avons répété à maintes occasions que le projet de loi vise la réglementation, pas la confiscation.  
La décision qui sera rendue à ce sujet permettra simplement de déterminer si les pouvoirs que la Constitution confère au gouvernement canadien ont été exercés convenablement à l'égard de cette loi. 
Comme point de droit, nous ferons valoir que cela est conforme à la paix, à l'ordre ainsi qu'au bon gouvernement, lesquels relèvent clairement des champs de compétence fédéraux. 
Nous espérons que ce point de vue sera confirmé, et il est fort probable qu'il le soit.  
