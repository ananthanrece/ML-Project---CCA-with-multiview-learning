Question no 62- M. Leon E. Benoit: 
Le gouvernement peut-il présenter en détail la méthode suivie par l'Agence d'examen de la lutte antiparasitaire pour établir les niveaux de la récupération de ses coûts?  
L'hon. Allan Rock (ministre de la Santé, Lib.):
Le recouvrement des coûts est une politique que le gouvernement fédéral a mise en place dans le but de réduire le déficit. 
Le Cabinet a établi le niveau de recouvrement des coûts de l'Agence de réglementation de la lutte antiparasitaire à 22 millions de dollars à sa création en 1995.  
Le processus visant à déterminer l'établissement des niveaux de recouvrement des coûts est précisé dans une politique du Conseil du Trésor. 
L'Agence de réglementation de la lutte antiparasitaire a mis cette politique en oeuvre sous la surveillance de représentants du Conseil du Trésor.  
Ils ont confirmé que les frais proposés étaient fondés sur le coût de la prestation des services.  
Des représentants du Secrétariat du Conseil du Trésor, d'Agriculutre et Agroalimentaire Canada, d'Industrie Canada et de Ressources naturelles Canada ont participé pleinement aux diverses études d'impact. 
Le Secrétariat du Conseil du Trésor a participé à la planification de l'étude d'impact sur les entreprises menée à l'égard des propositions de recouvrement des coûts de l'Agence de réglementation de la lutte antiparasitaire. 
Des représentants du Secrétariat du Conseil du Trésor et d'Industrie Canada ont participé, le 22 avril 1996, à la réunion sur l'étude d'impact sur les entreprises tenue avec les intervenants. 
Des représentants des ministères susmentionnés ont participé à la réunion du 29 mai 1996 du Conseil consultatif canadien provisoire de la lutte antiparasitaire pour discuter des études d'impact. 
Le 20 septembre 1996, le Secrétariat du Conseil du Trésor a convoqué une réunion interministérielle à laquelle participaient le Bureau du Conseil privé, Agriculture et Agroalimentaire Canada, Industrie Canada, Ressources naturelles Canada et l'Agence de réglementation de la lutte antiparasitaire en préparation aux rencontres des 23 et 30 septembre 1996 avec les intervenants.  
Les processus de réaménagement étalbis par la nouvelle agence ont entraîné des économies qui ont permis de réduire la portion du budget à recouvrer à 15 millions de dollars. 
Cette somme a pu être réduite de trois millions additionnels au cours des six prochaines années grâce à des subventions de Santé Canada et d'Agriculture et Agroalimentaire Canada. 
Le montant total à recouvrer, comme il est précisé dans l'Analyse d'impact de la réglementation du 6 avril 1997, est de 12 millions.  
