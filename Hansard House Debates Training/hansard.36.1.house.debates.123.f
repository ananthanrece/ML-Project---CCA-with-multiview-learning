JOUR DÉSIGNÉ-LA LOI SUR LES ARMES À FEU 
Que la Chambre condamne le gouvernement pour son refus de remplacer le projet de loi C-68, Loi sur les armes à feu, par un projet de loi qui sévit contre l'utilisation criminelle d'armes à feu et de révoquer sa politique sur l'enregistrement des armes à feu qui, de l'avis de la Chambre: a) confisque la propriété privée; b) contient des dispositions déraisonnables en matière de perquisition et de saisie; c) viole les lignes directrices du Conseil du Trésor en matière de coûts et avantages; d) constitue un gaspillage de l'argent des contribuables; e) est un affront aux propriétaires d'armes à feu respectueux des lois; f) va exacerber le trafic illicite d'armes à feu.  
M. Chuck Strahl:
Monsieur le Président, je tiens à vous faire savoir, et à indiquer au Bureau, que les députés du Parti réformiste partageront aujourd'hui leur temps et procéderont à des interventions de 10 minutes chacune.  
M. Jim Pankiw:
Monsieur le Président, je suis très heureux de présenter et de défendre la motion dont nous sommes saisis.  
On dit que les mauvaises lois sont la pire forme de tyrannie. 
Le projet de loi C-68, Loi sur les armes à feu, est une mesure législative qui enfreint le droit des Canadiens à la propriété et qui correspond à un énorme gaspillage d'argent.  
Le but du projet de loi C-68 est franchement une insulte à l'égard des trois millions de propriétaires d'armes à feu au Canada. 
La raison de cela deviendra très évidente au cours du débat d'aujourd'hui.  
Pour commencer, je voudrais expliquer les motifs pour lesquels le gouvernement libéral veut imposer l'enregistrement des armes à feu.  
L'actuel ministre de la Santé et ancien ministre de la Justice à l'origine de cette mesure législative a déclaré que seules la police et l'armée devraient avoir accès aux armes à feu.  
Analysons un peu sa déclaration. 
Voilà un avocat de Toronto qui n'a aucune idée de ce qu'est la vie dans le Canada rural. 
Le ministre essaie de faire croire aux Canadiens que criminaliser la possession légale d'armes à feu nous permettra de réduire la criminalité.  
Soyons bien clairs. 
Nous parlons ici de fusils de chasse et de carabines, non pas d'armes de poing. 
Les armes de poing doivent être enregistrées depuis 1934 et nous savons tous à quel point cette mesure a réduit la criminalité, n'est-ce pas?  
Contribuera-t-elle à réduire la criminalité? 
En quoi cela va-t-il réduire l'usage des armes à feu à des fins criminelles?  
Le ministre l'ignore peut-être, mais les criminels n'enregistrent pas leurs armes à feu et lorsqu'ils commettent des entrées par effraction, ils ne se privent pas de voler une arme à feu parce qu'elle est enregistrée. 
J'irais même plus loin: le criminel qui vole une arme à feu ne s'empêchera pas de l'utiliser pour commettre un crime, qu'elle soit ou non enregistrée.  
Il faut savoir d'où viennent ces gens. 
Ils ne comprennent pas que, pour un agriculteur de la Saskatchewan, un fusil de calibre 22 constitue un outil de travail. 
Les avocats de Toronto n'ont pas l'habitude de rencontrer des mouffettes atteintes de rage sur leurs terrains. 
Par ailleurs, un avocat de Toronto pourrait bien faire figure, aux yeux de nombreux agriculteurs de la Saskatchewan, de mouffette enragée. 
Quoi qu'il en soit, l'utilité d'une carabine servant d'outil est très importante pour de nombreux Canadiens qui vivent en région rurale. 
La chose n'est peut-être pas évidente pour ceux qui veulent pratiquer une nouvelle sociologie appliquée.  
Que veulent-ils dire lorsqu'ils affirment que l'enregistrement est une première étape? 
Le ministre aura le pouvoir d'interdire des fusils de chasse de calibre 10 et d'en ordonner la confiscation, sans indemnisation pour leurs propriétaires. 
Ce pourrait ensuite être des fusils de chasse de calibre 12, puis de calibre 16 et enfin de calibre 20 et on finirait par interdire complètement la possession légale d'armes à feu au Canada.  
La motion que nous avons présentée aujourd'hui vise une longue liste de lacunes du projet de loi. 
Je parlerai plus précisément d'un aspect de la motion, soit les lignes directrices du Conseil du Trésor en matière de coûts et avantages. 
La politique du Conseil du Trésor précise que, lorsque le gouvernement s'apprête à instaurer de nouveaux règlements, il doit présenter une analyse des coûts et avantages. 
La politique stipule précisément que: «Au moment d'établir des règlements, les organismes de réglementation doivent démontrer que les avantages de la réglementation l'emportent sur les coûts pour les Canadiens et les Canadiennes, leurs gouvernements et leurs entreprises et doivent s'assurer que les ressources limitées dont le gouvernement dispose vont aux secteurs dans lesquels elles procureront les plus grands avantages.»  
Le nouveau système d'enregistrement des armes à feu créé en vertu du projet de loi C-68 n'est pas à la hauteur de ce critère du Conseil du Trésor. 
Le gouvernement a notamment laissé de nombreuses questions importantes sans réponse. 
Par exemple, quel est le nombre approximatif de personnes qui obtiendront un permis? 
Le gouvernement l'ignore. 
Combien d'armes à feu seront enregistrées? 
Cela reste indéterminé. 
Quelles seront les répercussions pour les entreprises et l'économie? 
C'est à voir. 
Combien d'emplois seront perdus? 
Combien d'entreprises devront fermer leurs portes? 
Nous l'ignorons. 
Cette réglementation améliorera-t-elle la sécurité publique? 
Il est évident que les données que nous verrons aujourd'hui nous incitent fortement à penser que non. 
Comme ces questions restent sans réponse, il est impossible de savoir si l'enregistrement sera une mesure efficace par rapport à ses coûts. 
Malgré tout, le gouvernement insiste pour implanter le système d'enregistrement. 
Parce que c'est la première étape de mesures visant à éliminer la possession légale d'armes à feu au Canada.  
L'enregistrement devait débuter le 1 er octobre, mais la date a été repoussée au 1 er décembre. 
Au départ, le système d'enregistrement devait coûter 85 millions de dollars. 
Les prévisions se situent maintenant bien au-delà des 120 millions. 
Selon certaines estimations, le système pourrait coûter plus de 500 millions de dollars. 
Compte tenu de l'importance des montants en jeu, nous pourrions convenir, je crois, qu'il vaudrait mieux consacrer cet argent aux soins de santé, par exemple, à l'éducation ou encore à la réduction des impôts des contribuables à revenus faibles ou moyens, qui sont surchargés à cause des habitudes dépensières du gouvernement libéral. 
Celui-ci n'a fait aucune étude de rentabilité sur cette loi parce qu'il ne voulait pas gêner ses propres efforts pour éliminer la possession légale d'armes à feu au Canada.  
Je suis très heureux que le député de Yorkton-Melville ait appuyé ma motion. 
