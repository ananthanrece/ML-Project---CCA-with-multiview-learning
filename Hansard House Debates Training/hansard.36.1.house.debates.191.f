Les produits culturels ne sont pas de simples biens commerciaux que l'on peut emballer et vendre. 
Les produits et les services culturels sont différents des produits et services d'autres industries et doivent être traités différemment.  
Cependant, le Canada n'est pas tout seul dans ses efforts pour promouvoir la culture et les industries culturelles. 
Comme le Canada, beaucoup de pays appuient directement leurs industries culturelles.  
