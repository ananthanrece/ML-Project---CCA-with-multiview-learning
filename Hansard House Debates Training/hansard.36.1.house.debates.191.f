Le gouvernement du Canada croit que la population doit avoir accès aux produits culturels étrangers, mais il reconnaît aussi que nous devons nous garder un créneau pour pouvoir nous exprimer. 
Notre culture est un élément essentiel de notre existence. 
L'échange d'histoires et d'idées et la création d'une meilleure compréhension mutuelle chez les Canadiens sont des manières efficaces de bâtir une société saine et multiculturelle. 
