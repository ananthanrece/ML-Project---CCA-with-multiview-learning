Mme Eleni Bakopanos (secrétaire parlementaire du ministre de la Justice et procureur général du Canada, Lib.):
Monsieur le Président, lorsque le ministre des Ressources naturelles a annoncé l'orientation future de la Devco, il a fait savoir que la direction de la Devco voulait examiner les offres globales en matière de ressources humaines avec les représentants syndicaux. 
Certaines de ces discussions ont déjà eu lieu.  
Les 111 millions de dollars qui ont été approuvés par le gouvernement pour des mesures d'adaptation de la main-d'oeuvre comprennent: 60 millions de dollars pour un programme d'encouragement à la retraite anticipée, 46 millions de dollars pour des indemnités de départ et 5 millions de dollars pour la formation des employés qui recevront une indemnité de départ.  
Les critères d'admission au programme d'encouragement à la retraite anticipée ne sont pas tombés du ciel. 
Ce sont les critères qui avaient été négociés, en 1996, entre la Devco et ses syndicats, par le truchement d'un comité mixte de planification. 
Il s'agit des critères s'appliquant, aux termes des conventions collectives de la Devco, au programme d'encouragement à la retraite anticipée qui s'inscrirait dans le cadre de toute autre réduction d'effectifs.  
Je tiens à préciser que le programme d'encouragement à la retraite anticipée n'a rien à voir avec les prestations de retraite auxquelles ont droit les employés de la Devco ayant participé à l'un des régimes de pension de l'entreprise. 
Les travailleurs seront toujours admissibles à leurs prestations de retraite.  
Je tiens également à préciser que la Devco respectera les dispositions des conventions collectives qu'elle a signées avec ses syndicats. 
Selon certaines de ces dispositions, en cas de fermeture d'une mine, les employés sont mis à pied selon l'ordre inverse d'ancienneté.  
Cela signifie que, parmi les employés qui ont le plus d'ancienneté, mais qui ne seront pas admissibles au programme d'encouragement à la retraite anticipée, nombreux sont ceux qui continueront de travailler.  
