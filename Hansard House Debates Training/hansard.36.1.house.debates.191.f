M. Jason Kenney (Calgary-Sud-Est, Réf.): 
Il est absolument vrai que nous tenons à réduire de beaucoup les sommes gaspillées dans les programmes de genre de eux que ce ministère administre.  
Nous croyons que les Canadiens peuvent protéger leur patrimoine national et leur identité culturelle mieux que les bureaucrates et les politiciens fédéraux. 
Nous croyons que les programmes de distribution de drapeaux gratuits et les subventions à des groupes d'intérêt particulier qui demandent toujours plus d'argent illustrent parfaitement ce qui ne va pas dans ce gouvernement. 
À notre avis, accorder des subventions énormes à des sociétés d'État déjà obèses qui gaspillent leurs gros budgets à entretenir des bureaucrates et des cadres intermédiaires donne peu de résultats concrets.  
À cet égard, nous sommes fiers d'avoir les mêmes priorités que les Canadiens. 
Si j'interviens aujourd'hui, c'est pour parler du dernier accès de folie de la ministre du Patrimoine canadien. 
Après avoir été très efficace dans l'opposition, la ministre est maintenant d'une nullité gênante pour le Cabinet, le gouvernement et, je crois, le Parlement tout entier en raison du genre d'extrémisme politique qu'elle pratique.  
Le projet de loi C-55 vise à faire reculer le Canada des décennies et à le ramener à l'ère du protectionnisme, une ère au cours de laquelle les pays étaient fermés sur eux-mêmes plutôt qu'ouverts sur le monde et où l'on faisait des campagnes électorales du genre de celle que la ministre du Patrimoine canadien a menée contre l'Accord de libre-échange nord-américain en 1988.  
Lorsque le Parti libéral était l'opposition, ses députés disaient aux Canadiens que le libre-échange sonnerait le glas du Canada et qu'en mettant fin à notre souveraineté économique, il nous ferait aussi perdre notre souveraineté culturelle et politique. 
Je me souviens que la ministre du Patrimoine canadien et ses collègues jouaient alors les prophètes de malheur, affirmant que le libre-échange entre ces deux grands partenaires que sont le Canada et les États-Unis engendrerait le désastre économique. 
Nous savons tous que cet argument s'est révélé spécieux.  
Le plus important moteur de la croissance économique du Canada au cours de la dernière décennie a été le commerce bilatéral avec les États-Unis, ce pays avec lequel nous effectuons 80 p. 100 de nos échanges commerciaux, soit environ un milliard de dollars par jour. 
Le projet de loi C-55 vise une toute petite partie des échanges annuels de 350 milliards de dollars de plus entre nos deux pays, soit le marché de la publicité de 400 millions de dollars des magazines.  
Pas un seul Canadien ordinaire, à l'extérieur du milieu politique étrange et tordu que constituent Ottawa et le caucus libéral, ne voulait que nous ramenions l'économie des décennies en arrière en consacrant le protectionnisme dans ce projet de loi, c'est-à-dire en imposant des restrictions à la liberté d'expression, en pénalisant les publications américaines qui contiennent de la publicité canadienne. 
Pas un seul Canadien ne me l'a demandé.  
Nous sommes plutôt saisis de mesures comme le projet de loi C-55, qui concerne les préoccupations obscures d'un groupe d'entreprises présentant un intérêt relativement limité.  
Mes électeurs m'ont fait savoir qu'ils trouvent scandaleux que le gouvernement donne suite à ce projet de loi. 
La ville de Calgary, que je représente en partie, abrite une importante industrie de matières plastiques qui est en pleine expansion. 
Par la voix de leur représentante commerciale, Mme Barshefsky, nos collègues américains nous ont fait savoir que l'industrie canadienne des matières plastiques pourrait faire l'objet de mesures de représailles si le projet de loi à l'étude était adopté. 
Qu'est-ce que tout cela signifie en réalité? 
Cela pourrait par exemple entraîner l'imposition de tarifs qui pourraient s'avérer désastreux pour l'industrie des matières plastiques et pour les habitants de ma circonscription.  
Le projet de loi est tout simplement irréfléchi. 
Toutefois, le gouvernement continue d'affirmer qu'il est nécessaire. 
La question ne se pose donc pas.  
Et même si c'était le cas, cela relève à mon avis de la liberté de chacun. 
Dans notre examen des mesures législatives, nous devons plutôt nous reporter aux principes fondamentaux. 
Le premier principe qui devrait régir toute action de la part du gouvernement devrait être d'accroître la liberté. 
Je sais que c'est un mot qui ne plaît pas à tous. 
Je crois que les Canadiens devraient avoir le choix, la liberté de choisir les revues qu'ils lisent, les publications et les périodiques qu'ils reçoivent sans que le gouvernement ne vienne leur dire ce qui est acceptable et sous quelle présentation.  
C'est vraiment le libéralisme classique, rétro, des années 60, marqué au coin du protectionnisme, du repli sur soi-même et de l'esprit de clocher, qui resurgit dans ce projet de loi.  
Si un Canadien veut commander la version dédoublée du Sports Illustrated , pourquoi l'en empêcherait-on? 
Quel mal y a-t-il à cela? 
Quels torts cause-t-on à la culture canadienne en laissant aux gens le droit d'exercer leur libre choix, en les laissant décider ce qu'ils veulent lire? 
Je ne peux tout simplement pas comprendre les raisons justifiant ce projet de loi.  
En essayant d'imposer des sanctions gouvernementales fondamentalement à ceux qui consomment ce type de produits, non seulement nous violons leur liberté d'expression, nous menaçons clairement une partie importante de notre économie et de notre croissance économique.  
J'attends encore une réponse convaincante de la part du gouvernement aux menaces très graves de nos alliés américains qui ont menacé d'avoir recours à des droits négatifs et des mesures de compensation si nous allons de l'avant avec ce projet de loi, comme le gouvernement semble maintenant décidé à le faire.  
Je sais que ce projet de loi est très semblable, quant au fond et à la forme, à une mesure législative qui a été adoptée au cours de la dernière législature, mais que l'Organisation mondiale du commerce avait jugé contraire au GATT. 
Tout ce que je peux dire, c'est que le Canada, en tant que pays commerçant, doit être un champion du libre-échange. 
Il est clair que nous devons respecter les décisions de l'Organisation mondiale du commerce. 
Nous ne devons pas essayer, en ayant recours à des projets de loi comme celui-ci, de circonvenir des décisions de l'OMC et d'autres organismes de règlement des différends. 
Ce faisant, nous ruinons notre crédibilité en tant que pays commerçant sur la scène internationale. 
Pour cette raison, je pense que nous causons encore davantage de torts à notre base économique en tant que pays commerçant et exportateur sur la scène internationale.  
Pour toutes ces raisons, au nom de mes électeurs, je vais m'opposer avec véhémence à ce projet de loi. 
Même si le gouvernement veut adopter cette mesure envers et contre tous à la Chambre en ayant recours à la motion de clôture aujourd'hui, je vais m'opposer à ce projet de loi et appuyer les amendements de mon collègue de Dauphin-Swan River.  
