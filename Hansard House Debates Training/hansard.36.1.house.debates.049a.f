HANSARD RÉVISÉ * NUMÉRO 049A 
CHAMBRE DES COMMUNES 
Le jeudi 18 décembre 1997 
La séance est ouverte à 17 heures.  
Prière 
Le vice-président:
Je désire informer la Chambre que, conformément au paragraphe 28(4) du Règlement, j'ai convoqué la Chambre aujourd'hui à la seule fin d'accorder la sanction royale à certains projets de loi.  
LA SANCTION ROYALE 
Le vice-président:
J'ai l'honneur d'informer la Chambre que j'ai reçu le message suivant: 
Résidence du Gouverneur général, 
le 18 décembre 1997 
Monsieur le Président, 
J'ai l'honneur de vous aviser que le très honorable Roméo LeBlanc, Gouverneur général du Canada, se rendra à la Chambre du Sénat aujourd'hui, le 18 décembre 1997, à 17 heures, afin de donner la sanction royale à certains projets de loi.  
Veuillez agréer, monsieur le Président, l'assurance de ma haute considération.  
Le secrétaire du Gouverneur général 
* * * 
MESSAGE DU SÉNAT 
Le vice-président:
J'ai l'honneur d'annoncer à la Chambre que le Sénat lui a adressé un message pour l'informer qu'il a adopté certains projets de loi sans amendement.  
L'huissier du bâton noir apporte le message suivant: 
Monsieur le Président, c'est le désir de Son Excellence le Gouverneur général que cette honorable Chambre se rende immédiatement auprès de lui dans la salle du Sénat.  
En conséquence, le Président et les députés se rendent au Sénat.  
Et de retour: 
Le vice-président:
J'ai l'honneur de faire savoir à la Chambre que, lorsqu'elle s'est rendue au Sénat, il a plu à Son Excellence le Gouverneur général de donner, au nom de Sa Majesté, la sanction royale aux projets de loi suivants: 
Projet de loi C-16, Loi modifiant le Code criminel et la Loi d'interprétation (arrestation et entrée dans les habitations)-Chapitre 39.  
Projet de loi C-2, loi constituant l'Office d'investissement du régime de pensions du Canada et modifiant le Régime de pensions du Canada, la Loi sur la sécurité de la vieillesse et d'autres lois en conséquence-Chapitre 40.  
Comme il est 17 h 16, la Chambre s'ajourne à 11 heures le lundi 2 février 1998, conformément aux paragraphes 24(1) et 28(2) du Règlement.  
(La séance est levée à 17 h 16.) 
