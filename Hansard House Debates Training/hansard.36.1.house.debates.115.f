L'hon. Andy Scott (solliciteur général du Canada, Lib.):
Monsieur le Président, je remercie la Chambre d'avoir donné son consentement. 
Je viens de quitter la salle de lecture, où nous tenions une séance de familiarisation sur notre initiative de prévention du crime. 
J'étais ravi de voir le nombre de représentants de votre personnel qui participaient à cette séance. 
Vous méritez des félicitations.  
Je suis heureux de prendre la parole à la Chambre aujourd'hui à l'étape de la troisième lecture du projet de loi C-3, qui prévoit l'établissement d'une banque nationale de données génétiques au Canada. 
La Loi sur l'identification par les empreintes génétiques fera du Canada un des rares pays du monde à avoir un système national de ce genre. 
Je suis très heureux de dire que cette mesure législative avant-gardiste est un élément essentiel de notre stratégie visant à rendre nos collectivités plus sûres. 
La sécurité publique est ma priorité. 
C'est pourquoi le projet de loi C-3 est un aspect important de mon engagement envers les Canadiens à cet égard.  
Cette mesure législative vise à créer, pour les policiers, un outil pratique d'application de la loi qui résistera à l'épreuve du temps. 
Depuis que le projet de loi a été présenté en septembre dernier, les députés ont été prudents dans l'étude de cette mesure législative. 
Cette approche est louable, étant donné la portée des questions entourant l'utilisation et le risque d'une mauvaise utilisation des profils d'identification génétique et des échantillons, ainsi que les considérations légales et éthiques. 
C'est pourquoi le projet de loi C-3 a été renvoyé au comité avant son étude en deuxième lecture.  
J'ai souhaité qu'on propose des améliorations et je pensais bien qu'après tous ces travaux, nous aurions un meilleur projet de loi. 
À mon avis, c'est précisément ce qui s'est passé.  
Je veux maintenant expliquer comment le projet de loi C-3 a été élaboré, comment il sera appliqué et quels seront ses avantages, et enfin, comment il a été amélioré grâce aux travaux du Comité permanent de la justice et des droits de la personne.  
La présentation de la Loi sur l'identification par les empreintes génétiques a constitué la deuxième phase de la stratégie du gouvernement en matière d'ADN.  
La présentation du projet de loi sur l'identification par les empreintes génétiques marque la deuxième étape de la stratégie du gouvernement concernant l'ADN. 
La première étape importante a notamment consisté à établir les exigences à respecter pour obtenir des échantillons d'ADN dans le cadre d'enquêtes criminelles. 
En juillet 1995, des modifications au Code criminel ont donc été adoptées afin d'autoriser la police à demander un mandat pour prélever des échantillons d'ADN sur des suspects dans des enquêtes criminelles.  
Cette mesure législative a fourni à la police un instrument efficace qui a aidé à résoudre des centaines de crimes graves. 
Il est efficace parce qu'il a permis d'éliminer des suspects et de prononcer des condamnations. 
Il a également servi à obtenir des plaidoyers de culpabilité, évitant à des victimes le traumatisme de la comparution, et réduit les frais généraux des enquêtes et des poursuites. 
Il a aussi résisté à une contestation en vertu de la Constitution.  
Ayant fermement mis en place la loi autorisant la police à obtenir un mandat afin de prélever des échantillons d'ADN, le gouvernement passe à la deuxième étape de son initiative et crée une structure permettant le stockage des profils d'identification génétique et l'utilisation de ces profils dans des enquêtes sur des crimes graves.  
Une banque nationale de données génétiques fera beaucoup pour aider la police à établir des liens avec un suspect, quand des éléments de preuve sont laissés sur le lieu d'un crime. 
La capacité de stocker et d'extraire ensuite des profils d'identification génétique permettra de raccourcir des enquêtes et de prévenir des récidives. 
Tout cela est synonyme d'une plus grande sécurité pour tous les Canadiens. 
De plus, le projet de loi C-3 autorisera la police à prélever des échantillons d'ADN chez des contrevenants reconnus coupables d'infractions criminelles désignées. 
Ces dernières incluent les crimes de très graves préjudices corporels, dont les homicides et les agressions sexuelles, qui seraient liées à des substances corporelles trouvées sur le lieu d'un crime et pouvant servir d'éléments de preuve.  
Les échantillons seront analysés et les profils tracés seront enregistrés dans la banque de données, dans le fichier des condamnés. 
La banque de données contiendrait aussi un fichier de criminalistique renfermant les profils d'identification génétique établis à partir de substances corporelles trouvées sur les lieux de crimes. 
Grâce à cette structure, les profils peuvent faire l'objet de renvois dans le système.  
Les avantages d'un tel système sont évidents. 
Les renseignements stockés dans la banque de données génétiques aideront les policiers à identifier plus rapidement les suspects lorsqu'ils n'ont pas d'indices, et à repérer les récidivistes qui exercent leur activité dans plus d'un territoire. 
Ce système pourrait aussi dissuader les contrevenants de récidiver, puisqu'ils sauront que leur profil d'identification génétique se trouve dans la banque de données et qu'ils ne pourront contrevenir à la loi impunément.  
Tout au long de l'élaboration du projet de loi C-3, le gouvernement fédéral a sollicité l'avis et les conseils de nombreux groupes et intervenants, notamment ceux qui sont sur la première ligne.  
En outre, le Comité permanent de la justice et des droits de la personne a tenu 15 audiences sur le projet de loi et a entendu des représentants de 17 organismes, y compris des associations de policiers, des groupes de défense des victimes et des organisations juridiques.  
Ces consultations ont permis de constater qu'il existait un appui solide à la création d'une banque nationale de données génétiques, mais qu'il y avait aussi des préoccupations concernant les valeurs canadiennes que sont la protection de la vie privée et du public, et le respect des droits individuels garantis par la charte. 
Afin de donner suite à ces préoccupations et d'améliorer le projet de loi dans son ensemble, un certain nombre d'amendements ont été apportés depuis que celui-ci a été déposé, l'automne dernier.  
Divers groupes d'intérêts, y compris le commissaire à la protection de la vie privée, le Barreau du Québec et le Comité canadien d'action sur le statut de la femme, ont laissé entendre que le projet de loi ne renfermait pas des garanties suffisantes pour empêcher l'utilisation de profils d'identification génétique provenant des échantillons fournis par les victimes, les suspects innocentés et les personnes qui fournissent volontairement des échantillons pour aider la police.  
Le gouvernement a donc déposé une motion précisant que l'accès à l'information dans le fichier de criminalistique sera interdit de façon permanente, si ces renseignements ont trait à une victime, ou à une personne qui n'est plus considérée comme un suspect dans une enquête criminelle.  
L'analyse des empreintes génétiques a beaucoup progressé depuis qu'elle a été utilisée pour la première fois, il y a dix ans, par le système de justice pénale. 
Comme l'a mentionné un membre du comité, une empreinte digitale est une impression qu'on laisse, tandis que l'empreinte génétique est une partie de soi.  
Pour protéger davantage la vie privée des innocents, le projet de loi contient une nouvelle disposition qui précise que les renseignements concernant les empreintes génétiques doivent être rendus inaccessibles une fois pour toutes s'ils concernent quelqu'un qui n'est plus considéré comme suspect.  
Lors des audiences du comité qui portaient sur le projet de loi, plusieurs témoins et membres du comité ont dit que l'on pourrait allonger la liste des infractions désignées de manière à inclure d'autres infractions graves pour lesquelles une preuve fondée sur les empreintes génétiques pourraient s'avérer utile.  
Le comité a tenu compte des remarques en ce sens en ajoutant l'infanticide à la liste des infractions primaires et en élargissant la liste des infractions secondaires de manière à ce qu'elle comprenne la conduite dangereuse et avec capacité affaiblie causant des lésions corporelles ou la mort, de même qu'un certain nombre d'infractions sexuelles.  
À mon avis, ces modifications seront très utiles aux policiers et permettront d'améliorer la sécurité publique. 
Je rappelle aux députés que la police peut déjà, au moment de l'arrestation, faire des prélèvements aux fins de l'analyse des empreintes génétiques, si elle a un mandat qui l'y autorise. 
Les ministères de la Justice et du Solliciteur général ont tenu de vastes consultations sur cette question, et le Comité permanent de la justice et des droits de la personne a étudié celle-ci en profondeur. 
La grande majorité des personnes consultées se sont dites d'avis que le prélèvement de substances corporelles une fois que l'individu est trouvé coupable respectera les droits de tous les Canadiens en vertu de la Charte.  
Elles pensent aussi que les prélèvements au moment de l'arrestation ou de la mise en accusation risqueraient fort d'être déclarés anticonstitutionnels. 
Comme bien des gens et bien des organismes ont continué d'exercer des pressions afin que le gouvernement élargisse la portée de cette disposition, ma collègue, la ministre de la Justice a demandé un avis juridique impartial à trois des juges les plus éminents de notre pays.  
Chacun a conclu qu'une proposition donnant le droit de prélever des échantillons au moment de l'arrestation pour les inclure dans une banque de données génétiques ne pourrait résister à une contestation fondée sur la charte des droits. 
Certains députés ont repoussé les avis juridiques en soutenant que la charte n'est qu'un obstacle à la justice.  
Le Parlement a le devoir d'exercer son pouvoir d'une manière qui respecte la charte. 
Prélever des échantillons pour la banque des données au moment de la condamnation plutôt qu'au moment de l'arrestation ou de l'inculpation n'empêchera pas les policiers d'accomplir leur travail.  
Cela fournira aux policiers un outil d'enquête efficace qui leur permettra d'effectuer leur travail et assurera que le pouvoir d'utiliser cet outil soit conforme à l'exigence que nous impose la Constitution telle que la Cour suprême l'a définie récemment.  
Je le répète, le projet de loi qui sort des audiences du comité est plus fort. 
