LE TOKAMAK DE VARENNES 
M. Stéphane Bergeron (Verchères, BQ):
Monsieur le Président, je veux, ce soir, faire suite à une question que je posais, le 15 mai dernier, à la ministre de l'Environnement afin de savoir si elle reconnaissait la pertinence de la recherche et du développement d'énergies renouvelables, comme la fusion nucléaire que s'emploie à développer le Tokamak de Varennes dans le but de faire face au phénomène des changements climatiques.  
Dans la mesure où la ministre de l'Environnement confirme cette pertinence, le gouvernement fédéral serait désormais malvenu de soutenir qu'il ne peut poursuivre sa participation au projet Tokamak de Varennes.  
Justement, hier, dans le cadre d'un autre débat sur la motion d'ajournement portant sur ce projet de recherche des plus prometteurs, le secrétaire parlementaire du ministre des Ressources naturelles répétait que le retrait de la contribution fédérale au Tokamak de Varennes résultait d'un choix budgétaire découlant d'impératifs financiers difficiles.  
Avant d'obtenir une réponse analogue, ce soir, je vous dis qu'il n'en est rien. 
Non seulement l'argent est aujourd'hui disponible, puisque le gouvernement fédéral accumule maintenant les surplus, mais il s'agit d'une décision irresponsable du point de vue budgétaire. 
À quoi bon investir dans le développement de compétences techniques et scientifiques de haut niveau, si ces cerveaux doivent par la suite s'expatrier pour pouvoir mettre leur expertise à profit?  
Avec la fermeture du Tokamak de Varennes, les spécialistes formés chez nous aux frais des contribuables iront exercer leur savoir dans le domaine de la fusion nucléaire au Japon, aux États-Unis et au sein de la communauté européenne. 
Ainsi, d'ici quelques années, le Canada sera donc vraisemblablement contraint d'acheter à prix d'or une technologie qu'il aura lui-même contribué à développer à coups de millions de dollars. 
Tout cela coûtera finalement beaucoup plus cher au Canada que son investissement annuel de 7,2 millions de dollars.  
Quelle démonstration édifiante d'une saine gestion des finances publiques! 
Quel esprit visionnaire! 
J'ajoute que le Tokamak de Varennes, par les très hauts standards de qualité qu'il impose aux plans scientifique et technologique, a permis à ses partenaires de développer une expertise à la fine pointe de la technologie. 
La compagnie MPB Technologies, par exemple, a pu décrocher un contrat de 64 millions de dollars grâce à la technologie développée en collaboration avec le Centre canadien de fusion magnétique.  
On estime que les retombées économiques que génère le Tokamak de Varennes rapportent beaucoup plus au gouvernement fédéral, en termes de retombées fiscales, que son investissement annuel de 7,2 millions de dollars.  
Dans le but d'éviter le démantèlement prochain du patrimoine technologique du Tokamak de Varennes, et puisque le ministre des Ressources naturelles s'est montré ouvert à cette éventualité, j'aimerais savoir de quelle façon le gouvernement entend mettre à contribution les installations et le savoir-faire de l'équipe du Tokamak dans le cadre d'études connexes s'inscrivant dans sa stratégie sur les changements climatiques. 
Ne pourrait-on pas continuer de développer l'expertise du Tokamak de Varennes, reconnue mondialement, dans le domaine des micro-ondes?  
Le Forum national sur le changement climatique déplorait, hier, les faibles moyens mis de l'avant par le gouvernement fédéral pour lutter contre les émissions de gaz à effets de serre. 
C'est d'autant plus inquiétant, qu'au tournant de l'an 2000, selon le commissaire à l'environnement et au développement durable, les émissions de gaz à effets de serre auront connu une hausse approximative de 11 p. 100 par rapport au niveau de référence de 1990.  
Une telle statistique rend encore plus pertinente l'utilisation future de formes d'énergies propres et sécuritaires, telle la fusion magnétique, que s'emploie à développer le Tokamak de Varennes. 
L'alternative énergétique proposée par le Tokamak représente un risque environnemental beaucoup moins sérieux que la technologie mise de l'avant par les réacteurs CANDU qui, elle, bénéficie toujours d'un soutien indéfectible de la part du gouvernement fédéral.  
C'est une entreprise à long terme à laquelle le Tokamak de Varennes peut contribuer positivement, pour autant que le gouvernement lui permette de le faire.  
