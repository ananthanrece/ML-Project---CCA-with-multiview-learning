LES COMITÉS DE LA CHAMBRE 
ENVIRONNEMENT ET DÉVELOPPEMENT DURABLE 
M. Rick Laliberte (Rivière Churchill, NPD):
Monsieur le Président, je propose que le troisième rapport du Comité permanent de l'environnement et du développement durable, présenté le 25 mai 1998, soit agréé.  
-Je voudrais saisir cette occasion pour attirer l'attention de mes collègues de la Chambre des communes et de tous les citoyens de notre merveilleux pays appelé Canada sur un rapport d'une importance cruciale qu'a déposé le Comité permanent de l'environnement et du développement durable.  
Le troisième rapport de notre comité s'intitulait «L'intérêt public d'abord! 
L'application des lois canadiennes sur la pollution». 
Il a été déposé le 25 mai 1998. Comme c'est la Semaine de l'environnement, le temps est tout indiqué pour mettre cette question en relief et pour informer tous les députés des détails que renferme ce rapport du comité.  
Conformément à l'article 108 du Règlement, le Comité permanent de l'environnement et du développement durable a procédé à l'étude de l'application de la Loi canadienne sur la protection de l'environnement et des dispositions antipollution de la Loi sur les pêches.  
Le comité a formulé 24 recommandations. 
Voici les points saillants de quelques-unes d'entre elles.  
Le comité a aussi recommandé que la ministre de l'Environnement et le ministre des Pêches et des Océans élaborent et publient une politique globale d'observation et d'application des dispositions antipollution de la Loi sur les pêches dans les six mois du dépôt de ce rapport.  
Il a recommandé que la ministre de l'Environnement s'assure que les sociétés réglementées soient informées de leurs obligations légales aux termes des lois et de la réglementation environnementales du gouvernement fédéral et du fait que celles-ci continuent de s'appliquer et doivent être respectées.  
Dans bien des cas, des ententes conclues entre les compétences provinciales et fédérales ont négligé de souligner que les obligations légales aux termes des lois fédérales sont toujours inchangées.  
Le comité a recommandé que le nouveau projet de loi sur la protection de l'environnement, le projet de loi C-32, permette de désigner des inspecteurs et des enquêteurs dotés des pleins pouvoirs d'un agent de la paix pour assurer la protection de notre environnement. 
Le projet de loi C-32 fait actuellement l'objet d'un examen à la Chambre. 
Les inspecteurs et les enquêteurs qui travaillent sur place ont très peu de pouvoir pour imposer des amendes, mis à part le fait qu'ils peuvent donner des avertissements et entamer le processus de poursuite.  
Il a aussi recommandé que la ministre crée sans tarder une capacité professionnelle d'analyse et de collecte de renseignements. 
C'est essentiel, car la pollution touche nos collectivités, notre santé et nos familles. 
Nous devons donc créer un organisme de renseignement au sein des forces de police, comme la GRC, la police provinciale, ou les agents des douanes qui contrôlent nos frontières.  
Il a recommandé également une disposition de protection des informateurs pour tout ce qui touche aux obligations du fédéral. 
Il y aura des cas où ils devront étendre leur autorité et mettre en lumière les infractions que leur compagnie ou leur employeur commet à l'égard de notre environnement. 
C'est une recommandation cruciale qui met le gouvernement au défi.  
La ministre devrait revoir la structure actuelle de l'application de la loi et créer des directions régionales afin que les décisions soient prises non pas par des gestionnaires, mais par les membres du personnel chargé de faire respecter la loi. 
L'application doit relever du personnel qui en est chargé et non des gestionnaires ou des politiciens.  
Une autre recommandation est de retarder la signature du sous-accord sur l'application proposé par le Conseil canadien des ministres de l'environnement. 
Dans l'accord d'harmonisation récemment signé, il y a un sous-accord qui doit être présenté plus tard. 
Nous recommandons fortement que l'on remette cela, car avec la structure actuelle on ne fait pas respecter les responsabilités fédérales.  
La ministre devrait publier toutes les données sur l'application de la Loi canadienne sur la protection de l'environnement, de la Loi sur les pêches et de la Loi sur les additifs à base de manganèse. 
Il faut publier toutes ces données pour que les médias les étudient dans l'intérêt du public et pour que les dirigeants et les politiques des assemblées législatives des provinces et des territoires et de la Chambre des communes examinent à fond les moyens d'application de la loi qui existent au Canada. 
Les lois sur l'environnement sont particulièrement importantes pour la santé future de nos enfants.  
Le ministre de l'Environnement devrait être tenu de publier et de déposer au Parlement un rapport annuel détaillé sur les mesures prises l'année précédente à l'égard des lois et règlements dont la LCPE et la Loi sur les pêches confient la responsabilité au ministère.  
Si les divers niveaux de gouvernement ne peuvent collaborer pour présenter un rapport aux Communes, il y a vraiment quelque chose qui cloche dans les relations que nous avons avec les ministères provinciaux.  
Une autre recommandation importante exhorte le gouvernement et le ministre de l'Environnement à procéder à une étude approfondie afin de déterminer si le méthylmercure rejeté dans le milieu aquatique lorsque des terres sont inondées à cause de nouveaux réservoirs, de barrages ou d'aménagements hydroélectriques sur les grandes rivières des régions septentrionales s'accumule dans le poisson et tout le reste de la chaîne alimentaire. 
Cette étude s'impose parce que, aux termes du règlement de la LCPE, le mercure est considéré comme une substance naturelle et non comme un produit toxique répandu par l'homme dans l'environnement. 
Cependant, étant donné que la construction de barrages fait augmenter la présence de mercure dans la chaîne alimentaire, nous devons creuser la question pour trouver des solutions.  
La recommandation la plus importante veut que le ministre demande et que le gouvernement du Canada accorde plus de ressources pour pouvoir faire respecter comme il se doit la législation sur l'environnement. 
À maintes reprises, les témoins interrogés au comité ont dit que les ressources faisaient défaut à cause des deux étapes de la revue des programmes. 
Cette revue était de nature financière et n'avait pas pour but de vérifier si l'application de la législation permettait de protéger l'environnement. 
Ces examens visaient à déterminer où l'on pourrait exercer des compressions qui réduiraient le moins possible le bien-être financier de notre pays. 
Nous sommes témoins de notre bien-être financier. 
Nous constatons que des programmes qui étaient déficitaires ont maintenant des excédents notables. 
Le temps est maintenant venu de considérer l'environnement comme notre plus grande priorité, surtout dans notre beau pays.  
Les principaux éléments du rapport, tels que soulignés dans le document, portent sur la responsabilité d'Environnement Canada de faire appliquer efficacement la réglementation; les problèmes que suscitent les mesures d'application dans le cadre des ententes fédérales-provinciales-territoriales et auprès de la population canadienne, et c'est la partie cruciale du rapport; le droit de la population à être tenue au courant et son droit à la protection de notre environnement.  
Je veux signaler à la Chambre l'existence d'un livre qui a tenu une grande place dans nos travaux. 
Ce livre dit que les citoyens d'une nation devraient avoir une charte des droits. 
La Charte des droits et libertés de notre pays devrait englober la protection de l'environnement, afin que nous puissions vivre à l'abri des poisons et des toxines des nombreuses industries qui ne se gênent pas pour déverser ces matières et autres polluants dans nos eaux, notre air et nos terres, abîmant les ressources vitales dont dépend notre survie.  
Dans ce contexte, nous avons la Loi canadienne sur la protection de l'environnement. 
C'est notre charte des droits. 
Qui surveillerait les infractions au code de la route, trop fréquentes malgré le respect volontaire du code qu'on attend de nos citoyens? 
La réglementation de la Loi canadienne sur la protection de l'environnement et cette recommandation du comité montrent que l'application est un aspect crucial de la protection de l'environnement.  
La LCPE compte 26 règlements d'application concernant les BPC, les déversements dans l'océan, l'air limpide et les eaux pures, les CFC, les dioxines, les furanes et les combustibles. 
Toutes ces questions concernent chacune des circonscriptions au Canada. 
Tous les députés devraient être conscients des répercussions, de l'incapacité du gouvernement et du ministère d'appliquer ces mesures législatives.  
La Loi sur les pêches compte également des règlements d'application relatifs à l'habitat du poisson, aux rivières, lacs, océans et littoraux. 
Il ne faut pas oublier la situation catastrophique des pêches de l'Atlantique et le déclin des stocks de poisson, la côte du Pacifique, les changements dans l'environnement, la protection des espèces halieutiques et l'économie qui en dépend. 
En tout, la Loi canadienne sur la protection de l'environnement et la Loi sur les pêches comptent 32 règlements d'application.  
Sept accords d'équivalence et à caractère administratif ont été conclus récemment avec les provinces et les territoires. 
Trois de ces accords sont régis par la Loi canadienne sur la protection de l'environnement et deux par la Loi sur les pêches. 
L'un d'eux, le règlement fédéral sur les pâtes et papiers, concerne le Québec, c'est un accord majeur entre les gouvernements fédéral et provincial.  
Les Territoires du Nord-Ouest définissent un accord cadre pour les cinq bureaux régionaux qui se conforment aux modalités d'application et d'interprétation, soit les bureaux de l'Atlantique, du Québec, de l'Ontario, des Prairies et du Nord, et enfin, celui de la région Pacifique-Yukon. 
Les bureaux régionaux jouent un rôle important. 
J'attire votre attention sur les aspects majeurs du rapport, notamment la nécessité d'une exécution efficace et l'existence de ressources limitées par suite de l'examen du programme de l'environnement. 
Ce fleuve est un cours d'eau majeur. 
Le budget de la région du Pacifique-Yukon a été réduit de 30 p. 100, depuis le 1 er avril dernier. 
On prévoit que le nombre d'inspections, qui se situe normalement à 550, tombera à 385 à cause des compressions budgétaires. 
Ces compressions affecteront certainement les ressources d'exécution. 
Les postes vacants ne sont pas comblés. 
Nous devons utiliser ces ressources là où elles sont nécessaires. 
Il a fallu attendre jusqu'à deux ans pour que les postes vacants soient comblés au Québec, alors c'est une des provinces les plus industrialisées du Canada.  
Les données que le ministère transmet au comité laissent beaucoup à désirer. 
Nous attendons encore des renseignements que nous avons réclamés depuis fort longtemps. 
Ça ne bouge pas de ce côté-là. 
Personne ne sait jusqu'à quel point la situation s'est dégradée. 
On ignore totalement où en sont les questions d'application et d'exécution. 
En Ontario, deux inspecteurs consacrent quatre ou cinq jours à chaque entreprise pour vérifier l'inventaire national des rejets de polluants, alors qu'il s'agit d'une nouvelle obligation internationale. 
On s'engage envers la communauté internationale à dresser l'inventaire de la situation de la pollution au Canada, mais on n'augmente pas les effectifs chargés de cette tâche. 
En Colombie-Britannique, là où les impacts vont bien au-delà de l'industrie, il y a environ 17 200 sites pour un total de 16 inspecteurs.  
Des articles entiers de la Loi canadienne de protection de l'environnement ne sont pas appliqués au Canada. 
Il faut à tout prix que cette législation soit appliquée. 
Or, dans les Prairies, il y a 15 inspecteurs pour couvrir près de la moitié de notre masse territoriale.  
En 1992-1993, on a enregistré, dans le cadre de la Loi canadienne de protection de l'environnement, 1 233 inspections, 93 enquêtes, 105 avertissements, deux instructions, et 22 poursuites. 
En 1996, la situation s'est détériorée. 
Il y a eu seulement 97 avertissements, 15 poursuites, aucune instruction ni condamnation, ce qui témoigne d'une nette réduction de la présence fédérale dans l'environnement et d'une baisse manifeste de l'effort d'application des règlements.  
Pressé de questions devant le comité, le sous-ministre a admis que les ressources étaient insuffisantes en matière d'application de la loi concernant l'environnement. 
Le gouvernement continue d'encourager les mesures volontaires. 
Dans plusieurs parties du rapport, on note une faiblesse au chapitre des ressources et de la mise en application ainsi qu'un manque d'harmonisation entre les provinces.  
Il n'y a pas suffisamment de comptes rendus au Parlement au chapitre des infractions aux règles sur les pêches et l'habitat. 
Les organismes provinciaux de réglementation et d'application de la loi sont censés fournir ces renseignements au ministre des Pêches. 
L'entente conclue entre le Québec et le Canada sur l'application du règlement fédéral sur les pâtes et papiers a été mise en évidence. 
Il y a eu 20 infractions au cours de l'année où l'entente a été renouvelée et signée et aucune poursuite n'a été intentée.  
Le protocole d'entente sur l'aquaculture entre le ministère des Pêches et Océans et le Nouveau-Brunswick est encore plus inquiétant. 
On constate des maladies répandues et la pollution des milieux marins dans cette région. 
Des pesticides sont déversés afin de corriger un problème d'aquaculture, mais l'environnement naturel s'en trouve compromis.  
En 1997, l'Ontario s'est retiré de l'entente conclue avec le MPO sur la protection de l'habitat du poisson. 
En Ontario, on s'attend à ce que les agents d'exécution en place en Ontario fassent ce que la province de l'Ontario est censée faire.  
Les ressources ont été réduites. 
On s'attend à davantage et ce rapport en fait état. 
Nous devons nous pencher sur la question de la mise en application de nos règles touchant la protection de l'environnement et la pollution.  
J'implore la Chambre de tenir compte des répercussions de ce rapport et de relever le défi qui vise à faire de l'environnement la première priorité, tout particulièrement en cette semaine de l'environnement.  
