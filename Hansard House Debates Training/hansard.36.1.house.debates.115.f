LA LOI SUR LES JUGES 
La Chambre reprend l'étude, interrompue le 3 juin, du projet de loi C-37, Loi modifiant la Loi sur les juges et d'autres lois en conséquence, dont le comité a fait rapport sans propositions d'amendement, et de la motion no 1.  
M. Jason Kenney (Calgary-Sud-Est, Réf.): 
Monsieur le Président, je suis ravi de pouvoir participer au débat à l'étape du rapport du projet de loi C-37, tendant à modifier la Loi sur les juges, après avoir été si rudement interrompu par le NPD qui a proposé une motion de forme.  
La mesure législative vise à augmenter de 8,3 p. 100 la rémunération versée aux juges nommés par le gouvernement fédéral sur une période de deux ans. 
Déjà, les juges nommés par le gouvernement fédéral touchent en moyenne quelque 140 000 $, ce qui n'est pas une somme à dédaigner. 
Cela représente une portion assez considérable des recettes publiques.  
