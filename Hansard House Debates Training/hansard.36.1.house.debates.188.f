Quelle est la représentation des minorités visibles parmi les juges nommés par le gouvernement actuel?  
L'Association du Barreau canadien admet que le fléau du racisme qui caractérise la profession juridique contamine tout le système, depuis la correction des examens d'admission aux facultés de droit jusqu'aux nominations à la magistrature, en passant par les pratiques d'embauche. 
Chaque député a l'obligation tant morale que politique de combattre le racisme dans sa vie privée et dans sa collectivité ainsi que dans toutes les activités, programmes ou politiques du gouvernement fédéral.  
Notre charte des droits nous garantit que nous jouissons de diverses libertés «sans discrimination fondée sur la race, l'origine nationale, la couleur ...». 
La profession juridique canadienne est misérablement en-deçà du critère énoncé dans notre charte des droits.  
