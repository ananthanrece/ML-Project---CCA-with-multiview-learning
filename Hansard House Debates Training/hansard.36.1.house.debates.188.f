LE DÉCÈS DE JACK WEBSTER 
M. Paul Forseth (New Westminster-Coquitlam-Burnaby, Réf.): 
Monsieur le Président, Jack Webster, légendaire communicateur de Colombie-Britannique, est décédé mardi. 
Des amis ont déclaré que Jack aurait probablement voulu que l'article nécrologique précise qu'il est décédé le mardi 2 mars, à 10 h 18.  
Né en 1918 à Glasgow, en Écosse, Jack Webster a immigré au Canada en 1947. 
Il a consacré sa vie au journalisme parlé. 
Tous les Canadiens l'ont vu animer l'émission Front Page Challenge au réseau anglais de Radio-Canada.  
Jack a été un pionnier des tribunes libres au poste de radio CKNW de New Westminster. 
Il était célèbre pour sa capacité de relever un fait anodin à 9 heures le matin et d'en faire un grand reportage avant midi.  
Jack Webster n'était pas homme à éluder les difficultés ou à succomber à la rectitude politique. 
Un prix de journalisme est maintenant décerné en son honneur. 
Le Canada le regrettera. 
La voix irascible de son courage lui a valu le titre de grand Canadien.  
