Fortifiés par notre courage politique et sachant que les Canadiens appuyaient nos orientations, nous avons éliminé le déficit en quatre ans et enregistré un surplus budgétaire de 3,5 milliards de dollars en 1998, le premier depuis 28 ans.  
Nous avons mis de l'ordre dans les finances du pays. 
Nous avons rempli notre promesse. 
