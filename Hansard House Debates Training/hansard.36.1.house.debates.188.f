DÉCLARATIONS DE DÉPUTÉS 
M. David Pratt (Nepean-Carleton, Lib.):
Monsieur le Président, on dit souvent que l'avenir du Canada n'est prometteur que dans la mesure où ses jeunes le sont. 
Avec des adolescents comme Kristin Willemsen, de Stittsville, dans nos collectivités, les perspectives de notre pays pour le nouveau millénaire n'en sont que meilleures.  
Mlle Willemsen a récemment reçu le Prix du Jeune citoyen ontarien de l'année 1998, offert par l'Association des journaux communautaires de l'Ontario, en reconnaissance de sa contribution remarquable à sa collectivité.  
Kristin n'est pas une adolescente typique. 
À seulement 15 ans, elle est une source d'inspiration pour nous tous. 
Elle maintient une moyenne exceptionnelle de 94,5 p. 100 dans ses cours de 10e année à l'école secondaire St. Paul, tout en participant activement à divers sports entre établissements d'enseignement et en aidant même ses pairs dans leurs études. 
Elle est également bénévole à la bibliothèque locale et auprès de la chorale communautaire, et elle s'adonne au patinage synchronisé.  
Bref, les réalisations de Kristin font honneur à son école, à sa famille et, bien sûr, à notre collectivité. 
Selon l'auteur de proposition de sa candidature: «Kristin est dévouée à la collectivité et représente un excellent modèle pour tous les jeunes.» 
Voilà une très bonne raison d'envisager notre avenir avec optimisme.  
