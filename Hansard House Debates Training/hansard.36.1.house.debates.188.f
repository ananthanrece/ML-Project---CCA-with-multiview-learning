LE RACISME 
M. Gordon Earle (Halifax-Ouest, NPD):
Monsieur le Président, le système juridique canadien est aussi blanc qu'une tempête de neige à Ottawa. 
Il est infecté par le racisme, et son état ne s'améliore pas avec le temps.  
D'après une étude faite en Nouvelle-Écosse, les chances d'une personne de couleur d'être engagée par l'étude où elle a fait son stage d'apprentissage sont pour ainsi dire nulles. 
