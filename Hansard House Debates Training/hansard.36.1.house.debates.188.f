LES ESPÈCES MENACÉES 
M. Rick Casson (Lethbridge, Réf.): 
Monsieur le Président, il est prévu que, ce printemps, la ministre de l'Environnement, après bien des retards, présentera enfin un projet de loi qui accordera à nos espèces menacées la protection dont elles ont désespérément besoin. 
Avant que la ministre ne dépose ce projet de loi, je voudrais préciser clairement une chose, pour qu'elle ne commette pas les mêmes erreurs que ses prédécesseurs ont commises.  
C'est la collaboration, et non l'affrontement, qui assurera la prospérité de notre faune menacée.  
Le gouvernement doit collaborer avec les provinces et les propriétaires fonciers. 
Il doit reconnaître les pratiques de gestion responsables des propriétaires fonciers et créer un climat permettant de les récompenser pour ces pratiques. 
Il doit respecter les droits de propriété privée et offrir une indemnisation équitable aux propriétaires fonciers touchés par les programmes de rétablissement des espèces en péril.  
La faune ne peut être protégée par une réglementation gouvernementale pyramidale de commandement et de contrôle. 
Elle est protégée lorsque le gouvernement collabore avec les propriétaires fonciers par voie de mesure législative dont le respect est volontaire et fondé sur des encouragements. 
Elle est protégée lorsque tous les membres de la société unissent leurs efforts. 
Il est temps d'offrir à nos espèces en péril la collaboration et la protection qu'elles méritent.  
