M. Denis Paradis (secrétaire parlementaire du ministre des Affaires étrangères, Lib.):
Monsieur le Président, il me fait plaisir de prendre la parole à l'occasion du débat en deuxième lecture du projet de loi C-214, parrainé par l'honorable député de Beauharnois-Salaberry.  
Ce projet de loi nous invite à traiter de la pratique canadienne de conclusion des traités, élément important de la prérogative du gouvernement du Canada.  
D'entrée de jeu, je dois indiquer à cette Chambre que le gouvernement n'a pas l'intention d'appuyer le projet de loi C-214, pour les raisons suivantes.  
Ce projet de loi affecte sérieusement la division des pouvoirs au Canada et met en cause certains aspects prioritaires de la politique étrangère du Canada.  
Aujourd'hui, le contexte international influe directement sur le quotidien de nos concitoyens. 
Ils sont de plus en plus mobiles, voyageant, faisant des affaires et travaillant de par le monde. 
Comme davantage de problématiques dépassent les frontières traditionnelles, les États adoptent des approches de plus en plus concertées, que ce soit pour la lutte contre la criminalité, la promotion de la paix, le désarmement, la protection de l'environnement, le développement durable ou le commerce international.  
Cette concertation des pays amène un dialogue international constant et, du même coup, une croissance du nombre d'accords internationaux. 
Le Canada est présentement partie-et notre collègue mentionnait des chiffres tantôt-à près de 3 000 instruments bilatéraux et multilatéraux.  
Dans le but d'informer le Parlement des obligations découlant de ces traités, le ministère des Affaires étrangères et du Commerce international a déjà pour pratique de déposer à la Chambre-je l'ai fait moi-même au cours des derniers jours-et devant le Sénat le texte de tous les traités entrés en vigueur qui ne requièrent pas de mise en oeuvre législative particulière.  
De plus, tous ces traités sont aussi déposés, sous forme électronique, devant les deux Chambres et auprès de la Bibliothèque du Parlement pour consultation par tous les députés et sénateurs canadiens. 
En plus de recevoir toute cette information, les parlementaires jouent un rôle direct dans la mise en oeuvre des traités que le Canada souhaite ratifier.  
Il est clairement établi, en droit constitutionnel canadien, que la négociation et la conclusion d'un traité, c'est-à-dire l'acte par lequel le Canada souhaite être lié par un traité, relèvent exclusivement du pouvoir exécutif fédéral canadien. 
Par contre, il appartient toujours au pouvoir législatif de mettre en oeuvre les obligations issues d'un traité.  
Si un traité entraîne la modification des lois actuelles ou l'adoption de nouvelles lois, seul le législateur peut alors agir. 
Selon le champ de compétence visé par le traité, le pouvoir de mise en oeuvre législative relèvera du Parlement ou, selon le cas, des législatures provinciales.  
Ce rôle est essentiel, puisqu'en l'absence de participation du pouvoir législatif, les engagements internationaux contractés par le Canada resteront lettre morte, faute de mise en oeuvre en droit interne.  
Par le biais de ce pouvoir de mise en oeuvre, le Parlement est donc régulièrement appelé à étudier et à débattre de traités. 
Nous n'avons qu'à penser aux exemples récents de la Loi de mise en oeuvre de la Convention sur les mines antipersonnel, relativement à l'interdiction des mines antipersonnel et leur destruction, de la Loi de mise en oeuvre du Traité d'interdiction complète des essais nucléaires, sur laquelle je reviendrai plus tard, ou de la Loi sur la corruption d'agents publics étrangers, qui donne suite à la Convention de l'OCDE sur la lutte contre la corruption d'agents publics étrangers dans les transactions commerciales internationales.  
Je souligne que, même si nous jugeons ces modifications législatives inutiles, en pratique, le rôle du Parlement à l'égard de la conclusion de traité continue d'évoluer. 
Un autre exemple est celui du Parlement qui, au printemps dernier, a débattu le projet de loi S-22 devant mettre en oeuvre un accord avec les Américains, avant même que l'accord ne soit conclu, afin de donner au Parlement plus de latitude pour déterminer quels pouvoirs le Canada accorderait aux agents des douanes américaines dans les aéroports canadiens.  
Le Parlement joue donc un rôle avant la conclusion d'un traité et s'il ne le fait pas toujours, il le fait souvent dans des cas importants.  
Pour ce qui est des traités portant sur des sujets relevant de la compétence des provinces, le gouvernement du Canada est déjà tenu, en droit constitutionnel, de s'assurer de l'appui des provinces avant de ratifier un traité international qui requiert une mise en oeuvre législative provinciale.  
À titre d'exemple, le gouvernement fédéral consulte les gouvernements provinciaux relativement aux conventions de La Haye portant sur le droit international privé et relativement à l'élaboration de la position canadienne aux négociations sur les conventions de protection de l'environnement. 
Il arrive aussi que des représentants des provinces fassent partie des délégations canadiennes lors de la négociation de traités qui intéressent les provinces.  
D'autre part, le projet de loi C-214 traite d'une certaine prérogative royale provinciale en ce qui a trait à la négociation et à la conclusion des traités. 
Il est clairement établi au Canada qu'une telle prérogative provinciale n'existe pas et que la prérogative en matière de négociation et de conclusion de tout traité international relève exclusivement de l'exécutif fédéral canadien.  
Ensuite, le projet de loi C-214 porte atteinte à la politique étrangère canadienne. 
Les crises qui affectent notre monde ne peuvent pas servir de plate-forme partisane sur la scène politique nationale. 
Le gouvernement du Canada, redevable devant le Parlement, est chargé de la conduite des affaires étrangères du Canada. 
S'il veut se faire entendre et démontrer son leadership, il doit parler d'une seule voix sur la scène internationale.  
Le refus partisan du Sénat républicain américain, par exemple, de ratifier le Traité d'interdiction complète des essais nucléaires a secoué, non seulement le Canada, mais toute la communauté internationale, a amoindri les espoirs de paix et de stabilité mondiale qu'avait inspiré jusqu'à maintenant le traité et a porté un coup dur à la réputation des États-Unis, alors que le président Clinton lui-même appuyait ouvertement la ratification du traité.  
Cette désunion de nos voisins américains illustre bien ce qui arrive lorsque la partisanerie stérile s'immisce dans la conduite des affaires étrangères d'un pays. 
Le Canada ne souhaite pas vivre un changement aussi radical dans la conduite de ses affaires étrangères.  
Au surplus, le projet de loi C-214 ralentit le processus de conclusion des traités et empêche le Canada de faire preuve d'initiative dans l'élaboration de conventions internationales. 
Voici un exemple des possibles obstacles que le projet de loi C-214 pourrait engendrer.  
La Convention sur les mines antipersonnel, signée ici même à Ottawa, est un instrument international jugé prioritaire par le Canada. 
Nous avons été le premier pays à ratifier le traité en décembre 1997. 
Si le projet de loi C-214 avait alors été en vigueur, cela n'aurait pu être le cas.  
Le Canada doit avoir une pratique de conclusion des traités qui lui permet d'atteindre ses objectifs de politique étrangère et de faire face rapidement et efficacement aux situations changeantes et urgentes. 
La pratique actuelle répond à ces impératifs.  
Permettez-moi de citer en ce sens un exemple où la flexibilité qu'alloue présentement le système canadien sert les intérêts du Canada.  
Afin de favoriser l'expansion des affaires dans le secteur du transport aérien, par exemple, le gouvernement conclut régulièrement des accords en matière de transport aérien avec d'autres pays. 
Ce type d'accord permet aux avions commerciaux d'un État partie à l'entente de circuler librement dans l'espace aérien de l'autre partie à l'entente, et ce, afin d'accroître le nombre de destinations que les transporteurs des deux pays peuvent desservir.  
