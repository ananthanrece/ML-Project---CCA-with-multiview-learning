LE COMMERCE 
M. Lee Morrison (Cypress Hills-Grasslands, Réf.): 
Monsieur le Président, cette semaine des représentants de 135 États souverains sont réunis à Seattle pour amorcer le prochain cycle de négociations sur la mondialisation du commerce.  
Le Canada, dont les exportations représentent plus de 40 p. 100 de son produit national brut, a un grand intérêt dans ces discussions, particulièrement en ce qui a trait aux subventions déloyales dont bénéficient les produits agricoles aux États-Unis et en Europe.  
Les protestataires canadiens et américains endurcis, qui sont pour la plupart des gens de la classe moyenne chaudement vêtus et bien nourris, ont fait de l'opposition au commerce mondial leur cheval de bataille pour la semaine et ils essaient de paralyser les négociations.  
J'aimerais bien savoir combien de ces obstructionnistes moralisateurs en beaux habits ont déjà eu le courage d'assumer leur conviction en refusant d'acheter des produits provenant des pays qui exploitent habituellement leurs travailleurs. 
Sont-ils prêts à débourser 50 $ pour une chemise nord-américaine ou achètent-ils plutôt des chemises faites en Chine pour 12 $? 
