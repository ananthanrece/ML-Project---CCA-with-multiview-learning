Le très hon. Jean Chrétien (premier ministre, Lib.):
Monsieur le Président, nous avons eu un débat sur ce point il y a longtemps. 
Le député devrait lire attentivement le premier livre rouge, où nous énoncions très clairement notre politique à cet égard.  
Je suis très heureux d'accepter les compliments que le chef de l'opposition a adressés au gouvernement pour s'être assuré de façon très efficace que les Canadiens payaient leur dû.  
M. Preston Manning (chef de l'opposition, Réf.): 
Monsieur le Président, seul un libéral peut être fier de percevoir des taxes. 
