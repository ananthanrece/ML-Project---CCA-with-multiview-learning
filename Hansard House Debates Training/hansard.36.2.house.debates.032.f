Le centre local de renseignements touristiques décrit l'histoire de Chemainus comme celle d'une petite localité qui a vécu pendant plus de cent ans de l'industrie du bois-d'oeuvre. 
En 1980, les inquiétudes au sujet des inévitables compressions chez MacMillan Bloedel ont poussé les gens de l'endroit à discuter de la revitalisation du centre de la localité dans l'espoir d'attirer un certain tourisme.  
Profitant d'un programme provincial de revitalisation des centres-villes, les responsables ont conçu un plan d'amélioration des commerces maintenant connu comme le projet des murales. 
Ils ont recommandé qu'on peigne de grandes murales de qualité sur les murs extérieurs de divers bâtiments, illustrant l'histoire de Chemainus comme localité forestière.  
La première murale a été peinte en 1982 par un artiste de l'île de Vancouver, Frank Lewis. 
Ces étonnantes murales peintes par des professionnels sont inspirées de photos historiques authentiques des colons et elles illustrent de façon saisissante l'histoire de la vallée de Chemainus, avec son industrie forestière et ses chefs des premières nations.  
Outre ses magnifiques murales, Chemainus s'enorgueillit maintenant d'un nouveau restaurant-théâtre populaire de 270 places et de nombreuses boutiques passionnantes, et un grand nombre d'artisans de talents y habitent. 
Dès la troisième année, les murales attiraient environ 15 000 touristes, selon les estimations. 
Aujourd'hui, Chemainus accueille chaque année plus de 400 000 personnes, qui viennent voir ses 33 murales connues dans le monde entier. 
Comme un panneau le dit à l'entrée de la localité: «Bienvenue dans la petite localité débrouillarde».  
Chemainus, c'est le succès de la détermination, du courage et de la largeur de vues. 
