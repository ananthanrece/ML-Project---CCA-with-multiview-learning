LES SANS-ABRI 
M. Pat O'Brien (London-Fanshawe, Lib.):
Monsieur le Président, en juillet dernier, j'ai eu le plaisir d'être l'hôte d'un forum d'une journée sur les sans-abri qui s'est tenu à London en Ontario.  
La ministre du Travail, qui est également responsable des sans-abri, a passé une journée très instructive avec nous.  
Bon nombre d'organismes qui s'occupent des sans-abri ont décrit la nature du problème à London et dans le district. 
Plusieurs sans-abri ont également eu l'occasion de parler directement à la ministre.  
J'aimerais remercier et féliciter ma collègue, la ministre du Travail, de son dévouement et de son ardeur à élaborer un plan d'action gouvernemental pour s'attaquer au problème des sans-abri. 
Il est bien entendu que nous devrons prévoir des fonds supplémentaires dans le prochain budget pour réaliser ce plan.  
J'ai bon espoir que le ministre des Finances acceptera de traiter ce grave problème en priorité et d'accroître les fonds qui lui sont consacrés de façon que nous puissions enfin éliminer l'itinérance et offrir un logement convenable à tous les Canadiens. 
