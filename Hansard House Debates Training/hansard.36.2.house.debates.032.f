La mesure à l'étude pourrait constituer un moyen grâce auquel les parlementaires pourraient et devraient avoir leur mot à dire à propos de la conclusion de ce genre de traités.  
Nous sommes très déçus de la réaction des députés ministériels en face.  
M. Ted McWhinney (Vancouver Quadra, Lib.):
Monsieur le Président, cette résolution nous pose des problèmes et je vais les traiter très rapidement.  
On n'étudie pas de façon empirique la pratique canadienne et on ne fait pas de distinction entre les traités exécutoires et non exécutoires. 
Environ 99 p. 100 des traités formulés par le Canada sont non exécutoires. 
Cela signifie que pour qu'ils soient appliqués dans le cadre de la loi canadienne, des lois doivent être adoptées au Parlement. 
Je suis étonné d'entendre l'opposition officielle dire qu'elle ne sait rien à ce sujet. 
Ces députés ont-ils dormi? 
Il existe un comité des affaires étrangères. 
Il est compétent pour entendre ces questions. 
Ses membres sont compétents pour soulever des questions lorsque les lois arrivent à la Chambre. 
Quelqu'un a joué à Rip Van Winkle et les députés de l'opposition ne sont pas très crédibles en disant cela.  
D'une certaine façon, nous avons fusionné l'acte très officiel de ratification avec la mise en oeuvre par voie législative. 
À témoin, le traité sur les mines terrestres, quand il s'était agi pour nous d'être les premiers à le ratifier, vu que nous l'avions parrainé. 
Il demeure que chaque traité conclu est soumis à l'étude des deux chambres du Parlement et au Comité des affaires étrangères.  
Je voudrais, en deuxième lieu, dire à quel point je suis étonné par cette analyse de droit comparé. 
C'est notre première leçon. 
Sir Austen Chamberlain affirmait que le droit comparé peut se révéler un piège pour qui n'est pas sur ses gardes, et un signal pour qui n'est pas innocent. 
Il me parlait pas de cela, mais peu importe.  
Comment peut-on vouloir appliquer une loi convenant à une société donnée à une autre société et prétendre établir des comparaisons à moins que les systèmes juridiques de ces deux sociétés ne soient concordants?  
Les systèmes en vigueur au Canada et en Allemagne sont totalement différents des systèmes en vigueur en Grande-Bretagne, en Australie et ailleurs. 
Le député qui a présenté le projet de loi serait horrifié si les systèmes en vigueur aux États-Unis, en Australie ou ailleurs devaient être appliqués au Canada. 
Ces systèmes établissent la suprématie de la loi fédérale de mise en oeuvre des traités. 
Dans ces pays, la ratification d'un traité entraîne automatiquement le pouvoir de légiférer. 
Au Canada, c'est l'inverse et, en 1957, la cour allemande qui s'était penché sur notre expérience avait déclaré qu'elle nous imiterait.  
Aucun traité ne peut être mis en oeuvre sans une loi fédérale ni, s'il touche aux pouvoirs des provinces, sans une loi provinciale. 
En matière de pêches, par exemple, il faudra plusieurs années de négociation patiente et parfois frustrante avec les provinces. 
Permettez-moi de le signaler.  
Le troisième point que je veux faire valoir, c'est tout simplement qu'il n'est pas bon de faire indirectement ce qu'on peut pas faire directement. 
Après une lecture très attentive, je trouve que l'article 6 du projet de loi relatif aux traités est là par inadvertance et traduit une méconnaissance de la Constitution canadienne, ce que je ne crois pas, ou est le fruit d'une espièglerie.  
Reconnaissons les faits! 
Dans la législation canadienne, il n'y a pas de traité qui soit conclu par une province. 
Cela n'existe tout simplement pas. 
Par conséquent, comment peut-on prétendre enfouir un amendement constitutionnel dans une disposition d'un projet de loi? 
On ne le peut tout simplement pas.  
Une bonne partie de tout cela constitue un acte de surérogation, une affirmation de ce que le Parlement fait déjà. 
Je regarde les centaines d'heures passées à l'étude de l'AMI au sein des comités des affaires étrangères. 
On a discuté de la question dans tous les sens. 
Puis, il y a eu la question du traité sur les mines antipersonnel avec la participation de l'opposition et d'autres, que le ministre a reconnue.  
J'aurais dit qu'à part l'article 6, cela rappelle ce que disait Quintius Horatius Flaccus: «Parturient montes, nascetur riduculus mus». 
Ce qui veut dire à peu près qu'on s'emporte pour pas grand chose.  
Je suis également tenté par l'idée qu'il s'agit peut-être d'une tentative de faire indirectement ce qui ne peut être fait directement.  
Je conseillerais au député, que je respecte d'ailleurs pour ses qualités et son grand sens de l'humour, de nous revenir avec un meilleur texte. 
Je lui suggérerais également de proposer à ses étudiants des exercices qui ont des racines plus profondes dans la jurisprudence sociologique. 
Autrement dit, de s'assurer que les sociétés dont il a fait l'étude sont parentes avant d'en retirer le droit établi. 
Le droit comparé n'a rien à voir avec ces collections de papillons dans lesquelles on peut se permettre de réunir des monstres de sociétés différentes. 
Il doit y avoir une certaine pertinence.  
