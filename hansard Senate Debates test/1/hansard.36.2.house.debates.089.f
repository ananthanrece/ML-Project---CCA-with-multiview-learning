Il est extrêmement important que les Canadiens puissent s'identifier au serment d'allégeance et que celui-ci soit une source d'inspiration pour nous tous. 
Il n'y a pas de meilleur moyen de s'assurer de sa pertinence.  
En tant que Canadiens, il est de nombreuses choses dont nous pouvons être fiers, voire reconnaissants: la liberté, l'égalité, la qualité de l'eau et de l'air, les vastes étendues, l'accès à des soins de santé et à un enseignement de qualité, toutes ces choses que nous prenons pour acquises et pourtant que beaucoup de gens dans le monde n'ont pas et pour lesquelles ils se battent. 
Un serment d'allégeance officiel à notre drapeau servirait à rappeler que nous faisons partie des rares privilégiés. 
Nous avons lutté pour faire du Canada le meilleur pays où vivre au monde et nous avons gagné.  
Un serment d'allégeance servirait aussi à rappeler tout ce qu'il a fallu d'efforts pour arriver là où nous en sommes aujourd'hui. 
Cela nous donnerait le temps de méditer sur les devoirs qui sont les nôtres en tant que citoyens de cette grande nation. 
