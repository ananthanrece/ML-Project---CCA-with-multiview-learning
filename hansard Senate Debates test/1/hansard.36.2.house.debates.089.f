L'AMICAL GÉANT 
L'hon. Christine Stewart (Northumberland, Lib.):
Monsieur le Président, au nom de tous les Canadiens j'offre mes condoléances à la famille et aux amis de M. Bob Homme, l'amical géant du Canada, qui est décédé hier à son domicile de Grafton en Ontario, dans ma circonscription de Northumberland.  
Du début de 1958 jusqu'au milieu des années 80, soit pendant près de 30 ans, l'émission Friendly Giant a fait partie des meubles à la télévision anglaise de Radio-Canada. 
M. Homme était non seulement la vedette de l'émission, il en était aussi la force créatrice. 
Au cours de cette période, pendant laquelle ont été produits plus de 3 000 épisodes, M. Homme, l'amical géant, et ses loyaux compagnons, Jérôme la girafe et Rusty le coq, ont établi un lien durable avec des générations d'enfants canadiens. 
L'Ordre du Canada a été accordé en 1998 à M. Homme en reconnaissance de ses réalisations exceptionnelles et importantes. 
On le regrettera beaucoup, mais on s'en rappellera toujours affectueusement.  
