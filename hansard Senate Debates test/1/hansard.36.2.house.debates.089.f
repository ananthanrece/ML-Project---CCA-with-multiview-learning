M. Jim Gouk (Kootenay-Boundary-Okanagan, Alliance canadienne):
Monsieur le Président, au fil des ans, on a gaspillé des milliards de dollars, vous avez bien entendu, des milliards de dollars, venant des contribuables pour subventionner VIA Rail.  
Étant donné les milliards de dollars dilapidés par VIA, ma question s'adresse aux contribuables et non au ministre. 
Quel avantage les contribuables, leurs familles, leurs villes ou leurs régions ont-ils tiré des milliards de dollars dépensés ou, plus important encore, de quelle façon cet argent aurait-il pu être mieux utilisé?  
Le ministre prétend que le secteur privé ne s'intéresse pas à VIA et qu'il est donc nécessaire d'accorder des subventions à cette entreprise. 
À mon avis, le secteur privé est intéressé, et je lance au ministre le défi suivant. 
Si j'arrive à convaincre des exploitants du secteur privé à se montrer intéressés à prendre le contrôle de VIA Rail sans faire appel à des subventions des contribuables, s'engagera-t-il à ordonner au comité des transports d'examiner le potentiel de cette expression d'intérêt?  
Des millions de Canadiens qui viennent tout juste de produire leur déclaration de revenus veulent savoir si le gouvernement dépense leur argent de façon responsable.  
Si le ministre continue de verser des subventions de près de 750 000 $ par jour à VIA, alors que cette entreprise peut être exploitée par le secteur privé sans qu'il en coûte un cent aux contribuables, les Canadiens auront alors eu leur réponse.  
