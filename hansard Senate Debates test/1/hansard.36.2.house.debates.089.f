LA LOI SUR LE SERMENT D'ALLÉGEANCE AU DRAPEAU DU CANADA 
-Madame la Présidente, je suis très heureuse d'amorcer le débat sur mon projet de loi d'initiative parlementaire, le projet de loi C-451, à savoir la Loi instituant un serment d'allégeance au drapeau du Canada.  
Je travaille sur ce projet de loi depuis fort longtemps. 
Il a été introduit pour la première fois en 1996, mais il est mort au Feuilleton quand des élections ont été déclenchées l'année suivante. 
Je n'ai pas pu le réintroduire avant ce printemps et j'ai été très heureuse quand il a été pigé lors du tirage au sort pour les Affaires émanant des députés.  
L'idée de ce projet de loi vient d'une discussion que j'ai eue avec une de mes commettantes, Joyce Hammond. 
Quand nous avons constaté qu'il y avait non pas un, mais plusieurs serments d'allégeance en usage, nous avons convenus que l'adoption d'un serment d'allégeance officiel s'imposait.  
Un serment officiel d'allégeance au drapeau du Canada et à tout ce qu'il représente serait un magnifique cadeau, à l'aube d'un nouveau millénaire. 
Le XIXe siècle nous a donné un pays. 
Le XXe siècle nous a donné un drapeau. 
Au XXIe siècle, le Canada devrait se doter d'un serment d'allégeance.  
Des gens m'ont demandé pourquoi un serment d'allégeance était important. 
Nous avons un hymne national. 
Qu'avons-nous besoin de plus? 
La réponse est simple. 
Un serment nous donnerait l'occasion, lorsque flotterait le drapeau, de montrer que nous tenons au Canada et à tout ce que le drapeau représente. 
Quand on voit la feuille d'érable, on pense au Canada, mais en récitant le serment, on penserait vraiment à ce que notre drapeau et notre pays représentent. 
La feuille d'érable incarne toutes les valeurs auxquelles nous tenons en tant que Canadiens. 
Comme vous le savez, notre drapeau jouit d'un très grand respect dans le monde. 
Un serment réaffirmerait notre attachement à ces valeurs.  
D'autres ont demandé pourquoi nous devrions prêter allégeance à un morceau de tissu. 
À cela, je réponds que notre drapeau est bien plus qu'un morceau de tissu. 
Il représente non seulement nos valeurs, mais encore notre histoire et nos traditions communes. 
Le drapeau représente les millions de gens qui sont venus au Canada pour garantir une vie meilleure à leur famille. 
Il représente les deux langues officielles travaillant de concert. 
Il représente la démocratie et la liberté. 
Et, bien sûr, pour bien des gens, il évoque les agents de la GRC, le castor et la neige.  
Le fait est que si l'on ne fait que regarder le drapeau, on passe à côté de la question. 
Le serment ferait en sorte que le drapeau soit un puissant symbole pour tous les Canadiens. 
D'aucuns craignent que le fait de prêter un serment d'allégeance au drapeau du Canada ne soit en quelque sorte trop américain. 
Je tiens à faire remarquer que les États-Unis ne sont pas le seul pays où les citoyens doivent prêter allégeance. 
De plus, si nos voisins du Sud ont eu une bonne idée, pourquoi ne pas la leur emprunter?  
Nombre de Canadiens pensent qu'un serment d'allégeance est une excellente idée. 
Ce projet de loi a reçu l'appui de 500 municipalités d'un océan à l'autre et de Canadiens de plus de 700 localités différentes. 
On continue de recevoir des lettres, des courriels et des téléphones d'appui.  
Récemment, j'ai reçu une lettre de Les Peate, secrétaire national de l'Association canadienne des anciens combattants de la guerre de Corée. 
M. Peate a écrit ceci: 
Le moment est peut-être venu de nous affirmer et d'avoir un «serment d'allégeance» uniformisé et officiel, qui ne doit pas nécessairement être obligatoire, mais qui devrait être à la disposition des écoles, des groupes d'anciens combattants, des organismes de services et d'autres groupes où nous pouvons encore exprimer notre fierté d'être Canadiens. 
Je partage l'avis de M. Peate. 
Je ne propose pas de rendre obligatoire la prestation d'un serment. 
Le patriotisme imposé peut avoir des conséquences dangereuses. 
Par contre, je pense vraiment que les Canadiens devraient avoir un serment officiel qu'ils pourraient utiliser pour exprimer leur patriotisme.  
M. Peate raconte l'histoire d'un confrère ancien combattant qui a visité une filiale de la légion américaine. 
Après que les anciens combattants américains eurent prononcé leur serment d'allégeance, ils ont invité les Canadiens à faire de même. 
Les confrères de M. Peate ont dû admettre avec embarras que nous n'avions pas de serment officiel. 
C'est un embarras qui pourrait facilement être évité si ce projet de loi était adopté.  
De nombreux anciens combattants ont écrit pour témoigner leur appui au projet de loi C-451. 
Qu'ils se soient battus pour défendre les valeurs que notre drapeau représente ou pour participer à des missions de maintien de la paix pour maintenir ces mêmes valeurs, un serment d'allégeance officiel revêt une importance particulière pour eux. 
Ils ne sont pas les seuls à croire qu'ils ont besoin de rendre hommage à notre drapeau et à tout ce qu'il représente.  
Les jeunes Canadiens ont aussi montré un vif intérêt pour le projet de loi. 
Que ce soit à l'instigation d'enseignants ou de la propre initiative des élèves, j'ai reçu des textes de serments d'élèves de tout le Canada. 
