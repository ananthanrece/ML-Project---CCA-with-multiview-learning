Aux termes de cette mesure, l'Alberta s'engage à respecter les principes qui sous-tendent la Loi canadienne sur la santé, que connaissent bien tous les intervenants dans le système de santé.  
Selon un autre grand principe, le projet de loi interdit l'exploitation d'un hôpital privé en Alberta. 
Le libellé prescrit clairement et sans la moindre ambiguïté qu'il est interdit d'exploiter un hôpital privé en Alberta. 
Il y a ensuite une définition précise de ce qu'est un hôpital privé. 
Seuls les hôpitaux publics seront autorisés à fournir la gamme complète des services hospitaliers, dont les salles d'urgence, les services diagnostics et médicaux et les chirurgies majeures. 
Cela me semble assez clair.  
On parle ensuite de ce que vise ce projet de loi, soit permettre un séjour d'une nuit pour des chirurgies qui n'étaient pratiquées autrefois que dans des cliniques de chirurgie d'un jour. 
Plus particulièrement, une clinique de chirurgie autorisée en vertu de cette mesure ne pourrait offrir qu'une gamme limitée de services chirurgicaux. 
Ce ne pourrait pas être un hôpital offrant tous les services. 
On dit ensuite que seul le Collège des médecins et des chirurgiens, auquel j'appartiens, peut décider des services offerts et accréditer ces cliniques.  
Une des grandes préoccupations que suscitent les cliniques privées qui offrent des services dans n'importe quelle région du pays, c'est le risque de resquillage. 
Ce projet de loi rend expressément illégal le resquillage. 
Il rend également illégal l'imposition de frais pour des services couverts par une assurance.  
Le relèvement de la qualité du service est un autre argument contre la pratique de chirurgies dans des cliniques privées au Canada. 
Disons, par exemple, qu'une personne se rend dans une clinique de ce genre pour subir une chirurgie de la cataracte. 
La clinique relèverait la qualité de la chirurgie, de sorte qu'elle coûterait plus cher. 
On ajoute que, si une personne décide d'obtenir un service de meilleure qualité, comme un plâtre en fibre de verre ou une lentille adaptable à la suite d'une chirurgie de la cataracte, elle doit le demander par écrit, le coût doit être indiqué et les intéressés doivent signer une entente. 
Si elle change d'avis, elle peut se désister. 
Il s'agit par exemple de la chirurgie plastique et de traitements effectués à l'aide de nouvelles technologies qui ne sont pas encore remboursés par notre régime canadien de soins de santé. 
L'autre préoccupation est que ce genre de processus pourrait bien amener des établissements étrangers à vouloir profiter de la nouvelle proposition. 
Tout contrat serait rendu public.  
