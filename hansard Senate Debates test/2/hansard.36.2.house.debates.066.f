Notre santé est tributaire des aliments que nous mangeons. 
Il est essentiel que ces aliments soient de qualité, qu'ils soient abordables, sans danger, accessibles et que leur approvisionnement soit assuré.  
Avant d'aborder les détails d'un plan national de lutte contre la faim, je tiens à souligner l'apport d'un organisme qui a beaucoup contribué à l'élaboration de ce plan, soit le FoodShare de Toronto. 
