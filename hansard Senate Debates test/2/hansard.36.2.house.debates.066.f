QUESTION DE PRIVILÈGE 
LE COMITÉ PERMANENT DE LA CITOYENNETÉ ET DE L'IMMIGRATION 
M. Joe Fontana (London-Centre-Nord, Lib.):
Monsieur le Président, et chers collègues, je soulève la question de privilège au sujet de la divulgation prématurée d'un document confidentiel du Comité permanent de la citoyenneté et de l'immigration, qui n'en était qu'au stade de l'ébauche.  
Le député de Lakeland, qui se trouve être le vice-président du comité, a envoyé un avis à l'intention des médias qui dit ceci: 
Ottawa, le jeudi 16 mars 2000. 
À 15 h 30 aujourd'hui, dans la salle 130-S de l'édifice du Centre, le député Leon Benoit fera une conférence de presse afin de rendre public le rapport du Comité de la citoyenneté et de l'immigration. 
Ce rapport, intitulé «La protection des réfugiés et la sécurité des frontières:
Un juste équilibre», est confidentiel et doit être soumis à la Chambre des communes.  
Je remarque qu'à la page 884 du chapitre 20 du manuel intitulé La procédure et les usages de la Chambre des communes , on lit: 
Même lorsqu'un rapport est adopté en séance publique, il est considéré comme confidentiel jusqu'à sa présentation à la Chambre. 
De plus, lorsqu'un rapport de comité a été étudié et approuvé pendant des séances à huis clos du comité, toute divulgation de son contenu avant sa présentation, par des membres ou des non-membres, peut être jugée comme une atteinte au privilège. 
Des Présidents ont jugé que les questions de privilège relatives à des rapports ayant fait l'objet d'une fuite ne seraient pas prises en considération à moins qu'une accusation précise ne soit portée contre une personne, une organisation ou un groupe, cette accusation devant non seulement viser les personnes de l'extérieur de la Chambre qui ont rendu publics les documents étudiés à huis clos, mais encore désigner la source de la fuite au sein de la Chambre elle-même.  
Il y a matière à question de privilège non seulement pour moi, mais aussi et surtout pour les membres du Comité de la citoyenneté et de l'immigration, lesquels n'ont pas ménagé leurs efforts au cours des deux ou trois derniers mois et qui, mercredi ou jeudi de cette semaine, poursuivaient leur travail, comme je l'ai dit, au stade de l'ébauche.  
Malheureusement, le député de Lakeland et son collègue ont décidé de ne pas participer à ces réunions, ce qui explique ce qu'ils ont fait. 
À mon avis, il s'agit d'une importante question de privilège qui touche tous les députés de la Chambre. 
Étant donné que nous ne pouvons pas tous siéger aux comités des uns et des autres, il nous faut travailler les uns pour les autres au sein de nos comités.  
Par conséquent, tant que les documents n'ont pas été déposés à la Chambre, il n'est pas juste que les députés, à la Chambre des communes, n'aient pas l'occasion de voir les rapports avant qu'ils ne fassent l'objet de fuites et, dans ce cas-ci, intentionnellement, comme on le rapporte dans les médias.  
Je demande donc que cette question de privilège soit immédiatement renvoyée au Comité permanent de la procédure et des affaires de la Chambre pour qu'il se penche sur cette atteinte au privilège qui, à mon avis, mine la valeur de la Chambre des communes et notre raison d'être. 
Après avoir passé la majeure partie de la journée d'hier à parler de cette institution, de son importance et du respect que nous lui devons, je pense que c'est absolument déplorable.  
Monsieur le Président, je vous demande de renvoyer immédiatement cette atteinte au privilège de la part du député de Lakeland au comité de la procédure et des affaires de la Chambre afin qu'il en fasse rapport à la Chambre.  
