La crise agricole que traverse notre pays est bien réelle. 
Nous n'avons jamais assisté à une crise agricole de cette ampleur en période de prospérité. 
Certains rejettent la faute sur les fermiers. 
Ils disent que si les revenus agricoles sont faibles, c'est à cause des fermiers qui ne savent pas s'y prendre. 
Or, la réalité aujourd'hui, c'est que les fermiers produisent des pois chiches et des lentilles, qu'ils élèvent des sangliers, qu'ils utilisent des semences génétiquement modifiées, du matériel de semis de dernier cri, et que malgré tous ces investissements et toutes ces innovations, le revenu agricole net des fermiers n'a jamais été aussi faible depuis les années 30. 
La crise agricole frappe les fermiers du monde entier et quand on voit cela, il est difficile de croire que c'est la faute des fermiers ou que les fermiers sont seuls à pouvoir régler le problème. 
