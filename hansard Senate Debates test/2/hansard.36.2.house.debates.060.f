LA LOI SUR L'ACCÈS À L'INFORMATION 
-Monsieur le Président, je suis heureux de représenter mon projet de loi d'initiative parlementaire, Loi modifiant la Loi sur l'accès à l'information, qui s'appliquerait à toutes les sociétés d'État.  
Pourtant, certaines sociétés d'État, comme la SRC et Postes Canada, qui sont financées par le Trésor public, ne sont pas assujetties à la Loi sur l'accès à l'information.  
Les sociétés d'État doivent être transparentes, accessibles et responsables envers tous les contribuables, car ce sont eux qui paient la note.  
Mon projet de loi prévoit que toutes les sociétés d'État puissent faire l'objet d'examens publics et qu'elles fassent preuve d'honnêteté, d'intégrité et de transparence. 
Autrement dit, toutes les sociétés d'État seraient soumises à la Loi sur l'accès à l'information.  
