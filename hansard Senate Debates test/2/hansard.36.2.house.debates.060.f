LE BUDGET 
M. Claude Drouin (Beauce, Lib.):
Monsieur le Président, le budget fédéral est spectaculaire, et ce mot n'est pas trop fort pour qualifier les baisses d'impôt.  
Notre gouvernement réindexe l'impôt des particuliers, une rupture importante avec la politique et les décisions de l'ancien gouvernement conservateur. 
Cette mesure protégera les contribuables canadiens contre l'inflation en matière fiscale.  
Parmi les exemples de l'impact des mesures fiscales issues du dernier budget, on constate qu'une famille type de quatre personnes à revenu unique de 35 000 $ ne paiera aucun impôt fédéral net; une famille type de quatre personnes avec un revenu de 40 000 $ verra son impôt fédéral net réduit d'au moins 1 623 $ par année, soit de 48 p. 100. 
En somme, les allègements consentis dans nos trois derniers budgets, conjugués à ceux prévus au Plan quinquennal, verront l'impôt sur le revenu de l'ensemble des Canadiens réduit en moyenne d'au moins 22 p. 100 par année d'ici 2004. 
Les familles ayant des enfants en bénéficieront davantage.  
Voilà des mesures qui favorisent la famille canadienne.  
