LES TROUBLES D'APPRENTISSAGE 
M. Lynn Myers (Waterloo-Wellington, Lib.):
Monsieur le Président, ce mois-ci, Troubles d'apprentissage-Association canadienne et plusieurs autres organismes bénévoles d'un bout à l'autre du pays organisent toute une série d'activités pour sensibiliser le public aux troubles d'apprentissage.  
Environ trois millions de Canadiens de tous âges, enfants, jeunes et adultes, ont des troubles d'apprentissage. 
La probabilité que les étudiants qui ont des troubles d'apprentissage abandonnent les études secondaires est deux fois plus grande par rapport à leurs pairs qui n'en ont pas. 
Des études ont aussi montré qu'il existait des liens entre les troubles d'apprentissage et les taux de suicide parmi les adolescents, de délinquance juvénile, de détention chez les adultes et de mères adolescentes.  
En cette aube du nouveau millénaire, il est essentiel que tous les Canadiens aient de bonnes facultés d'apprentissage. 
